<p>Научно-технический отчет, содержащий: </p>
 
<p>научно-методические основы мониторинга правоприменения в пределах полномочий Минобрнауки России;</p>
 
<p>отраслевая методика мониторинга правоприменения в пределах полномочий Минобрнауки России и полученные результаты её экспериментальных исследований;</p>
 
<p>предложения в Доклады о результатах мониторинга правоприменения в 2011-2012 годах;</p>
 
