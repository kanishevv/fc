<p><b>Статья 15. Общие требования к организации образовательного процесса</b></p>
 
<p>1. Организация образовательного процесса в образовательном учреждении регламентируется учебным планом (разбивкой содержания образовательной программы по учебным курсам, по дисциплинам и по годам обучения), годовым календарным учебным графиком и расписаниями занятий, разрабатываемыми и утверждаемыми образовательным учреждением самостоятельно. Государственные органы управления образованием обеспечивают разработку базисных учебных планов и примерных программ курсов, дисциплин.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>2. Органы государственной власти, органы управления образованием и органы местного самоуправления не вправе изменять учебный план и учебный график гражданского образовательного учреждения после их утверждения, за исключением случаев, предусмотренных законодательством Российской Федерации.</p>
 
<p>3. Образовательное учреждение самостоятельно в выборе системы оценок, формы, порядка и периодичности промежуточной аттестации обучающихся.</p>
 
<p>4. Освоение образовательных программ основного общего, среднего (полного) общего и всех видов профессионального образования завершается обязательной итоговой аттестацией выпускников.</p>
 
<p>5. Научно-методическое обеспечение итоговых аттестаций и объективный контроль качества подготовки выпускников по завершении каждого уровня образования обеспечиваются государственной аттестационной службой, независимой от органов управления образованием, в соответствии с государственными образовательными стандартами.</p>
 
<p>6. Дисциплина в образовательном учреждении поддерживается на основе уважения человеческого достоинства обучающихся, воспитанников, педагогов. Применение методов физического и психического насилия по отношению к обучающимся, воспитанникам не допускается.</p>
 
<p>7. Родителям (законным представителям) несовершеннолетних обучающихся, воспитанников должна быть обеспечена возможность ознакомления с ходом и содержанием образовательного процесса, а также с оценками успеваемости обучающихся.</p>
 
<p>8. Образовательное учреждение осуществляет индивидуальный учет результатов освоения обучающимися образовательных программ, а также хранение в архивах данных об этих результатах на бумажных и (или) электронных носителях в порядке, утвержденном федеральным (центральным) государственным органом управления образованием.</p>
 
<p>(п. 8 введен Федеральным законом от 10.01.2003 № 11-ФЗ)</p>
 
