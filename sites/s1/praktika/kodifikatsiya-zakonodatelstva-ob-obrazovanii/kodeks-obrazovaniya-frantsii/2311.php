<p align="justify"><b>Статья L.231-3</b></p>
 
<p align="justify"> </p>
 
<p align="justify">Представители преподавателей-исследователей избираются представителями тех же категорий, избранными в Национальный Совет высшего образования и научных исследований.</p>
 
<p align="justify"> </p>
 
<p align="justify">Представители преподавателей и другого персонала назначаются министром, ответственным за образование, пропорционально результатам профессиональных выборов по предложению наиболее представительных профсоюзных организаций, от персонала, представившего своих кандидатов на эти выборы.</p>
 
<p align="justify"> </p>
 
<p align="justify">Представители родителей учащихся назначаются министром, ответственным за образование, по предложению ассоциаций родителей учащихся пропорционально результатам выборов в административные советы и школьные советы.</p>
 
<p align="justify"> </p>
 
<p align="justify">Представители студентов назначаются министром, ответственным за образование, по предложению студенческих ассоциаций пропорционально результатам выборов в Национальный Совет высшего образования и научных исследований.</p>
 
<p align="justify"> </p>
 
<p align="justify">Представители учащихся лицеев избираются из представителей их делегатов на уровне округов.</p>
 
