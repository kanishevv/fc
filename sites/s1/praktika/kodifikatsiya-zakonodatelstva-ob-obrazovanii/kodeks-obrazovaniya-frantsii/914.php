<span class="textmar"> </span>
<p><span class="textmar">Никто не может быть директором частного технического образовательного учреждения, если он не имеет французского гражданства или не является гражданином другого Государства &ndash; Члена Европейского Сообщества или другого Государства &ndash; участника договора о европейском экономическом пространстве, если он не соответствует требованиям по возрасту и по профессиональной пригодности, которые определены декретом после получения заключения Высшего Совета образования.</span></p>
