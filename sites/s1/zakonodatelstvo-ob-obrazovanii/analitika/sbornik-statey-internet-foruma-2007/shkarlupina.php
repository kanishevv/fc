  <p><span class="text">Права органов местного управления, гарантируемые Конституцией и федеральным законом, предполагают их обязанности в правотворческой деятельности &ndash; принятие нормативных актов, где четко определены все вопросы правового регулирования деятельности. Прерывность (дисфункция) действий института местного самоуправления связана не столько с парламентаризмом, сколько с нарушениями в распределении компетенций между федеральной властью и властью субъектов Федерации и муниципальными образованиями. Для устранения данного нарушения необходимо определить права каждой из ветвей власти в распределении полномочий и законодательно закрепить их. Если законы субъектов Федерации противоречат федеральным законам, то первые должны быть приведены в соответствие со вторыми.</span></p>
 
  <p><span class="text">После принятия Закона &laquo;Об образовании&raquo; перед региональными органами управления образованием возникли сложные задачи изменения содержания и методов их деятельности и установления новых взаимосвязей с федеральными и муниципальными органами управления. Решение этих задач продолжается и сегодня.</span></p>
 
