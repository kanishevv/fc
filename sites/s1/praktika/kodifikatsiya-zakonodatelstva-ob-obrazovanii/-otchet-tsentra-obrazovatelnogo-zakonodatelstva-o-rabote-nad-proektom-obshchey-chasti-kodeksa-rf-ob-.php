<h1><b><span class="text"> Отчет Центра образовательного законодательства о работе над проектом Общей части Кодекса РФ об образовании в 2003г.</span></b></h1>
 
<p><b><a href="http://lexed.ru/kodeks/2003/otchet_2003_1.html" ><span class="text"> 
        <br />
       </span></a></b></p>
 
<p align="justify"><span class="text">В 2003 году Центром образовательного законодательства была продолжена работа по подготовке проекта Общей части Кодекса Российской Федерации (далее &ndash; Проект). С целью ознакомления с Проектом широкой научно-педагогической общественности, членов Совета Федерации и депутатов Государственной Думы Федерального Собрания Российской Федерации, всех, участвующих в законотворческой деятельности, разработанная в 2002 г. первая версия проекта Общей части Кодекса Российской Федерации была издана отдельным тиражом (Кодекс российской Федерации об образовании. Общая часть. Проект. – М.: Готика. 10.03.2003. – 208 с) и опубликована в газете &laquo;Образовательное право&raquo; (№ 12, 16, 17, 18; 2003).</span></p>
 
<p align="justify"><span class="text">Была осуществлена экспертиза текста Проекта в профильных комитетах по образованию и науке Совета Федерации и Государственной Думы, Института законодательства и сравнительного правоведения при Правительстве Российской Федерации, в подразделениях Минобразования России; в вузах, научных и общественных организациях; на страницах периодической печати газеты: «Образовательное право», «Поиск», «Вузовские вести».</span></p>
 
<p align="justify"><span class="text"> Результаты экспертизы первой версии проекта Общей части Кодекса Российской Федерации об образовании были обсуждены на «круглых столах»: в Саратовской государственной академии права «Систематизация законодательства Российской Федерации об образовании: проблемы и перспективы», май 2003 г.; Государственным университете – Высшая школа экономики «Проблемы правового обеспечения экономики российского образования в процессе его модернизации», ноябрь 2003 г.; на научно-практических конференциях в Санкт-Петербургском государственном педагогическом университете им. А.И. Герцена «Образование и право: статус участников образовательных отношений», ноябрь 2003, Российском профессионально-педагогическом университете «Проблемы внедрения дисциплины &ldquo;Образовательное право&rdquo;», г. Екатеринбург, ноябрь 2003 г.</span></p>
 
<p align="justify"><span class="text">Помимо мероприятий, предусмотренных Календарным планом, обсуждение содержания Проекта и результатов его экспертизы были осуществлены на научно-практической конференции в Московском государственном областном педагогическом институте «Проект Кодекса Российской Федерации об образовании и перспективы развития образовательного права», май 2003 г.; в Федеральной ассоциации юридических вузов – МГУ, июнь 2003 г; на заседаниях: Правления Российского Союза ректоров, июнь 2003 г.; Президиума Международной академии наук высшей школы; на Международной межвузовской научно-практической конференции «Образовательное право и качество юридического образования» в Санкт-Петербургском гуманитарном университете профсоюзов, ноябрь 2003 г.</span></p>
 
<p align="justify"><span class="text">Замечания и предложения экспертов, участников научно-практических конференций и круглых столов были систематизированы в форме Сводной таблицы замечаний и предложений по первой версии Проекта. Поступило свыше 500 предложений по внесению изменений и дополнений в опубликованную первую версию Проекта.</span></p>
 
<p align="justify"><span class="text">С учетом поступивших предложений, а также критической оценки разработчиков Проекта, результатов своего труда, содержание проекта Общей части Кодекса РФ об образовании претерпело существенные изменения.</span></p>
 
<p align="justify"><span class="text">В процессе доработки Проекта ставилась задача по устранению максимально большего числа недочетов и недостатков, в том числе: 1) выявить и устранить в Проекте пробелы и противоречия, нечеткие и длинные нормативные предписания; 2) унифицировать применяемую терминологию, повысить уровень юридико-технического оформления нормативных предписаний; 3) более полно учесть нормы международного права, включая положения Болонской декларации; 4) привести в более полное соответствие с положениями Концепции развития российского образования на период до 2010 г., Программы социально-экономического развития Российской Федерации на среднесрочную перспективу (2003–2005 гг.); устранить факты недостаточно логичного помещения нормативного материала в отдельные разделы и главы.</span></p>
 
<p align="justify"><span class="text">В процессе доработки в проект Общей части Кодекса Российской Федерации об образовании были внесены следующие изменения и дополнения:</span></p>
 
<p align="justify"><span class="text">1) выделены в самостоятельный раздел главы Кодекса по вопросам управления системой образования;</span></p>
 
<p align="justify"><span class="text">2) исключена глава 11 «Основания возникновения, изменения и прекращения образовательных правоотношений», а также ст. 59–70 и ст. 224 (всего 26 статей);</span></p>
 
<p align="justify"><span class="text">3) из текста исключено 53 части и абзаца;</span></p>
 
<p align="justify"><span class="text">4) вновь подготовлены и включены в текст проекта две главы: «Возникновение, изменение и прекращение образовательных правоотношений» и «Управление образовательным учреждением (организацией)» (всего 25 статей), а также подчастей статей;</span></p>
 
<p align="justify"><span class="text">5) дана новая редакция 36 частей и абзацев.</span></p>
 
