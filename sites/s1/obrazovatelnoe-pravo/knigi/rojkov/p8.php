</p>
<p style="text-align: justify;">
	<span class="text">5.2.Подрядчик гарантирует качество и работу электроприборов в течение гарантийного срока. Гарантийный срок начинается с момента передачи товара покупателю.</span>
</p>
<p style="text-align: justify;">
	<span class="text">5.3.Приемка выполненных работ производится Заказчиком не позд­нее следующего рабочего дня с момента получения Заказчиком от Подрядчика уведомления в устной или письменной форме о заверше­нии выполнения работ.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Заказчик обязан в сроки и в порядке, предусмотренные настоящим договором, с участием Подрядчика осмотреть и принять установлен­ные электроприборы по акту сдачи-приемки, а при обнаружении от­ступлений от договора, ухудшающих результат работы, или иных не­достатков в работе немедленно заявить об этом Подрядчику.</span>
</p>
<p style="text-align: justify;">
	<span class="text">5.4.Заказчик, обнаруживший недостатки в работе при ее приемке, вправе ссылаться на них только в случаях, если в акте приемки были оговорены эти недостатки либо возможность последующего предъяв­ления требования об их устранении.</span>
</p>
<p style="text-align: justify;">
	<span class="text">5.5.Заказчик, принявший работу без проверки, лишается права ссы­латься на недостатки работы, которые могли быть установлены при обычном способе ее приемки (явные недостатки).</span>
</p>
<p style="text-align: justify;">
	<span class="text">5.6.Заказчик, обнаруживший после приемки работы отступления от настоящего договора или иные недостатки, которые не могли быть установлены при обычном способе приемки (скрытые недостатки), в том числе такие, которые были умышленно скрыты Подрядчиком, обя­зан известить об этом Подрядчика в разумный срок после их обнару­жения с указанием своего требования согласно п. 6.2 настоящего до­говора.</span>
</p>
<p style="text-align: justify;">
	<b><span class="text"> <br>
 </span><span class="text">6. Ответственность сторон</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">6.1.Подрядчик несет ответственность за ненадлежащее качество работ.</span>
</p>
<p style="text-align: justify;">
	<span class="text">6.2.В случае обнаружения недостатков электроприборов либо не­надлежащего выполнения работ по их установке Заказчик по согласо­ванию с Плательщиком вправе потребовать от Подрядчика:</span>
</p>
<p style="text-align: justify;">
	<span class="text">—соразмерного уменьшения цены;</span>
</p>
<p style="text-align: justify;">
	<span class="text">—безвозмездного устранения недостатков товара в разумный срок;</span>
</p>
<p style="text-align: justify;">
	<span class="text">—возмещения расходов Плательщика на устранение недостатков товара.</span>
</p>
<p style="text-align: justify;">
	<span class="text">6.3. За поставки и установки электроприборов Подрядчик уплачи­вает Плательщику неустойку в размере 0,5% процента за каждый день просрочки до полного исполнения обязательства.</span>
