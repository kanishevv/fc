<p style="text-align: justify;">
	<span class="text">Гранты (grants), стипендии (scholarship), бесплатные стажировки (work-study) и другие виды безвозмездной помощи не полностью покрывают обучение в колледже или университете. Многие студенты дополнительно к собственным сбережениям пользуются государственными и частными займами. Федеральная программа образовательных займов предлагает сниженные процентные ставки и более гибкие планы погашения кредитов, чем большинство потребительских кредитов. Также существует возможность применить налоговый вычет по подоходному налогу в сумме, эквивалентной выплатам процентов по образовательному кредиту, но в сумме, не превышающей 2500 долл. США.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Процентная ставка по Стаффордским займам, взятым впервые после 1 июля 2006 г. зафиксирована на уровне 6,8%. Данная ставка применяется как в период обучения, так и в льготный период, и в период погашения займа. Еще более низкая процентная ставка применяется по взятым впервые Стаффордским образовательным займам, если заимствование имело место в период с 1 июля 2008 года по 30 июня 2012 года. В 2008–2009 гг. ставка составляет 6%, в 2009–2010 гг. – 5,6%, в 2010–2011 гг. – 4,5%, в 2011–2012 гг. – 3,4%. Процентная ставка на займыPLUS, взятые впервые, после 1 июля 2006 г. составляет 8,5% по программе Федеральной помощи семьям в образовательных кредитах (Federal Family Education Loan Program, FFELP) и 7,9% по программе Прямого образовательного займа Вильяма Форда (William D. Ford Federal Direct Loan Program, FDLP).</span>
</p>
<p style="text-align: justify;">
	<span class="text">В контексте многолетнего дефицита федерального бюджета США и намечающихся тенденций американской финансовой системы к неустойчивому развитию в предстоящие годы, вышеприведенные процентные ставки представляют не что иное, как повышенные обязательства федерального правительства США перед населением и новые поиски средств покрытия дефицита федерального бюджета.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В случае дальнейшего отказа американского правительства от финансирования другой колоссальной статьи расходов федерального бюджета – военных расходов, а также в условиях известных трудностей с увеличением объема налоговых доходов, вопрос с покрытием дефицита бюджета и нехваткой финансовых ресурсов для финансирования сферы образования и образовательных нужд населения федеральные власти будут решать методом выпуска новых государственных облигаций, то есть, новых заимствований.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В американской практике для таких целей чаще всего используютсякраткосрочные казначейские векселя (TreasurybillsилиT-Bills), сроком на один год, с выплатой суммы и процентов только после срока погашения[7]. Более того, размер процентной ставки по Стаффордским образовательным займам и займамPLUSпересматривается ежегодно 1 июля и зависит от суммы, привлеченной в мае при продаже краткосрочных казначейских векселей.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Во второй половине 2008 г. процентная ставка по Стаффордскому образовательному займу составляла 3,61% в период обучения и льготный период по окончании обучения, и 4,21% в период погашения. Ставка по займамPLUSнаходилась на уровне 5,01%. Для сохранения текущей сравнительно низкой процентной ставки обучающийся вправе консолидировать заем, тогда ставка останется неизменной.В противном случае возврат образовательного кредита в 2009–2010 гг.с высокой вероятностью будет производиться по более высоким процентным ставкам, что связано с неблагоприятными тенденциями развития американской экономики.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Непосредственными проводникам средств федерального бюджета, направляемых на поддержку системы льготного кредитования образования, выступают банки. В дополнение к льготной ставке, большим разнообразием по сравнению с потребительским кредитованием отличаются и способы погашения обязательств. Так, существуют программы под общим названием «прощение образовательного долга» (Loan Forgiveness)в обмен на службу в государственных учреждениях, армии, выполнение определенной работы.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В настоящее время в США существуют следующие возможности «прощения долга» по федеральным образовательным кредитам, требующие от заемщиков выполнения определенных действий либо службы в определенных организациях:</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – членство в организации «АмериКорпс» (AmeriCorps), которая привлекает граждан Америки для удовлетворения потребности государства и местных сообществ в различного рода социальных работниках и волонтерах (сиделках с больными стариками, надомных воспитателей для детей с физическими недостатками, спасателей в случае масштабных экологических катастроф и т.п.)[8], которое при 12-месячной работе, помимо зарплаты, дает уменьшение образовательного долга на4 725 долл. США;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – членство в организации «Отряды мира» (PeaceCorps), учрежденной Дж. Кеннеди в 1960 г. и привлекающейграждан Америки для выполнения различных миссий мира в развивающихся странах и помощи бедным[9]. Членство в этой организации позволяет уменьшить сумму задолженности по Стаффордским, Перкинсовским и консолидированным займам, например, по Перкинсовским займам можно сократить сумму задолженности на 15% за каждый год службы;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – участие в программе АмериКорпс «Волонтеры на службе Америки» (Volunteers in Service to America, VISTA). Данная организация создана для сокращения числа бездомных, неграмотных, голодных и бедных внутри США методом непосредственной помощи этим социальным группам[10]. Членство в ней дает возможность уменьшить сумму долга по образовательному займу на4725 долл. США;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – служба в армии США и Программа возврата образовательного займа (Student Loan Repayment Program,SLRP), позволяет погасить до 10 000 долл. США;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – преподавание выпускников педагогических вузов и факультетов в школах согласно Закона США о государственной защите образования, прежде всего, в сельских и удаленных школах, позволяет погасить Перкинсовский образовательный кредит в первый и второй год работы – по 15% за год, в третий и четвертый – по 20%, и на пятый год – 30% за год[11]; работа в школах, в которых не хватает учителей в течение 5 лет, позволяет также сократить долг по Стаффордскому кредиту на не облагаемую подоходным налогом сумму вплоть до 17 500 долл. США, что прямо предусмотрено Законом США о защите налогоплательщиков-учителей 2004 г. (theTaxpayer-TeacherProtectionActof2004).</span>
</p>
<p>
</p>
 <span style="font-size: 13px;"><span class="text"></span></span>
<p style="text-align: justify;">
	– служба в государственных и муниципальных учреждениях и некоммерческих организациях, которая также может стать основа­нием прощения образовательного кредита согласно Закону США&nbsp;О Сокращении затрат на колледжи и доступе к образованию 2007 г., например, юридическое консультирование в определенных некоммерческих учреждениях (для этого необходимо иметь высшее юридическое образование)[12], медицинская помощь и работа в определенных медицинских учреждениях для помощи населению, особо нуждающемуся в бесплатной медицинской помощи[13], проведение клинических исследований в Государственных институтах здравоохранения США (US National Institutes of Health) и поддержка студентов, проводящих клинические исследования, с помощью «Программы прощения кредита медицинским работникам Государственных институтов здравоохранения США»[14];
</p>
<p>
</p>
<p style="text-align: justify;">
	<span class="text"> – другие программы для выпускников медицинских вузов, например, участие в Программе здравоохранения индейцев (Indian Health Service IHS), прощение образовательного кредита в рамках Программы прощения долга работникам учреждений с неблаго­приятными для здоровья условиями труда (Disadvantaged Health Professions Faculty Loan Repayment Program), различные программы военно-воздушных сил, армии, флота для медицинских работников.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Условия предоставления Перкинсовского и Стаффордского образовательных кредитов прямо предусматривают прощение долга в случае, когда должник определенное время работает учителем в начальных и средних классах и преподает детям из семей с низкими доходами, а также преподает в специализированных коррекционных школах, причем как в публичных, так и частных, участвует в образовательном компоненте программы «Хед Старт» (Head StartProgram) для семей с низкими доходами. Данная программа установлена одноименным Законом 1965 г. и управляется Департаментом здравоохранения и социального обслуживания США).</span>
</p>
<p>
</p>
 <span style="font-size: 13px;"><span class="text"></span></span>
<p style="text-align: justify;">
	Федеральная программа возмещения долга по федеральным образовательным займам[15]допускает, что федеральные органы вправе определять случаи, когда работнику-должнику может непосредственно переводиться сумма, направленная на погашение образовательного кредита. Таким образом, в данном случае речь идет о возмещении, а не о прощении долга, хотя разница с предыдущей формой погашения долга небольшая и состоит в техническом способе возмещения – средства выплачиваются непосредственно на счет должника, который затем переводит их кредитору, а не за должника, как в случае с прощением долга. Данная программа позволяет федеральному департаменту или агентству выплачивать одному работнику в течение&nbsp;финансового года сумму до 10 000 долл. США для погашения образовательного кредита.
</p>
<p>
</p>
<p style="text-align: justify;">
	<span class="text">Согласно ст. 108(f) Налогового кодекса США доходы в виде прощения образовательного кредита не облагаются подоходным на­логом. Из этого правила есть немногочисленные исключения. Так, если сумма по образовательному кредиту прощается образовательным учреждением, которое само освобождено от уплаты подоходного налога, сумма «прощения» долга исключается из валового дохода должника при условии, что сумма прощения пропорциональна количеству лет, отработанных по определенной профессии в определенном социальном учреждении.</span>
</p>
<p style="text-align: justify;">
	<span class="text">При консолидации федеральных образовательных займов в США применяются специальные правила, отличные от тех, что имеют место при консолидации обычных потребительских кредитов. Это объясняется законодательной гарантией федеральных образовательных кредитов. Так, однократная консолидация образовательных кредитов возможна с частным займодателем (чаще всего банком) и приводит только к «запиранию» процентной ставки по этим кредитам, которая останется неизменной даже при изменении федеральной процентной ставки по данному виду кредита в течение срока кредита. Повторная реконсолидация образовательного кредита допускается только с санкцииДепартамента образования США.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Доля региональных финансов в общем объеме публичных расходов на высшие учебные заведения неуклонно сокращается в последние 20 лет. Основная причина этого связана с усилением тенденции фискального федерализма. В абсолютном выражении объем средств, выделяемых из бюджетов штатов на потребности высшей школы, с поправкой на инфляцию, оставался в тот же период времени на прежнем уровне.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Устойчивый рост потребностей вузов в финансовом обеспечении, в первую очередь по причине роста числа студентов, не сопро­вождавшийся перераспределяющим ростом финансирования, привел к нехватке финансовых средств. Это, в свою очередь, усилило конкуренцию между высшими учебными заведениями и частично стимулировало повышение качества предоставляемых образовательных услуг. Однако положение с покрытием дефицита финансовых ресурсов в сфере высшего образования по-прежнему остается непростым. Для исправления сложившейся ситуации штаты часто идут на выпуск государственных ценных бумаг для оказания финансовой поддержки высшему образованию, а вузы вынуждены изыскивать недостающие средства иными способами, чаще всего, связанными с повышением платы за обучение или с более активным привлечением частного капитала.</span>
</p>
<p style="text-align: justify;">
	<span class="text">План первоочередных реформ системы финансирования и организации образования 44-го Президента США Б. Обамы предусматривает следующие ключевые пункты, которые также можно отнести к приоритетам федеральной образовательной поли­тики:</span>
</p>
<p style="text-align: justify;">
	<span class="text">·Сохранение профессии учителя: 30% молодых учителей, начавших работать по профессии, увольняются через 5 лет или раньше;</span>
</p>
<p style="text-align: justify;">
	<span class="text">·Существенное повышение динамики роста государственных расходов на высшее образование. За прошедшие 5 лет на высшую школу затраты возросли лишь на 40%. Задолженность выпускников вузов на момент получения диплома об окончании составляет в среднем 19000 долл. США. Если динамику расходов сохранить на том же уровне, то в период с 2001 по 2010 гг. около 2 млн. граждан, имеющих право поступить в вуз, попросту не будут этого делать, поскольку не смогут себе этого позволить. Сложная система применения налоговых скидок и большое количество заявлений, которые необходимо заполнить для их получения, привело к незнанию студентов о том, какие на самом деле у них есть возможности по финансированию собственного обучения.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Начать планируется с изменений объемов финансирования: суще­ственно увеличить федеральные расходы. Есть основания полагать, что политика увеличения государственных расходов на обра­зование будет реализована, так как первое законотворческое пред­­ло­жение Б. Обамы на посту сенатора состояло в увеличении финансирования студенческих грантов. Однако необходимо принять во внимание другое обстоятельство: нынешний Президент США полагает, что избыточные социальные льготы и есть нарушение гражданских прав.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Реформы по финансированию высшего образования предусматривают ведение новой налоговой скидки, направленной то, чтобы колледжи были доступны каждому американцу. Скидка будет полностью возмещаемой, а первые 4000 долл. США, потраченные на колледж, будут предоставлены бесплатно любому гражданину страны, в среднем на 2/3 покроют расходы на обучение в среднем вузе, а обучение в двухгодичных муниципальных колледжах предполагается сделать бесплатным, с обязательной отработкой 100 часов на муниципальной службе.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Усилия Администрации Президента США также направлены на упрощение процедуры подачи заявок на получение финансовой помощи: включение в налоговую декларацию специальной графы о том, что все личные данные могут быть использованы при получении налоговых скидок на образование и финансовой помощи на обучение. Это позволит сократить количество заполняемых форм и упростит получение адресной федеральной финансовой помощи.</span>
</p>
