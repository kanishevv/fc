<p><b>Статья 10. Формы получения образования</b></p>
 
<p>1. С учетом потребностей и возможностей личности образовательные программы осваиваются в следующих формах: в образовательном учреждении - в форме очной, очно-заочной (вечерней), заочной; в форме семейного образования, самообразования, экстерната.</p>
 
<p>Допускается сочетание различных форм получения образования.</p>
 
<p>2. Для всех форм получения образования в пределах конкретной основной общеобразовательной или основной профессиональной образовательной программы действует единый государственный образовательный стандарт.</p>
 
<p>3. Перечни профессий и специальностей, получение которых в очно-заочной (вечерней), заочной форме и в форме экстерната не допускается, в части компетенции Российской Федерации устанавливаются Правительством Российской Федерации.</p>
 
