<div align="justify"><b>Цель работы: </b> упорядочение действующего российского законодательства об образовании, приведение его в систему с учетом задач модернизации российского образования. </div>
 
<div align="justify"><b>Задачи реализации проекта: </b></div>
 
<div align="justify">- выбор и обоснование оптимальной формы систематизации правовых норм в сфере образования; </div>
 
<div align="justify">- ревизия законодательства и нормативных правовых актов в сфере образования для дальнейшей работы по его систематизации в форме инкорпорации; </div>
 
<div align="justify">- выявление нормативных правовых актов в сфере образования, находящихся в противоречии с действующим законодательством Российской Федерации, и определение юридической судьбы таких актов (составление проекта перечня нормативных правовых актов на отмену, подготовка проектов ведомственных правовых актов о внесении изменений и дополнений в данные нормативные правовые акты); </div>
 
<div align="justify">- составление перечня нормативных правовых актов, фактически утративших силу и подлежащих отмене; </div>
 
<div align="justify">- выявление коллизий между нормами образовательного законодательства и смежных отраслей права, как-то: административного, муниципального, бюджетного, гражданского, семейного и др.; </div>
 
<div align="justify">- выявление норм образовательного законодательства, противоречащих сформировавшейся судебно-арбитражной практике, в т.ч. правовым позициям Конституционного Суда РФ; </div>
 
<div align="justify">- выявление норм образовательного законодательства, противоречащих международным договорам и соглашениям Российской Федерации; </div>
 
<div align="justify">- мониторинг действующего образовательного законодательства на предмет соответствия требованиям, обусловленным участием Российской Федерации в Болонском процессе; </div>
 
<div align="justify">- устранение &laquo;скрытых коллизий&raquo;, т.е. коллизий, возникающих из-за неодинакового понимания отдельных категорий и понятий в разных актах образовательного законодательства; унификация понятийного аппарата образовательного законодательства; </div>
 
<div align="justify">- анализ базовых категорий образовательного законодательства: &laquo;законодательство об образовании&raquo;, &laquo;система законодательства об образовании&raquo;, &laquo;принципы (общие начала) законодательства об образовании&raquo;, &laquo;вступление акта законодательства об образовании в действие&raquo;, &laquo;обнародование (официальное опубликование) акта законодательства об образовании&raquo;; </div>
 
<div align="justify">- разработка словаря юридических терминов в образовании. </div>
 
