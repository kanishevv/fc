<p><span class="text" style="text-align: justify;">Статья 12. Управление высшим учебным заведением</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Управление высшим учебным заведением осуществляется в соответствии с законодательством Российской Федерации, типовым положением об образовательном учреждении высшего профессионального образования (высшем учебном заведении) и уставом высшего учебного заведения на принципах сочетания единоначалия и коллегиальности.</span></p>
 
<p><span class="text" style="text-align: justify;">Устав высшего учебного заведения (изменения устава и дополнения к нему) принимается общим собранием (конференцией) педагогических работников, научных работников, а также представителей других категорий работников и обучающихся высшего учебного заведения (далее - общее собрание (конференция)).</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Высшие учебные заведения, реализующие военные профессиональные образовательные программы (военно-учебные заведения) осуществляют свою деятельность в соответствии с законодательством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 01.12.2007 № 313-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Общее руководство государственным или муниципальным высшим учебным заведением осуществляет выборный представительный орган - ученый совет.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">В состав ученого совета входят ректор, который является председателем ученого совета, и проректоры, а также президент, если такая должность предусмотрена уставом. Другие члены ученого совета избираются общим собранием (конференцией) тайным голосованием.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 18.07.2006 № 113-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Состав, полномочия, порядок выборов и деятельности ученого совета определяются уставом высшего учебного заведения на основании типового положения об образовательном учреждении высшего профессионального образования (высшем учебном заведении).</span></p>
 
<p><span class="text" style="text-align: justify;">Уставами высших учебных заведений может быть предусмотрено создание попечительских советов.</span></p>
 
<p><span class="text" style="text-align: justify;">В части, не урегулированной законодательством Российской Федерации, порядок формирования органов управления образовательного учреждения и их компетенция определяются уставом образовательного учреждения.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 03.11.2006 № 175-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. Непосредственное управление высшим учебным заведением осуществляется ректором.</span></p>
 
<p><span class="text" style="text-align: justify;">Кандидатуры на должности ректоров государственных или муниципальных высших учебных заведений, прошедшие выдвижение в соответствии с их уставами, представляются на рассмотрение действующих на общественных началах аттестационных комиссий соответствующих уполномоченных органов исполнительной власти или исполнительно-распорядительных органов городских округов, муниципальных районов. Порядок выдвижения кандидатур на должность ректора должен предусматривать возможность их самовыдвижения.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 3 в ред. Федерального закона от 18.07.2006 № 113-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Положения об аттестационных комиссиях и их состав утверждаются соответствующими уполномоченными органами исполнительной власти или исполнительно-распорядительными органами городских округов, муниципальных районов. В состав такой аттестационной комиссии включаются:</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">1) при выборах ректоров высших учебных заведений, находящихся в ведении Российской Федерации, - представители федеральных органов государственной власти (50 процентов), представители общественных организаций и государственно-общественных объединений в системе высшего и послевузовского профессионального образования и представители органов государственной власти субъекта Российской Федерации, на территории которого расположены высшие учебные заведения (50 процентов);</span></p>
 
<p><span class="text" style="text-align: justify;">2) при выборах ректоров высших учебных заведений, находящихся в ведении субъекта Российской Федерации, - представители органов государственной власти субъекта Российской Федерации (50 процентов), представители общественных организаций и государственно-общественных объединений в системе высшего и послевузовского профессионального образования (50 процентов);</span></p>
 
<p><span class="text" style="text-align: justify;">3) при выборах ректоров муниципальных высших учебных заведений - представители органов местного самоуправления соответственно городского округа или муниципального района (50 процентов), представители общественных организаций и государственно-общественных объединений в системе высшего и послевузовского профессионального образования (50 процентов).</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 4 введен Федеральным законом от 18.07.2006 № 113-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Ректор государственного или муниципального высшего учебного заведения в порядке, установленном уставом такого высшего учебного заведения, избирается из числа кандидатур, согласованных с аттестационной комиссией соответствующего уполномоченного органа исполнительной власти или исполнительно-распорядительного органа городского округа, муниципального района, тайным голосованием на общем собрании (конференции) на срок до пяти лет. После избрания ректора между ним и органом исполнительной власти или исполнительно-распорядительным органом городского округа, муниципального района, в ведении которых находится такое высшее учебное заведение, заключается трудовой договор на срок до пяти лет.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Повторные выборы ректора проводятся в случае нарушения установленной настоящим Федеральным законом и (или) уставом высшего учебного заведения процедуры выборов ректора либо в случае признания выборов ректора несостоявшимися или недействительными.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 5 введен Федеральным законом от 18.07.2006 № 113-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6. В случае если государственное или муниципальное высшее учебное заведение в целом лишается государственной аккредитации, ректор высшего учебного заведения и отвечающие в пределах своей компетенции за качество подготовки выпускников проректоры освобождаются от занимаемых должностей органом исполнительной власти или исполнительно-распорядительным органом городского округа, муниципального района, в ведении которых находится такое высшее учебное заведение. Выборы ректора высшего учебного заведения в этом случае не допускаются, и он принимается соответствующим органом, осуществляющим управление в сфере образования, на работу по трудовому договору на срок не более чем пять лет. Учредителем высшего учебного заведения или уполномоченным этим учредителем органом, осуществляющим управление в сфере образования, по представлению ректора высшего учебного заведения утверждается новый состав ученого совета.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 6 введен Федеральным законом от 18.07.2006 № 113-ФЗ, в ред. Федеральных законов от 20.04.2007 № 56-ФЗ, от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">7. После возобновления государственной аккредитации высшего учебного заведения (но не ранее чем через один год со дня лишения его государственной аккредитации) в таком высшем учебном заведении избирается ученый совет в установленном настоящим Федеральным законом порядке.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 7 введен Федеральным законом от 18.07.2006 № 113-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">8. В создаваемом или реорганизуемом государственном или муниципальном высшем учебном заведении до избрания ученого совета устав такого высшего учебного заведения утверждается соответствующим органом исполнительной власти или исполнительно-распорядительным органом городского округа, муниципального района на срок не более чем один год. С ректором такого высшего учебного заведения заключается трудовой договор на указанный срок.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 8 введен Федеральным законом от 18.07.2006 № 113-ФЗ, в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
