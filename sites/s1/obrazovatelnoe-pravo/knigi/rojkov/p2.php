</p>
<p style="text-align: justify;">
	<span class="text">5.1.Исполнитель вправе отказать Заказчику и Потребителю в заклю­чении договора на новый срок по истечении действия настоящего договора, если Заказчик, Потребитель в период его действия допус­кали нарушения, предусмотренные гражданским законодательством и настоящим договором и дающие Исполнителю право в односто­роннем порядке отказаться от исполнения договора.</span>
