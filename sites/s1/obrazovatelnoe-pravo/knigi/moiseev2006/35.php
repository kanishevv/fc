<br><span style="font-size: 13px;"><span class="text" style="text-align: center;"><b><div><br></div><div><br></div><h3 style="text-align: left;">Меры, действия и мероприятия по управлению&nbsp;реализацией Программы</h3></b></span></span><p style="text-align: justify;"><span class="text"><br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">Программы перехода на профильное обучение обязательно должны включать в себя&nbsp;<b>встроенный механизм управления их реализацией.&nbsp;</b>Поскольку цели и планы действий в них уже описаны, в данном разделе важно показать действия организационного, мотивационно-стимулирующего, контрольно-мониторингового и аналитико-оценочного характера.</span></p>
 
<p style="text-align: justify;"><span class="text">К&nbsp;<b>организационным&nbsp;</b>действиям относятся, в частности - создание рабочих групп (команд) регионального, субрегионального (окружного), межмуниципального, муниципального и т.п. уровней, ответственных за реализацию конкретных действий конкретной Программы и Программы в целом, включая группу постоянно работающих в Программе менеджеров (Дирекция Программы, офис Программы).</span></p>
 
<p style="text-align: justify;"><span class="text">К действиям&nbsp;<b>руководства, мотивации, стимулирования&nbsp;</b>реализации Программ относятся действия и мероприятия по поощрению вовлечения в реализацию Программ широкого профессионального сообщества и населения, поощрения. Например, на уровне региона - это стимулирование муниципальных образовательных систем, локальных образовательных сетей, а также образовательных и иных организаций (в том числе - не из сферы образования), вносящих наиболее позитивный вклад в переход на профильное обучение. При этом неоценимую роль играет демонстрация значимости этого нововведения и Программы его реализации для первых лиц регионального образования и региона в целом, их личное активное участие в работе по управлению Программой.</span></p>
 
<p style="text-align: justify;"><span class="text">Действия&nbsp;<b>контрольного и оценочного характера&nbsp;</b>предполагают прежде всего описание в Программах ясной системы мониторинга и оценки процесса и результатов перехода на профильное обучение, что существенно облегчается при грамотном и точном целеполагании в соответствующих частях Программ и, наоборот, существенно замедляется или становится нереальным при отсутствии ясных главных и промежуточных целей.</span></p>
 
<p style="text-align: justify;"><span class="text">Здесь же важно спланировать возможные&nbsp;<b>упреждающие или реактивные меры по коррекции&nbsp;</b>стратегического плана Программы и плана его реализации.</span></p>
 
