</p>
<p style="text-align: justify;">
	<span class="text">Следует отметить, что обучающиеся, воспитанники дошкольных образовательных учреждений имеют довольно ограниченную дееспособность. Правовая ответственность во многом лежит на их родителях (законных представителях). Взаимоотношения между дошкольным образовательным учреждением и родителями (законными представителями) регулируется договором, включающим в себя взаимные права, обязанности и ответственность сторон, возникающие в процессе обучения, воспитания, присмотра и ухода. Нарушения прав ребёнка являются основанием для прекращения трудового договора (контракта) с педагогом.</span>
