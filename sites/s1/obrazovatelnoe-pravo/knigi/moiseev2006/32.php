   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">реальных (актуальных и перспективных) потребностей, ожиданий и запросов основных заинтересованных сторон Программы в регионе, реального социального заказа на профильное обучение;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">существующих в региональной, муниципальной образовательной системе, в конкретных образовательных учреждениях заделов и ресурсных возможностей, позволяющих оптимизировать процессы перехода на профильное обучение;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">методологии, культуры и традиций программно-целевого подхода;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">реального опыта 10 пилотных регионов РФ, конкретных экспериментальных площадок по подготовке к введению профильного обучения; материалов, подготовленных в ходе экспериментальной работы по переходу на профильное обучение.</span></p>
   </li>
 </ul>
 
