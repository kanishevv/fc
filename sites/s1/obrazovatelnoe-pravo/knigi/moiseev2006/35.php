<p style="text-align: justify;"><span class="text"><b>План-график перехода на профильное обучение</b></span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">При наличии достаточно подробного тактико-оперативного плана реализации целей и стратегии перехода образовательной системы на профильное обучение на конкретный план-график такого перехода ложится задача описания основных действий и мероприятий Программы<b>в логике их развертывания во времени.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Фактически в таком плане-графике действия, изложенные ранее в логике содержательных задач направлений, описываются в календарной форме.</span></p>
 
<p style="text-align: justify;"><span class="text">Для проверки реалистичности продолжительности и сроков выполнения конкретных действий Программ дополнительно к планам и графикам можно использовать ленточную диаграмму (или график) Гантта, в которой все действия выстраиваются в форме векторов разной длины, соответствующей их планируемой продолжительности во времени. В результате становится видно, удастся ли выполнить все эти действия за отведенное общее время. Построение такого графика сегодня возможно в автоматизированном виде, например, с использованием возможностей компьютерной программы Microsoft Project.</span></p>
 
