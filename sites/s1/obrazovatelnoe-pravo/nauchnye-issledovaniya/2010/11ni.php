<p align="justify">1. анализ и обобщение правовых аспектов международного сотрудничества независимых государств, образованных на постсоветском пространстве в денежно-кредитной сфере (исторический, организационный и правовой аспект);</p>
 
<p align="justify">2. оценка соблюдения национальных интересов, Конституции России и ее федерального законодательства в контексте принятых международно-правовых обязательств в отношениях с бывшими союзными республиками.</p>
 
