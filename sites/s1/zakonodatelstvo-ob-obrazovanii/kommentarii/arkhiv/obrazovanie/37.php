<p align="JUSTIFY"><span class="text">К таким органам относятся федеральные органы исполнительной власти в определенной отрасли компетенции, наделенные управленческими полномочиями в области образования.</span></p>

<p align="JUSTIFY"><span class="text">Целый ряд образовательных учреждений находятся в ведении федеральных отраслевых министерств и ведомств. Так, в соответствии с распоряжением Правительства РФ от 12 января 2005 года N16-р в перечень образовательных учреждений, находящихся в ведении Минэкономразвития России, включены Государственный университет – Высшая школа экономики, Всероссийская академия внешней торговли, Московский институт международного бизнеса при Всероссийской академии внешней торговли, Высшая коммерческая школа при Всероссийской академии внешней торговли, &quot;Высшие курсы иностранных языков (Центр) Министерства экономического развития и торговли Российской Федерации&quot; и др.</span></p>

<p align="JUSTIFY"><span class="text">Большое количество образовательных учреждений находятся в ведении Министерства обороны РФ, Министерства внутренних дел РФ, Министерства культуры и массовых коммуникаций РФ, Министерства здравоохранения и социального развития РФ, Министерства транспорта РФ, Министерства сельского хозяйства РФ или в ведении федеральных агентств вышеуказанных министерств и др. «Отраслевые» министерства осуществляют управленческие функции по отношению к находящимся в их ведении образовательным учреждениям. Соответствующие функции чаще всего возлагаются на управления (департаменты) кадров и учебных заведений, создаваемые в структуре «отраслевых» министерств и ведомств.</span></p>

