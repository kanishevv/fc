<p style="text-align: justify;"><span class="text">Как следствие происходящих перемен, профессиональное образование принимает сегодня иную парадигму развития. Основным его предназначением становится удовлетворение потребностей рынка труда в специалистах, причем именно по тем профессиям и специальностям, которые пользуются спросом у работодателей. А для этого необходимо знать и постоянно следить за изменением конъюнктуры спроса, выявлять новые потребности работодателей в профессиональных навыках и уровне квалификации работников, учитывать потенциальную емкость рынка труда.</span></p>
 
<p style="text-align: justify;"><span class="text">Таким образом, профессиональное образование встраивается в новую систему социального целеполагания. Достижение поставленной цели требует от профессиональной школы перестройки всей ее деятельности. К числу основных направлений такой перестройки можно отнести:</span></p>
 
<p style="text-align: justify;"><span class="text">•переориентация всей деятельности профессиональной школы от решения задачи удовлетворения потребностей индивида в получении образования к удовлетворению конкретных потребностей рынка труда в соответствующих специалистах;</span></p>
 
<p style="text-align: justify;"><span class="text">•воссоздание системы социального партнерства на новой качественной основе, ориентированной на перманентное взаимодействие профессионального образования с субъектами рынка труда;</span></p>
 
<p style="text-align: justify;"><span class="text">•включение профессионального образования в систему мониторинга рынка труда, решающего вопросы оценки спроса на профессии, выявления новых перспективных профессий, пользующихся спросом на рынке труда, установления новых видов профессиональной деятельности, перечня умений и квалификационных требований к работнику конкретной профессии со стороны работодателей;</span></p>
 
<p style="text-align: justify;"><span class="text">•участие учреждений профессионального образования совместно с социальными партнерами в формировании новых и корректировке действующих образовательных и профессиональных стандартов.</span></p>
 
<p style="text-align: justify;"><span class="text">Условия сбалансированности отраслевой структуры промышленной экономической деятельности, торговли, финансового обеспечения и других секторов экономики предопределяет требование сбалансированности подготовки соответствующих специалистов. Условия организации динамичного производства, способного обеспечивать новые возникающие потребности общества и государства, конкурентоспособность продукции, эффективно откликаться на потребительский спрос, быстро перевооружаться, избавляясь от устаревших производств, предопределяет требование быстро обучать и переучивать кадры специалистов.[43]Эту проблему должна решать система образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Для решения проблемы сбалансированности рынка труда и рынка образовательных услуг необходимо, по меньшей мере, решить три задачи:</span></p>
 
<p style="text-align: justify;"><span class="text">1) достичь равновесия на рынке труда специалистов и рабочих, при этом необходимо учитывать демографическую ситуацию;</span></p>
 
<p style="text-align: justify;"><span class="text">2) повысить экономическую эффективность подготовки кадров в системе профессионального образования с учетом прогнозов развития экономики;</span></p>
 
<p style="text-align: justify;"><span class="text">3) создать предпосылки повышения роли профессионального образования в процессе структурной перестройки хозяйственного комплекса региона.</span></p>
 
<p style="text-align: justify;"><span class="text">Это особенно важно сегодня при реализации федеральных законов «Об общих принципах организации законодательных (представительных) и исполнительных органов государственной власти субъектов Российской Федерации» и «Об общих принципах организации местного самоуправления в Российской Федерации» в сфере образования, когда основным заказчиком, потребителем и производителем профессиональных кадров становится субъект Федерации (регион). С учетом этого представляется важным определить основные механизмы развития рынка образовательных услуг на региональном уровне.</span></p>
 
<p style="text-align: justify;"><span class="text">Как нам представляется, равновесие на рынке труда специалистов и рабочих может быть достигнуто в результате структурной перестройки сети образовательных учреждений с учетом потребностей рынка труда, потребителей образовательных услуг и состояния образования в регионе. С этой целью может быть разработан так называемый «образовательный кадастр», или карта («зеркало») образования для данной конкретной территории.</span></p>
 
<p style="text-align: justify;"><span class="text">Повышения экономической эффективности подготовки кадров в системе профессионального образования, индикатором которой может быть снижение затрат на получение профессионального образования, можно достичь за счет сокращения сроков обучения по непрерывным образовательным программам, снижения численности безработных граждан и затрат на их переобучение в системе обучения незанятого населения, повышения роли внутрипроизводственного обучения и так называемого опережающего получения нескольких узких специальностей (дополнительных квалификаций).</span></p>
 
<p style="text-align: justify;"><span class="text">Создание предпосылок повышения роли профессионального образования в процессе структурной перестройки хозяйственного комплекса региона, формирования нового технологического уклада, зон структурных изменений («точек роста»)определяется высоким уровнем качества подготовки рабочих и специалистов, зависящим, прежде всего, от квалификации педагогических кадров, используемых ими современных информационных технологий, и адекватными требованиям социума и экономики образовательными стандартами.</span></p>
 
<p style="text-align: justify;"><span class="text">Учитывая важное практическое значение проблемы повышения эффективности системы образования в условиях социально-экономических реформ и изменившегося законодательства, в данный момент необходимо принимать во внимание следующее:</span></p>
 
<p style="text-align: justify;"><span class="text">1. Необходимо учитывать перспективы экономического развития и расстановку его приоритетов. Это дает возможность прогнозировать структурную и объемную потребность в трудовых ресурсах экономики.</span></p>
 
<p style="text-align: justify;"><span class="text">2. Требуется формирование прогноза перспективного развития науки и техники. Освоение новых знаний позволит подготовить специалистов необходимого качества для будущей экономики.</span></p>
 
<p style="text-align: justify;"><span class="text">3. Перспективная потребность и выпуск будущих специалистов должны корректироваться с расчетом на экономику прогнозного периода. Свойство инерционности системы образования требует упреждения целевых установок как минимум на 10-15 лет.</span></p>
 
