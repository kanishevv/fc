<p> 
  <br />
 <b>Статья 19. Начальное общее, основное общее, среднее (полное) общее образование</b></p>
 
<p>1. Общее образование включает в себя три ступени, соответствующие уровням образовательных программ: начальное общее, основное общее, среднее (полное) общее образование.</p>
 
<p>2. Обучение детей в образовательных учреждениях, реализующих программы начального общего образования, начинается с достижения ими возраста шести лет шести месяцев при отсутствии противопоказаний по состоянию здоровья, но не позже достижения ими возраста восьми лет. По заявлению родителей (законных представителей) учредитель образовательного учреждения вправе разрешить прием детей в образовательные учреждения для обучения в более раннем возрасте.</p>
 
<p>Нормативные сроки освоения основных образовательных программ начального общего, основного общего и среднего (полного) общего образования определяются федеральным законом.</p>
 
<p>(п. 2 в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>3. Основное общее образование и государственная (итоговая) аттестация являются обязательными.</p>
 
<p>4. Требование обязательности основного общего образования применительно к конкретному обучающемуся сохраняет силу до достижения им возраста пятнадцати лет, если соответствующее образование не было получено обучающимся ранее.</p>
 
<p>5. Предельный возраст обучающихся для получения основного общего образования в общеобразовательном учреждении по очной форме обучения - восемнадцать лет. Для категорий обучающихся, указанных в пунктах 10 - 12 статьи 50 настоящего Закона, предельный возраст получения основного общего образования может быть увеличен.</p>
 
<p>6. По согласию родителей (законных представителей) и местного органа управления образованием обучающийся, достигший возраста пятнадцати лет, может оставить общеобразовательное учреждение до получения им основного общего образования.</p>
 
<p>7. По решению органа управления образовательного учреждения за совершение противоправных действий, грубые и неоднократные нарушения устава образовательного учреждения допускается исключение из данного образовательного учреждения обучающихся, достигших возраста четырнадцати лет. Решение об исключении детей-сирот и детей, оставшихся без попечения родителей (законных представителей), принимается с согласия органов опеки и попечительства.</p>
 
<p>Об исключении обучающегося из образовательного учреждения последнее обязано в трехдневный срок проинформировать органы местного самоуправления. Органы местного самоуправления совместно с родителями (законными представителями) исключенного в месячный срок принимают меры, обеспечивающие его трудоустройство или продолжение обучения в другом образовательном учреждении.</p>
 
<p>8. Общеобразовательные учреждения по договорам и совместно с предприятиями, учреждениями, организациями могут проводить профессиональную подготовку обучающихся в качестве дополнительных (в том числе платных) образовательных услуг при наличии соответствующей лицензии (разрешения) на указанный вид деятельности. Начальная профессиональная подготовка проводится только с согласия обучающихся и их родителей (законных представителей).</p>
 
