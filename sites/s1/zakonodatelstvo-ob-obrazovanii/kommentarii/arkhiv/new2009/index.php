<p><span class="text" style="text-align: justify;">1. Подготовка научно-педагогических работников осуществляется в аспирантуре (адъюнктуре) и докторантуре высших учебных заведений, образовательных учреждений дополнительного профессионального образования, научных организаций, а также путем прикрепления к таким образовательным учреждениям и научным организациям соискателей для подготовки и защиты диссертаций на соискание ученой степени кандидата наук или доктора наук либо путем перевода педагогических работников на должности научных работников для подготовки диссертаций на соискание ученой степени доктора наук.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 1 в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
