<div align="justify"><b><i>Цели и задачи НИР:</i></b></div>

<ul>
  <li> 1. Анализ основных теоретических понятий, категорий и конструкций мусульманского права, касающихся финансово-правовых институтов.</li>

  <li> 2. Изучение конкретных институтов исламского финансового права &ndash; собственности, казны, налогов, отдельных договоров, применяемых исламскими финансовыми учреждениями.</li>

  <li> 3. Обзор практики функционирования исламских финансовых институтов в истории мусульманских государств.</li>

  <li> 4. Анализ правовых аспектов современных теорий исламской экономики и исламских финансов.</li>

  <li> 5. Изучение современного законодательства мусульманских стран, регулирующего исламские финансовые институты.</li>

  <li> 6. Оценка опыта функционирования отдельных финансовых исламских институтов в России.</li>

  <li> 7. Анализ правовых аспектов сотрудничества России с международными и зарубежными исламскими финансовыми центрами.</li>
</ul>
 
<p align="justify"><b><i>Объект исследования: </i></b>система институтов исламского финансового права</p>
 
<p align="justify"><b><i>Основные результаты:</i></b> текст, полностью готовый к публикации в качестве научной статьи &laquo;Мусульманское право собственности: юридическое осмысление религиозных постулатов&raquo; </p>
 
<div align="justify"><b><i>Новизна, научная и практическая значимость проекта и его результатов:</i></b></div>
 
<div align="justify">В рамках проекта было проведено исследование теоретических основ ведущих институтов исламского финансового права, а также современного законодательства мусульманских стран, регулирующего данные институты. Были подвергнуты анализу отдельные категории, понятия и конструкции мусульманского права, имеющие ключевое значение для финансово-правовых институтов. Изучались мусульманско-правовые концепции собственности, казны, налогов. Особый акцент в проекте был сделан на анализе исламских финансовых институтов, действующих в современном мире, включая налог &laquo;закят&raquo; и исламские банки. В ходе исследования было определено место финансовых институтов в современных концепциях исламской экономики. Особое внимание уделялось анализу современного законодательства мусульманских стран, посвященного исламским финансовым институтам. Была изучена практика создания и функционирования отдельных исламских финансовых институтов (банков, страховых компаний) в России. Проведенное исследование было направлено на подготовку обоснования основных целей, направлений и форм сотрудничества России с международными и зарубежными исламскими финансовыми институтами. В связи с этим изучались правовые акты организации и деятельности Исламского банка развития, созданного Организацией исламская конференция, а также исламских банков ряда стран, разрабатывающих планы сотрудничества с Россией.</div>
 
