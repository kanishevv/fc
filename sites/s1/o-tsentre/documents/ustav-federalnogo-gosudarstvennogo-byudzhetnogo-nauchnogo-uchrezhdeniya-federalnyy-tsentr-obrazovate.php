	<p align="center">
		 II. Предмет, цели и виды деятельности Центра
	</p>
	<p align="center">
 <br>
	</p>
	<p>
		 2.1. Предметом деятельности Центра является организация и проведение фундаментальных и прикладных научных исследований, научно-методическое и экспертное обеспечение правового регулирования образования.&nbsp;
	</p>
	<p>
		 2.2. Целью деятельности Центра является научное обеспечение развития и совершенствования российского законодательства в области образования, повышение эффективности и качества правового регулирования в сфере образования.&nbsp;
	</p>
	<p>
		 2.3. Основными видами деятельности Центра, осуществляемыми в рамках выполнения государственного задания, формируемого Учредителем&nbsp;<br>
		 (далее – государственное задание), являются:&nbsp;
	</p>
	<p>
		 1)&nbsp;&nbsp;научная деятельность в области права, педагогики и иных областях, оказывающих влияние на развитие и совершенствование образовательного законодательства в Российской Федерации;&nbsp;
	</p>
	<p>
		 2)&nbsp;&nbsp;организация проведения общественно-значимых мероприятий в сфере науки;&nbsp;
	</p>
	<p>
		 3)&nbsp;&nbsp;организационно-методическое и информационное обеспечение деятельности организаций, осуществляющих образовательную деятельность, в части применения образовательного законодательства.
	</p>
	<p>
		 4) информационно-технологическое обеспечение управления системой образования;
	</p>
	<p>
		 5) информационно-технологическое обеспечение образовательной деятельности;
	</p>
	<p>
		 6) методическое обеспечение образовательной деятельности;
	</p>
	<p>
		 7) организация и проведение олимпиад, конкурсов, мероприятий, направленных на выявление и развитие у обучающихся интеллектуальных и творческих способностей, способностей к занятиям физической культурой и спортом, интереса к научной (научно-исследовательской) деятельности, творческой деятельности, физкультурно-спортивной деятельности.<br>
	</p>
	<p>
		 2.4. Центр вправе осуществлять приносящую доход деятельность, предусмотренную настоящим уставом, лишь постольку, поскольку это служит достижению целей, ради которых он создан, и если это соответствует таким целям, а именно:<br>
	</p>
	<p>
		 1)&nbsp;&nbsp;выполнение научно-исследовательских работ сверх государственного задания;&nbsp;
	</p>
	<p>
		 2)&nbsp;&nbsp;оказание консультационных, информационных, экспертных услуг в установленной настоящим уставом сфере деятельности Центра;&nbsp;
	</p>
	<p>
		 3)&nbsp;&nbsp;анализ правоприменительной практики в сфере образования;
	</p>
	<p>
		 4)&nbsp;&nbsp;анализ эффективности применения законодательства об образовании на федеральном уровне и в субъектах Российской Федерации;
	</p>
	<p>
		 5)&nbsp;&nbsp;подготовка предложений по совершенствованию нормативных правовых актов в сфере образования;
	</p>
	<p>
		 6)&nbsp;&nbsp;проведение мероприятий по систематизации нормативных правовых актов об образовании;
	</p>
	<p>
		 7)&nbsp;&nbsp;информирование, консультирование населения по вопросам образовательного законодательства; размещение материалов правового характера по вопросам образования и базы данных нормативных правовых актов в сфере образования на электронном информационном ресурсе Центра;&nbsp;
	</p>
	<p>
		 8)&nbsp;&nbsp;участие в реализации мер по повышению правовой культуры работников сферы образования; распространение правовых знаний о законодательстве об образовании;
	</p>
	<p>
		 9)&nbsp;&nbsp;создание и управление правами на результаты интеллектуальной деятельности;&nbsp;
	</p>
	<p>
		 10)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;осуществление в установленном порядке издательско-полиграфической деятельности, выпуск, распространение и реализация печатной и аудиовизуальной продукции, информационных и других материалов, связанных с деятельностью Центра и созданных за счет средств, полученных Центром от приносящей доход деятельности;&nbsp;
	</p>
	<p>
		 11)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;организация и проведение конференций, семинаров и иных научных мероприятий, в том числе международных;&nbsp;
	</p>
	<p>
		 12)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;сдача в аренду имущества Центра;
	</p>
	<p>
		 13)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;оказание платных образовательных услуг в сфере дополнительного профессионального образования в соответствии с законодательством Российской Федерации;&nbsp;
	</p>
	<p>
		 14)&nbsp;&nbsp;реализация производимых продукции и услуг.&nbsp;
	</p>
	<p>
		 2.5. Право Центра осуществлять деятельность, для занятия которой необходимо получение специального разрешения (лицензии), возникает с момента получения такого разрешения (лицензии) или в указанный в нем срок и прекращается при прекращении действия разрешения (лицензии).
	</p>
	<p>
 <br>
	</p>
	<p align="center">
		 III. Организация деятельности и управление Центром
	</p>
	<p align="center">
 <br>
	</p>
	<p>
		 3.1. Управление Центом осуществляется в соответствии с законодательством Российской Федерации и настоящим уставом.
	</p>
	<p>
		 3.2. Компетенция Учредителя установлена настоящим уставом, а также федеральными законами и нормативными правовыми актами Президента Российской Федерации и Правительства Российской Федерации.&nbsp;
	</p>
	<p>
		 3.3. Центр возглавляет Директор, который является единоличным исполнительным органом управления Центра.&nbsp;
	</p>
	<p>
		 Назначение на должность и освобождение от должности директора Центра, заключение, изменение и прекращение трудового договора с ним осуществляется Учредителем в соответствии с трудовым законодательством и иными содержащими нормы трудового права нормативными правовыми актами.&nbsp;
	</p>
	<p>
		 3.4. Директор Центра:&nbsp;
	</p>
	<p>
		 1)&nbsp;&nbsp;осуществляет текущее руководство научной, финансово-хозяйственной и иной деятельностью Центра в соответствии с законодательством Российской Федерации и настоящим уставом;&nbsp;
	</p>
	<p>
		 2)&nbsp;&nbsp;без доверенности действует от имени Центра, представляет его интересы в органах государственной власти и органах местного самоуправления и во взаимоотношениях с юридическими и физическими лицами;&nbsp;
	</p>
	<p>
		 3)&nbsp;&nbsp;определяет структуру и утверждает штатное расписание Центра;&nbsp;
	</p>
	<p>
		 4)&nbsp;&nbsp;утверждает положения о структурных подразделениях Центра и другие локальные акты Центра;&nbsp;
	</p>
	<p>
		 5)&nbsp;&nbsp;назначает заместителей директора Центра и распределяет обязанности между ними;&nbsp;
	</p>
	<p>
		 6)&nbsp;&nbsp;осуществляет прием на работу и увольнение работников Центра;&nbsp;
	</p>
	<p>
		 7)&nbsp;&nbsp;издает приказы и распоряжения, обязательные для всех работников Центра;&nbsp;
	</p>
	<p>
		 8)&nbsp;&nbsp;распоряжается средствами и имуществом Центра в пределах своей компетенции и в соответствии с законодательством Российской Федерации и настоящим уставом;&nbsp;
	</p>
	<p>
		 9)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;утверждает план финансово-хозяйственной деятельности Центра в порядке, определяемом Учредителем;&nbsp;
	</p>
	<p>
		 10)&nbsp;&nbsp;выдает доверенности, заключает государственные контракты и иные гражданско-правовые договоры, совершает иные сделки в соответствии с законодательством Российской Федерации и настоящим уставом;&nbsp;
	</p>
	<p>
		 11)&nbsp;&nbsp;открывает лицевые счета в территориальных органах Федерального казначейства в соответствии с законодательством Российской Федерации;&nbsp;
	</p>
	<p>
		 12)&nbsp;&nbsp;подписывает совместно с главным бухгалтером финансовые документы;&nbsp;
	</p>
	<p>
		 13)&nbsp;&nbsp;осуществляет в пределах своей компетенции иные полномочия в соответствии с законодательством Российской Федерации и настоящим уставом.&nbsp;
	</p>
	<p>
		 3.5. Директор несет персональную ответственность за выполнение возложенных на Центр целей и задач, соблюдение финансовой дисциплины, сохранность имущества Центра, организацию ведения бухгалтерского учета, хранение документов бухгалтерского учета, соблюдение трудовых прав работников Центра, а также соблюдение и исполнение законодательства Российской Федерации.&nbsp;
	</p>
	<p>
		 3.6. Директор Центра имеет право делегировать осуществление отдельных полномочий заместителям директора Центра и другим работникам Центра.
	</p>
	<p>
		 3.7. Руководство отдельными направлениями деятельности Центра осуществляют заместители директора по направлениям деятельности. Распределение обязанностей между заместителями, их полномочия и ответственность устанавливаются приказом директора Центра. Приказ доводится до сведения всего коллектива Центра. Заместители директора Центра несут ответственность перед директором Центра за состояние дел порученных им направлений работы.
	</p>
	<p>
		 Заместители директора Центра&nbsp;принимаются на работу директором Центра по срочному трудовому договору, срок окончания которого не может превышать срока окончания полномочий директора Центра.
	</p>
	<p>
		 3.8.&nbsp;&nbsp;&nbsp;&nbsp;Ученый совет Центра является коллегиальным органом Центра, осуществляющим научно-методическое и информационно-аналитическое обеспечение научной деятельности&nbsp;&nbsp;Центра.
	</p>
	<p>
		 3.9.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;В состав ученого совета Центра входят директор Центра, заместители директора Центра и руководители структурных подразделений Центра.
	</p>
	<p>
		 3.10. Председателем ученого совета Центра является директор Центра.
	</p>
	<p>
		 Заседания ученого совета&nbsp;Центра&nbsp;проводятся не реже чем 1 раза в квартал.
	</p>
	<p>
		 Решение ученого совета&nbsp;Центра&nbsp;считается принятым, если за него проголосовало большинство его членов, присутствующих на заседании, при явке не менее 50 процентов списочного состава ученого совета, если иное не установлено законодательством Российской Федерации и настоящим уставом. При равенстве голосов, голос председателя ученого совета является решающим.
	</p>
	<p>
		 3.12. Решения ученого совета Центра вступают в силу с даты их подписания&nbsp;председателем ученого совета Центра.
	</p>
	<p>
		 3.13. Порядок организации работы ученого совета Центра определяется регламентом работы ученого совета Центра, утверждаемым директором Центра.
	</p>
	<p>
		 3.14. К компетенции ученого совета Центра относятся:
	</p>
	<p>
		 1)&nbsp;&nbsp;разработка и утверждение планов научных работ и развития Центра;
	</p>
	<p>
		 2)&nbsp;&nbsp;определение основных перспективных направлений развития научной деятельности&nbsp;Центра;
	</p>
	<p>
		 3)&nbsp;&nbsp;обсуждение плана финансово-хозяйственной деятельности и внесение в него предложений;
	</p>
	<p>
		 4)&nbsp;&nbsp;рассмотрение и принятие решений по вопросам методической, организационной, информационно-аналитической деятельности&nbsp;Центра, а также по вопросам международного сотрудничества&nbsp;Центра;
	</p>
	<p>
		 5)&nbsp;&nbsp;утверждение планов работы ученого совета&nbsp;Центра;
	</p>
	<p>
		 6)&nbsp;&nbsp;рассмотрение отчетов руководителей структурных подразделений&nbsp;Центра;
	</p>
	<p>
		 7)&nbsp;&nbsp;утверждение положений о научно-исследовательских и иных структурных подразделениях Центра;
	</p>
	<p>
		 8)&nbsp;&nbsp;рассмотрение вопросов о представлении работников&nbsp;Центра&nbsp;к награждению государственными наградами Российской Федерации и присвоению им почетных званий;
	</p>
	<p>
		 9)&nbsp;&nbsp;принятие решений по другим вопросам, отнесенным к компетенции ученого совета&nbsp;Центра, в соответствии с законодательством Российской Федерации, настоящим уставом и локальными нормативными актами&nbsp;Центра.
	</p>
	<p>
		 3.15. По вопросам, отнесенным к его компетенции, ученый совет Центра вправе принимать локальные нормативные акты Центра в порядке, установленном настоящим уставом.
	</p>
