<div align="justify"><b>Статья L. 238-1</b></div>
 
<p align="justify"> </p>
 
<p align="justify">Состав и полномочия Национального Совета сельскохозяйственного образования устанавливаются положениями статей L. 814-1 и L.814-2 Кодекса сельского хозяйства, воспроизводимых ниже:</p>
 
<p align="justify"> </p>
 
<p align="justify">&laquo;Ст. L.814-1. Национальный Совет сельскохозяйственного образования имеет в качестве президента министра сельского хозяйства и состоит из шестидесяти четырех членов, распределенных следующим образом:</p>
 
<p align="justify"> </p>
 
<p align="justify">1. a) восемь представителей Государства;</p>
 
<p align="justify"> </p>
 
<p align="justify">b) три представителя региона;</p>
 
<p align="justify"> </p>
 
<p align="justify">c) три представителя заинтересованных публичных учреждений;</p>
 
<p align="justify"> </p>
 
<p align="justify">d) шесть представителей ассоциаций и ответственных органов частных учреждений сельскохозяйственного образования, заключивших контракт с Государством, и их представительных федераций.</p>
 
<p align="justify"> </p>
 
<p align="justify">2. Двадцать представителей представительных профсоюзных организаций публичных и частных учреждений сельскохозяйственного образования, из которых, по меньшей мере пять представляют представительные профсоюзные организации персонала частных учреждений сельскохозяйственного образования, заключивших контракт с Государством.</p>
 
<p align="justify"> </p>
 
<p align="justify">3. а) Десять представителей представительных организаций родителей учащихся сельскохозяйственного образования, из которых, по меньшей мере два представляют представительные организации родителей учащихся частных учреждений сельскохозяйственного образования, заключивших контракт с Государством, и один представляет представительные национальные организации сельскохозяйственных семейных ассоциаций;</p>
 
<p align="justify"> </p>
 
<p align="justify">b) Десять представителей представительных профессиональных и профсоюзных организаций сельскохозяйственных работодателей, предпринимателей и наемных работников.</p>
 
<p align="justify"> </p>
 
<p align="justify">4. Четыре представителя учащихся и студентов.</p>
 
<p align="justify"> </p>
 
<p align="justify">Этот совет может также включать для консультативных целей и в пределах одной десятой числа его членов лиц, назначенных в силу их компетентности, в том числе в области научных исследований и пара-сельскохозяйственной деятельности.</p>
 
<p align="justify"> </p>
 
<p align="justify">Национальный Совет сельскохозяйственного образования обеспечивает представительство сельскохозяйственного образования в составе Высшего Совета образования.</p>
 
<p align="justify"> </p>
 
