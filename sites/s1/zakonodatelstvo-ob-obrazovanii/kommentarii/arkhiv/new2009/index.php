<p><span class="text" style="text-align: justify;">Статья 24. Полномочия федеральных органов государственной власти в сфере высшего и послевузовского профессионального образования</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 29.12.2006 № 258-ФЗ)</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. К полномочиям Федерального Собрания Российской Федерации в сфере высшего и послевузовского профессионального образования относятся:</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">1) принятие федеральных законов в области высшего и послевузовского профессионального образования и внесение изменений в соответствующие законы;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2) - 3) утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</span></p>
 
