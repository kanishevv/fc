<p>3. В случае введения субъектами Российской Федерации регионального компонента государственного образовательного стандарта финансирование связанных с этим затрат на бесплатное обучение студентов производится за счет средств бюджетов соответствующих субъектов Российской Федерации.</p>
 
<p>4. В пределах имеющихся внебюджетных средств образовательные учреждения самостоятельно могут оказывать социальную поддержку нуждающимся студентам.</p>
 
<p>(п. 4 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>5. Государственные образовательные учреждения самостоятельно определяют направления и порядок использования своих бюджетных и внебюджетных средств, в том числе их долю, направляемую на оплату труда и материальное стимулирование работников образовательных учреждений.</p>
 
<p>6. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
