<p><span class="text" style="text-align: justify;">Статья 8. Высшее учебное заведение, его задачи и структура</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Высшим учебным заведением является образовательное учреждение, учрежденное и действующее на основании законодательства Российской Федерации об образовании, имеющее статус юридического лица и реализующее в соответствии с лицензией образовательные программы высшего профессионального образования.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Основными задачами высшего учебного заведения являются:</span></p>
 
<p><span class="text" style="text-align: justify;">1) удовлетворение потребностей личности в интеллектуальном, культурном и нравственном развитии посредством получения высшего и (или) послевузовского профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">2) развитие наук и искусств посредством научных исследований и творческой деятельности научно-педагогических работников и обучающихся, использование полученных результатов в образовательном процессе;</span></p>
 
<p><span class="text" style="text-align: justify;">3) подготовка, переподготовка и повышение квалификации работников с высшим образованием и научно-педагогических работников высшей квалификации;</span></p>
 
<p><span class="text" style="text-align: justify;">4) формирование у обучающихся гражданской позиции, способности к труду и жизни в условиях современной цивилизации и демократии;</span></p>
 
<p><span class="text" style="text-align: justify;">5) сохранение и приумножение нравственных, культурных и научных ценностей общества;</span></p>
 
<p><span class="text" style="text-align: justify;">6) распространение знаний среди населения, повышение его образовательного и культурного уровня.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Высшие учебные заведения самостоятельны в формировании своей структуры, за исключением их филиалов.</span></p>
 
<p><span class="text" style="text-align: justify;">Структурные подразделения высшего учебного заведения могут наделяться по доверенности полностью или частично правомочиями юридического лица в порядке, предусмотренном уставом высшего учебного заведения. Статус и функции структурного подразделения высшего учебного заведения определяются положением, утвержденным в порядке, предусмотренном уставом высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">Структурные подразделения высшего учебного заведения могут реализовывать образовательные программы начального общего, основного общего, среднего (полного) общего, начального профессионального и среднего профессионального образования, а также образовательные программы дополнительного образования при наличии у высшего учебного заведения соответствующей лицензии.</span></p>
 
<p><span class="text" style="text-align: justify;">Филиалами высших учебных заведений являются обособленные структурные подразделения, расположенные вне места его нахождения.</span></p>
 
<p><span class="text" style="text-align: justify;">Филиалы федеральных государственных высших учебных заведений создаются учредителем по согласованию с федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования, соответствующими органами исполнительной власти субъектов Российской Федерации и органами местного самоуправления по месту нахождения филиала.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Типовое положение (положение) о филиалах федеральных государственных высших учебных заведений и порядок их организации разрабатываются и утверждаются федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Абзацы седьмой - восьмой утратили силу. - Федеральный закон от 20.04.2007 № 56-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">Показатели деятельности филиалов высших учебных заведений учитываются при государственной аккредитации таких высших учебных заведений.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 20.04.2007 № 56-ФЗ)</span></p>
 
