?><b>Статья L. 211-1</b> 
<p> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Образование есть публичная служба при сохранении полномочий, приданных территориальным объединениям.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L. 211-2</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Каждый год полномочные органы Государства утверждают общую педагогическую структуру образовательных учреждений второй ступени, учитывая прогнозируемую схему видов подготовки, упомянутую в статье L.214-1. Представитель Государства утверждает ежегодный список операций по строительству или расширению образовательных учреждений, в которых Государство обязуется финансировать штатные должности, которые оно считает необходимыми для их административного и педагогического функционирования. Этот список утверждается с учетом прогнозируемой программы инвестиций и по получении согласия соответствующей коммуны и полномочного территориального объединения.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.211-3</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Государство может создавать в исключительных случаях публичные образовательные учреждения первой и второй ступени, собственность которых на законных основаниях передается полномочному территориальному объединению на основании настоящей статьи.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Подобное создание образовательных учреждений допускается только в случае, когда полномочное территориальное объединение отказывается обеспечивать должную организацию публичной службы. В том, что касается образовательных учреждений второй ступени, это должно соответствовать прогнозируемой схеме видов подготовки, предусмотренной в статье L.214-1.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Сумма кредитов, выделяемых на это строительство, определяется каждый год Законом о бюджете.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Декрет Государственного Совета устанавливает условия применения настоящей статьи, и именно условия, в которых представитель Государства может осуществлять приобретения, разрешать строительство и осуществлять работу.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.211-4</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Во изменение положений статей L.212-4, L.212-2 и L.214-6 декрет устанавливает список образовательных учреждений, ответственность за которые и содержание которых полностью возлагаются на Государство.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.211-5</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Государство несет ответственность за образовательные учреждения, относящиеся к Министерству образования, Министерству юстиции и Министерству иностранных дел.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.211-6</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Государство после консультаций с территориальными объединениями, которые имеют отношение к проектам, реализуемым на их территории, устанавливает размещение и оснащение учреждений высшего образования.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.211-7</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">В соответствии с картой видов подготовки высшего образования, учрежденной статьей L.614-3, Государство может доверить территориальным объединениям или их группам строительство или расширение учреждений высшего образования, находящихся в ведении министра, ответственного за высшее образование, или министра сельского хозяйства.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">В этих целях Государство заключает соглашения с заинтересованным территориальным объединением или группой объединений; это соглашение уточняет, в частности, место размещения одного или нескольких возводимых сооружений, техническую программу строительства и финансовые обязательства сторон.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Эти обязательства относятся только к инвестиционным расходам и учитывают в случае необходимости недвижимые вклады территориальных объединений.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Территориальные объединения или их группы пользуются компенсационными фондами для тарифа на добавочную стоимость в рамках расходов, произведенных во исполнение первого абзаца настоящей Статьи.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify;"><b>Статья L.211-8</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Государство обеспечивает:</p>
 
<p style="text-align: justify;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">1. Оплату преподавательского персонала начальных школ и детских садов с дошкольной подготовкой, созданных согласно статье L.212-1, за исключением случаев, предусмотренных в статье L.216-1.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">2. Оплату персонала администрации и инспекции.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">3. Оплату персонала колледжей, за исключением случаев, предусмотренных статьей L.216-1.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">4. Оплату персонала лицеев, за исключением случаев, предусмотренных статьей L.216-1.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">5. Педагогические расходы колледжей, лицеев и учреждений специального образования, список которых установлен декретом.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">6. Оплату персонала высшего образования и научных исследований.</p>
