 <b>Статья 52. Права и обязанности родителей (законных представителей)</b></p>
 
<p>1. Родители (законные представители) несовершеннолетних детей до получения последними основного общего образования имеют право выбирать формы обучения, образовательные учреждения, защищать законные права и интересы ребенка, принимать участие в управлении образовательным учреждением.</p>
 
<p>2. Родители (законные представители) обучающихся, воспитанников обязаны обеспечить получение детьми основного общего образования.</p>
 
<p>(п. 2 введен Федеральным законом от 25.06.2002 № 71-ФЗ)</p>
 
<p>3. Родители (законные представители) обучающихся, воспитанников обязаны выполнять устав образовательного учреждения.</p>
 
<p>4. Родители (законные представители) имеют право дать ребенку начальное общее, основное общее, среднее (полное) общее образование в семье. Ребенок, получающий образование в семье, вправе на любом этапе обучения при его положительной аттестации по решению родителей (законных представителей) продолжить образование в образовательном учреждении.</p>
 
<p>5. Родители (законные представители) обучающихся, воспитанников несут ответственность за их воспитание, получение ими основного общего образования.</p>
 
