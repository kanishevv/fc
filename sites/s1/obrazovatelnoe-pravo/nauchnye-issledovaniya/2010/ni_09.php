<div><b><i>Цели и задачи НИР:</i></b> Развитие научно-методической и нормативной базы введения профильного обучения на основе индикативного планирования. Научно-методическое обеспечение реализации Постановления Правительства Российской Федерации от 09.06.2003 №334 &laquo;О проведении эксперимента по введению профильного обучения обучающихся в общеобразовательных учреждениях, реализующих программы среднего (полного) общего образования&raquo;.</div>
 
<br />
 
<div><b><i>Объект исследования:</i></b> система индикативного планирования введения профильного обучения в Российской Федерации.</div>
 
<br />
 
<div><b><i>Основные результаты:</i></b></div>
 
<div>аналитическая справка &laquo;Нормативное, методическое и организационное обеспечения готовности субъектов Российской Федерации к введению профильного обучения&raquo;; концепция индикативного планирования введения профильного обучения;</div>
 
<div>система индикаторов готовности общеобразовательного учреждения к введению профильного обучения и методические рекомендации по ее использованию;</div>
 
<div>система индикаторов готовности муниципального образования к введению профильного обучения и методические рекомендации по ее использованию;</div>
 
<div>система индикаторов готовности субъекта Российской Федерации к введению профильного обучения и методические рекомендации по ее использованию;</div>
 
<div>подготовка и издание книги &laquo;Определение готовности региональной, муниципальной и школьной систем образования к введению профильного обучения&raquo;.</div>
 
<div>Аналитическая справка &laquo;Нормативное, методическое и организационное обеспечения готовности субъектов Российской Федерации к введению профильного обучения&raquo; содержит основные выводы из анализа готовности субъектов Российской Федерации к введению профильного обучения на материале 10 регионов &ndash; участников эксперимента. В справке показаны состояние, достижения и проблемы в области нормативно-правового, методического и организационного обеспечения перехода на профильное обучение, в частности &ndash; сформировано представление о составе пакета нормативных документов регионального и муниципального уровней, необходимых для регламентации и поддержки введения профильного обучения.</div>
 
<div>Концепция индикативного планирования введения профильного обучения с опорой на проведенный анализ ситуации в экспериментальных и иных регионах, а также на изучение теоретических основ индикативного планирования, содержит интерпретацию ключевых понятий и подход к системе индикативного планирования введения профильного обучения. В Концепции обоснованы важнейшие оцениваемые параметры введения профильного обучения и готовности к такому введению, обозначены возможные индикаторы для оценки готовности к введению профильного обучения, предложен формат индикативного плана, объединяющий идеи стратегического, программно-целевого и индикативного планирования.</div>
 
<div>На основе положений Концепции индикативного планирования введения профильного обучения для трех уровней системы образования: регионального, муниципального и школьного, были предложены и обоснованы системы индикаторов готовности к введению профильного обучения и методические рекомендации по их использованию, позволяющие организаторам профильного обучения своевременно оценить состояние готовности своих образовательных систем или учреждений к переходу на профильное обучение и принять соответствующие организационно-управленческие решения.</div>
 
<div>Полученные научные результаты обобщены в работе &laquo;Определение готовности региональной, муниципальной и школьной систем образования к введению профильного обучения&raquo;.</div>
 
<br />
 
<div><b><i>Новизна, научная и практическая значимость проекта и его результатов:</i></b></div>
 
<div><b>Новизна проекта и его результатов</b> проявляется в том, что в рамках проекта впервые в отечественной практике разработаны концепция индикативного планирования введения профильного обучения, являющегося одним из приоритетов модернизации системы общего образования. Разработаны индикаторы готовности региона, муниципального образования, образовательного учреждения к введению профильного обучения и методические рекомендации по их использованию, что в совокупности представляет собой основы системы индикативного планирования введения профильного обучения.</div>
 
<div><b>Научная значимость проекта и его результатов</b> проявляется в том, что предложенные подходы доказывают перспективность индикативного планирования и могут быть успешно перенесены на другие значимые области планирования в образовании.</div>
 
<div><b>Практическая значимость проекта и его результатов</b> проявляется в том, что предложенные методические рекомендации по индикативному планированию и использованию спроектированных систем индикаторов образуют реальную основу для проведения оценки готовности образовательных систем и учреждений к введению профильного обучения и принятию на основе такой оценки адекватных управленческих решений, обеспечивающих планомерность, управляемость и эффективность перехода на профильное обучение, поддержание высокого уровня мотивации участников работы, рациональное использование и экономию времени и ресурсов.</div>
 
