  <p><span class="text">Департамент образования и науки субъекта Российской Федерации является органом исполнительной власти субъекта РФ, осуществляющим государственную политику в области образования, подготовки и аттестации педагогических кадров, координирующим деятельность муниципальных органов управления образованием и учреждений системы образования. </span></p>
 
  <p><span class="text">В официальных, финансовых и иных документах может быть использовано сокращенное наименование департамента. Департамент образуется Законом КК субъекта РФ &laquo;О структуре высшего исполнительного органа государственной власти субъекта РФ администрации субъекта РФ и системы исполнительных органов государственной власти субъекта РФ&raquo;[8].</span></p>
 
  <p><span class="text">Департамент в своей деятельности руководствуется Конституцией Российской Федерации, федеральными законами, указами и распоряжениями Президента Российской Федерации, постановлениями и распоряжениями Правительства Российской Федерации, иными нормативными правовыми актами Российской Федерации, законами и Уставом субъекта РФ, распоряжениями Главы Администрации (Губернатора) субъекта РФ, постановлениями и распоряжениями Администрации субъекта РФ.</span></p>
 
  <p><span class="text">Положение о департаменте утверждается постановлением Главы администрации субъекта РФ. Департамент осуществляет свою деятельность во взаимодействии с федеральными и органами исполнительной и законодательной власти субъекта РФ, органами местного самоуправления, общественными организациями. </span></p>
 
  <p><span class="text">Департамент является юридическим лицом, имеет свой расчетный, валютный и иные счета в банковских учреждениях, печати с изображением герба субъекта РФ, а также соответствующие бланки и штампы. Департамент является распорядителем средств федерального и краевого бюджетов, выделяемых департаменту на финансирование подведомственных учреждений, централизованных бюджетных и внебюджетных средств. </span></p>
 
  <p><span class="text">Имущество департамента является собственностью субъекта РФ и передается департаменту образования и науки на праве оперативного управления. </span></p>
 
  <p><span class="text">Структура департамента:</span></p>
 
  <p><span class="text">Департамент возглавляет руководитель департамента образования и науки субъекта РФ (далее по тексту - руководитель), назначаемый на должность и освобождаемый от должности постановлением Главы Администрации (Губернатора) субъекта РФ по представлению заместителя Главы Администрации (Губернатора) субъекта РФ, курирующего направление деятельности департамента. </span></p>
 
  <p><span class="text">Руководитель департамента имеет двух заместителей, в том числе одного первого. Заместители руководителя назначаются на должность Главой Администрации (Губернатором) субъекта РФ. Штатное расписание и численность работников утверждаются постановлением Администрации субъекта РФ. </span></p>
 
  <p><span class="text">Руководитель департамента осуществляет управление департаментом на основе единоначалия и несет персональную ответственность за выполнение возложенных на департамент задач и функций. </span></p>
 
  <p><span class="text">Организация работы персонала и взаимодействие подразделений департамента осуществляются руководителем департамента непосредственно или первым заместителем руководителя в соответствии с должностной инструкцией. </span></p>
 
  <p><span class="text">В департаменте образовывается коллегия в составе руководителя департамента (председатель коллегии) и членов коллегии. Положение о коллегии и персональный состав коллегии утверждаются постановлением Администрации края. </span></p>
 
  <p><span class="text">Руководитель департамента: </span></p>
 
  <p><span class="text">- Распределяет обязанности и устанавливает степень ответственности заместителей руководителя, начальников структурных подразделений аппарата департамента и руководителей отделом и управлений, подведомственных департаменту. </span></p>
 
  <p><span class="text">- Руководитель департамента издает в пределах своей компетенции приказы и распоряжения, дает инструкции и указания, подлежащие обязательному исполнению работниками аппарата департамента, подведомственными образовательными и иными учреждениями, организациями, и проверяет их исполнение; в необходимых случаях, совместно с руководителями других органов исполнительной власти края, издает приказы, инструкции. </span></p>
 
  <p><span class="text">- Назначает на должность и освобождает от должности начальников структурных подразделений департамента после согласования их кандидатур с заместителем Главы Администрации (Губернатора) субъекта РФ по организационной, кадровой работе и вопросам местного самоуправления, а также работников аппарата департамента. </span></p>
 
  <p><span class="text">- Назначает и освобождает от должности в установленном порядке руководителей подведомственных государственных учреждений. </span></p>
 
  <p><span class="text">- Утверждает сметы расходов образовательных учреждений, подведомственных департаменту. </span></p>
 
  <p><span class="text">- Утверждает регламент департамента, положения, подписывает договоры, лицензии, свидетельства об аккредитации, аттестационные листы, выдает доверенности, иные акты и документы, издание которых необходимо для надлежащей организации деятельности департамента. </span></p>
 
  <p><span class="text">- Представляет интересы департамента и сферы образования и науки субъекта РФ в государственных, общественных и других организациях, в том числе международных. </span></p>
 
  <p><span class="text">- Вносит в установленном порядке на рассмотрение проекты нормативных и правовых актов по вопросам образования в Администрацию субъекта РФ и Законодательное Собрание края. </span></p>
 
  <p><span class="text">- Подписывает договоры, соглашения о сотрудничестве, совместной деятельности с заинтересованными ведомствами;</span></p>
 
  <p><span class="text">- Имеет право первой подписи на банковских и финансовых документах.</span></p>
 
  <p><span class="text">Деятельность руководителя в системе управления особая. Он реализует интересы и установки государства и общества, являясь полномочным представителем целостной системы управления. С другой стороны, подлинный руководитель &ndash; лидер коллектива, способный выявлять, выражать и защищать интересы своей организации. В его деятельности смыкаются централизованные и демократические начала управления. Руководитель, опираясь на аппарат управления, на общественные организации, с их помощью направляет деятельность аппарата управления на выполнение задач, поставленных обществом. Он координирует и направляет работу всех функциональных служб управления, охватывает проблемы в целом, во всех их аспектах.</span></p>
 
  <p><span class="text">Анализ изменений, происходящих в организационных структурах управления на уровне субъектов Федерации, позволяет выделить ряд тенденций в их модернизации: </span></p>
 
  <p><span class="text">- создаются новые подразделения, нацеливаемые на выполнение вновь возникших функций управления &ndash; подразделения по реабилитации учащихся, экономике образования, защиты детства, развития образования и инноватики, альтернативному и негосударственному образованию, связям с общественностью, международным связям;</span></p>
 
  <p><span class="text">- в структуре органов управления или при этих органах создаются различные функциональные службы (аттестационные, инспекторские, диагностические), причем их руководители часто имеют высокий статус в общей управленческой иерархии;</span></p>
 
  <p><span class="text">- создаются службы, специально занимающиеся координацией работы подразделений органа управления, связей с другими структурами;</span></p>
 
  <p><span class="text">- создаются подразделения, концентрирующие в своем составе специалистов, курирующих все аспекты управления отдельными сферами образования (т.е. все функциональные специалисты группируются вокруг общего объекта управления);</span></p>
 
  <p><span class="text">- создаются различные коллегиальные органы, чаще с координационными функциями (региональные советы по развитию образования, советы руководителей муниципальных органов управления образованием), экспертные советы.</span></p>
 
  <p><span class="text">Должности федеральной государственной гражданской службы, классифицированные по государственным органам, категориям, группам, а также по иным признакам, составляют перечни должностей федеральной государственной службы, являющиеся соответствующими разделами Реестр должностей федеральной государственной гражданской службы. Реестр должностей федеральной государственной гражданской службы утверждается Указом Президента Российской Федерации.</span></p>
 
  <p><span class="text">Реестр должностей федеральной государственной гражданской службы субъекта Российской Федерации утверждается законом или иным нормативным правовым актом субъекта Российской Федерации.</span></p>
 
  <p><span class="text">Муниципальные образования также принимают &laquo;Реестры выборных муниципальных должностей&raquo; и &laquo;Реестр муниципальных должностей муниципальной службы органов местного самоуправления&raquo; с учетом нормативных правовых актов субъекта Российской Федерации.</span></p>
 
  <p><span class="text">Рассмотрим специфику государственного управления в муниципальном образовании на примере Думы г. Армавира, входящей состав Краснодарского края (субъекта Федерации). Изменения в нормативных документах, регламентирующих государственное управление на трех уровнях, происходят по нисходящей траектории. В соответствии с Законом Краснодарского края &laquo;О муниципальной службе Краснодарского края&raquo; и &laquo;О реестре выборных муниципальных должностей и реестре муниципальных должностей муниципальной службы органов местного самоуправления в Краснодарском крае&raquo; внесены изменения в структуре Армавирской городской Думы<a name="_ftnref9" href="http://www.lexed.ru/pravo/notes/conf/?shkarlupina.html#_ftn9" >[9]</a>.</span></p>
 
