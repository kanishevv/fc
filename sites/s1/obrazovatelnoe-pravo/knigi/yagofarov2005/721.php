<p style="text-align: justify;">
	<span class="text">Воспитанник дошкольного образовательного учреждения также является субъектом образовательного процесса. Его содержание определяется программами дошкольного образования. Дошкольное учреждение самостоятельно выбирает их из комплекса вариативных программ, рекомендованных государственными органами управления образованием, вносит коррективы, а также разрабатывает собственные (авторские) программы в соответствии с требованиями государственного образовательного стандарта.</span>
</p>
<p style="text-align: justify;">
