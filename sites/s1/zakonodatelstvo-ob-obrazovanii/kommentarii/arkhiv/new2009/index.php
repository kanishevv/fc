<p><span class="text" style="text-align: justify;">Статья 26. Государственный контроль качества высшего и послевузовского профессионального образования</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Государственный контроль качества высшего и послевузовского профессионального образования направлен на обеспечение единой государственной политики в области высшего и послевузовского профессионального образования, повышение качества подготовки специалистов, рациональное использование бюджетных средств, выделяемых на финансирование системы высшего и послевузовского профессионального образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 31.12.2005 № 199-ФЗ, от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Государственный контроль качества высшего и послевузовского профессионального образования осуществляется федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Если по результатам контроля качества подготовки обучающихся и выпускников выявлено несоответствие качества подготовки федеральным государственным образовательным стандартам, федеральным государственным требованиям и устанавливаемым в соответствии с пунктом 4 статьи 5 настоящего Федерального закона образовательным стандартам и требованиям, федеральный орган исполнительной власти, осуществляющий функции по контролю и надзору в сфере образования, вправе направить в высшее учебное заведение, образовательное учреждение дополнительного профессионального образования, научную организацию и их учредителям обязательное для исполнения предписание об устранении указанного несоответствия.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 20.04.2007 № 56-ФЗ, в ред. Федерального закона от 01.12.2007 № 309-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Государственный контроль за соблюдением высшим учебным заведением, образовательным учреждением дополнительного профессионального образования, научной организацией условий образовательной деятельности, предусмотренных лицензией, и (или) законодательства Российской Федерации в области образования обеспечивается федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в сфере образования, с участием органов исполнительной власти субъекта Российской Федерации, на территории которого расположены такие образовательные учреждения или такая научная организация. В случае, если было допущено нарушение этих условий и (или) законодательства Российской Федерации в области образования, федеральный орган исполнительной власти, осуществляющий функции по контролю и надзору в сфере образования, направляет в такие образовательные учреждения или такую научную организацию и их учредителям обязательное для исполнения предписание об устранении выявленного нарушения в определенный срок.</span></p>
 
<p><span class="text" style="text-align: justify;">В случае неисполнения указанного предписания федеральный орган исполнительной власти, осуществляющий функции по контролю и надзору в сфере образования, вправе приостановить действие лицензии, выданной им высшему учебному заведению, образовательному учреждению дополнительного профессионального образования, научной организации, полностью или в части осуществления образовательной деятельности по отдельным образовательным программам с указанием срока устранения выявленного нарушения условий образовательной деятельности, предусмотренных лицензией, и (или) законодательства Российской Федерации в области образования.</span></p>
 
<p><span class="text" style="text-align: justify;">Если в установленный срок выявленное нарушение не будет устранено, лицензия подлежит аннулированию. Решение об аннулировании лицензии принимается федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в сфере образования, и может быть оспорено в суде.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 2 в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. Утратил силу. - Федеральный закон от 20.04.2007 № 56-ФЗ.</span></p>
 
