 </span><span class="text">6. Основания изменения и расторжения договора</span></b>
</p>
<p style="text-align: justify;">
 <span class="text">6.1.Условия, на которых заключен настоящий договор, могут быть изменены либо по соглашению сторон, либо в соответствии с дей­ствующим законодательством Российской Федерации.</span>
</p>
<p style="text-align: justify;">
 <span class="text">6.2.Настоящий договор может быть расторгнут по соглашению сто­рон. По инициативе одной из сторон договор может быть расторгнут по основаниям, предусмотренным действующим законодательством Российской Федерации, а также в случае одностороннего отказа от исполнения договора в случаях, предусмотренных законом и пункта­ми 6.3–6.5 настоящего договора.</span>
