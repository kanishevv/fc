<p style="text-align: justify;">
 <span class="text">6.3.Заказчик вправе в любое время с согласия Потребителя расторгнуть настоящий договор при условии оплаты Исполнителю факти­чески понесенных расходов и услуг, оказанных до момента отказа.</span>
