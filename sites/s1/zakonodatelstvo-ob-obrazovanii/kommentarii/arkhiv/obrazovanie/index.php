<p><b>Статья 57. Международное сотрудничество Российской Федерации</b></p>
 
<p>1. Международное сотрудничество Российской Федерации в области образования осуществляется в соответствии с законодательством Российской Федерации и международными договорами Российской Федерации.</p>
 
<p>Если международным договором Российской Федерации установлены иные правила, чем те, которые предусмотрены законодательством Российской Федерации, применяются правила международного договора.</p>
 
<p>2. Органы управления образованием, образовательные учреждения имеют право устанавливать прямые связи с иностранными предприятиями, учреждениями и организациями.</p>
 
<p>3. Обучение, подготовка и повышение квалификации иностранных граждан в образовательных учреждениях Российской Федерации, равно как и граждан Российской Федерации в иностранных образовательных учреждениях, осуществляются по прямым договорам, заключаемым образовательными учреждениями, ассоциациями, органами управления образованием, иными юридическими лицами, а также физическими лицами в соответствии с международными договорами Российской Федерации.</p>
 
