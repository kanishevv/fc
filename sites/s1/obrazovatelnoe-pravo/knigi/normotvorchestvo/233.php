<p style="text-align: justify;">
	<span class="text"> <br>
 </span><span class="text">Из анализа таблицы 1 видно, что достаточно полно определены права и гарантии в сфере образования, однако, многие из них остаются продекларированными из-за отсутствия механизмов реализации и финансовой необеспеченности этих гарантий, например,выбор формы образования в порядке и на условиях, установленных законодательством Российской Федерации и Свердловской области; обучение в соответствии с государственными образовательными стандартами по индивидуальному учебному плану, ускоренный курс обучения в порядке, определенном уставом образовательной организации; получение денежных средств для осуществления семейного образования; или«гарантии для молодых граждан в сфере труда, занятости, содействие их предпринимательской деятельности, предоставление им гарантированного минимума социальных услуг», «доступ молодежи к получению общего и профессионального образования, а также возможности для повышения квалификации и профессиональной переподготовки на основе государственных стандартов в сфере образования»; или право педагоговна ежегодное бесплатное медицинское обследование и т.д. Также дело обстоит и с законами более широкого спектра действия, чем только образовательные организации, например, вОбластном законе «Об управлении государственной собственностью» не урегулированы вопросы передачи объектов государственной собственности, находящихся в ведении РФ, в собственность субъекта РФ, что является значительной проблемой в ситуации, когда более 30% областных образовательных организаций являются федеральной собственностью и др.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Таким образом, анализ действующих законов Свердловской области позволяет сделать вывод о том, что для сферы образования остаются неурегулированными ряд вопросов, решение которых необходимо в целях развития системы образования на основе правоотношений.</span>
</p>
