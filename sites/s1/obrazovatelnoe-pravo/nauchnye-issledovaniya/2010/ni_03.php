<p align="justify">Разработка системы объективной диагностики учебных достижений выпускников основной школы в условиях конкурсного приема в профильные классы. </p>
 
<p align="justify"> Работа выполнена в 2004 г. в рамках научной отраслевой программы &laquo;Научно-методическое обеспечение функционирования и модернизации системы образования&raquo; </p>

