<p><b>Статья 45. Платные дополнительные образовательные услуги государственного и муниципального образовательных учреждений</b></p>
 
<p>1. Государственное и муниципальное образовательные учреждения вправе оказывать населению, предприятиям, учреждениям и организациям платные дополнительные образовательные услуги (обучение по дополнительным образовательным программам, преподавание специальных курсов и циклов дисциплин, репетиторство, занятия с обучающимися углубленным изучением предметов и другие услуги), не предусмотренные соответствующими образовательными программами и государственными образовательными стандартами.</p>
 
<p>2. Доход от указанной деятельности государственного или муниципального образовательного учреждения используется данным образовательным учреждением в соответствии с уставными целями.</p>
 
<p>(п. 2 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>3. Платные образовательные услуги не могут быть оказаны вместо образовательной деятельности, финансируемой за счет средств бюджета. В противном случае средства, заработанные посредством такой деятельности, изымаются учредителем в его бюджет. Образовательное учреждение вправе обжаловать указанное действие учредителя в суд.</p>
 
