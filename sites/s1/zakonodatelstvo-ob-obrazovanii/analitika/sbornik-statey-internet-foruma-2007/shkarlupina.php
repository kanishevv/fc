  <p><span class="text">Анализ состояния сложившейся образовательной практики и теоретических разработок с позиции системного подхода показывает, что система образования в регионах и муниципальных образованиях представляет собой динамичную, но слабо интегрированную систему, в которой инновационные процессы объединяют часть образовательных учреждений в учебно-научно-педагогические комплексы.</span></p>
 
  <p><span class="text">Следует отметить, что проникновение рыночных отношений в образовательную сферу выявило необходимость смены традиционных иерархических управленческих отношений в руководстве образовательными учреждениями. Инновационные образовательные процессы стали опережать нормативно-правовое регулирование в управлении новыми видами и формами образовательных отношений. </span></p>
 
  <p><span class="text">В систему государственного и муниципального управления образованием все больше внедряется маркетинговый подход, понимаемый в широком смысле, как способ изучения личных запросов населения в получении тех или иных запросов населения в получении тех или иных знаний и ориентации учебных заведений на удовлетворение выявленных потребностей путем:</span></p>
 
  <p><span class="text">- предоставление учащимся, их родителям возможности выбора образовательных предметов, учреждений;</span></p>
 
  <p><span class="text">- формирование рынка образовательных услуг;</span></p>
 
  <p><span class="text">- расширения разнообразной сети образовательных услуг;</span></p>
 
  <p><span class="text">- формирование как государственной, так и негосударственной сети дошкольных, школьных общеобразовательных учреждений, учреждений средне - специального профессионального и высшего профессионального образования и учреждений дополнительного образования и их многоканального и многоуровневого финансирования и т.д.</span></p>
 
