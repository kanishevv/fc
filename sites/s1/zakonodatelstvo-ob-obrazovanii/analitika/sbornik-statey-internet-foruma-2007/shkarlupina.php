  <p><span class="text"><b><i>Г.Д. Шкарлупина</i>, </b><i>канд. ист. наук, доцент кафедры истории России АГПУ</i></span></p>
 <span class="text"> </span> 
  <p align="right"> </p>
 <span class="text"> </span> 
  <p><span class="text">Административная реформа в Российской Федерации значительно изменила правовое положение таких основополагающих институтов, которыми являются государственное управление и государственная служба. Принятые в последнее время федеральные законы систематизировали государственную службу по видам и уровням, определили трехзвенную структуру федеральных органов исполнительной власти и разграничили функции федеральных министерств, федеральных служб и федеральных агентств.</span></p>
 <span class="text"> </span> 
  <p><span class="text">В результате кардинальной перестройки государственного аппарата произошли изменения компетенции государственных органов и значительное обновление функций государственного управления, в том числе и в отношении системы образования.</span></p>
 <span class="text"> </span> 
  <p><span class="text">В течение 2004 года в Бюллетене Министерства образования и науки Российской Федерации публиковались документы, изменяющие регламент работы государственных органов в области образования: &laquo;Положение о Министерстве образования и науки Российской Федерации&raquo;[1], &laquo;Положение о Федеральном агентстве по образованию&raquo;[2], &laquo;Положение о Федеральной службе по интеллектуальной собственности, патентам и товарным знакам&raquo;[3] и другие.</span></p>
 <span class="text"> </span> 
  <p><span class="text">Прошел незначительный период с момента реформирования государственных структур управления образованием и наукой. Государственные органы управления образованием и наукой находятся в очередной раз в стадии реформирования. Документы, регламентирующие этот процесс, продолжают изменять структуру управления. Это тормозит реформирование органов управления на уровне субъектов РФ. А так как образование и наука не могут прервать образовательно-научный процесс, то в течение 15 лет находятся в стадии слабого государственного управления, особенно на уровне субъектов РФ, а иногда и вне государственного правового регулирования (негосударственная система образования, обновляемый реестр образовательных учреждений, дополнительные образовательные услуги и др.).</span></p>
 <span class="text"> </span> 
  <p><span class="text">Каждый субъект Российской Федерации имеет свою структуру государственной исполнительной власти. Она формируется на основании закона субъекта Федерации об исполнительных органах государственной власти.</span></p>
 <span class="text"> </span> 
  <p><span class="text">Административная реформа в субъектах Российской Федерации проходит период адаптации к условиям региона, определяется специфика государственного управления различными сферами общественной жизнедеятельности, и сфера образования и науки не является исключением.</span></p>
 <span class="text"> </span> 
  <p><span class="text">В условиях развития государственно-общественной системы управления немаловажное значение имеет деятельность управлений образования субъектов РФ. Эта деятельность предполагает: </span></p>
 <span class="text"> </span> 
  <p><span class="text">- обеспечение в пределах своей компетентности проведения единой государственной политики в области образования; создания системы непрерывного образования населения; высокого качества профессиональной подготовки, переподготовки и повышения квалификации рабочих и специалистов в подведомственных учебных заведениях; широкого применения эффективных форм интеграции учебных заведений с науки и производством; </span></p>
 <span class="text"> </span> 
  <p><span class="text">- реализацию программы всеобщего среднего образования молодежи как базового для последующей подготовки квалифицированных рабочих и специалистов, разностороннего развития личности;</span></p>
 <span class="text"> </span> 
  <p><span class="text">- оказание практической помощи нижестоящим органам управления и учреждениям образования в систематическом обновлении содержания образования с учетом современных потребностей;</span></p>
 <span class="text"> </span> 
  <p><span class="text">- финансирование программ и проектов развития образования в соответствии с данными полномочиями, а также лицензирование, аккредитование, аттестацию и экспертизу подведомственных учреждений;</span></p>
 <span class="text"> </span> 
  <p><span class="text">- координацию деятельности отделов (управлений), ведомств, организаций и взаимодействие с муниципальными властными структурами. депутатскими комиссиями по вопросам социальной политики[4].</span></p>
 <span class="text"> </span> 
  <p><span class="text">Существующие структуры управления субъектов РФ и муниципальных образований нацелены на воспроизводство системы, поддержание ее в состоянии относительного постоянства (равновесия). Вместе с тем, постоянные стратегии и тактика в условиях обновления и внедрения нового хозяйственного механизма вносят переориентацию в деятельность. На первый план выносится функция развития системы. Это обстоятельство требует принципиальных изменений в традиционных структурах управления.</span></p>
 <span class="text"> </span> 
  <p><span class="text">В состав управления системы образования и науки субъекта Российской Федерации входят Департамент (Министерство автономной республики), управления, отделы, службы и другие структурные подразделения. Возглавляет государственное управление субъекта Российской Федерации губернатор, который осуществляет руководство через своего заместителя, курирующего социальные вопросы, в том числе работу Департамента образования и науки субъекта Российской Федерации.</span></p>
 <span class="text"> </span> 
  <p><span class="text">Каждый орган системы управления образования и науки функционирует согласно определенной для него компетенции в пределах, предоставленных ему полномочий, при строгом и неуклонном соблюдении и использовании законов и основанных на них правовых актах.</span></p>
 <span class="text"> </span> 
  <p><span class="text">В настоящий момент одним из краеугольных параметров реформирования является уточнение задач всех структурных элементов управления системы непрерывного профессионального образования, начиная от высших органов и заканчивая конкретными должностными лицами.</span></p>
 <span class="text"> </span> 
  <p><span class="text">Становление полноправных субъектов федерации предполагает передачу на их уровень управления многих функций, принадлежавших ранее центру. Согласно российскому Закону &laquo;Об образовании&raquo;, и политика в этой сфере находится исключительно в их компетенции. Статья 30 Закона &laquo;Об образовании&raquo; специально посвящена порядку разграничения компетенции органов государственной власти и органов управления образованием:</span></p>
 <span class="text"> </span> 
  <p><span class="text">П.1. Установленная статьями 28 и 29 настоящего Закона компетенция в области образования федеральных органов управления образованием и органов государственной власти субъектов Российской Федерации является исчерпывающей и не может быть изменена иначе как законом (Между тем требуется серьезная переработка указанных статей в соответствии с административными преобразованиями уровней исполнительной власти.).</span></p>
 <span class="text"> </span> 
  <p><span class="text">П.2.Разграничение компетенции в области образования между федеральными органами законодательной и исполнительной власти образования определяется в соответствии с Конституцией Российской Федерации законом, регулирующим деятельность Правительства Российской Федерации (И на наш взгляд должно быть отражено в Законе РФ &laquo;Об образовании&raquo;).</span></p>
 <span class="text"> </span> 
  <p><span class="text">3. Разграничение компетенции в области образования между федеральными органами исполнительной власти определяется Правительством Российской Федерации.</span></p>
 <span class="text"> </span> 
  <p><span class="text">4. Разграничение компетенции в области образования между органами законодательной и исполнительной власти субъектов Российской Федерации, а также между их органами регулируется законодательством субъектов Российской Федерации (Но принципы разделения и объемы полномочий должны найти отражение в образовательном законодательстве.).</span></p>
 <span class="text"> </span> 
  <p><span class="text">5. Федеральные и ведомственные государственные органы управления образованием не вправе самостоятельно принимать к своему рассмотрению вопросы, отнесенные настоящим Законом к компетенции органов управления образованием субъектов Российской Федерации и касающихся обеспечения государственной и общественной безопасности, охраны здоровья и санитарно-эпидемиологического благополучия населения, защиты прав и свобод граждан.</span></p>
 <span class="text"> </span> 
  <p><span class="text"> Тем не менее, мы считаем, что в законе должны быть четко оговорены контрольные функции государства по указанным направлениям, закреплены нормативы санитарно-эпидемиологического благополучия, узаконены меры по защите прав и свобод граждан в случае их нарушения.</span></p>
 <span class="text"> </span> 
  <p><span class="text">6. Федеральные государственные органы управления образованием вправе в пределах своей компетенции инспектировать на территории Российской Федерации любые образовательные учреждения и любой орган управления образованием в порядке надзора, а также могут предоставлять это право другим государственным органам управления образованием.[5] Пока последняя норма работает произвольно, так как нет четкой фиксации разграничения и передачи компетенций в порядке надзора на разных уровнях исполнительной власти. </span></p>
 <span class="text"> </span> 
  <p><span class="text">Пункт 6 статьи 30 полностью построен в соответствии с традиционной структурой вертикали управления системой образования сохраняет еще ряд существенных недостатков прошлого. Она хороша для работы на всех уровнях системы образования, когда они построены по линейно-функциональному принципу, т.е. по своему типу являются жесткими и могут хорошо работать только в стабильной среде. Однако реальная среда, в которой сегодня вынуждена действовать система образования, характеризуется высокой степенью нестабильности. </span></p>
 <span class="text"> </span> 
  <p><span class="text">Каждый субъект Российской Федерации имеет свою структуру государственной исполнительной власти. Она формируется на основании закона субъекта Федерации об исполнительных органах государственной власти.</span></p>
 <span class="text"> </span> 
  <p><span class="text">Закон Краснодарского края &laquo;О внесении изменений в Закон Краснодарского края &laquo;О структуре высшего исполнительного органа государственной власти Краснодарского края - администрации Краснодарского края и системы исполнительных органов государственной власти Краснодарского края&raquo; был принят Законодательным Собранием Краснодарского края 29 декабря 2005 года[6].</span></p>
 <span class="text"> </span> 
  <p><span class="text">В приложении №2 к вышеуказанному Закону дана схема структуры высшего исполнительного органа государственной власти Краснодарского края &ndash; администрации Краснодарского края и система органов государственной власти Краснодарского края.</span></p>
