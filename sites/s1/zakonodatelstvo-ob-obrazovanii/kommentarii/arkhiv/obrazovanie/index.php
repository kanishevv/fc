 <b>Статья 22. Начальное профессиональное образование</b></p>
 
<p>1. Начальное профессиональное образование имеет целью подготовку работников квалифицированного труда (рабочих, служащих) по всем основным направлениям общественно полезной деятельности на базе основного общего образования.</p>
 
<p>По отдельным профессиям начальное профессиональное образование может базироваться на среднем (полном) общем образовании.</p>
 
<p>2. Начальное профессиональное образование может быть получено в образовательных учреждениях начального профессионального образования (профессионально-технических и иных училищах данного уровня).</p>
 
