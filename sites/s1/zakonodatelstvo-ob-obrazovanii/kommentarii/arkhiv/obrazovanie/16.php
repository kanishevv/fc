<p align="JUSTIFY"><span class="text">1. Любой образовательный процесс инициируется приёмом поступающего в образовательное учреждение. С момента приёма или зачисления в образовательное учреждение поступающий (применительно к вузу - абитуриент) становится участником образовательного процесса и обретает правовой статус обучающегося.</span></p>

<p align="JUSTIFY"><span class="text">Порядок приёма в образовательные учреждения России, регламентирован действующим законодательством об образовании (прежде всего, Законом об образовании и Федеральным законом о высшем образовании) и локальными нормативными актами соответствующего образовательного учреждения. При этом нормы локальных актов, устанавливающие порядок приёма в государственные и муниципальные образовательные учреждения, не могут регулировать вопросы, которые уже урегулированы в Законе об образовании и других актах законодательства. Таким образом, применительно к государственным и муниципальным образовательным учреждениям, закрепляется принцип &laquo;остаточной компетенции учредителя&raquo;.</span></p>

<p align="JUSTIFY"><span class="text">Законодательство об образовании в большинстве случаев регулирует порядок приёма обучающихся в государственные образовательные учреждения, которые составляют львиную долю от общего числа образовательных учреждений России. Тем не менее, существуют и частные образовательные учреждения и учреждения различных общественных и религиозных организаций, применительно к которым все типовые положения об образовательных учреждениях носят рекомендательный характер. Следовательно, при определении порядка приёма в данные образовательные учреждения первостепенное значение имеют нормы локальных нормативных актов.</span></p>

<p align="JUSTIFY"><span class="text">Под локальными нормативными актами законодатель понимает устав образовательного учреждения, утверждённый учредителем. В действительности, не всегда условия и порядок приёма в образовательное учреждение закреплён уставом. Так, согласно п. 9 Типового положения о военном образовательном учреждении высшего профессионального образования<sup><sup>97</sup></sup> порядок приема граждан Российской Федерации и военнослужащих других государств в высшие военно-учебные заведения определяются нормативными правовыми актами федерального органа.</span></p>

<p align="JUSTIFY"><span class="text">Более того, в комментируемой статье нет ни слова о типовом положении как источнике, регламентирующем порядок приёма в образовательное учреждение. Этот недостаток уже отмечался в специальной литературе<sup><sup>98</sup></sup>. Несмотря на это, детальный перечень условий и требований к поступающим в любое образовательное учреждение, ограничения и иные особенности в большинстве случаев закреплены именно в уставе данного учреждения.</span></p>

<p align="JUSTIFY"><span class="text">2. <b>Гарантия приёма в образовательные учреждения, реализующие общедоступные образовательные программы и ограничения на приём.</b> Согласно п. 3 ст. 5 Закона об образовании государство гарантирует гражданам общедоступность и бесплатность дошкольного, начального общего, основного общего, среднего (полного) общего образования и начального профессионального образования в государственных и муниципальных образовательных учреждениях (образовательные учреждения и соответствующие ступени образования и образовательные программы далее будут именоваться «общедоступные»). Этот пункт вступает в противоречие с п. 2 ст. 43 Конституции РФ, в котором указанное положение распространяется на всех, а не только на граждан России. Указанное противоречие устранено на уровне подзаконных актов, но не Закона об образовании. Это объясняется (но отнюдь не оправдывается) тем, что Закон об образовании был принят ранее Конституции РФ.</span></p>

<p align="JUSTIFY"><span class="text">В связи с изложенной гарантией государства, на учредителей государственных и муниципальных образовательных учреждений, реализующих общедоступные образовательные программы, возлагается обязанность организовать и обеспечить приём всех граждан России (если учредителем является федеральный орган государственной власти) либо всех граждан, проживающих на территории субъекта Российской Федерации (если учредитель &ndash; орган государственной власти субъекта Российской Федерации) либо только тех граждан, которые проживают на территории муниципального образования (когда учредитель – орган местного самоуправления).</span></p>

<p align="JUSTIFY"><span class="text">Из сказанного можно сделать вывод, что в России согласно Закону об образовании существуют следующие общие ограничения на приём в государственные и муниципальные образовательные учреждения, реализующие общедоступные образовательные программы бесплатно для обучающихся:</span></p>

<p align="JUSTIFY"><span class="text">- по признаку гражданства (заведомо незаконное в силу прямого действия и верховенства Конституции России, фактически не действует ещё и по нижеизложенной причине);</span></p>

<p align="JUSTIFY"><span class="text">- по территории проживания поступающего, которая определяется территорией распространения публичной власти учредителя.</span></p>

<p align="JUSTIFY"><span class="text">Следует, однако, заметить, что учредитель по своему усмотрению вправе частично либо полностью снять эти ограничения, при этом такое решение и соответствующие нормы не будут противоречить Закону об образовании хотя бы уже потому, что они улучшают положение иностранных граждан и лиц без гражданства, а также иногородних граждан и граждан из других муниципальных образований и регионов.</span></p>

<p align="JUSTIFY"><span class="text">Так, в Порядке приёма в государственные учреждения начального профессионального образования Российской Федерации, утвержденном приказом Министерства образования Российской Федерации от 30 июня 1995 г. № 367<sup><sup>99</sup></sup>, указано, что в данные образовательные учреждения принимаются граждане России, а также иностранные граждане и лица без гражданства, проживающие на территории России, имеющие основное общее или среднее (полное) общее образование. Хотя в данном случае, конечно же, можно говорить не о доброй воле Минобразования России, а о следовании Конституции РФ. Кроме этого, постановлением Правительства Российской Федерации от 19 марта 2001 г. № 196, утверждающим Типовое положение об общеобразовательном учреждении<sup><sup>100</sup></sup>, фактически дезавуировано и второе ограничение, поскольку в абз. 2 п. 46 данного Типового положения прямо указано, что не проживающим на данной территории может быть отказано в приеме только по причине отсутствия свободных мест в учреждении. Однако эта норма распространяется лишь на государственные образовательные учреждения.</span></p>

<p align="JUSTIFY"><span class="text">Необходимо иметь в виду, что речь идёт только о законном проживании на соответствующей территории, что подтверждается:</span></p>

<p align="JUSTIFY"><span class="text">для граждан России – отметкой о постоянной регистрации в общегражданском паспорте либо свидетельством о регистрации по месту пребывания<sup><sup>101</sup></sup> (т.н. «временная регистрация»).</span></p>

<p align="JUSTIFY"><span class="text">для иностранных граждан и лиц без гражданства – разрешением на временное проживание или видом на жительство<sup><sup>102</sup></sup>.</span></p>

<p align="JUSTIFY"><span class="text">В связи с этим, установление администрациями образовательных учреждений начального профессионального образования, учредителями которых является Минобразования России, ограничений на приём граждан из других регионов и иногородних заведомо незаконно. Этот вывод содержится и в письме Министерства образования Российской Федерации от 13 мая 2002 года № 277/19-12<sup><sup>103</sup></sup>.</span></p>

<p align="JUSTIFY"><span class="text">Ограничение на приём в общедоступные государственные образовательные учреждения субъекта Российской Федерации и муниципальные образовательные учреждения обучающихся из других городов и регионов в форме установления предельной численности обучающихся и приоритетности приёма местных жителей существуют не только в России. Однако на западе оно преимущественно связано с проблемой доставки обучающихся от места проживания к образовательному учреждению и обратно, поскольку такая транспортировка чаще всего осуществляется за счёт публичных финансов. Поэтому, в отличие от России, на Западе эти ограничения в максимальной степени детализированы и связаны с принципом «близости школы к дому»<sup><sup>104</sup></sup>, с одной стороны, который, вместе с тем, сбалансирован принципом «свободы выбора школы законными представителями учащегося», с другой.</span></p>

<p align="JUSTIFY"><span class="text">Ещё раз подчеркнём, что требование об общедоступности и бесплатности распространяется не на все образовательные учреждения России, реализующие образовательные программы дошкольного, основного общего, среднего (полного) общего и начального профессионального образования, а лишь на те, учредителем которых является публичный субъект, то есть данная норма не распространяется на негосударственные образовательные учреждения. Поэтому, к примеру, частная школа может устанавливать дополнительные требования при приёме учеников, как к уровню их умственной и физической подготовленности, так и ограничения в виде относительно высокой платы за зачисление. Однако все они должны быть записаны в уставе и иных локальных актах данного образовательного учреждения.</span></p>

<p align="JUSTIFY"><span class="text">Несмотря на то, что согласно российскому законодательству в общедоступные образовательные учреждения гарантируется бесплатный приём, на практике существует ещё одно существенное ограничение, которое особенно распространено при приёме в городские дошкольные образовательные учреждения и учреждения начального профессионального образования. Это ограничение состоит:</span></p>

<p align="JUSTIFY"><span class="text">в квоте на общее число обучающихся в дошкольном образовательном учреждении, которая установлена в лицензии на право ведения образовательной деятельности;</span></p>

<p align="JUSTIFY"><span class="text">в расчётных показателях по подготовке, переподготовке и повышению квалификации рабочих кадров и специалистов для учреждений начального профессионального образования.</span></p>

<p align="JUSTIFY"><span class="text">Этим ограничения не исчерпываются. Существуют также <b><i>возрастные цензы</i></b>, которые закреплены в Законе об образовании или типовых положениях о соответствующем общедоступном образовательном учреждении. В государственных и муниципальных дошкольных образовательных учреждениях могут содержаться только дети в возрасте от 2-х месяцев до 7 лет<sup><sup>105</sup></sup>. В образовательные учреждения, реализующие образовательные программы начального общего образования, согласно п. 2 ст. 19 Закона об образовании могут приниматься дети в возрасте от 6 лет 6 месяцев до 8 лет. Для второй ступени общего образования (основного общего) предельный возраст обучающегося составляет 18 лет, однако может увеличиваться для детей и подростков с отклонениями в развитии, подростков с общественно опасным поведением, достигших возраста 11 лет, а также для граждан, содержащихся в воспитательно-трудовых и исправительно-трудовых учреждениях.</span></p>

<p align="JUSTIFY"><span class="text">Для поступления в государственные образовательные учреждения начального профессионального образования Российской Федерации (не субъекта РФ и не муниципальные) нет возрастных цензов, но существует образовательный ценз, который по общему правилу заключается в наличии у претендента основного общего или среднего (полного) общего образования<sup><sup>106</sup></sup>.</span></p>

<p align="JUSTIFY"><span class="text">Последняя группа ограничений обусловлена государственными задачами по предотвращению распространения определённых заболеваний и обеспечения безопасности участников образовательного процесса и тех лиц, которые представляют угрозу для окружающих<sup><sup>107</sup></sup>.</span></p>

<p align="JUSTIFY"><span class="text">3. <b>Право родителей на информацию.</b> Согласно п. 2 комментируемой статьи поступающий и его родители (законные представители) имеют право на получение информации об образовательном учреждении и предстоящем образовательном процессе, в частности, вправе знакомиться с уставом образовательного учреждения, лицензией на право ведения образовательной деятельности, свидетельством о государственной аккредитации образовательного учреждения и другими документами, которые регламентируют образовательный процесс.</span></p>

<p align="JUSTIFY"><span class="text">Здесь нужно обратить особое внимание на права родителей, которые хотя и не являются непосредственными участниками образовательного процесса, тем не менее, вносят решающий вклад в успешное освоение ребёнком образовательных программ (в большей степени, конечно, общеобразовательных программ). На родителях (законных представителях) в соответствии с п.2 ст.63 Семейного кодекса Российской Федерации<sup><sup>108</sup></sup> лежит обязанность обеспечить получение их детьми основного общего образования.</span></p>

<p align="JUSTIFY"><span class="text">Опять-таки, термин «гражданин», используемый законодателем применительно к поступающему в образовательное учреждение представляется не совсем корректным ввиду вышеуказанного противоречия Конституции России. Вместо «гражданин» более уместным представляется термин «поступающий».</span></p>

<p align="JUSTIFY"><span class="text">В 2000 году<sup><sup>109</sup></sup> перечень основных документов, с которыми администрация <i>любого</i> образовательного учреждения обязана ознакомить поступающего либо его родителей (законных представителей), был расширен. Помимо устава, в этот перечень теперь входят: лицензия на право ведения образовательной деятельности и свидетельство о государственной аккредитации образовательного учреждения. Под «другими документами», регламентирующими организацию образовательного процесса, прежде всего, понимаются учебный план, годовой календарный учебный график и расписание занятий, которые распространяются на обучающихся и, в случае приёма в данное образовательное учреждение, будут распространяться на поступающего.</span></p>

<p align="JUSTIFY"><span class="text">Соблюдение права обучающегося, его законных представителей на информацию особенно важно в свете того, что в России практически не задействован институт письменного договора между обучающимся (его законным представителем) и образовательным учреждением, в котором можно было бы отразить существенные условия, относящиеся к образовательному процессу и результатам обучения<sup><sup>110</sup></sup>.</span></p>

<p align="JUSTIFY"><span class="text">4. <b>Общие условия приёма в учреждения профессионального образования</b>. О приёме в учреждения начального профессионального образования, относящихся к общедоступным, уже было сказано. Что касается доступности и бесплатности среднего, высшего и послевузовского профессионального образования, то при поступлении в соответствующие типы образовательных учреждений поступающий должен не просто подать заявление, а пройти конкурс или соревнование за право быть принятым в образовательное учреждение, обеспечивающее отбор наиболее подготовленных претендентов<sup><sup>111</sup></sup>.</span></p>

<p align="JUSTIFY"><span class="text">Для реализации этой цели, во-первых, необходимо создать определённую обстановку, в которой проведение вступительных испытаний было бы полностью контролируемым, во-вторых, исключить избирательность действия условий конкурса, обращая особое внимание на недопустимость коррупции и непредвзятость экзаменаторов (которые в идеальном случае должны быть из разных учреждений и организаций), и, наконец, в-третьих, составить задания таким образом, чтобы в относительно короткое время экзаменатор смог выявить глубину знаний, навыков и оценить степень подготовленности и способности претендента. Доступ к процедуре прохождения вступительных испытаний на места, финансируемые за счёт бюджетов, должен быть бесплатным.</span></p>

<p align="JUSTIFY"><span class="text">Немаловажное значение имеет принятая система оценок и степень дифференциации оценок. Тенденции последнего времени показывают, что стандартная пятибалльная шкала в условиях жёстких вступительных конкурсов сильно огрубляет результаты, ухудшает сопоставимость и зачастую при равенстве оценок, полученных на вступительных экзаменах, вынуждает приёмные комиссии руководствоваться иными критериями, тогда как в действительности, именно оценка должна лежать в основе главного критерия, по которому в ряду поступающих можно выделить наиболее способных и подготовленных.</span></p>

<p align="JUSTIFY"><span class="text">До начала проведения вступительных испытаний, ежегодно или чаще, образовательное учреждение среднего или высшего профессионального образования образует приёмную комиссию, которую возглавляет руководитель этого учреждения. Приёмная комиссия обязана довести до всеобщего сведения:</span></p>

<p align="JUSTIFY"><span class="text">- сроки подачи документов претендентами и проведения конкурса;</span></p>

<p align="JUSTIFY"><span class="text">- контрольные цифры приёма на места по различным специальностям, финансируемые за счёт бюджета и места, полностью или частично финансируемые самими обучающимися (коммерческие места);</span></p>

<p align="JUSTIFY"><span class="text">- содержание и форму вступительных испытаний по отдельным предметам, включая установление максимального времени, которое даётся претенденту (абитуриенту) на подготовку;</span></p>

<p align="JUSTIFY"><span class="text">- порядок, сроки подачи и рассмотрения апелляций.</span></p>

<p align="JUSTIFY"><span class="text">В перечне документов, подаваемых заявителем приёмной комиссии государственного образовательного учреждения, должны в обязательном порядке содержаться оригиналы документов, подтверждающих образовательный ценз абитуриента:</span></p>

<p align="JUSTIFY"><span class="text">- для желающего осваивать образовательную программу среднего профессионального образования таким документом является аттестат об основном общем образовании либо аттестат о среднем (полном) общем образовании<sup><sup>112</sup></sup>;</span></p>

<p align="JUSTIFY"><span class="text">- для освоения программы высшего профессионального образования<sup><sup>113</sup></sup> – документ государственного образца о среднем (полном) общем или среднем профессиональном образовании, а также диплом о начальном профессиональном образовании, если в нем есть запись о получении предъявителем среднего (полного) общего образования.</span></p>

<p align="JUSTIFY"><span class="text">Кроме того, на первый курс вуза, полномочия учредителя которого осуществляет федеральный орган исполнительной власти, могут приниматься лица, имеющие диплом государственного образца о неполном высшем профессиональном образовании, академическую справку установленного образца о незаконченном высшем профессиональном образовании или диплом государственного образца о высшем профессиональном образовании различных ступеней.</span></p>

<p align="JUSTIFY"><span class="text">Дополнительно в перечень документов, которые абитуриент представляет приёмной комиссии образовательного учреждения как среднего, так и высшего профессионального образования, вместе с заявлением, входят: документ, удостоверяющий личность, необходимое число фотографий, а также документы, подтверждающие права заявителя на льготы при поступлении, если он на них претендует.</span></p>

<p align="JUSTIFY"><span class="text">В п. 3 комментируемой статьи закреплён перечень лиц, которые имеют право на внеконкурсное зачисление исключительно в государственные образовательные учреждения профессионального образования<sup><sup>114</sup></sup> и только в образовательные учреждения среднего профессионального и высшего профессионального образования. К ним относятся:</span></p>

<ul>
  <li>
    <p align="JUSTIFY"><span class="text">дети-сироты и дети, оставшиеся без попечения родителей, а также лица в возрасте до 23 лет из числа детей-сирот и детей, оставшихся без попечения родителей;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">дети-инвалиды, инвалиды I и II групп, которым согласно заключению учреждения Государственной службы медико-социальной экспертизы не противопоказано обучение в соответствующих образовательных учреждениях;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">граждане в возрасте до 20 лет, имеющие только одного родителя - инвалида I группы, если среднедушевой доход семьи ниже величины прожиточного минимума, установленного в соответствующем субъекте Российской Федерации;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">граждане, уволенные с военной службы и поступающие в соответствующие образовательные учреждения на основании рекомендаций командиров воинских частей, участники боевых действий и инвалиды боевых действий.</span></p>
  </li>
</ul>

<p align="JUSTIFY"><span class="text">В целях социальной защиты тех категорий лиц, которые более других нуждаются в государственной поддержке, Закон об образовании также предусматривает <i>преимущественное</i> право на поступление в государственные образовательные учреждения среднего профессионального и высшего профессионального образования граждан, уволенных с военной службы. При буквальном толковании преимущественное право означает, что при равенстве проходных баллов гражданина, уволенного с военной службы, и гражданина, не относящегося к данной категории, будет зачислен уволенный с военной службы. Очевидно, в данном случае для применения указанной специальной нормы в составе пакета документов, подаваемого заявителем приёмной комиссии, должна быть копия приказа об увольнении.</span></p>

<p align="JUSTIFY"><span class="text">Вышеназванные «особые» случаи поступления в среднее специальное или высшее учебное заведение включают прохождение вступительных испытаний, специальный порядок установлен только в отношении оценки результатов. Существуют также случаи, когда зачисление возможно вообще без прохождения вступительных испытаний.</span></p>

<p align="JUSTIFY"><span class="text">В Законе об образовании указан один такой случай применительно к поступлению в государственные образовательные учреждения среднего или высшего профессионального образования, когда поступающим является победитель или призёр заключительного этапа Всероссийской олимпиады школьников либо член сборной команды Российской Федерации, участвовавший в международных олимпиадах по общеобразовательным предметам и сформированной в порядке, определяемом Правительством Российской Федерации. При этом данная льгота действует только в том случае, если указанное лицо претендует на обучение по тому направлению подготовки (специальности), которое соответствует профилю олимпиады.</span></p>

<p align="JUSTIFY"><span class="text">Льготы устанавливаются исключительно законами. Письмом Минобразования России от 17.04.03 N 14-52-445ин/13<sup><sup>115</sup></sup> были направлены извлечения из законодательных актов Российской Федерации, предусматривающих льготы по приёму в государственные высшие учебные заведения. В связи с тем, что данное Письмо не опубликовано, приведём его полностью.</span></p>

