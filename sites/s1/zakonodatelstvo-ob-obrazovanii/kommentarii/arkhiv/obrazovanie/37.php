<p align="JUSTIFY"><span class="text">5. Муниципальные органы управления образованием.</span></p>

<p align="JUSTIFY"><span class="text">В структуре местной администрации могут создаваться органы управления образованием (управления и отделы образованием). Такие органы осуществляют управление муниципальными образовательными учреждениями, формирует различные экспертные и аттестационные комиссии и т.д.</span></p>

