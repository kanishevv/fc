<p><b>Статья 25. Послевузовское профессиональное образование</b></p>
 
<p>1. Послевузовское профессиональное образование предоставляет гражданам возможность повышения уровня образования, научной, педагогической квалификации на базе высшего профессионального образования.</p>
 
<p>2. Послевузовское профессиональное образование может быть получено в аспирантуре, ординатуре, адъюнктуре и докторантуре, создаваемых в образовательных учреждениях высшего профессионального образования и научных организациях, имеющих соответствующие лицензии.</p>
 
<p>(п. 2 в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
