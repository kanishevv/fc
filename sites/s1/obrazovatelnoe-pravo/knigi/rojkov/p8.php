<p style="text-align: justify;">
	<span class="text">Общество с ограниченной ответственностью «Электроприбор-люкс», именуемое в дальнейшем «Подрядчик»; в лице генерального директо­ра Куликова С.А., действующего на основании Устава, Муниципальное образовательное учреждение «Средняя общеобразовательная школа№ 17», именуемое в дальнейшем Заказчик, в лице директора Ивано­ва К.В., действующего на основании Устава, и Благотворительный фонд поддержки МОУ «Средняя общеобразовательная школа № 17», име­нуемый в дальнейшем Плательщик, в лице директора Петровой Е.Ю., действующей на основании Устава, заключили настоящий договор о нижеследующем:</span>
</p>
<p style="text-align: justify;">
	<b><span class="text"> <br>
 </span><span class="text">1. Предмет договора</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">1.1.Подрядчик обязуется поставить и установить электроприборы, Плательщик оплатить, а Заказчик принять электроприборы и работы по их установке в объеме, определенном в Счете № , выставлен­ным Подрядчиком и являющимся неотъемлемой частью настоящего договора.</span>
</p>
<p style="text-align: justify;">
	<span class="text">1.2.Работы выполняются Подрядчиком из его материалов, его си­лами и средствами.</span>
</p>
<p style="text-align: justify;">
	<span class="text">1.3.Риск случайной гибели или случайного повреждения результата работ несет Подрядчик до момента принятия этого результата Заказ­чиком. Данный факт подтверждается соответствующим актом, под­писанным Подрядчиком и Заказчиком. При просрочке принятия За­казчиком результата работ по настоящему договору риск случайной гибели или случайного повреждения этого результата несет Заказчик с момента, когда он должен был его принять.</span>
</p>
<p style="text-align: justify;">
	<span class="text"> <br>
 </span>
</p>
<p style="text-align: justify;">
	<b><span class="text">2.Сроки и порядок выполнения работ</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">2.1. Поставка электротоваров и выполнение комплекса работ по их установке по настоящему договору начинается в трехдневный срок с момента оплаты товара Плательщиком. Срок окончания выполнения работ — не позднее трех рабочих дней с момента начала работ.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Сроки выполнения работ, установленные настоящим договором, могут быть изменены только соглашением между Заказчиком и Под­рядчиком, которое оформляется в виде дополнения к договору или на основании закона.</span>
</p>
<p style="text-align: justify;">
	<span class="text">2.2.Выполнение работ производится Подрядчиком по месту нахож­дения Заказчика.</span>
