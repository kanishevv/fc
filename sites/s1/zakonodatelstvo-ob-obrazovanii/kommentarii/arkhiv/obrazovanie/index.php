<p><b>Статья 38. Государственный контроль за качеством образования в аккредитованных образовательных учреждениях</b></p>
 
<p>1. Государственная аттестационная служба может направить образовательному учреждению, имеющему государственную аккредитацию, рекламацию на качество образования и (или) несоответствие образования требованиям соответствующего государственного образовательного стандарта.</p>
 
<p>2. Основаниями для рассмотрения высшим органом государственной аттестационной службы вопроса о направлении указанной рекламации являются:</p>
 
<p>1) применительно к образовательным учреждениям, реализующим общеобразовательные программы:</p>
 
<p>решение общего собрания родителей (законных представителей) обучающихся образовательного учреждения;</p>
 
<p>официальное представление отделения государственной аттестационной службы по месту нахождения образовательного учреждения;</p>
 
<p>2) применительно к образовательным учреждениям, реализующим профессиональные образовательные программы:</p>
 
<p>решение общего собрания обучающихся образовательного учреждения;</p>
 
<p>официальное представление государственной службы занятости населения.</p>
 
<p>3. Решение о направлении указанной рекламации или об отклонении требований заявителей о рекламации принимается высшим органом государственной аттестационной службы и сообщается заявителям в месячный срок. Отказ государственной аттестационной службы от направления рекламации может быть обжалован заявителями в суд.</p>
 
<p>(в ред. Федерального закона от 16.11.1997 № 144-ФЗ)</p>
 
<p>4. Повторная в течение двух лет рекламация автоматически влечет за собой лишение образовательного учреждения его государственной аккредитации. Возобновление государственной аккредитации осуществляется в том же порядке, что и ее получение.</p>
 
