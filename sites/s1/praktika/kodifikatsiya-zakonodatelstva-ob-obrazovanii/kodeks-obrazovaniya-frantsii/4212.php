<p><span class="textmar"><b>Статья L.421-11</b></span></p>

<p> </p>

<p><span class="textmar">Бюджет местного публичного образовательного учреждения готовится, принимается и становится исполняемым при исполнении следующих условий:</span></p>

<p> </p>

<p><span class="textmar">a) до 1 ноября года, предшествующего исполнению, сумма, предусмотренная на участие территориального объединения в расходах на оборудование и функционирование учреждения, которое от него зависит, и распределение этой суммы на оборудование и материальное функционирование учреждения, утверждённые руководящим представительным органом этого объединения, официально представляются руководителю учреждения. Доля участия территориального объединения не может быть уменьшена во время принятия и изменения его бюджета;</span></p>

<p> </p>

<p><span class="textmar">b) руководитель учреждения готовит проект бюджета в зависимости от утверждённого распределения и в пределах совокупности ресурсов, которыми располагает учреждение. Он представляет его административному совету;</span></p>

<p> </p>

<p><span class="textmar">c) бюджет учреждения принимается сбалансировано в течение тридцати дней, последующих за официальным сообщением об участии объединения, от которого зависит учреждение;</span></p>

<p> </p>

<p><span class="textmar">d) бюджет, принятый административным советом, передается представителю Государства, прикрепленному объединению, а также органам управления образованием в течение пяти дней после его принятия.</span></p>

<p> </p>

<p><span class="textmar">Бюджет становится исполняемым через тридцать дней после последней даты получения его вышеуказанными инстанциями, если только в течение этого срока органы управления образованием или прикрепленного местного объединения не выразят свое мотивированное несогласие с утверждённым бюджетом;</span></p>

<p> </p>

<p><span class="textmar">e) в случае несогласия бюджет регулируется совместно прикрепленными объединениями и органами управления образованием. Он передается представителю Государства и становится исполняемым.</span></p>

<p> </p>

<p><span class="textmar">При отсутствии согласия между двумя инстанциями в течение двух месяцев со дня принятия бюджета бюджет регулируется представителем Государства с учетом мнения региональной расчетной палаты. Представитель Государства может по отношению к предыдущему бюджету, кроме исключений, связанных с эволюцией числа учащихся или поддержанием запаса материалов или состоянием помещений, увеличить долю, приходящуюся на объединение только в пропорции, не превышающей ни эволюцию продукта прямого налогообложения этого объединения, ни эволюцию поступлений, ассигнованных Государством и предназначенных для удовлетворения педагогических расходов этого учреждения;</span></p>

<p> </p>

<p><span class="textmar">f) если бюджет не принят в течение тридцати дней после уведомления об участии объединения, от которого зависит учреждение, применяется процедура, описанная в пункте е. Однако срок, предусмотренный во втором абзаце пункта е, составляет один месяц после изъятия бюджета представителем Государства из ведения прикрепленного объединения и органов управления образованием;</span></p>

<p> </p>

<p><span class="textmar">g) распределение прикрепленными объединениями кредитов между учреждениями основывается на таких критериях, как число учащихся, масштаб учреждения, тип образования, группы населения, охваченные обучением, качественные показатели охвата обучением.</span></p>

