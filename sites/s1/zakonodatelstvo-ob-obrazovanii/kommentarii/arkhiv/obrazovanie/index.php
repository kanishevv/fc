<p><b>Статья 54. Оплата труда работников образовательных учреждений</b></p>
 
<p>1. Заработная плата и должностной оклад работнику образовательного учреждения выплачиваются за выполнение им функциональных обязанностей и работ, предусмотренных трудовым договором (контрактом). Выполнение работником образовательного учреждения других работ и обязанностей оплачивается по дополнительному договору, за исключением случаев, предусмотренных законодательством Российской Федерации.</p>
 
<p>2 - 4. Утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
