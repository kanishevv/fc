 <b>Статья 27. Документы об образовании</b></p>
 
<p>1. Образовательное учреждение в соответствии с лицензией выдает лицам, прошедшим итоговую аттестацию, документы о соответствующем образовании и (или) квалификации в соответствии с лицензией. Форма документов определяется самим образовательным учреждением. Указанные документы заверяются печатью образовательного учреждения.</p>
 
<p>2. Образовательные учреждения, имеющие государственную аккредитацию и реализующие общеобразовательные (за исключением дошкольных) и профессиональные образовательные программы, выдают лицам, прошедшим итоговую аттестацию, документы государственного образца об уровне образования и (или) квалификации, заверяемые печатью соответствующего образовательного учреждения.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>3. Гражданам, завершившим послевузовское профессиональное образование, защитившим квалификационную работу (диссертацию, по совокупности научных работ), присваивается ученая степень и выдается соответствующий документ.</p>
 
<p>4. Документ государственного образца о соответствующем уровне образования является необходимым условием для продолжения обучения в государственном или муниципальном образовательном учреждении последующего уровня образования.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>Указанные в документах о начальном профессиональном, среднем профессиональном, высшем профессиональном образовании квалификации и в документах о послевузовском профессиональном образовании ученые степени дают право их обладателям заниматься профессиональной деятельностью, в том числе занимать должности, для которых в установленном порядке определены обязательные квалификационные требования к соответствующему образовательному цензу.</p>
 
<p>5. В Российской Федерации устанавливаются следующие образовательные уровни (образовательные цензы):</p>
 
<p>1) основное общее образование;</p>
 
<p>2) среднее (полное) общее образование;</p>
 
<p>3) начальное профессиональное образование;</p>
 
<p>4) среднее профессиональное образование;</p>
 
<p>5) высшее профессиональное образование;</p>
 
<p>6) послевузовское профессиональное образование.</p>
 
<p>6. Лицам, не завершившим образование данного уровня, выдается справка установленного образца.</p>
 
