<p><span class="text" style="text-align: justify;">Статья 5. Федеральные государственные образовательные стандарты высшего профессионального образования и федеральные государственные требования к послевузовскому профессиональному образованию. Основные образовательные программы высшего и послевузовского профессионального образования</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 01.12.2007 № 309-ФЗ)</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Федеральные государственные образовательные стандарты высшего профессионального образования предназначены для обеспечения:</span></p>
 
<p><span class="text" style="text-align: justify;">1) единства образовательного пространства Российской Федерации;</span></p>
 
<p><span class="text" style="text-align: justify;">2) качества высшего профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">3) основы для объективной оценки деятельности образовательных учреждений, реализующих образовательные программы высшего профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">4) признания и установления эквивалентности документов иностранных государств о высшем профессиональном образовании.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Федеральные государственные образовательные стандарты высшего профессионального образования, а также образовательные стандарты и требования, устанавливаемые в соответствии с пунктом 4 настоящей статьи, включают в себя требования к:</span></p>
 
<p><span class="text" style="text-align: justify;">1) структуре основных образовательных программ, в том числе требования к соотношению частей основной образовательной программы и их объему, а также к соотношению обязательной части основной образовательной программы и части, формируемой участниками образовательного процесса;</span></p>
 
<p><span class="text" style="text-align: justify;">2) условиям реализации основных образовательных программ, в том числе кадровым, финансовым, материально-техническим и иным условиям;</span></p>
 
<p><span class="text" style="text-align: justify;">3) результатам освоения основных образовательных программ.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Разработка и утверждение федеральных государственных образовательных стандартов высшего профессионального образования осуществляются в порядке, определяемом Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">4. В случаях, предусмотренных настоящим Федеральным законом, реализация образовательных программ высшего профессионального образования осуществляется на основе образовательных стандартов и требований, самостоятельно устанавливаемых федеральными государственными высшими учебными заведениями, перечень которых утверждается указом Президента Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">5. Основная образовательная программа высшего профессионального образования обеспечивает реализацию федерального государственного образовательного стандарта с учетом вида высшего учебного заведения, образовательных потребностей и запросов обучающихся и включает в себя учебный план, рабочие программы учебных курсов, предметов, дисциплин (модулей) и другие материалы, обеспечивающие качество подготовки обучающихся, а также программы учебной и производственной практики, календарный учебный график и методические материалы, обеспечивающие реализацию соответствующей образовательной технологии.</span></p>
 
<p><span class="text" style="text-align: justify;">6. Федеральный орган исполнительной власти, осуществляющий функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования, обеспечивает разработку на основе федеральных государственных образовательных стандартов или федеральных государственных требований примерных основных образовательных программ высшего профессионального образования и основных образовательных программ послевузовского профессионального образования (для обучающихся в аспирантуре, адъюнктуре, интернатуре, ординатуре), включающих в себя примерные программы учебных курсов, предметов, дисциплин (модулей).</span></p>
 
<p><span class="text" style="text-align: justify;">7. К структуре основной образовательной программы послевузовского профессионального образования (за исключением докторантуры) уполномоченным федеральным органом исполнительной власти устанавливаются федеральные государственные требования.</span></p>
 
<p><span class="text" style="text-align: justify;">8. Перечень направлений подготовки (специальностей) высшего и послевузовского профессионального образования устанавливается в порядке, определяемом Правительством Российской Федерации.</span></p>
 
