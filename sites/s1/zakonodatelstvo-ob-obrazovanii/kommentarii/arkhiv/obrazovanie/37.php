<p align="LEFT" style="text-align: justify;"><span class="text"><b>Федеральное агентство по образованию (Рособразование)</b></span></p>

<p align="LEFT" style="text-align: justify;"><span class="text">
    <br />
  </span></p>

<p align="JUSTIFY"><span class="text">Положение о Рособразовании утверждено постановлением Правительства РФ от 17 июня 2004 года №288<sup><sup>225</sup></sup>.</span></p>

<p align="JUSTIFY"><span class="text">Рособразование &ndash; это федеральный орган исполнительной власти, осуществляющий функции по управлению государственным имуществом, оказанию государственных услуг в сфере образования, воспитания, молодежной политики и развития общедоступных образовательных ресурсов.</span></p>

<p align="JUSTIFY"><span class="text">Федеральное агентство по образованию осуществляет следующие полномочия:</span></p>

<p align="JUSTIFY"><span class="text">1) проводит в установленном порядке конкурсы и заключает государственные контракты на размещение заказов на поставку товаров, выполнение работ, оказание услуг, проведение научно-исследовательских, опытно-конструкторских и технологических работ для государственных нужд в установленной сфере деятельности, в том числе для обеспечения нужд Агентства;</span></p>

<p align="JUSTIFY"><span class="text">2) осуществляет полномочия собственника в отношении федерального имущества, необходимого для обеспечения исполнения функций федеральных органов государственной власти в установленной сфере деятельности, в том числе имущества, переданного федеральным государственным унитарным предприятиям, федеральным государственным учреждениям и казенным предприятиям, подведомственным Агентству;</span></p>

<p align="JUSTIFY"><span class="text">3) осуществляет функции главного распорядителя и получателя средств федерального бюджета, предусмотренных на содержание Агентства и реализацию возложенных на него функций;</span></p>

<p align="JUSTIFY"><span class="text">4) осуществляет функции государственного заказчика федеральных целевых программ;</span></p>

<p align="JUSTIFY"><span class="text">5) взаимодействует в установленном порядке с органами государственной власти иностранных государств и международными организациями в установленной сфере деятельности;</span></p>

<p align="JUSTIFY"><span class="text">6) организует повышение квалификации и переподготовку научно-педагогических работников государственных учреждений высшего профессионального образования и государственных научных организаций, действующих в системе высшего и послевузовского профессионального образования;</span></p>

<p align="JUSTIFY"><span class="text">7) осуществляет:</span></p>

<ul>
  <li>
    <p align="JUSTIFY"><span class="text">определение для образовательных учреждений профессионального образования, находящихся в ведении Агентства, контрольных цифр приема граждан, обучающихся за счет средств федерального бюджета, а также квот по целевому приему;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">размещение на конкурсной основе среди высших учебных заведений, имеющих государственную аккредитацию, государственного задания по подготовке специалистов с высшим профессиональным образованием, в том числе инженерных и научных кадров для оборонных отраслей промышленности;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">распределение между образовательными учреждениями квот на стипендии Президента Российской Федерации, Правительства Российской Федерации, иные именные стипендии и назначение их в установленном порядке;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">открытие в установленном порядке аспирантур и докторантур в образовательных учреждениях высшего и дополнительного профессионального образования и научных организациях; (данный пункт в части открытия аспирантуры противоречит приказу Минобразования России от 27.30.2004 №814, зарегистрирован Минюстом России 05.08.1998 №1582, открытие аспирантуры осуществляется с выдачей лицензии, что входит в компетенцию Рособрнадзора).</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">утверждение на установленный законодательством Российской Федерации срок избранных общими собраниями (конференциями) образовательных учреждений высшего профессионального образования, находящихся в ведении Агентства, ректоров (директоров), а при наличии вакантной должности ректора возложение исполнения его обязанностей на одного из проректоров;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">оформление в установленном порядке соответствующих документов для выезда за границу обучающихся, воспитанников и работников сферы образования;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">организует конгрессы, конференции, олимпиады, семинары, выставки и другие мероприятия в установленной сфере деятельности;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">осуществляет экономический анализ деятельности находящихся в ведении Агентства государственных унитарных предприятий и утверждает экономические показатели их деятельности, проводит в организациях, находящихся в ведении Агентства, проверки финансово-хозяйственной деятельности и использования имущественного комплекса;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">осуществляет прием граждан, обеспечивает своевременное и полное рассмотрение устных и письменных обращений граждан, принятие по ним решений и направление заявителям ответов в установленный законодательством Российской Федерации срок;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">осуществляет иные функции по управлению государственным имуществом и оказанию государственных услуг в установленной сфере деятельности.</span></p>
  </li>
</ul>

<p align="JUSTIFY"><span class="text">Рособразование возглавляет руководитель, назначаемый на должность и освобождаемый от должности Правительством РФ по представлению Министра образования и науки РФ.</span></p>

<p align="JUSTIFY"><span class="text">Руководитель Рособразования несет персональную ответственность за выполнение возложенных на Агентство функций.</span></p>

<p align="JUSTIFY"><span class="text">Руководитель Федерального агентства по образованию:</span></p>

<p align="JUSTIFY"><span class="text">1) распределяет обязанности между своими заместителями;</span></p>

<p align="JUSTIFY"><span class="text">2) представляет Министру образования и науки РФ:</span></p>

<ul>
  <li>
    <p align="JUSTIFY"><span class="text">проект положения о Федеральном агентстве по образованию;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">предложения о предельной численности и фонде оплаты труда работников Агентства;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">предложения о назначении на должность и об освобождении от должности заместителей руководителя Агентства;</span></p>
  </li>

  <li>
