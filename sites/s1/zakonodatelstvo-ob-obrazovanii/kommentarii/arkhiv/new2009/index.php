<p><span class="text" style="text-align: justify;">1. Под автономией образовательного учреждения высшего профессионального образования (далее - высшее учебное заведение) понимается его самостоятельность в подборе и расстановке кадров, осуществлении учебной, научной, финансово-хозяйственной и иной деятельности в соответствии с законодательством и уставом высшего учебного заведения, утвержденным в установленном законодательством порядке.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Высшее учебное заведение несет ответственность за свою деятельность перед личностью, обществом и государством. Контроль за соответствием деятельности высшего учебного заведения целям, предусмотренным его уставом, осуществляют в пределах своей компетенции учредитель высшего учебного заведения и федеральный орган исполнительной власти, осуществляющий функции по контролю и надзору в сфере образования и выдавший лицензию на ведение образовательной деятельности (далее - лицензия).</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. Педагогическим работникам из числа профессорско-преподавательского состава, научным работникам и студентам высшего учебного заведения предоставляются академические свободы, в том числе свобода педагогического работника высшего учебного заведения излагать учебный предмет по своему усмотрению, выбирать темы для научных исследований и проводить их своими методами, а также свобода студента получать знания согласно своим склонностям и потребностям.</span></p>
 
<p><span class="text" style="text-align: justify;">Предоставляемые академические свободы влекут за собой академическую ответственность за создание оптимальных условий для свободного поиска истины, ее свободного изложения и распространения.</span></p>
 
