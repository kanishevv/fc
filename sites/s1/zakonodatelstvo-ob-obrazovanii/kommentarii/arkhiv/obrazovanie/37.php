<p align="JUSTIFY"><span class="text">6. В пункте 6 комментируемой статьи закрепляется, что органам управления образованием подконтрольны подведомственные им образовательные учреждения. В случае нарушения образовательным учреждением законодательства Российской Федерации в области образования и (или) своего устава государственные органы управления образованием имеют право своим предписанием приостановить в этой части деятельность образовательного учреждения. Окончательное же решение по данному вопросу принимает суд.</span></p>

