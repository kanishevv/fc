<p style="text-align: justify;">
	<span class="text">При выплате заработной платы, надбавок, доплат и премий по тру­довым договорам налогообложение ЕСН идет по графе «итого». При выполнении той или иной работы, оказании услуг физическими лица­ми по гражданско-правовым договорам ставка по ЕСН уменьшается на платеж в Фонд социального страхования Российской Федерации(т.е. на 2,9%) (п. 3 ст. 238 НК РФ). При привлечении преподавателей к проведению занятий по обучению детей заключается гражданско-пра­вовой договор оказания услуг.</span>
</p>
