 <b>Статья 33. Порядок создания и регламентации деятельности образовательного учреждения</b></p>
 
<p>1. Образовательное учреждение создается и регистрируется в соответствии с законодательством Российской Федерации.</p>
 
<p>Порядок создания федеральных государственных образовательных учреждений устанавливается Правительством Российской Федерации, государственных образовательных учреждений, находящихся в ведении субъекта Российской Федерации, - органом исполнительной власти субъекта Российской Федерации, муниципальных образовательных учреждений - органом местного самоуправления.</p>
 
<p>(п. 1 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>2. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>3. Для регистрации образовательного учреждения учредитель представляет документы в соответствии с федеральным законом о государственной регистрации юридических лиц.</p>
 
<p>(в ред. Федеральных законов от 21.03.2002 № 31-ФЗ, от 08.12.2003 № 169-ФЗ)</p>
 
<p>4. Уполномоченный орган в сроки, установленные федеральным законом о государственной регистрации юридических лиц, производит регистрацию образовательного учреждения, о чем в письменной форме уведомляет заявителя, финансовые органы, соответствующий государственный орган управления образованием.</p>
 
<p>(в ред. Федеральных законов от 21.03.2002 № 31-ФЗ, от 08.12.2003 № 169-ФЗ)</p>
 
<p>5. Права юридического лица у образовательного учреждения в части ведения финансово-хозяйственной деятельности, предусмотренной его уставом и направленной на подготовку образовательного процесса, возникают с момента регистрации образовательного учреждения.</p>
 
<p>6. Право на ведение образовательной деятельности и льготы, установленные законодательством Российской Федерации, возникают у образовательного учреждения с момента выдачи ему лицензии (разрешения).</p>
 
<p>7. Лицензия на право ведения образовательной деятельности выдается уполномоченным органом исполнительной власти на основании заключения экспертной комиссии. Лицензии на право ведения образовательной деятельности образовательным учреждениям религиозных организаций (объединений) выдаются по представлению руководства соответствующей конфессии.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>8. Экспертная комиссия создается уполномоченным органом исполнительной власти по заявлению учредителя и проводит свою работу в месячный срок. В экспертную комиссию на паритетных началах входят представители государственного органа управления образованием, соответствующего органа местного самоуправления и (или) местного (муниципального) органа управления образованием, действующих образовательных учреждений, общественности.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>9. Предметом и содержанием экспертизы является установление соответствия условий осуществления образовательного процесса, предлагаемых образовательным учреждением, государственным и местным требованиям в части строительных норм и правил, санитарных и гигиенических норм, охраны здоровья обучающихся, воспитанников и работников образовательных учреждений, оборудования учебных помещений, оснащенности учебного процесса, образовательного ценза педагогических работников и укомплектованности штатов. Содержание, организация и методики образовательного процесса предметом экспертизы не являются.</p>
 
<p>Особенности предмета и содержания экспертизы, проводимой в отношении образовательных учреждений, использующих дистанционные образовательные технологии для реализации образовательных программ частично или в полном объеме, устанавливаются федеральным (центральным) государственным органом управления образованием, если иное не установлено федеральным законом.</p>
 
<p>(абзац введен Федеральным законом от 10.01.2003 № 11-ФЗ)</p>
 
<p>10. Требования экспертизы не могут превышать среднестатистических показателей для территории, на которой зарегистрировано образовательное учреждение.</p>
 
<p>11. Затраты на проведение экспертизы оплачиваются учредителем.</p>
 
<p>12. В лицензии, выдаваемой образовательному учреждению, фиксируются контрольные нормативы, предельная численность контингента обучающихся, воспитанников и срок действия этой лицензии.</p>
 
<p>13. Отрицательное заключение по результатам экспертизы и основанный на нем отказ в выдаче лицензии образовательному учреждению могут быть обжалованы учредителем в суд.</p>
 
<p>(в ред. Федерального закона от 16.11.1997 № 144-ФЗ)</p>
 
<p>14. Контроль за соблюдением образовательным учреждением независимо от его организационно-правовой формы предусмотренных лицензией условий обеспечивает уполномоченный орган исполнительной власти. В случае нарушения этих условий лицензия подлежит изъятию.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>15. Возобновление лицензии осуществляется в том же порядке, что и ее получение.</p>
 
<p>16. Права образовательного учреждения на выдачу своим выпускникам документа государственного образца о соответствующем уровне образования, на пользование печатью с изображением Государственного герба Российской Федерации, а также право общеобразовательного учреждения на включение в схему централизованного государственного финансирования возникают с момента их государственной аккредитации, подтвержденной свидетельством о государственной аккредитации.</p>
 
<p>17. Свидетельство о государственной аккредитации образовательного учреждения подтверждает его государственный статус, уровень реализуемых образовательных программ, соответствие содержания и качества подготовки выпускников требованиям государственных образовательных стандартов, право на выдачу выпускникам документов государственного образца о соответствующем уровне образования.</p>
 
<p>Свидетельство о государственной аккредитации, выдаваемое дошкольным образовательным учреждениям и учреждениям дополнительного образования детей, подтверждает государственный статус соответствующего образовательного учреждения, уровень реализуемых им образовательных программ, категорию этого образовательного учреждения.</p>
 
<p>18. Государственная аккредитация образовательных учреждений проводится уполномоченными органами исполнительной власти на основании заявления образовательного учреждения и заключения по его аттестации.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>19. Аттестация образовательного учреждения проводится по его заявлению государственной аттестационной службой с привлечением ведущих образовательных учреждений, общественности. Аттестация проводится один раз в пять лет, если иное не предусмотрено законом. Затраты на проведение аттестации оплачиваются образовательным учреждением.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>20. Целью и содержанием аттестации является установление соответствия содержания, уровня и качества подготовки выпускников образовательного учреждения требованиям государственных образовательных стандартов. Условием аттестации образовательного учреждения являются положительные результаты итоговой аттестации не менее чем половины его выпускников в течение трех последовательных лет.</p>
 
<p>Первая аттестация вновь созданного образовательного учреждения может проводиться по его заявлению после первого выпуска обучавшихся, но не ранее чем через три года после получения лицензии при условии положительных результатов итоговой аттестации не менее чем половины его выпускников.</p>
 
<p>Первая аттестация вновь создаваемых образовательных учреждений начального общего, основного общего и среднего (полного) общего образования может проводиться поэтапно по ступеням образования - начальное общее, основное общее, среднее (полное) общее.</p>
 
<p>21. Аттестационное заключение государственной аттестационной службы может быть обжаловано в суд только в части процедуры аттестации. Образовательное учреждение вправе потребовать повторной аттестации не ранее чем через 12 месяцев с момента отказа ему в государственной аккредитации.</p>
 
<p>22. Аттестация дошкольных образовательных учреждений, образовательных учреждений для детей-сирот и детей, оставшихся без попечения родителей (законных представителей), специальных (коррекционных) образовательных учреждений для обучающихся, воспитанников с отклонениями в развитии, учреждений дополнительного образования, а также вновь созданных экспериментальных образовательных учреждений проводится соответствующим государственным органом управления образованием в порядке, предусмотренном типовыми положениями об этих образовательных учреждениях.</p>
 
<p>23. Образовательное учреждение может быть лишено государственной аккредитации по результатам аттестации.</p>
 
<p>24. Филиалы (отделения) образовательных учреждений проходят лицензирование, аттестацию и государственную аккредитацию в общем порядке, установленном для образовательных учреждений настоящим Законом.</p>
 
<p>(в ред. Федерального закона от 21.03.2002 № 31-ФЗ)</p>
 
<p>Филиалы образовательного учреждения, реализующего образовательную программу (образовательные программы) в полном объеме посредством дистанционных образовательных технологий (за исключением некоторых занятий) в этих филиалах, вправе проходить аттестацию и государственную аккредитацию в составе образовательного учреждения, обособленными структурными подразделениями которого они являются.</p>
 
<p>(абзац введен Федеральным законом от 10.01.2003 № 11-ФЗ)</p>
 
<p>25. Образовательные учреждения могут получать общественную аккредитацию в различных российских, иностранных и международных общественных образовательных, научных и промышленных структурах. Такая аккредитация не влечет за собой дополнительные финансовые обязательства со стороны государства.</p>
 
