<p align="center"><span class="textmar"><b><i>Секция 6. Положения, относящиеся к частным сельскохозяйственным учреждениям образования в режиме контракта</i></b></span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.442-21</b></span></p>

<p> </p>

<p><span class="textmar">Отношения между Государством и частными образовательными сельскохозяйственными учреждениями регулируются положениями статей с L.813-3 по L.813-7 Кодекса сельского хозяйства, воспроизводимых ниже:</span></p>

<p> </p>

<p><span class="textmar">&laquo;Ст.L.813-3. &ndash; Ассоциация или организация, ответственная за частное образовательное сельскохозяйственное учреждение, должна, если она желает, чтобы это учреждение участвовало в публичной службе и пользовалось в этом качестве финансовой помощью Государства, обратиться с просьбой о подписании контракта с Государством. Этим контрактом ассоциация или организация обязуется:</span></p>

<p> </p>

<p><span class="textmar">1. Соответствовать по направлениям подготовки, предусматриваемым в этом контракте, прогнозируемой национальной схеме видов подготовки сельскохозяйственного образования, упомянутой в статье L.814-2.</span></p>

<p> </p>

<p><span class="textmar">2. Предлагать учащимся виды подготовки, осуществляемые персоналом, который обладает квалификацией, требуемой действующими регламентациями.</span></p>

<p> </p>

<p><span class="textmar">3. Следовать национальным программам и в рамках своего педагогического проекта готовить учащихся к государственным дипломам сельскохозяйственного образования.</span></p>

<p> </p>

<p><span class="textmar">4. Подчиняться административному, педагогическому и финансовому контролю Государства.</span></p>

<p> </p>

<p><span class="textmar">5. Уважать права и требовать от своего персонала выполнения обязанностей, как они предусмотрены в следующих статьях.</span></p>

<p> </p>

<p><span class="textmar">Государство может заключать контракты только на виды подготовки, которые соответствуют потребностям, определенным национальной прогнозируемой схемой видов подготовки сельскохозяйственного образования в пределах кредитов, вписанных на эти цели в Закон о бюджете.</span></p>

<p> </p>

<p><span class="textmar">Всякое изменение прогнозируемой схемы может повлечь за собой пересмотр контракта.</span></p>

<p> </p>

<p><span class="textmar">Типовые контракты утверждаются декретом Государственного Совета.</span></p>

<p> </p>

<p><span class="textmar">&laquo;Ст. L.813-4. &ndash; Национальные федерации, представляющие ассоциации и организации, ответственные за частные образовательные сельскохозяйственные учреждения, могут получать непосредственно помощь от Государства для решения задач, представляющие общий интерес, выполнение которых их члены им доверяют, независимо от задач обучения и задач по подготовке учителей&raquo;.</span></p>

<p> </p>

<p><span class="textmar">&laquo;Ст. L. 813-5. &ndash; Государство может участвовать в оплате капиталовложений, относящихся к образовательным сельскохозяйственным учреждениям в режиме контракта, за исключением расходов на начальное строительство&raquo;.</span></p>

<p> </p>

<p><span class="textmar">&laquo;Ст.L.813-6. &ndash; Ассоциация или организация может обратиться с просьбой о включении в систему публичного образования учреждения, за которое она несет ответственность. Просьба может быть принята только после согласия заинтересованного публичного объединения. В случае принятия действующие сотрудники получают назначения согласно штатному расписанию публичного сельскохозяйственного образования или нанимаются по контракту&raquo;.</span></p>

<p> </p>

