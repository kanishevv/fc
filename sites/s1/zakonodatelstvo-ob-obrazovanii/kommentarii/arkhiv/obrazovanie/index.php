<p>2) законодательство субъектов Российской Федерации в области образования;</p>
 
<p>3) установление порядка создания, реорганизации, ликвидации и финансирования образовательных учреждений субъекта Российской Федерации в соответствии с настоящим Федеральным законом;</p>
 
<p>(п. 3 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>4) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>5) разработка и реализация республиканских, региональных программ развития образования, в том числе международных, с учетом национальных и региональных социально-экономических, экологических, культурных, демографических и других особенностей;</p>
 
<p>5.1) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>6) формирование государственных органов управления образованием и руководство ими, назначение руководителей этих органов (по согласованию с федеральными органами управления образованием);</p>
 
<p>6.1) обеспечение государственных гарантий прав граждан на получение общедоступного и бесплатного начального общего, основного общего, среднего (полного) общего образования, а также дополнительного образования в общеобразовательных учреждениях посредством выделения субвенций местным бюджетам в размере, необходимом для реализации основных общеобразовательных программ в части финансирования расходов на оплату труда работников общеобразовательных учреждений, расходов на учебные пособия, технические средства обучения, расходные материалы и хозяйственные нужды (за исключением расходов на содержание зданий и коммунальных расходов, осуществляемых из местных бюджетов) в соответствии с нормативами, установленными законами субъекта Российской Федерации;</p>
 
<p>(пп. 6.1 в ред. Федерального закона от 22.08.2004 № 122-ФЗ (ред. 29.12.2004))</p>
 
<p>7) создание, реорганизация и ликвидация образовательных учреждений субъекта Российской Федерации, лицензирование образовательных учреждений, за исключением лицензирования на право ведения образовательной деятельности по программам высшего профессионального и послевузовского профессионального образования;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>8) установление региональных (национально-региональных) компонентов государственных образовательных стандартов;</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>9) формирование бюджетов субъектов Российской Федерации в части расходов на образование;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>10) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>11) установление нормативов финансирования образования субъекта Российской Федерации;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ (ред. 29.12.2004))</p>
 
<p>12) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>13) установление для образовательных учреждений, находящихся в ведении субъекта Российской Федерации, дополнительных к федеральным требований к образовательным учреждениям в части строительных норм и правил, санитарных норм, охраны здоровья обучающихся, воспитанников, оснащенности учебного процесса и оборудования учебных помещений;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>14) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>15) информационное обеспечение в пределах своей компетенции образовательных учреждений, организация издания учебной литературы, разработка базисных учебных планов и примерных программ курсов, дисциплин;</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>16) организация подготовки, переподготовки, повышения квалификации педагогических работников;</p>
 
<p>17) обеспечение соблюдения законодательства Российской Федерации в области образования и контроль исполнения государственных образовательных стандартов;</p>
 
<p>18) издание нормативных документов в пределах своей компетенции.</p>
 
