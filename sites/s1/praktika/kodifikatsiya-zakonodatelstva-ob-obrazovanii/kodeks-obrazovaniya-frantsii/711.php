<span class="textmar"> </span>
  <p><span class="textmar"><b>Статья L.711-5</b></span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Преобразование публичных учреждений высшего образования административного характера в публичные учреждения научного, культурного и профессионального характера осуществляется декретом.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Принимающие решение инстанции этих учреждений остаются при исполнении своих функций вплоть до применения новых уставных положений. Их исполнительные органы остаются при исполнении своих функций до окончания своего мандата. Если он закончился раньше, то срок действия этого мандата продлевается до выборов новых советов. Постановление министра, ответственного за высшее образование, устанавливает список тех из этих учреждений, устав которых вырабатывается переходными ассамблеями, которые должны включать половину выборных представителей советов, действующих на данный момент. Это постановление устанавливает также состав и правила функционирования этих ассамблей, а также срок, по окончании которого, если новые уставы не выработаны, министр прекращает их деятельность.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar"><b>Статья L.711-6</b></span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Положения статьей L.611-1, с L.612-1 по L.612-7, с L.613-1 по L.613-5, первого абзаца статьи L.614-3, статей первого раздела книги VII, за исключением статей с L.713-5 по L.713-8 и статей L.811-5, L.811-6, L.951-1, L.951-2, L.952-1, L.952-3, L.952-6, L.952-13, с L.953-1 по L.953-4 могут быть распространены декретом Государственного Совета, полностью или частично, с требуемыми адаптациями в случае необходимости на сектора подготовки и публичные учреждения высшего образования, которые не подчиняются власти или контролю со стороны министра, отвечающего за высшее образование, после согласования со всеми заинтересованными сторонами. Распространение обусловлено получением соответствующего заключения административных советов учреждений и согласием министра, который осуществляет над ними опеку.</span></p>
