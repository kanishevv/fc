</ul>

<p style="text-align: justify;"><span class="text">Пункт 3 комментируемой статьи предполагает, что при необходимости регламентации указанных в п. 1 комментируемой статьи сторон деятельности образовательного учреждения иными локальными актами последние рассматриваются в качестве дополнений (приложений) к уставу и подлежат утверждению учредителем и регистрации в федеральном органе исполнительной власти, уполномоченном в области государственной регистрации юридических лиц и индивидуальных предпринимателей в качестве изменений в устав образовательного учреждения.</span></p>

