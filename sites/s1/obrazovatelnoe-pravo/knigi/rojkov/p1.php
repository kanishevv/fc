 </span><span class="text">III. Ответственность исполнителя и потребителя</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">20.Исполнитель оказывает образовательные услуги в порядке и&nbsp; , определенные договором и уставом государственного или му­ниципального образовательного учреждения, негосударственной об­разовательной организации.</span>
</p>
<p style="text-align: justify;">
	<span class="text">21.За неисполнение либо ненадлежащее исполнение обязательств по договору исполнитель и потребитель несут ответственность, пре­дусмотренную договором и законодательством Российской Федера­ции.</span>
</p>
<p style="text-align: justify;">
	<span class="text">22.При обнаружении недостатков оказанных образовательных ус­луг, в том числе оказания их не в полном объеме, предусмотренном образовательными программами и учебными планами, потребитель вправе по своему выбору потребовать:</span>
</p>
<p style="text-align: justify;">
	<span class="text">а)безвозмездного оказания образовательных услуг, в том числе оказания образовательных услуг в полном объеме в соответствии с образовательными программами, учебными планами и договором;</span>
</p>
<p style="text-align: justify;">
	<span class="text">б) соответствующего уменьшения стоимости оказанных образова­тельных услуг;</span>
</p>
<p style="text-align: justify;">
	<span class="text">в)возмещения понесенных им расходов по устранению недостатков оказанных образовательных услуг своими силами или третьими лица­ми.</span>
</p>
<p style="text-align: justify;">
	<span class="text">23.Потребитель вправе отказаться от исполнения договора и по­требовать полного возмещения убытков, если в установленный дого­вором срок недостатки оказанных образовательных услуг не устране­ны исполнителем. Потребитель также вправе расторгнуть договор, если им обнаружены существенные недостатки оказанных образова­тельных услуг или иные существенные отступления от условий догово­ра<i>(в ред. постановления Правительства РФ от 28 декабря 2005 г.</i><i>№ 815, — Собрание законодательства РФ от 9 января 2006 г. № 1,ст. 156)</i>.</span>
</p>
<p style="text-align: justify;">
	<span class="text">24.Если исполнитель своевременно не приступил к оказанию обра­зовательных услуг или если во время оказания образовательных услуг стало очевидным, что оно не будет осуществлено в срок, а также в случае просрочки оказания образовательных услуг потребитель впра­ве по своему выбору:</span>
</p>
<p style="text-align: justify;">
	<span class="text">а)назначить исполнителю новый срок, в течение которого исполни­тель должен приступить к оказанию образовательных услуг и (или) за­кончить оказание образовательных услуг;</span>
</p>
<p style="text-align: justify;">
	<span class="text">б) поручить оказать образовательные услуги третьим лицам за ра­зумную цену и потребовать от исполнителя возмещения понесенных расходов;</span>
</p>
<p style="text-align: justify;">
	<span class="text">в)потребовать уменьшения стоимости образовательных услуг;</span>
</p>
<p style="text-align: justify;">
	<span class="text">г) расторгнуть договор.</span>
</p>
<p style="text-align: justify;">
	<span class="text">25.Потребитель вправе потребовать полного возмещения убытков, причиненных ему в связи с нарушением сроков начала и (или) оконча­ния оказания образовательных услуг, а также в связи с недостатками оказанных образовательных услуг.</span>
