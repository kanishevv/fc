<p align="JUSTIFY"><span class="text"><b>Федеральная служба по надзору в сфере образования и науки (Рособрнадзор)</b></span></p>

<p align="JUSTIFY"><span class="text">
    <br />
  </span></p>

<p align="JUSTIFY"><span class="text">Положение о Рособрнадзоре было утверждено постановлением Правительства РФ от17 июня 2004 года №300<sup><sup>224</sup></sup>, в котором Рособрнадзор назван федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в области образования и науки. Он находится в ведении Минобрнауки России.</span></p>

<p align="JUSTIFY"><span class="text">Среди полномочий Рособрнадзора в сфере образования отметим следующие:</span></p>

<p align="JUSTIFY"><span class="text">1) осуществляет контроль и надзор:</span></p>

<ul>
  <li>
    <p align="JUSTIFY"><span class="text">за исполнением законодательства Российской Федерации в области образования;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">за качеством образования в образовательных учреждениях и подготовки выпускников образовательных учреждений по завершении каждого уровня образования;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">за деятельностью советов по защите докторских и кандидатских диссертаций (диссертационных советов), ученых (научно-технических) советов образовательных учреждений по вопросам присвоения ученых званий работникам образовательных учреждений;</span></p>
  </li>
</ul>

<p align="JUSTIFY"><span class="text">2) осуществляет лицензирование, аттестацию и государственную аккредитацию образовательных учреждений и их филиалов, а также научных организаций (в сфере послевузовского и дополнительного профессионального образования). Данный пункт противоречит ст.21 Закона Российской Федерации &quot;Об образовании&quot; в части реализации образовательных программ дополнительного профессионального образования в научных организациях.</span></p>

<p align="JUSTIFY"><span class="text">3) осуществляет организационное и техническое обеспечение работы Высшей аттестационной комиссии;</span></p>

<p align="JUSTIFY"><span class="text">4) рассматривает в установленном порядке вопросы присвоения ученых званий профессора по специальности и профессора по кафедре, доцента по специальности и доцента по кафедре, присуждения ученых степеней доктора и кандидата наук и выдает соответствующие аттестаты и дипломы установленного образца, а также рассматривает вопросы лишения (восстановления) указанных ученых званий и степеней;</span></p>

<p align="JUSTIFY"><span class="text">5) выдает разрешения на создание советов по защите докторских и кандидатских диссертаций (диссертационных советов), устанавливает их компетенцию и определяет перечни научных специальностей, по которым им предоставляется право приема к защите диссертаций;</span></p>

<p align="JUSTIFY"><span class="text">6) подтверждает, признает и устанавливает эквивалентность документов об образовании, ученых степенях и званиях, полученных за рубежом и в Российской Федерации, и выдает соответствующие документы;</span></p>

<p align="JUSTIFY"><span class="text">7) осуществляет функции главного распорядителя и получателя средств федерального бюджета, предусмотренных на содержание Службы и реализацию возложенных на нее функций;</span></p>

<p align="JUSTIFY"><span class="text">8) организует прием граждан, обеспечивает своевременное и полное рассмотрение обращений граждан, принимает по ним решения и направляет заявителям ответы в установленный законодательством Российской Федерации срок;</span></p>

<p align="JUSTIFY"><span class="text">9) взаимодействует в установленном порядке с органами государственной власти иностранных государств и международными организациями в установленной сфере деятельности;</span></p>

<p align="JUSTIFY"><span class="text">10) проводит в установленном порядке конкурсы и заключает государственные контракты на размещение заказов на поставку товаров, выполнение работ, оказание услуг для нужд Службы, а также на проведение научно-исследовательских работ для государственных нужд в установленной сфере деятельности;</span></p>

<p align="JUSTIFY"><span class="text">11) осуществляет в установленном порядке проверку деятельности образовательных учреждений;</span></p>

<p align="JUSTIFY"><span class="text">12) осуществляет иные функции в установленной сфере деятельности.</span></p>

<p align="JUSTIFY"><span class="text">Для реализации перечисленных полномочий Рособрнадзор наделяется следующими правами:</span></p>

<p align="JUSTIFY"><span class="text">- организовывать проведение необходимых расследований, испытаний, экспертиз, анализов и оценок, а также научных исследований по вопросам осуществления контроля и надзора в установленной сфере деятельности;</span></p>

<p align="JUSTIFY"><span class="text">- давать юридическим и физическим лицам разъяснения по вопросам, отнесенным к компетенции Службы;</span></p>

<p align="JUSTIFY"><span class="text">- привлекать в установленном порядке для проработки вопросов, отнесенных к установленной сфере деятельности Службы, научные и иные организации, ученых и специалистов;</span></p>

<p align="JUSTIFY"><span class="text">- пресекать факты нарушения законодательства Российской Федерации в установленной сфере деятельности, а также применять предусмотренные законодательством Российской Федерации меры ограничительного, предупредительного и профилактического характера, направленные на недопущение и (или) ликвидацию последствий нарушений юридическими лицами и гражданами обязательных требований в установленной сфере деятельности;</span></p>

<p align="JUSTIFY"><span class="text">- создавать совещательные и экспертные органы (советы, комиссии, группы, коллегии) в установленной сфере деятельности.</span></p>

<p align="JUSTIFY"><span class="text">Рособрнадзор возглавляет руководитель, назначаемый на должность и освобождаемый от должности Правительством РФ по представлению Министра образования и науки РФ.</span></p>

<p align="JUSTIFY"><span class="text">Руководитель Рособрнадзора несет персональную ответственность за выполнение функций, возложенных на Службу.</span></p>

<p align="JUSTIFY"><span class="text">Он имеет заместителей, назначаемых на должность и освобождаемых от должности Министром образования и науки РФ по представлению руководителя Службы.</span></p>

<p align="JUSTIFY"><span class="text">Руководитель:</span></p>

<p align="JUSTIFY"><span class="text">1) распределяет обязанности между своими заместителями;</span></p>

<p align="JUSTIFY"><span class="text">2) представляет Министру образования и науки Российской Федерации:</span></p>

<ul>
  <li>
    <p align="JUSTIFY"><span class="text">проект положения о Федеральной службе по надзору в сфере образования и науки;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">предложения о предельной численности и фонде оплаты труда работников Службы;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">предложения о назначении на должность и освобождении от должности заместителей руководителя Службы;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">ежегодный план работы и прогнозные показатели деятельности Службы, а также отчет о ее деятельности;</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">предложения по формированию федерального бюджета и финансированию Службы;</span></p>
  </li>
</ul>

<p align="JUSTIFY"><span class="text">3) назначает на должность и освобождает от должности работников Службы;</span></p>

<p align="JUSTIFY"><span class="text">4) решает в соответствии с законодательством Российской Федерации о государственной службе вопросы, связанные с прохождением федеральной государственной службы в Рособрнадзоре;</span></p>

<p align="JUSTIFY"><span class="text">5) утверждает структуру и штатное расписание аппарата Службы в пределах установленных Правительством РФ численности и фонда оплаты труда работников, смету расходов на ее содержание в пределах утвержденных на соответствующий период ассигнований, предусмотренных в федеральном бюджете;</span></p>

<p align="JUSTIFY"><span class="text">6) издает приказы по вопросам, отнесенным к компетенции Службы.</span></p>

