 <b>Статья 26. Дополнительное образование</b></p>
 
<p>1. Дополнительные образовательные программы и дополнительные образовательные услуги реализуются в целях всестороннего удовлетворения образовательных потребностей граждан, общества, государства.</p>
 
<p>В пределах каждого уровня профессионального образования основной задачей дополнительного образования является непрерывное повышение квалификации рабочего, служащего, специалиста в связи с постоянным совершенствованием образовательных стандартов.</p>
 
<p>2. К дополнительным образовательным программам относятся образовательные программы различной направленности, реализуемые:</p>
 
