 </p>
 
<p><b>Статья 46. Платная образовательная деятельность негосударственного образовательного учреждения</b></p>
 
<p>1. Негосударственное образовательное учреждение вправе взимать плату с обучающихся, воспитанников за образовательные услуги, в том числе за обучение в пределах государственных образовательных стандартов.</p>
 
<p>2. Платная образовательная деятельность такого образовательного учреждения не рассматривается как предпринимательская, если получаемый от нее доход полностью идет на возмещение затрат на обеспечение образовательного процесса (в том числе на заработную плату), его развитие и совершенствование в данном образовательном учреждении.</p>
 
<p align="LEFT">3. Взаимоотношения негосударственного образовательного учреждения и обучающегося, воспитанника, его родителей (законных представителей) регулируются договором, определяющим уровень образования, сроки обучения, размер платы за обучение, иные условия.</p>
 
