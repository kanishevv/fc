<p><b>Глава IV. ЭКОНОМИКА СИСТЕМЫ ОБРАЗОВАНИЯ</b></p>
 
<p> </p>
 
<p><b>Статья 39. Отношения собственности в системе образования</b></p>
 
<p>1. За образовательным учреждением в целях обеспечения образовательной деятельности в соответствии с его уставом учредитель закрепляет объекты права собственности (землю, здания, сооружения, имущество, оборудование, а также другое необходимое имущество потребительского, социального, культурного и иного назначения), принадлежащие учредителю на праве собственности или арендуемые им у третьего лица (собственника).</p>
 
<p>Земельные участки закрепляются за государственными и муниципальными образовательными учреждениями в бессрочное бесплатное пользование.</p>
 
<p>2. Объекты собственности, закрепленные учредителем за образовательным учреждением, находятся в оперативном управлении этого учреждения.</p>
 
<p>3. Образовательное учреждение несет ответственность перед собственником за сохранность и эффективное использование закрепленной за этим учреждением собственности. Контроль деятельности образовательного учреждения в этой части осуществляется учредителем или иным юридическим лицом, уполномоченным собственником.</p>
 
<p>4. Государственная и (или) муниципальная собственность, закрепленная за образовательным учреждением, может отчуждаться собственником в порядке и на условиях, которые установлены законодательством Российской Федерации, законодательством субъектов Российской Федерации и правовыми актами органов местного самоуправления, принятыми в пределах своих полномочий.</p>
 
<p>5. Негосударственное образовательное учреждение (организация) может быть собственником имущества в соответствии с законодательством Российской Федерации.</p>
 
<p>(п. 5 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>6. Изъятие и (или) отчуждение собственности, закрепленной за образовательным учреждением, допускаются только по истечении срока договора между собственником (уполномоченным им юридическим лицом) и образовательным учреждением или между собственником (уполномоченным им юридическим лицом) и учредителем, если иное не предусмотрено этим договором.</p>
 
<p>7. Образовательному учреждению принадлежит право самостоятельного распоряжения в соответствии с законодательством Российской Федерации средствами, полученными за счет внебюджетных источников.</p>
 
<p>(п. 7 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>8. Негосударственная образовательная организация может быть признана банкротом на общих основаниях, установленных законом.</p>
 
<p>9. Образовательное учреждение отвечает по своим обязательствам находящимися в его распоряжении денежными средствами. При недостаточности у образовательного учреждения указанных средств ответственность по его обязательствам несет собственник имущества, закрепленного за образовательным учреждением, в порядке, определяемом законом.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>10. При ликвидации образовательного учреждения денежные средства и иные объекты собственности за вычетом платежей по покрытию своих обязательств направляются на цели развития образования в соответствии с уставом образовательного учреждения.</p>
 
<p>(п. 10 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>11. Образовательное учреждение вправе выступать в качестве арендатора и арендодателя имущества.</p>
 
<p>(п. 11 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>12. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>13. Государственные и муниципальные образовательные учреждения, закрепленные за ними на праве оперативного управления или находящиеся в их самостоятельном распоряжении объекты производственной и социальной инфраструктуры, в том числе жилые помещения, расположенные в зданиях учебного, производственного, социального, культурного назначения и в сельской местности, общежития, а также клинические базы учреждений медицинского образования, находящиеся в оперативном управлении образовательных учреждений или в ином ведении, приватизации (разгосударствлению) не подлежат.</p>
 
