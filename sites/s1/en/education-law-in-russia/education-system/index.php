 <span class="textmar"><b>Types and levels of education</b></span>
</div>
<div style="text-align: justify;">
 <span class="textmar">The federal law on Education in Russian Federation No. 273-FZ of 29 December 2012 changed the system of levels of education (educational qualifications).</span>
</div>
<div style="text-align: justify;">
 <span class="textmar"> Education is divided into general education, professional education, non-formal supplementary education (extracurriculum education for children and further education for adults, further professional education) and vocational training, providing the possibility of implementing the right to education throughout life (lifelong learning). </span>
</div>
<div style="text-align: justify;">
 <span class="textmar">The following levels of general education have been established: </span>
</div>
<ol>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">preschool education;</span>
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
		 primary general education;
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
		 basic general education;
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
		 secondary general education.
	</div>
 </li>
</ol>
<div style="text-align: justify;">
 <span class="textmar">As for the professional education the following levels have been established:</span>
	<ol>
		<li>secondary vocational education;</li>
		<li>higher education - bachelor;</li>
		<li>higher education – specialist, master;</li>
		<li>higher education – training of highly qualified personnel.</li>
	</ol>
</div>
<div style="text-align: justify;">
 <span class="textmar">It should be noted that educational levels (educational qualifications) established according to the new federal law are renamed and/or restructured as follows:</span>
</div>
<div style="text-align: justify;">
 <span class="textmar"></span>
</div>
<ol>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">secondary (complete) general education becomes secondary education;</span>
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">initial vocational training becomes secondary vocational education - training of skilled workers (employees);</span>
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">higher professional education of bachelor, specialist or master levels remains the same;</span>
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">postgraduate education (PhD, internship, residency) is renamed as training of highly qualified personnel.</span>
	</div>
 </li>
</ol>
<div style="text-align: justify;">
 <span class="textmar"></span>
</div>
<div style="text-align: justify;">
 <span class="textmar">In accordance with the new classification of education levels the names of educational programs are also altered. The law provides for the implementation of primary and secondary education programs (curricula).</span>
</div>
<div style="text-align: justify;">
 <span class="textmar">Basic educational curricula include:</span>
</div>
<div style="text-align: justify;">
 <span class="textmar"></span>
</div>
<ol>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">basic general educational curricula - educational curricula for preschool education, primary general education, basic general education, secondary general education;</span>
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">basic vocational educational curricula: a) educational curricula of secondary vocational education - educational curricula of qualified workers’ and officers’ training, educational curricula of middle ranking personnel training; b) educational curricula of higher education – bachelors’ educational curricula, specialists’ educational curricula, masters’ educational curricula, educational curricula of academic and pedagogical personnel training in postgraduate training (adjunct training), residency training educational curricula, educational curricula of assistantship; </span>
	</div>
 </li>
	<li>
	<div style="text-align: justify;">
 <span class="textmar">basic vocational training curricula - vocational training curricula by professions of workers and posts of officers, workers’ and officers’ retraining curricula, workers’ and officers’ advanced training curricula.</span>
	</div>
 </li>
</ol>
<div style="text-align: justify;">
 <span class="textmar"></span>
</div>
<div style="text-align: justify;">
 <span class="textmar">Supplementary educational curricula consist of<br>
	<ol>
		<li>supplementary general educational curricula - supplementary general enrichment curricula, supplementary prior to vocational education curricula;</li>
		<li>supplementary vocational educational curricula - advanced training curricula, vocational retraining curricula.</li>
	</ol>
 </span>
</div>
<div style="text-align: justify;">
 <span class="textmar">From the list of basic professional curricula educational programs of initial vocational education are excluded due to the abolition of primary vocational education as a separate level of education. These programs are restructured to be included into either educational programs of secondary vocational education or vocational training programs.</span>
</div>
<div style="text-align: justify;">
 <span class="textmar">Postgraduate doctoral education is excluded from the list of educational programs of higher education and becomes classified as a scientific research program regulated by the legislation on science and state science and technology policy.</span>
</div>
<div style="text-align: justify;">
 <span class="textmar">The forms and methods of receiving education are also modified as follows. Education can be obtained either from an educational organisation (institutional forms of education) full-time or part-time, or through non-formal and informal forms, such as family education and self-learning with the right to pass intermediate and final state examinations in order to obtain certification from educational organisations.</span>
</div>
<div style="text-align: justify;">
 <span class="textmar">Such form of education as ‘externship’ is thus excluded from the list of forms and methods. External students can be trained both in the form of self-education and family education, as well as in any other form of education, in non-accredited educational organisations.</span>
</div>
<div style="text-align: justify;">
 <span class="textmar">Special forms of training are provided for students who need long-term treatment or children with disabilities, whose state of health does not allow them to attend educational institutions. Training in educational programs of primary general, basic and secondary education is organised for such individuals at home or in health care facilities.</span>
</div>
<div style="text-align: justify;">
 <span class="textmar">A combination of different forms of education and training forms is allowed.</span>
</div>
<div style="text-align: justify;">
 <span class="textmar">It is important to mention that for professional education the form and method of training for the main curricula is determined by the appropriate federal state educational standards, whereas forms of education and training for supplementary educational programs and vocational training programs are now defined by the organisations conducting educational activities on their own.</span>
