</p>
<p style="text-align: justify;">
	<span class="text">б)требовать надлежащего исполнения Подрядчиком его обязанно­стей, предусмотренных настоящим договором;</span>
</p>
<p style="text-align: justify;">
	<span class="text">в)изменить условия настоящего договора в отношении места и объе­ма выполнения работ или отказаться от договора в любой момент при условии возмещения Подрядчику понесенных им убытков в связи с исполнением настоящего договора.</span>
