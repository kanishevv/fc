<p><b>Статья 18. Дошкольное образование</b></p>
 
<p>1. Родители являются первыми педагогами. Они обязаны заложить основы физического, нравственного и интеллектуального развития личности ребенка в раннем детском возрасте.</p>
 
<p>2. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>3. Для воспитания детей дошкольного возраста, охраны и укрепления их физического и психического здоровья, развития индивидуальных способностей и необходимой коррекции нарушений развития этих детей в помощь семье действует сеть дошкольных образовательных учреждений.</p>
 
<p>4. Отношения между дошкольным образовательным учреждением и родителями (законными представителями) регулируются договором между ними, который не может ограничивать установленные законом права сторон.</p>
 
<p>5. Органы местного самоуправления организуют и координируют методическую, диагностическую и консультативную помощь семьям, воспитывающим детей дошкольного возраста на дому.</p>
 
