<p><b>Статья 51. Охрана здоровья обучающихся, воспитанников</b></p>
 
<p> 
  <br />
 1. Образовательное учреждение создает условия, гарантирующие охрану и укрепление здоровья обучающихся, воспитанников.</p>
 
<p>Учебная нагрузка, режим занятий обучающихся, воспитанников определяются уставом образовательного учреждения на основе рекомендаций, согласованных с органами здравоохранения.</p>
 
<p>2. Для детей, нуждающихся в длительном лечении, организуются оздоровительные образовательные учреждения, в том числе санаторного типа. Учебные занятия для таких детей могут проводиться образовательными учреждениями на дому или в лечебных учреждениях. Обеспечение указанных мероприятий является расходным обязательством субъекта Российской Федерации.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>3. Педагогические работники образовательных учреждений обязаны проходить периодические бесплатные медицинские обследования, которые проводятся за счет средств учредителя.</p>
 
<p>4. Медицинское обслуживание обучающихся, воспитанников образовательного учреждения обеспечивают органы здравоохранения. Образовательное учреждение обязано предоставить помещение с соответствующими условиями для работы медицинских работников.</p>
 
<p>5. Расписание занятий в образовательном учреждении должно предусматривать перерыв достаточной продолжительности для питания обучающихся, воспитанников.</p>
 
<p>Организация питания в образовательном учреждении возлагается на образовательные учреждения. В образовательном учреждении должно быть предусмотрено помещение для питания обучающихся, воспитанников.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>6. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>7. Ответственность за создание необходимых условий для учебы, труда и отдыха обучающихся, воспитанников образовательных учреждений несут должностные лица образовательных учреждений в соответствии с законодательством Российской Федерации и уставом данного образовательного учреждения.</p>
 
