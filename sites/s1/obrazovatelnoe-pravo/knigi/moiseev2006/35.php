<p style="text-align: justify;"><span class="text">Наличие обязательно заполняемой графы «цена вопроса» ориентирует разработчиков Программ на понимание и расчет необходимых финансовых ресурсов по каждой строчке бюджета Программы.</span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p> </p>

<span style="font-size: 13px;"><span class="text"></span></span><p><b>Меры по ресурсному обеспечению перехода на профильное&nbsp;</b><b>обучение</b></p>
 
<p></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">Успешность массового перехода на профильное обучение сильно зависит от количества, качества, своевременности получения и грамотности использования разнообразных ресурсов, таких как:</span></p>
 
<ul> 
  <p style="text-align: justify;"><span class="text">имеющаяся образовательная сеть и инфраструктура, аудиторный фонд, который можно использовать для организации профильного обучения,</span></p>
 
  <p style="text-align: justify;"><span class="text">структурно-институциональные ресурсы, новые образовательные, сервисные и управленческие структуры, создаваемые для работы по профильному обучению,</span></p>
 
  <p style="text-align: justify;"><span class="text">нормативно-правовые и иные документы,</span></p>
 
  <p style="text-align: justify;"><span class="text">концептуальные ресурсы - идеи и опыт организации профильного обучения,</span></p>
 
  <p style="text-align: justify;"><span class="text">программно-методическое обеспечение - БУПы, стандарты, программы, учебники, дидактические материалы, методические пособия базового и профильного уровней (в том числе - по элективным курсам);</span></p>
 
  <p style="text-align: justify;"><span class="text">информация,</span></p>
 
  <p style="text-align: justify;"><span class="text">кадры, способные организовывать, и осуществлять и сопровождать;</span></p>
 
  <p style="text-align: justify;"><span class="text">ресурсы системы обучения (подготовки, переподготовки и повышения квалификации) и воспитания таких кадров,</span></p>
 
  <p style="text-align: justify;"><span class="text">материально-технические ресурсы, оборудование, средства обучения, транспортные средства и т.д.,</span></p>
 
  <p style="text-align: justify;"><span class="text">рекламно-пропагандистское и PR-сопровождение;</span></p>
 
  <p style="text-align: justify;"><span class="text">полезные связи (как ресурс);</span></p>
 
  <p style="text-align: justify;"><span class="text">ресурсы стимулирования и поддержки;</span></p>
 
  <p style="text-align: justify;"><span class="text">финансовое обеспечение (последнее приведено в конце списка не в силу малой важности, а потому, что в конечном счете все (или почти все) прочие ресурсы приходится в конечном счете оценивать в стоимостном выражении).</span></p>
 </ul>
 
<p style="text-align: justify;"><span class="text">Эти и иные виды ресурсов также сыграют при планировании роль основных задач (направлений) работы, по которым и идет проектирование и описание действий.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом разработчики Программ должны обеспечить четкую увязку ресурсообеспечивающих мер, действий и мероприятий с главными, собственно образовательными мероприятиями (см. предыдущий фрагмент) и главными целями Программ по содержанию и по времени<b>(речь идет о желательном опережении получения нужных ресурсов, особенно кадровых, нормативных, программно-методических, финансовых по отношению к изменениям непосредственно в процессах обучения учащихся).</b></span></p>
 
<p style="text-align: justify;"><span class="text">Возможная форма планирования для данного фрагмента Программ приведена в таблице 26.</span></p>
 
<p style="text-align: justify;"><span class="text">Ожидаемые результаты в этой матрице будут описаны в терминах конкретных видов ресурсов и их использования. Например, по направлению подготовка, переподготовка и повышении квалификации кадров в качестве результатов могут быть перечислены количество подготовленных педагогов, методистов, руководителей, тьюторов, профориентаторов.</span></p>
 
