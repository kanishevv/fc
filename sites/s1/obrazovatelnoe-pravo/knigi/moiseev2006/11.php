 <span class="text"><b>Полная готовность</b>, когда имеются все необходимые и достаточные условия, и речь идет только о времени, которое требуется для массового перехода на профильное обучение.</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span class="text"><b>Неполная готовность</b>, когда наличествуют лишь некоторые из этих условий, и создание недостающих условий требует дополнительного времени и усилий.</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
