<p><span class="textmar">По просьбе заинтересованных правительств и после получения заключения комиссии по инженерным званиям дипломы и звания инженеров могут быть приняты Государством. Они должны содержать указание страны выдачи.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
