  <p> </p>
 
  <p><b>Статья L.231-11</b></p>
 
  <p> </p>
 
  <p>Прошения об отмене, составленные в соответствии со статьей L.231-10, могут быть представлены только после истечения минимального срока после вынесения окончательных решений.</p>
 
  <p> </p>
 
  <p>Срок составляет два года для временных отстранений, запрещения и отчисления постоянного характера.</p>
 
