<p align="justify"><b>Статья L.231-2</b></p>
 
<p align="justify"> </p>
 
<p align="justify">Высший Совет образования возглавляется министром, ответственным за образование, или его представителем и состоит из представителей преподавателей, преподавателей-исследователей, другого персонала, родителей учащихся, студентов, учащихся лицеев, представителей территориальных объединений, внешкольных и семейных ассоциаций, представителей воспитательных, экономических, социальных и культурных интересов.</p>
 
