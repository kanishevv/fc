?><span class="textmar"><b>Статья L.442-13</b></span>
<p> </p>

<p><span class="textmar">Заключение контракта, предусматриваемого в статьях L.442-5 и L.442-12, подчиняется в том, что касается классов частных школ, правилам и критериям, принятым для открытия и закрытия соответствующих классов публичного образования, причем все условия функционирования являются равными.</span></p>

<p> </p>

<p><span class="textmar">В том, что касается классов частных образовательных учреждений второй ступени, заключение контракта подчиняется правилам и критериям, упомянутым в предыдущем абзаце и, кроме того, совместимостью с оценкой общих потребностей, фигурирующей в прогнозируемых схемах, региональных планах и в карте высших подготовок, предусмотренных в статьях L.214-1 и L.214-2.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.442-14</b></span></p>

<p> </p>

<p><span class="textmar">Сумма кредитов, предоставляемых для оплаты преподавательского персонала классов, являющихся объектом контрактов, предусмотренных в статьях L.442-5 и L.442-12, за их преподавательскую деятельность, определяется ежегодно Законом о бюджете. Она фиксируется в зависимости от численности принятых учащихся и типов подготовки, осуществляемой в публичных образовательных учреждениях и в классах в режиме контракта частных образовательных учреждений, и принимая во внимание специфические трудности, с которыми сталкиваются публичные образовательные учреждения в связи с особыми демографическими и лингвистическими условиями. Любой новый контракт может быть заключен только в пределах кредитов, упомянутых в настоящей статье.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.442-15</b></span></p>

<p> </p>

<p><span class="textmar">Частные образовательные учреждения, заключившие с Государством один из контрактов, предусматриваемых статьями L.442-5 и L.442-12, получают от Государства в пределах кредитов, вписанных на эти цели в Закон о бюджете, субсидию на капиталовложения, которые они используют по статье для строительства, обустройства и оборудования, предназначенных для дополнительного обучения, готовящего к профессиональной подготовке, предусматриваемой в статье L.332-3.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.442-16</b></span></p>

<p> </p>

<p><span class="textmar">Частные образовательные учреждения, заключившие с Государством один из контрактов, предусматриваемых статьями L.442-5 и L.442-12, получают от Государства в пределах кредитов, вписанных на эти цели в Закон о бюджете, либо информационные педагогические материалы, необходимые для выполнения программ обучения первой и второй ступени, либо субсидии, позволяющие приобрести эти материалы.</span></p>

<p> </p>

<p><span class="textmar">Территориальные объединения могут участвовать в приобретении дополнительных информационных материалов, упомянутых выше, но их участие не должно превосходить то, которое они оказывают публичным образовательным учреждениям, за которые они несут ответственность в применении статьей L.212-4, L.213-2 и L.214-6.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.442-17</b></span></p>

<p> </p>

<p><span class="textmar">Гарантия Государства может быть предоставлена на условиях, определяемых декретом, по займам, выданным объединениями или ассоциациями национального масштаба, для финансирования строительства, приобретения и переоборудования учебных помещений, используемых частными учреждениями, готовящими к дипломам, выдаваемым Государством.</span></p>

<p> </p>

<p><span class="textmar">Такая же возможность предоставлена коммунам для школ, департаментам &ndash; для колледжей, регионам &ndash; для лицеев в интересах объединений и ассоциаций местного масштаба.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.442-18</b></span></p>

<p> </p>

<p><span class="textmar">Декреты, принятые советом министров и будучи заслушанными Государственным Советом, определяют необходимые меры для применения статей L.141-2, L.151-1, L.442-1, L.442-2, L.442-4, L.442-5, L.442-12, L.442-15, L.914 -1 иL.914-2.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.442-19</b></span></p>

<p> </p>

<p><span class="textmar">Положения статей с L.442-8 по L.442-11 и L.442-13 применяются к частным образовательным сельскохозяйственным учреждениям, если только они подчиняются положениям главы III раздела I книги VIII Кодекса сельского хозяйства.</span></p>

