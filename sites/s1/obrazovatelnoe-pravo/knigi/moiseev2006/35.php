<p style="text-align: left;"><span class="text" style="text-align: center;"></span></p><div><b>Общая стратегия перехода на профильное обучение&nbsp;(на примере регионального уровня)</b></div><p></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">После определения главных, стратегических целей конкретных Программ необходимо выбрать и четко сформулировать в Программе ее<b>стратегию</b>- стратегию массового перехода региональной образовательной системы на профильное обучение, то есть<b>принципиальный подход, принципиальное управленческое решение о главных путях и средствах достижения программных целей.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Стратегия разрабатывается на основе данных анализа и прогнозирования, кратко представленных в информационно-аналитическом фрагменте конкретной Программы и предполагает ответ на вопросы:</span></p>
 
<ol> 
  <p style="text-align: justify;"><span class="text">Какой&nbsp;<b>принципиальный подход к переходу на профильное обучение будет осуществляться в нашем регионе?</b></span></p>
 
  <p style="text-align: justify;"><span class="text">Каковы&nbsp;<b>важнейшие области изменений</b>, производимых в образовательной системе и ее связях с внешней средой для достижения поставленных целей?</span></p>
 
  <p style="text-align: justify;"><span class="text">Каковы важнейшие&nbsp;<b>векторы изменений в каждой из выбранных областей (от чего - к чему идет переход)?</b></span></p>
 
  <p style="text-align: justify;"><span class="text">Каковы основные&nbsp;<b>компоненты (составляющие, направления) деятельности, ведущей к достижению поставленных стратегических целей?</b></span></p>
 
  <p style="text-align: justify;"><span class="text">Каковы важнейшие&nbsp;<b>рубежи перехода, что должно быть достигнуто в ходе стратегических действий?</b></span></p>
 
  <p style="text-align: justify;"><span class="text">Каковы&nbsp;<b>последовательность этих действий, их этапы и как выглядит общая стратегия наших действий по этапам и шагам?</b></span></p>
 </ol>
 
<p style="text-align: justify;"><span class="text">Общая стратегия перехода на профильное обучение, складывающаяся из ответов на эти вопросы, имеет свою&nbsp;<b>логику развертывания и составные части (частные стратегии)</b>.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом логика описания стратегии может и совпадать с логикой ее выстраивания, и быть отличной от нее. При проектировании важно отстраивать планируемые действия от цели. Ближе всего к такой цели стоят&nbsp;<b>действия по изменению в образовательном процессе на завершающем этапе общего образования (его целях, содержании, технологии, организации, условиях и т.п.)</b>. Исходя из этих планируемых изменений необходимо обеспечить «подачу» требуемых для них ресурсов, а затем спланировать, как наилучшим образом управлять всей этой системой.</span></p>
 
<p style="text-align: justify;"><span class="text">При изложении стратегии часто идут наоборот - «сверху» - от управления и ресурсного обеспечения - к изменениям в образовании.</span></p>
 
<p style="text-align: justify;"><span class="text">В этом случае сначала приводят краткое изложение&nbsp;<b>стратегии управления переходом на профильное обучение,&nbsp;</b>так как эти изменения должны по возможности опережать&nbsp;<b>изменения в образовательной и ресурсообеспечивающей системе и процессах.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Далее важно описать стратегию&nbsp;<b>ресурсного обеспечения перехода на профильное обучение, который, по возможности, должен проходить в опережающем режиме по отношению к собственно образовательным нововведениям. Хотя всем ясно, что ожидать с началом изменений в школах до полного получения всех желаемых ресурсов - было бы ошибочно и контрпродуктивно.</b></span></p>
 
<p style="text-align: justify;"><span class="text"><b>Наконец, необходимо описать стратегию изменений в образовательной системе и процессе</b>(для основной и старшей ступеней общего образования).</span></p>
 
<p style="text-align: justify;"><span class="text">Логика описания стратегии предполагает также разумную меру<b>детализации описания</b>. Например, возможно описание<b>единой переходной стратегии региона</b>, но более информативным и полезным окажется выстраивание<b>вариативных стратегий перехода для областного центра, крупных и малых городов и сельской местности, для разных типов муниципальных образований региона.</b>В Региональной Программе далее, рекомендуется «прописывать» стратегии (варианты стратегий) перехода на профильное обучение на<b>местном и школьном уровнях.</b></span></p>
 
<p style="text-align: justify;"><span class="text">При выборе и описании региональной стратегии на профильное обучение важно видеть<b>«стратегические развилки»,</b>то есть такие точки, которые требуют выбора одной из возможных стратегий.</span></p>
 
<p style="text-align: justify;"><span class="text">Цели Программ, взятые вместе со стратегиями их достижения,образуют<b>стратегический план перехода на профильное обучение.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Описание стратегии (как и стратегических целей в Региональной Программе), должно быть:</span></p>
 
<p style="text-align: justify;"><span class="text">а) весьма кратким, лаконичным;</span></p>
 
<p style="text-align: justify;"><span class="text">б) четким и понятным;</span></p>
 
<p style="text-align: justify;"><span class="text">в) не подменяющим описания конкретных мер, действий и мероприятий (если в стратегическом плане важен только принципиальный подход и направления «главного удара», то в последующих, «тактических» и «оперативных» фрагментах Программ, происходит их необходимая, но также не чрезмерная конкретизация и детализация).</span></p>
 
<p style="text-align: justify;"><span class="text">При описании целей и стратегии Программ перехода надо стремиться к тому, чтобы воображаемый читатель, эксперт или будущий исполнитель конкретной Программы смог бы&nbsp;<b>однозначно понять, в чем главная суть и специфика подхода к организации массового перехода на профильное обучение в данном образовательном учреждении, муниципальном образовании, субъекте РФ, даже не прибегая к чтению дальнейших детализирующих и конкретизирующих планов.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Примерный вариант изложения данного раздела Региональной Программы приводится ниже.</span></p>
 
