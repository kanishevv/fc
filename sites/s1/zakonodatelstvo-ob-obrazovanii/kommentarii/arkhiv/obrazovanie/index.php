<p><b>Статья 1. Государственная политика в области образования</b></p>
 
<p>1. Российская Федерация провозглашает область образования приоритетной.</p>
 
<p>2. Организационной основой государственной политики Российской Федерации в области образования является Федеральная целевая программа развития образования.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>3. Федеральная целевая программа развития образования разрабатывается и утверждается Правительством Российской Федерации.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>4. Доклад Правительства Российской Федерации о ходе реализации Федеральной целевой программы развития образования ежегодно представляется палатам Федерального Собрания Российской Федерации и публикуется в официальном печатном органе.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>5. В государственных и муниципальных образовательных учреждениях, органах управления образованием создание и деятельность организационных структур политических партий, общественно-политических и религиозных движений и организаций (объединений) не допускаются.</p>
 
