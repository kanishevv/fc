<p><b>Статья 35. Управление государственными и муниципальными образовательными учреждениями</b></p>
 
<p>1. Управление государственными и муниципальными образовательными учреждениями осуществляется в соответствии с законодательством Российской Федерации и уставом соответствующего образовательного учреждения.</p>
 
<p>2. Управление государственными и муниципальными образовательными учреждениями строится на принципах единоначалия и самоуправления. Формами самоуправления образовательного учреждения являются совет образовательного учреждения, попечительский совет, общее собрание, педагогический совет и другие формы. Порядок выборов органов самоуправления образовательного учреждения и их компетенция определяются уставом образовательного учреждения.</p>
 
<p>3. Непосредственное управление государственным или муниципальным образовательным учреждением осуществляет прошедший соответствующую аттестацию заведующий, директор, ректор или иной руководитель (администратор) соответствующего образовательного учреждения.</p>
 
<p>Статус руководителя государственного образовательного учреждения федерального подчинения устанавливается Правительством Российской Федерации.</p>
 
<p>4. Руководитель государственного и муниципального образовательного учреждения в соответствии с уставом соответствующего образовательного учреждения может быть:</p>
 
<p>1) избран коллективом образовательного учреждения;</p>
 
<p>2) избран коллективом образовательного учреждения при предварительном согласовании кандидатуры (кандидатур) с учредителем;</p>
 
<p>3) избран коллективом образовательного учреждения с последующим утверждением учредителем;</p>
 
<p>4) назначен учредителем с предоставлением совету образовательного учреждения права вето;</p>
 
<p>5) назначен учредителем;</p>
 
<p>6) нанят учредителем.</p>
 
<p>В гражданских образовательных учреждениях высшего профессионального образования назначение ректоров не допускается.</p>
 
<p>5. Разграничение полномочий между советом образовательного учреждения и руководителем образовательного учреждения определяется уставом образовательного учреждения.</p>
 
<p>6. Руководителям государственных и муниципальных образовательных учреждений совмещение их должностей с другими руководящими должностями (кроме научного и научно-методического руководства) внутри или вне образовательных учреждений не разрешается.</p>
 
<p>7. Должностные обязанности руководителей государственных и муниципальных образовательных учреждений, их филиалов (отделений) не могут исполняться по совместительству.</p>
 
