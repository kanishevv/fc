<p><b>Статья 56. Трудовые отношения в системе образования</b></p>
 
<p>1. Для работников образовательного учреждения работодателем является данное образовательное учреждение, если иное не предусмотрено законодательством Российской Федерации.</p>
 
<p>2. Лицо, лишенное решением суда права работать в образовательном учреждении в течение определенного срока, не может быть принято на работу в образовательное учреждение в течение этого срока.</p>
 
<p>(п. 2 введен Федеральным законом от 25.07.2002 № 112-ФЗ)</p>
 
<p>3. Трудовые отношения работника образовательного учреждения и образовательного учреждения регулируются трудовым договором (контрактом). Условия трудового договора (контракта) не могут противоречить законодательству Российской Федерации о труде.</p>
 
<p>4. Помимо оснований прекращения трудового договора по инициативе администрации, предусмотренных законодательством Российской Федерации о труде, основаниями для увольнения педагогического работника образовательного учреждения по инициативе администрации этого образовательного учреждения до истечения срока действия трудового договора (контракта) являются:</p>
 
<p>1) повторное в течение года грубое нарушение устава образовательного учреждения;</p>
 
<p>2) применение, в том числе однократное, методов воспитания, связанных с физическим и (или) психическим насилием над личностью обучающегося, воспитанника;</p>
 
<p>3) появление на работе в состоянии алкогольного, наркотического или токсического опьянения.</p>
 
<p>Увольнение по настоящим основаниям может осуществляться администрацией без согласия профсоюза.</p>
 
