<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.642-2</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Лица, именующие себя &laquo;дипломированными инженерами&raquo;, должны непосредственно сопровождать это название одним из инженерных званий, созданных Государством или признанных Государством, или одним из инженерных званий, законным образом депонированных в соответствии со статьями L.642-4 и L.642-10.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.642-3</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Комиссия по инженерным званиям, члены которой назначаются министром, ответственным за высшее образование, запрашивается по всем вопросам, касающимся званий дипломированного инженера.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Состав этой комиссии устанавливается декретом Государственного Совета; она включает, в частности, представительство университетов, институтов, школ и Больших учреждений, а также профессиональных организаций.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.642-4</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Комиссия по инженерным званиям решает в первой инстанции и по их требованию, представляют ли программы и дают ли образование достаточное для выдачи диплома инженера частные технические школы, открытые на законном основании.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Эти решения могут быть приняты только после получения доклада об этих программах и этом образовании одного или нескольких инспекторов или лиц, которым поручена инспекция.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.642-5</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Представители заинтересованных школ получают сообщение о докладе или докладах инспекции и могут попросить быть заслушанными; им разрешается предоставить все элементы информации, которую они считают полезной. Они могут так же, как и министр, ответственный за высшее образование, подавать апелляционную жалобу в течение двух месяцев после решения в Высший Совет образования, который выносит решение в последней инстанции.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Жалоба рассматривается при выслушивании противоположных сторон в течение трех месяцев.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Ни в коем случае, выдача дипломов не может иметь места до решения по апелляции.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Решения комиссии по инженерным званиям, а также Высшего Совета образования являются мотивированными.</span></p>
