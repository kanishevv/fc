 <b>Статья 13. Устав образовательного учреждения</b></p>
 
<p>1. В уставе образовательного учреждения в обязательном порядке указываются:</p>
 
<p>1) наименование, место нахождения (юридический, фактический адрес), статус образовательного учреждения;</p>
 
<p>2) учредитель;</p>
 
<p>3) организационно-правовая форма образовательного учреждения;</p>
 
<p>4) цели образовательного процесса, типы и виды реализуемых образовательных программ;</p>
 
<p>5) основные характеристики организации образовательного процесса, в том числе:</p>
 
<p>а) язык (языки), на котором ведутся обучение и воспитание;</p>
 
<p>б) порядок приема обучающихся, воспитанников;</p>
 
<p>в) продолжительность обучения на каждом этапе обучения;</p>
 
<p>г) порядок и основания отчисления обучающихся, воспитанников;</p>
 
<p>д) система оценок при промежуточной аттестации, формы и порядок ее проведения;</p>
 
<p>е) режим занятий обучающихся, воспитанников;</p>
 
<p>ж) наличие платных образовательных услуг и порядок их предоставления (на договорной основе);</p>
 
<p>з) порядок регламентации и оформления отношений образовательного учреждения и обучающихся, воспитанников и (или) их родителей (законных представителей);</p>
 
<p>6) структура финансовой и хозяйственной деятельности образовательного учреждения, в том числе в части:</p>
 
<p>а) использования имущества, закрепленного за образовательным учреждением;</p>
 
<p>(пп. &quot;а&quot; в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>б) финансирования и материально-технического обеспечения деятельности образовательного учреждения;</p>
 
<p>в) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>г) осуществления предпринимательской и иной приносящей доход деятельности;</p>
 
<p>(пп. &quot;г&quot; в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>д) запрета на совершение сделок, возможными последствиями которых является отчуждение или обременение имущества, закрепленного за образовательным учреждением, или имущества, приобретенного за счет средств, выделенных этому учреждению собственником образовательного учреждения;</p>
 
<p>(пп. &quot;д&quot; введен Федеральным законом от 22.08.2004 № 122-ФЗ)</p>
 
<p>е) порядка распоряжения имуществом, приобретенным учреждением за счет доходов, полученных от предпринимательской и иной приносящей доход деятельности;</p>
 
<p>(пп. &quot;е&quot; введен Федеральным законом от 22.08.2004 № 122-ФЗ)</p>
 
<p>ж) открытия счетов в органах казначейства (за исключением негосударственных образовательных учреждений);</p>
 
<p>(пп. &quot;ж&quot; введен Федеральным законом от 22.08.2004 № 122-ФЗ)</p>
 
<p>7) порядок управления образовательным учреждением, в том числе:</p>
 
<p>а) компетенция учредителя;</p>
 
<p>б) структура, порядок формирования органов управления образовательного учреждения, их компетенция и порядок организации деятельности;</p>
 
<p>в) порядок комплектования работников образовательного учреждения и условия оплаты их труда;</p>
 
<p>г) порядок изменения устава образовательного учреждения;</p>
 
<p>д) порядок реорганизации и ликвидации образовательного учреждения;</p>
 
<p>8) права и обязанности участников образовательного процесса;</p>
 
<p>9) перечень видов локальных актов (приказов, распоряжений и других актов), регламентирующих деятельность образовательного учреждения.</p>
 
<p>2. Устав гражданского образовательного учреждения в части, не урегулированной законодательством Российской Федерации, разрабатывается и принимается образовательным учреждением и утверждается его учредителем. Порядок утверждения устава федерального государственного образовательного учреждения устанавливается Правительством Российской Федерации, государственного образовательного учреждения, находящегося в ведении субъекта Российской Федерации, - органом исполнительной власти субъекта Российской Федерации, муниципального образовательного учреждения - органом местного самоуправления.</p>
 
<p>(п. 2 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>3. При необходимости регламентации указанных в настоящей статье сторон деятельности образовательного учреждения иными локальными актами последние подлежат регистрации в качестве дополнений к уставу образовательного учреждения.</p>
 
<p>4. Локальные акты образовательного учреждения не могут противоречить его уставу.</p>
 
