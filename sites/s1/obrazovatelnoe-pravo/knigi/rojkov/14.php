?><p style="text-align: justify;">
	Вопросы налогообложения доходной деятельности образователь­ного учреждения регулируются Налоговым кодексом РФ. Федераль­ный закон от 22 августа 2004 г. № 122-ФЗ внес изменения в Закон РФ«Об образовании», исключив все нормы, регулирующие налоговые отношения. Само слово «налог» больше не встречается в тексте Зако­на «Об образовании».
</p>
<p style="text-align: justify;">
	<span class="text">Конечно, вопросы налогообложения образовательного учреждения достаточно сложны для директора школы. Тут не обойтись без помо­щи квалифицированных специалистов: бухгалтера, экономиста. Наша цель сейчас не научить директора самостоятельно вести налоговый учет, а дать общие представления о том, плательщиком каких налогов и в каких размерах является образовательное учреждение, каким об­разом можно оптимизировать уплату налогов, как не допустить со­вершения типичных для образовательных учреждений нарушений на­логового законодательства. Поэтому мы рассмотрим основные мо­менты налогообложения образовательных учреждений, не вдаваясь в подробности ведения и сдачи налоговой отчетности.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Образовательное учреждение изначально находится на общем ре­жиме налогообложения. На сегодняшний день общий режим нало­гообложения является единственно возможным для государствен­ных и муниципальных образовательных учреждений. Кроме общего режима налогообложения существуют специальные режимы. Для организаций сферы образования весьма выгодной является упро­щенная система налогообложения, позволяющая уменьшить нало­говое бремя и упростить налоговую отчетность. Однако для бюд­жетных учреждений, к которым, напоминаем, относятся все государственные и муниципальные образовательные учреждения, пе­реход на упрощенную систему налогообложения не допускается (пп. 17 п. 3 ст. 346.12 НК РФ). Такой режим может быть использо­ван только различного рода негосударственными образовательны­ми организациями, оказывающими дополнительные образователь­ные услуги.</span>
</p>
<p style="text-align: justify;">
	<span class="text">При общем режиме налогообложения любая доходная деятельность образовательного учреждения подлежит налогообложению налогом на добавленную стоимость и налогом на прибыль, а выплаты работни­кам — единым социальным налогом, если налоговым законодатель­ством не установлены те или иные налоговые льготы.</span>
</p>
<p style="text-align: justify;">
	<b><span class="text"> <br>
 </span></b>
</p>
<p style="text-align: justify;">
	<b><span class="text">Налог на добавленную стоимость (НДС)</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">НДС облагается реализация товаров, выполнение работ и оказание услуг на территории Российской Федерации.</span>
</p>
<p style="text-align: justify;">
	<span class="text">НДС представляет собой косвенный налог, который включается в цену товара (работы, услуги) и оплачивается покупателем, заказчи­ком, но в федеральный бюджет перечисляется продавцом, исполни­телем услуг, т.е. школой.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Сумма налога исчисляется по соответствующей налоговой ставке исходя из суммы, полученной от реализации товаров (работ, услуг). НК РФ в зависимости от того, что является предметом реализации, предусматривает три величины налоговых ставок: 0, 10 и 18 процен­тов. В деятельности образовательных учреждений может иметь место применение налоговой ставки 10 или 18%. Так, например, если обра­зовательное учреждение сдает в аренду помещение и сумма арендной платы составляет без учета НДС 5000 рублей, то общая сумма, кото­рую заплатит арендатор, будет составлять 5000 + 18% = 5900 рублей(в том числе, 900 рублей — НДС).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Кроме того, для образовательных учреждений НК РФ устанавливает налоговые льготы. Так, не подлежат налогообложению (освобождают­ся от налогообложения) НДС:</span>
</p>
<p style="text-align: justify;">
	<span class="text">— услуги по содержанию детей в дошкольных учреждениях, прове­дению занятий с несовершеннолетними детьми в кружках, секциях (включая спортивные) и студиях (пп. 4 п. 2 ст. 149 НК РФ). Следует отметить, что наличие лицензии для применения льготы по НДС при проведении занятий с детьми в кружках и секциях для общеобразовательных учреждений предусмотрено п. 6 ст. 149 НК РФ, согласно ко­торому деятельность, подлежащая лицензированию, не облагается НДС при наличии соответствующей лицензии. Требование наличия ли­цензии при организации кружков и секций (т.е. реализации программ дополнительного образования детей) предусматривается п. 2 Типо­вого положения об общеобразовательном учреждении*;</span>
</p>
<p style="text-align: justify;">
	<span class="text">—реализация продуктов питания, непосредственно произведенных студенческими и школьными столовыми, столовыми других учебных заведений, столовыми медицинских организаций, детских дошколь­ных учреждений и реализуемых ими в указанных учреждениях, а также продуктов питания, непосредственно произведенных организациями общественного питания и реализуемых ими указанным столовым или указанным учреждениям. Данная льгота применяется в отношении сту­денческих и школьных столовых, столовых других учебных заведений, столовых медицинских организаций только в случае полного или час­тичного финансирования этих учреждений из бюджета или из средств фонда обязательного медицинского страхования (пп. 4 п. 2 ст. 149 НК РФ);</span>
</p>
<p style="text-align: justify;">
	<span class="text">—услуги в сфере образования по проведению некоммерческими образовательными организациями учебно-производственного (по на­правлениям основного и дополнительного образования, указанным в лицензии) или воспитательного процесса, за исключением консуль­тационных услуг, а также услуг по сдаче в аренду помещений (пп. 14 п. 2 ст. 149 НК РФ).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Таким образом, не облагаются НДС платные образовательные ус­луги как по дополнительным профессиональным программам, ока­зываемые образовательным учреждением в виде занятий в факуль­тативных кружках и секциях, так и по преподаванию специальных курсов и циклов дисциплин, репетиторству, по проведению заня­тий по углубленному изучению предметов при наличии соответству­ющей лицензии (т.е. при указании данных программ в приложении к основной лицензии на осуществление образовательной деятель­ности).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Следует отметить, что судебная практика признает наличие льготы по НДС и в случаях, когда в суммы платы за обучение включены и услуги гардероба, охраны, исходя из того, что такие услуги признаются необходимой составляющей образовательного процесса.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Во многих государственных и муниципальных образовательных уч­реждениях столовые обслуживаются соответствующими комбината­ми питания, которые и осуществляют учет произведенных и реализо­ванных товаров, а администрация образовательного учреждения име­ет возможность отстраниться по крайней мере от налогового учета деятельности столовой. Однако нередки случаи, когда столовые функ­ционируют и как структурные подразделения учреждения. Поэтому рассмотрим условия, при соблюдении которых возможно освобож­дение реализации продуктов питания в столовых от НДС:</span>
</p>
<p style="text-align: justify;">
	<span class="text">—образовательное учреждение является государственным или му­ниципальным, и столовая финансируется частично или полностью из соответствующего бюджета;</span>
</p>
<p style="text-align: justify;">
	<span class="text">—продукты питания производятся в столовой. При этом Налоговый кодекс РФ не раскрывает понятие «производства», включается ли сюда, например, разогрев, расфасовка продуктов, разлив напитков? Нало­говый кодекс РФ не дает четкого ответа на данный вопрос.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Очевидно, что облагаются НДС реализация продуктов питания, про­изведенных и расфасованных на стороне (конфеты, шоколад, напитки в бутылках и т.д.), а, следовательно, образовательное учреждение, реализуя и те, и другие продукты, должно вести раздельный учет таких операций, что на практике крайне затруднительно. Также следует иметь в виду, что часть продуктов питания (например, хлебобулочные изде­лия) облагается НДС по ставке 10%, другие же продукты (например, шоколад, газированные напитки) подлежат налогообложению по ставке18% (ст. 164 НК РФ).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Сдача в аренду как движимого, так и недвижимого имущества обла­гается налогом на добавленную стоимость по ставке 18%.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Кроме указанных выше налоговых льгот для учреждений сферы об­разования, НК РФ устанавливает общее (для всех организаций, в том числе и для государственных и муниципальных образовательных уч­реждений) правило освобождения от налогообложения НДС. Соглас­но п. 1 ст. 145 НК РФ организации имеют право на освобождение от исполнения обязанностей налогоплательщика НДС, связанных с ис­числением и уплатой налога, если за три предшествующих последо­вательных календарных месяца сумма выручки от реализации товаров(работ, услуг) этих организаций без учета НДС не превысила в сово­купности два миллиона рублей. Как видим, многие учреждения обще­го образования могут использовать это освобождение. В таком слу­чае не подлежат обложению НДС все операции образовательного уч­реждения (в том числе и сдача имущества в аренду, реализация то­варов, производимых учреждением и т.д.).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Для получения права на освобождение от исполнения обязанностей налогоплательщика по НДС необходимо не позднее 20-го числа ме­сяца, начиная с которого организация претендует на освобождение, представить в налоговый орган:</span>
</p>
<p style="text-align: justify;">
	<span class="text">письменное уведомление об использовании права на освобожде­ние от исполнения обязанностей налогоплательщика, связанных с ис­числением и уплатой НДС (форма утверждена приказом Министер­ства РФ по налогам и сборам от 4 июля 2002 г. № БГ-3-03/342);</span>
</p>
<p style="text-align: justify;">
	<span class="text">выписку из бухгалтерского баланса;</span>
</p>
<p style="text-align: justify;">
	<span class="text">выписку из книги продаж;</span>
</p>
<p style="text-align: justify;">
	<span class="text">копию журнала полученных и выставленных счетов-фактур.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Уведомление и соответствующие документы могут передаваться не­посредственно в налоговый орган или по почте заказным письмом. В последнем случае днем их представления в налоговый орган счита­ется шестой день со дня направления заказного письма.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Освобождение предоставляется сроком на 12 месяцев, по истечении которых учреждение может продлить срок действия освобождения.</span>
</p>
<p align="center" style="text-align: justify;">
	<b><span class="text"> <br>
 </span><span class="text">Налог на прибыль организаций</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">Государственные и муниципальные образовательные учреждения яв­ляются плательщиками налога на прибыль организаций. Налог на при­быль платится с доходов, полученных от платных образовательных и иных услуг, от аренды, от реализации произведенных образовательным уч­реждением товаров (в терминологии НК РФ такие доходы бюджетного учреждения именуются доходами от коммерческой деятельности).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Глава 25 НК РФ содержит специальную статью 321.1, которая уста­навливает особенности ведения учета по налогу на прибыль для бюд­жетных учреждений.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Налоговая база бюджетных учреждений по налогу на прибыль опре­деляется как разница между полученной суммой дохода (без учета налога на добавленную стоимость) и суммой фактически осуществ­ленных расходов, связанных с ведением коммерческой деятельности. Таким образом, в составе доходов и расходов бюджетных учрежде­ний, включаемых в налоговую базу, не учитываются средства, получен­ные от учредителя (собственника) на ведение уставной деятельности.</span>
</p>
<p style="text-align: justify;">
	<span class="text">На примере оказания платных образовательных услуг рассмотрим, какие расходы могут быть отнесены к числу расходов по коммерчес­кой деятельности:</span>
</p>
<p style="text-align: justify;">
	<span class="text">1)выплата заработной платы учителям за проведение платных заня­тий (с учетом налогов);</span>
</p>
<p style="text-align: justify;">
	<span class="text">2)расходы на приобретение книг, учебников для осуществления плат­ных занятий, канцелярских товаров, расходных материалов для копи­ровальной техники.</span>
</p>
<p style="text-align: justify;">
	<span class="text">При этом важно, чтобы целью использования данных товаров было именно оказание платных услуг, а не ведение основной образователь­ной деятельности, финансируемой за счет средств учредителя.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В смете доходов и расходов может быть предусмотрено финанси­рование расходов по оплате коммунальных услуг, услуг связи, транс­портных расходов по обслуживанию административно-управленчес­кого персонала за счет двух источников: средств учредителя и средств, полученных от коммерческой деятельности. В таком случае включение данных расходов бюджетного учреждения в расходы от коммерческой деятельности производится пропорционально объему средств, по­лученных от коммерческой деятельности, в общей сумме доходов(включая средства учредителя). Следует учитывать, что в общую сум­му доходов от коммерческой деятельности для этой цели не включа­ются доходы, полученные от сдачи имущества в аренду. При этом если в смете доходов и расходов финансирование расходов по оплате ком­мунальных услуг, услуг связи (за исключением мобильной связи) и на ремонт основных средств, приобретенных (созданных) за счет бюд­жетных средств, вообще не предусматривается, то такие расходы учи­тываются при определении налоговой базы по налогу на прибыль при условии, что эксплуатация указанных основных средств связана с ве­дением такой деятельности.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Кроме того, согласно п. 4 ст. 321.1. в расходы, связанные с ведением коммерческой деятельности при определении налоговой базы по налогу на прибыль включаются все расходы на ремонт основных средств, эксплуатация которых связана с ведением некоммерческой и (или) коммерческой деятельности и которые приобретены (созда­ны) за счет бюджетных средств, если финансирование данных расхо­дов не предусмотрено в смете доходов и расходов бюджетного уч­реждения или не осуществляется за счет бюджетных средств. Таким образом, если ремонт зданий образовательного учреждения не фи­нансируется за счет соответствующего бюджета, расходы на прове­дение этого ремонта могут включаться в расходы на осуществление коммерческой деятельности и, соответственно, уменьшать налого­вую базу по налогу на прибыль.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Полученная в результате исчисления сумма доходов бюджетного уч­реждения от коммерческой деятельности облагается по налоговой ставке 24% (ст. 284 НК РФ)*.</span>
</p>
<p style="text-align: justify;">
	<i><span class="text">* Эта ставка делится между бюджетами Российской Федерации (6,5%) и субъектов Российской Федерации (17,5%). При этом налоговая ставка налога, подлежащего зачислению в бюджеты субъектов Российской Федерации, законами субъектов Российской Федерации может быть понижена для отдельных категорий налогоплательщиков, но не может быть ниже 13,5%.</span></i>
</p>
<p align="center" style="text-align: justify;">
	<b><span class="text"> <br>
 </span><span class="text">Единый социальный налог</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">Образовательное учреждение, как и любая организация, при выпла­те работникам заработной платы и иных выплат, в том числе по граж­данско-правовым договорам оказания услуг, выполнения работ, яв­ляется налогоплательщиком&nbsp;<b>единого социального налога (ЕСН)&nbsp;</b>(гл. 24 НК РФ).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Объектом налогообложения ЕСН являются суммы выплат работни­кам, как за счет средств учредителя (сумма налога включается в нор­матив бюджетного финансирования), так и за счет коммерческой дея­тельности, которые включаются в расходы при определении налого­вой базы по налогу на прибыль организаций. Налоговый кодекс РФ устанавливает регрессную шкалу налоговых ставок по ЕСН (п. 1 ст. 241),которая, правда, малоприменима для бюджетных учреждений (исхо­дя из того, что ежегодные доходы работников школы редко превыша­ют 280 000 рублей).</span>
</p>
<p style="text-align: justify;">
	<span class="text"> <br>
 </span>
