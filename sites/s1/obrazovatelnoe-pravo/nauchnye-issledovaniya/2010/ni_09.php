<p align="justify"><b>Работа выполнена в 2005 г.</b> в рамках раздела 4.2. &laquo;Научно-методическое обеспечение совершенствования структуры и содержания общего и дополнительного образования&raquo; подпрограммы № 4 &laquo;Исследования в области образования, молодежной политики и социальной политики в сфере образования&raquo; ведомственной научной программы &laquo;Развитие научного потенциала высшей школы&raquo;.</p>
 
