<p><span class="text" style="text-align: justify;">Статья 4. Структура системы высшего и послевузовского профессионального образования</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">Структура системы высшего и послевузовского профессионального образования представляет собой совокупность:</span></p>
 
<p><span class="text" style="text-align: justify;">федеральных государственных образовательных стандартов высшего профессионального образования, федеральных государственных требований к послевузовскому профессиональному образованию и образовательных программ высшего и послевузовского профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 01.12.2007 № 309-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">имеющих лицензии высших учебных заведений, научных организаций и образовательных учреждений соответствующего дополнительного профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 01.12.2007 № 308-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">научных, проектных, производственных, клинических, медико-профилактических, фармацевтических, культурно-просветительских учреждений, организаций и предприятий, ведущих научные исследования и обеспечивающих функционирование и развитие высшего и послевузовского профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">органов, осуществляющих управление в сфере образования, и подведомственных им учреждений, организаций и предприятий;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">общественных и государственно-общественных объединений (творческих союзов, профессиональных ассоциаций, обществ, научных и методических советов и иных объединений).</span></p>
 
