<p><span class="text" style="text-align: justify;">(п. 5 в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6. Прием иностранных граждан в аспирантуру (адъюнктуру), ординатуру, интернатуру и докторантуру высших учебных заведений, образовательных учреждений дополнительного профессионального образования, научных организаций осуществляется в соответствии с международными договорами Российской Федерации и межправительственными соглашениями Российской Федерации, а также в порядке, предусмотренном пунктом 2 статьи 29 настоящего Федерального закона.</span></p>
 
<p><span class="text" style="text-align: justify;">Прием лиц без гражданства в аспирантуру (адъюнктуру), ординатуру, интернатуру и докторантуру высших учебных заведений, образовательных учреждений дополнительного профессионального образования, научных организаций осуществляется в порядке, установленном уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 20.04.2007 № 56-ФЗ, от 23.07.2008 № 160-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">7. Для лиц, имеющих среднее профессиональное образование соответствующего профиля, а также по решению ученого совета высшего учебного заведения в порядке, определенном федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования, для лиц, уровень образования или способности которых являются достаточным основанием для получения высшего профессионального образования по сокращенным или по ускоренным программам бакалавриата, допускается получение высшего профессионального образования по таким программам. Получение высшего профессионального образования по сокращенным программам подготовки специалиста и программам магистратуры не допускается.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 7 в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">8. Высшее учебное заведение создает обучающимся необходимые условия для получения качественного образования. Запрещается использование антигуманных, а также опасных для жизни или здоровья обучающихся методов обучения.</span></p>
 
<p><span class="text" style="text-align: justify;">9. Учебная и производственная практика, предусмотренная федеральными государственными образовательными стандартами высшего профессионального образования, осуществляется на основе договоров между высшими учебными заведениями и предприятиями, учреждениями и организациями, в соответствии с которыми указанные предприятия, учреждения и организации независимо от их организационно-правовых форм обязаны предоставлять места для прохождения практики студентов высших учебных заведений, имеющих государственную аккредитацию.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 01.12.2007 № 309-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">10. Требования к государственной итоговой аттестации (выпускной работе, государственным экзаменам, диссертации) определяются федеральными государственными образовательными стандартами высшего профессионального образования и федеральными государственными требованиями, а также устанавливаемыми в соответствии с пунктом 4 статьи 5 настоящего Федерального закона образовательными стандартами и требованиями. Контроль за выполнением указанных требований осуществляется в установленном законодательством порядке.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 01.12.2007 № 309-ФЗ)</span></p>
 
