<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.914-2</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Если запрос о включении частных образовательных учреждений в публичное образование получает положительное решение в соответствии со статьей L.442-4, действующие учителя либо принимаются в штат и перераспределяются в рамках публичного образования, либо остаются в качестве работающих по контракту.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.914-3</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Никто не может исполнять функции директора или преподавателя, или классного руководителя в частной начальной школе, не имея свидетельства о профессиональной пригодности к начальному образованию.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Никто не может участвовать в процессе обучения в частной школе без действенного и постоянного присутствия в том же классе, где проводится обучение, одного из учителей школы, если он не соответствует требованиям по возрасту, определяемым статьей L.921-1, и если он не обладает свидетельством о профессиональной пригодности к начальному образованию.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.914-4</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Никто не может быть директором или преподавателем в частном детском саду с дошкольной подготовкой или в частной начальной школе, если он не имеет французского гражданства или не является гражданином другого Государства &ndash; члена Европейского Сообщества или другого Государства &ndash; участника договора о европейском экономическом пространстве и если он не соответствует, кроме того, условиям профессиональной пригодности, установленных статьей L.914-3 и не соответствует требованиям возраста, установленными статьей L.921-1.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Однако иные иностранные граждане, удовлетворяющие двум перечисленным выше требованиям, могут преподавать в частных школах на основании разрешения, выдаваемого ректором после получения заключения академического Совета национального образования.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Другие иностранные граждане, имеющие только иностранные дипломы о профессиональной пригодности, должны получить предварительно подтверждение эквивалентности этих дипломов с французскими свидетельствами.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Условия, на которых эта эквивалентность может быть установлена, определяются декретом, принятым после получения заключения Высшего Совета образования.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">В особых случаях (в отношении) школ, предназначенных исключительно для иностранных детей, проживающих во Франции, иностранные граждане, которые обращаются с просьбой руководить этими школами или преподавать в них, могут быть освобождены ректором академии от обязательного обладания свидетельством о профессиональной пригодности после получения заключения академического Совета национального образования. </span></p>
