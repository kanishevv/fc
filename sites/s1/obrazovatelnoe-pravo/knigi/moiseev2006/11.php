	</p>
 </li>
</ol>
<p style="text-align: justify;">
 <span class="text">Эти три уровня можно распространить и на<b>оценку готовности отдельных групп условий введения профильного обучения.</b>Тогда может оказаться, что при<b>общей неполной готовности</b>имеется, например, полная мотивационная готовность людей в школах при неполной готовности программно-методических и финансовых ресурсов, недостаточной компетентности кадров и нулевой готовности в сфере информационного и пропагандистского сопровождения.</span>
</p>
