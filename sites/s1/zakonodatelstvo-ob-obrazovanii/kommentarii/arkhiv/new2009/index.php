<p><span class="text" style="text-align: justify;">Статья 16. Студенты высших учебных заведений</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Студентом высшего учебного заведения является лицо, в установленном порядке зачисленное в высшее учебное заведение для обучения.</span></p>
 
<p><span class="text" style="text-align: justify;">Студенту высшего учебного заведения бесплатно выдаются студенческий билет и зачетная книжка установленного образца.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Наряду с закрепленными в Законе Российской Федерации &quot;Об образовании&quot; правами студенты высших учебных заведений имеют право:</span></p>
 
<p><span class="text" style="text-align: justify;">1) выбирать факультативные (необязательные для данного направления подготовки (специальности)) и элективные (избираемые в обязательном порядке) курсы, предлагаемые соответствующими факультетом и кафедрой;</span></p>
 
<p><span class="text" style="text-align: justify;">2) участвовать в формировании содержания своего образования при условии соблюдения федеральных государственных образовательных стандартов высшего профессионального образования. Указанное право может быть ограничено условиями договора, заключенного между студентом высшего учебного заведения и физическим или юридическим лицом, оказывающим ему содействие в получении образования и последующем трудоустройстве;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 01.12.2007 № 309-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3) осваивать помимо учебных дисциплин по избранным направлениям подготовки (специальностям) любые другие учебные дисциплины, преподаваемые в данном высшем учебном заведении, в порядке, предусмотренном его уставом, а также преподаваемые в других высших учебных заведениях (по согласованию между их руководителями);</span></p>
 
<p><span class="text" style="text-align: justify;">4) участвовать в обсуждении и решении важнейших вопросов деятельности высших учебных заведений, в том числе через общественные организации и органы управления высших учебных заведений;</span></p>
 
<p><span class="text" style="text-align: justify;">5) бесплатно пользоваться в государственных и муниципальных высших учебных заведениях библиотеками, информационными фондами, услугами учебных, научных и других подразделений высшего учебного заведения в порядке, установленном уставом высшего учебного заведения; принимать участие во всех видах научно-исследовательских работ, конференциях, симпозиумах;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6) представлять свои работы для публикации, в том числе в изданиях высшего учебного заведения;</span></p>
 
<p><span class="text" style="text-align: justify;">7) обжаловать приказы и распоряжения администрации высшего учебного заведения в установленном законодательством Российской Федерации порядке;</span></p>
 
<p><span class="text" style="text-align: justify;">8) получать образование по военной специальности в установленном законодательством Российской Федерации порядке;</span></p>
 
<p><span class="text" style="text-align: justify;">9) переходить с платного обучения на бесплатное в порядке, предусмотренном уставом высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Студенты федеральных государственных высших учебных заведений, обучающиеся по очной форме обучения (далее - студенты очной формы обучения) и получающие образование за счет средств федерального бюджета, обеспечиваются стипендиями в размере 1 100 рублей в порядке и на условиях, которые определяются уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти. Студентам-инвалидам I и II групп, сиротам, а также детям, оставшимся без попечения родителей, - размер стипендии увеличивается на пятьдесят процентов. Порядок назначения и выплаты стипендий устанавливается уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти. Студенты высших учебных заведений вправе получать стипендии, назначенные направившими их на обучение юридическими или физическими лицами, а также именные стипендии на основании соответствующего положения. Студенты федеральных государственных высших учебных заведений из числа граждан, проходивших в течение не менее трех лет военную службу по контракту в Вооруженных Силах Российской Федерации, других войсках, воинских формированиях и органах на воинских должностях, подлежащих замещению солдатами, матросами, сержантами, старшинами, и уволенных с военной службы по основаниям, предусмотренным подпунктами &quot;б&quot; - &quot;г&quot; пункта 1, подпунктом &quot;а&quot; пункта 2 и пунктом 3 статьи 51 Федерального закона &quot;О воинской обязанности и военной службе&quot;, обучающиеся по очной форме обучения за счет средств федерального бюджета, обеспечиваются стипендиями в размере, повышенном на 50 процентов по сравнению с размером, установленным для студентов федеральных государственных высших учебных заведений, обучающихся по очной форме обучения.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 07.08.2000 № 122-ФЗ, от 07.07.2003 № 119-ФЗ, от 22.08.2004 № 122-ФЗ, от 21.04.2005 № 35-ФЗ, от 06.01.2007 № 1-ФЗ, от 13.07.2007 № 131-ФЗ, от 15.07.2008 № 119-ФЗ, от 23.07.2008 № 160-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Студентам очной формы обучения федеральных государственных высших учебных заведений выделяются дополнительные средства на оказание поддержки нуждающимся студентам в размере двадцати пяти процентов стипендиального фонда, предусматриваемого в установленном порядке в расходах федерального бюджета. Федеральным государственным высшим учебным заведениям выделяются дополнительные средства в сумме двукратного месячного размера академической стипендии для организации культурно-массовой, физкультурной и оздоровительной работы со студентами очной формы обучения.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Абзацы третий - четвертый утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">По медицинским показаниям и в других исключительных случаях студенту высшего учебного заведения предоставляется академический отпуск в порядке, установленном федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Абзац утратил силу с 1 июля 2006 года. - Федеральный закон от 06.07.2006 № 104-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">Студентам высших учебных заведений предоставляется право на бесплатное пользование услугами государственных и муниципальных библиотек, а также на бесплатное посещение государственных и муниципальных музеев.</span></p>
 
<p><span class="text" style="text-align: justify;">Для студентов очной и очно-заочной (вечерней) форм обучения не менее чем два раза в учебном году устанавливаются каникулы общей продолжительностью не менее чем семь недель.</span></p>
 
<p><span class="text" style="text-align: justify;">Каждый обучающийся, нуждающийся в жилой площади, должен быть обеспечен отвечающим санитарным нормам и правилам местом в общежитии при наличии соответствующего жилищного фонда высшего учебного заведения. Размер платы за проживание в общежитии, коммунальные и бытовые услуги для обучающихся не может превышать пяти процентов размера стипендии.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">При наличии обучающихся, нуждающихся в жилой площади, не допускается использование не по назначению входящей в жилищный фонд высшего учебного заведения жилой площади общежитий (сдача в аренду и иные сделки), а также использование, приводящее к ее уменьшению.</span></p>
 
<p><span class="text" style="text-align: justify;">С каждым обучающимся, проживающим в общежитии, заключается договор, типовая форма которого утверждается уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ, от 23.07.2008 № 160-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Другие права студентов могут быть установлены законодательством и (или) уставом высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">3.1. Законами субъектов Российской Федерации могут устанавливаться меры социальной поддержки студентов высших учебных заведений, финансируемые за счет средств бюджетов субъектов Российской Федерации (за исключением субвенций из федерального бюджета).</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 3.1 введен Федеральным законом от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Студент имеет право на восстановление в высшем учебном заведении в течение пяти лет после отчисления из него по собственному желанию или по уважительной причине с сохранением той основы обучения (бесплатной или платной), в соответствии с которой он обучался до отчисления.</span></p>
 
<p><span class="text" style="text-align: justify;">Порядок и условия восстановления в высшем учебном заведении студента, отчисленного по неуважительной причине, определяются уставом высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">5. Студент высшего учебного заведения имеет право получать от администрации высшего учебного заведения информацию о положении в сфере занятости населения Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">6. Студентам высших учебных заведений гарантируется свобода перехода в другое высшее учебное заведение в порядке, установленном федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования. При переходе из одного высшего учебного заведения в другое за студентом сохраняются все права как за обучающимся впервые на данном уровне высшего профессионального образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ, от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">7. За успехи в учебе и активное участие в научно-исследовательской работе студенты высших учебных заведений получают моральное и (или) материальное поощрение в соответствии с уставом высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">8. Студенты высших учебных заведений обязаны овладевать знаниями, выполнять в установленные сроки все виды заданий, предусмотренных учебным планом и образовательными программами высшего профессионального образования, соблюдать устав высшего учебного заведения, правила внутреннего распорядка и правила общежития.</span></p>
 
<p><span class="text" style="text-align: justify;">9. За нарушение студентом обязанностей, предусмотренных уставом высшего учебного заведения и правилами его внутреннего распорядка, к нему могут быть применены дисциплинарные взыскания вплоть до отчисления из высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">Дисциплинарное взыскание, в том числе отчисление, может быть наложено на студента высшего учебного заведения после получения от него объяснения в письменной форме.</span></p>
 
<p><span class="text" style="text-align: justify;">Дисциплинарное взыскание применяется не позднее чем через один месяц со дня обнаружения проступка и не позднее чем через шесть месяцев со дня его совершения, не считая времени болезни студента и (или) нахождения его на каникулах. Не допускается отчисление студентов во время их болезни, каникул, академического отпуска или отпуска по беременности и родам.</span></p>
 
<p><span class="text" style="text-align: justify;">10. Статус студента негосударственного высшего учебного заведения, имеющего государственную аккредитацию, в отношении академических прав и академических свобод приравнивается к статусу студента государственного или муниципального высшего учебного заведения, за исключением права на получение государственной стипендии. Статус студента негосударственного высшего учебного заведения, не имеющего государственной аккредитации, определяется уставом этого высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
