	<p align="center">
		 IV. Экономика Центра
	</p>
	<p align="center">
 <br>
	</p>
	<p>
		 4.1.&nbsp;Центр самостоятельно осуществляет финансово-хозяйственную деятельность, решает вопросы, связанные с заключением договоров и государственных контрактов, определением своих обязательств и иных условий, не противоречащих законодательству Российской Федерации и настоящему уставу. Центр обеспечивает исполнение своих обязательств в соответствии с государственным заданием, планом финансово-хозяйственной деятельности и в пределах денежных средств, полученных Центром из всех видов источников финансового обеспечения деятельности Центра.
	</p>
	<p>
		 4.2.&nbsp;Имущество Центра находится в федеральной собственности. Имущество, закрепленное за Центром, может быть отчуждено только в установленном порядке.&nbsp;
	</p>
	<p>
		 Собственником имущества Центра является Российская Федерация.
	</p>
	<p>
		 Министерство просвещения Российской Федерации в порядке и пределах, определенных федеральными законами, актами Президента Российской Федерации и Правительства Российской Федерации, осуществляет функции и полномочия собственника в отношении имущества, переданного Центру.
	</p>
	<p>
		 4.3.&nbsp;Центр приобретает право оперативного управления на имущество, закрепленное собственником за Центром и приобретенное Центром по иным основаниям, в соответствии с Гражданским кодексом Российской Федерации.
	</p>
	<p>
		 Земельный участок, необходимый для выполнения Центром своих уставных задач, предоставляется ему на праве постоянного (бессрочного) пользования.
	</p>
	<p>
		 Объекты культурного наследия (памятники истории и культуры) народов Российской Федерации, культурные ценности, природные ресурсы (за исключением земельных участков), ограниченные для использования в гражданском обороте или изъятые из гражданского оборота, закрепляются за Центром на условиях и в порядке, которые определяются федеральными законами и иными нормативными правовыми актами Российской Федерации.
	</p>
	<p>
		 4.4.&nbsp;В составе движимого имущества Центра выделяется особо ценное движимое имущество.
	</p>
	<p>
		 Под особо ценным движимым имуществом понимается движимое имущество, без которого осуществление Центром своей уставной деятельности будет существенно затруднено. Порядок отнесения имущества к категории особо ценного движимого имущества устанавливается Правительством Российской Федерации. Виды такого имущества определяются Учредителем.
	</p>
	<p>
		 Перечни особо ценного движимого имущества определяются Учредителем.
	</p>
	<p>
		 4.5.&nbsp;Центр владеет, пользуется закрепленным имуществом в пределах, установленных законодательством Российской Федерации, в соответствии с целями своей деятельности, назначением имущества и, если иное не установлено законодательством Российской Федерации, распоряжается имуществом с согласия собственника этого имущества.
	</p>
	<p>
		 Центр без согласия собственника не вправе распоряжаться особо ценным движимым имуществом, закрепленным за ним собственником или приобретенным Центром за счет средств, выделенных ему собственником на приобретение такого имущества, а также недвижимым имуществом.
	</p>
	<p>
		 Остальным находящимся на праве оперативного управления имуществом Центр вправе распоряжаться самостоятельно, если иное не предусмотрено законодательством Российской Федерации.
	</p>
	<p>
		 4.6.&nbsp;Центр не вправе совершать сделки, возможными последствиями которых является отчуждение или обременение имущества, закрепленного за Центром, или имущества, приобретенного за счет средств, выделенных Центру Учредителем, за исключением случаев, если совершение таких сделок допускается федеральными законами.
	</p>
	<p>
		 4.7.&nbsp;Центр вправе выступать в качестве арендатора и (или) арендодателя имущества в порядке, установленном законодательством Российской Федерации.
	</p>
	<p>
		 4.8.&nbsp;&nbsp;Крупная сделка может быть совершена Центром только с предварительного согласия Учредителя Центра.
	</p>
	<p>
		 Крупной сделкой признается сделка или несколько взаимосвязанных сделок, связанная с распоряжением денежными средствами, отчуждением иного имущества (которым в соответствии с федеральным законом Центр вправе распоряжаться самостоятельно), а также с передачей такого имущества в пользование или в залог при условии, что цена такой сделки либо стоимость отчуждаемого или передаваемого имущества превышает 10 процентов балансовой стоимости активов Центра, определяемой по данным его бухгалтерской отчетности на последнюю отчетную дату.
	</p>
	<p>
		 Крупная сделка, совершенная без предварительного согласия Учредителя, может быть признана недействительной по иску Центра или его Учредителя, если будет доказано, что другая сторона в сделке знала или должна была знать об отсутствии предварительного согласия Учредителя Центра.
	</p>
	<p>
		 Директор Центра несет перед Центром ответственность в размере убытков, причиненных Центру в результате совершения крупной сделки без согласования с Учредителем, независимо от того, была ли эта сделка признана недействительной.
	</p>
	<p>
		 Сделка, в совершении которой имеется заинтересованность, определяемая в соответствии с критериями, установленными статьей 27 Федерального закона&nbsp;<br>
		 «О некоммерческих организациях», должна быть одобрена Учредителем Центра.
	</p>
	<p>
		 Сделка, в совершении&nbsp;&nbsp;которой имеется заинтересованность и которая совершена без предварительного согласия Учредителя, может быть признана судом недействительной.
	</p>
	<p>
		 Заинтересованное лицо несет перед Центром ответственность в размере убытков, причиненных им Центром. Если убытки причинены Центром несколькими заинтересованными лицами, их ответственность перед Центром является солидарной.&nbsp;
	</p>
	<p>
		 Центр вправе осуществлять иные сделки с имуществом в случаях и порядке, предусмотренных законодательством Российской Федерации.
	</p>
	<p>
		 4.9.&nbsp;&nbsp;Центр в установленном порядке представляет сведения об имуществе, принадлежащем ему на соответствующем вещном праве, в федеральный орган исполнительной власти, осуществляющий ведение реестра федерального имущества.
	</p>
	<p>
		 4.10.&nbsp;Источниками формирования имущества Центра являются:
	</p>
	<p>
		 1) имущество, находящееся в федеральной собственности, закрепленное за Центром на праве оперативного управления;
	</p>
	<p>
		 2) имущество, приобретенное Центром за счет средств, выделенных ему Учредителем на приобретение такого имущества, и (или) средств от приносящей доход деятельности;
	</p>
	<p>
		 3) имущество, полученное Центром по иным основаниям, предусмотренным законодательством Российской Федерации.
	</p>
	<p>
		 4.11.&nbsp;Центр отвечает по своим обязательствам всем находящимся у него на праве оперативного управления имуществом, в том числе приобретенным за счет доходов, полученных от приносящей доход деятельности, за исключением особо ценного движимого имущества, закрепленного за Центром собственником этого имущества или приобретенного Центром за счет средств, выделенных собственником этого имущества, а также недвижимого имущества, независимо от того, по каким основаниям оно поступило в оперативное управление Центра и за счет каких средств оно приобретено.
	</p>
	<p>
		 4.12.&nbsp;По обязательствам Центра, связанным с причинением вреда гражданам, при недостаточности имущества Центра, на которое в соответствии с пунктом 4.11 настоящего устава может быть обращено взыскание, субсидиарную ответственность несет собственник имущества Центра.
	</p>
	<p>
		 4.13.&nbsp;Центр не отвечает по обязательствам собственника своего имущества.
	</p>
	<p>
		 4.14.&nbsp;Финансовое обеспечение деятельности Центра осуществляется за счет:&nbsp;
	</p>
	<p>
		 1)&nbsp;субсидии из федерального бюджета на выполнение государственного задания с учетом расходов на содержание недвижимого имущества и особо ценного движимого имущества, закрепленного за Центром на праве оперативного управления или приобретенного Центром за счет средств, выделенных ему Учредителем на приобретение такого имущества, с учетом расходов на уплату налогов, в качестве объекта налогообложения по которым признается соответствующее имущество, в том числе земельные участки;
	</p>
	<p>
		 2) субсидии из федерального бюджета на иные цели, предусмотренные законодательством Российской Федерации, а также бюджетных ассигнований на выполнение федеральной адресной инвестиционной программы;
	</p>
	<p>
		 3) доходов, получаемых от приносящей доход деятельности Центра;
	</p>
	<p>
		 4) доходов, получаемых от использования прав на результаты интеллектуальной деятельности, и средств индивидуализации, включая вознаграждение по лицензионным договорам, в соответствии с законодательством Российской Федерации;
	</p>
	<p>
		 5) грантов, предоставленных на безвозмездной основе физическими и юридическими лицами;
	</p>
	<p>
		 6) грантов в форме субсидий;
	</p>
	<p>
		 7) средств, безвозмездно полученных на ведение уставной деятельности от физических и юридических лиц;
	</p>
	<p>
		 8) добровольных имущественных целевых взносов и пожертвований юридических и физических лиц, в том числе иностранных;
	</p>
	<p>
		 9) средств, полученных от сдачи в аренду имущества Центра.
	</p>
	<p>
		 4.15.&nbsp;Финансовое обеспечение выполнения государственного задания осуществляется с учетом расходов на содержание недвижимого имущества и особо ценного движимого имущества, закрепленного за Центром Учредителем или приобретенного Центром за счет средств, выделенных ему Учредителем на приобретение такого имущества, с учетом расходов на уплату налогов, в качестве объекта налогообложения по которым признается соответствующее имущество, в том числе земельные участки, с учетом мероприятий, направленных на развитие Центра, перечень которых определяется Учредителем. В случае сдачи в аренду с согласия Учредителя недвижимого имущества или особо ценного движимого имущества, закрепленного за Центром Учредителем или приобретенного Центром за счет средств, выделенных ему Учредителем на приобретение такого имущества, финансовое обеспечение такого имущества Учредителем не осуществляется.
	</p>
	<p>
		 4.16.&nbsp;Центр в установленном порядке имеет право:
	</p>
	<p>
		 1) доводить до структурных подразделений в части направлений их деятельности государственное задание на осуществление основных видов деятельности, предусмотренных пунктом 2.3 настоящего устава, за счет субсидий из федерального бюджета на выполнение государственного задания;
	</p>
	<p>
		 2) осуществлять функции заказчика при закупке товаров, работ и услуг для нужд Центра;
	</p>
	<p>
		 3) выступать заказчиком при осуществлении капитального строительства, реконструкции и технического перевооружения строек и объектов федеральной формы собственности;
	</p>
	<p>
		 4) совершать крупные сделки и сделки, в совершении которых имеется заинтересованность, в соответствии с законодательством Российской Федерации, локальными нормативными актами Центра и настоящим уставом;
	</p>
	<p>
		 5) проводить капитальный и текущий ремонт зданий и сооружений, в которых осуществляется деятельность Центра;
	</p>
	<p>
		 6) осуществлять организацию досуга работников Центра за счет средств, полученных от приносящей доход деятельности Центра;
	</p>
	<p>
		 7) обеспечивать выполнение работ по договорам с юридическими и (или) физическими лицами (в том числе иностранными);
	</p>
	<p>
		 8) осуществлять иные права, предусмотренные законодательством Российской Федерации.
	</p>
	<p>
		 4.17.&nbsp;Доходы, полученные Центром от приносящей доход деятельности, поступают в его самостоятельное распоряжение и используются им в соответствии с законодательством Российской Федерации для достижения целей, ради которых он создан, в соответствии с утвержденным в установленном порядке планом финансово-хозяйственной деятельности.
	</p>
	<p>
		 4.18.&nbsp;Если порядком предоставления средств не установлено иное, Центр самостоятельно определяет направления и порядок использования своих средств, в том числе долю, направляемую на оплату труда и материальное стимулирование работников Центра.&nbsp;
