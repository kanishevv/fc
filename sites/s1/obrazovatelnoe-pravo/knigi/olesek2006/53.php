?><p style="text-align: justify;">
	 Важным событием в жизни высшей школы, выходящим хронологически за пределы ХIХ в., но органически связанным с ним, был переход на так называемый предметный метод организации учебного процесса. Это была, по сути, самостоятельная реформа. С реформой восьмидесятых ее связывают единые принципы устава 1884 г. Суть нового этапа реформы состояла в том, чтобы, следуя передовому опыту западноевропейских университетов, освободить учебный процесс от наиболее жестких форм, стесняющих его свободное развитие. В основу преобразований высшей школы был положен отказ от курсовой принадлежности студента, обрекающей его следовать по ступеням учебы вместе со всем курсом, и переход к более свободной системе индивидуального учета его успехов в связи с количеством прослушанных курсов и сданных экзаменов.
</p>
<p style="text-align: justify;">
 <span class="text">Предварительная дискуссионная&nbsp;</span>подготовка реформирования
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <span class="text">Реформирование университетского учебного процесса, связанного с реализацией идей интенсивной образовательной технологии устава 1884 г., готовилось основательно. Только в 1901 г. появилось более 50 крупных публикаций на тему о необходимости совершенствования учебного процесса в университетах. Наиболее радикальные сторонники предметной системы высказывались за перестройку всего порядка преподавания и контроля за усвоением знаний.</span>
</p>
<p style="text-align: justify;">
 <span class="text">В этом плане настойчиво выступал, начиная с 60-х гг., такой крупный научный и педагогический авторитет, как Н. И. Писарев, служивший попечителем Новороссийского (1856-1858) и Киевского (1858-1861) учебных округов. Он был решительным противником излишней опеки над студентами, исходил из того, что «молодые люди поступают в университет уже готовые к самостоятельным занятиям наукой», считал всякие лекции излишними, поскольку они воспринимаются пассивно и потому должны быть заменены живыми беседами с профессорами, что располагало бы студентов к самостоятельному овладению знаниями.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Поступившие в университет должны были, по мысли реформаторов, начинать с чтения руководств, составленных факультетами по всем учебным предметам. Когда все слушатели ознакомятся с учебником по известному предмету, открываются занятия под руководством профессора. Он называет определенный раздел учебника и предлагает составить по нему замечания и вопросы. Ознакомившись с работами студентов, профессор выбирает лучшие, и авторы приглашаются за кафедру и читают их в виде лекции. Профессор присутствует в аудитории, не делает никаких объяснений, выступая лишь в качестве критика.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Главное достоинство такой организации учебного процесса видели в том, что «учащиеся усвояют научные истины после самостоятельного их искания». Линия на развитие самостоятельной работы студентов, взятая в тридцатые годы, в эпоху Уварова, поддержанная и развитая в шестидесятых (Пирогов и др.), а также в восьмидесятых (разделение учебного года на семестры и введение их зачетов, конкурс между профессорами и приват-доцентами, право выбора студентами преподавателя, обязательные консультации профессоров во внеучебное время и т. п.), находит продолжение в начале ХХ в.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Известный профессор столичного университета В. Сергеевич высказывал резонные сомнения в целесообразности выдвинутых предложений о чересчур полной академической свободе. «И профессора, - возражал он, - не всегда проповедуют истины, самостоятельно ими найденные, а здесь студенты призываются к такому исканию истины. Это заманчиво, но достижимо ли?» И, развивая свою мысль, продолжал со ссылкой на зарубежный опыт. Беседы были известны и немецким университетам, но они не заменяли там лекции, а имели место наряду с ними, для желающих. Так, в Гейдельберге в начале шестидесятых беседами по философии руководил известный профессор. На этих беседах, к примеру, один из присутствующих должен был изложить определенное место из «Критики чистого разума» Канта, другой - возразить на это положение. Охотников излагать суть учения и возражать не оказалось. «Но это, - продолжал автор, - еще не самая большая беда. Возможен и худший исход предложенных бесед. Это - появление говорунов, которые готовы спорить обо всем, решительно ничего порядком не зная. А таких болтунов везде очень много. Во что же тогда обратится аудитория?»</span>
</p>
<p style="text-align: justify;">
 <span class="text">Знакомство с материалами дискуссии поражает размахом и основательностью проработки всех сторон предстоящей реформы. Развернутую аргументацию в пользу новой системы находим в брошюре В. Н. Ивановского «Предметная система в наших университетах и ее применение к философским наукам» (СПб., 1907). В своих рассуждениях автор пытается рассмотреть проблему в двух ракурсах. Когда говорят о «предметной системе», разъясняет он, не всегда отдают себе отчет в том, что под этим термином можно подразумевать две совершенно разные вещи: 1) предметную систему экзаменов и 2) предметную систему учебного плана.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Предметная система экзаменов состоит в том, что студент экзаменуется по каждому изучаемому предмету, по мере того как он к данному экзамену подготовился. Эта система противоположна теперешней курсовой в двух отношениях. При курсовой системе экзаменуется: 1) сразу весь личный состав данного курса и 2) по всем предметам данного курса. В противоположность этому, при предметной системе экзаменов, испытания по каждому предмету происходят несколько раз в течение учебного года - в определенные дни (в заранее опубликованные сроки), между которыми выбирает сам студент. «Предметная система экзаменов», писал он, в чистом виде существует в некоторых учебных заведениях, например в Парижской Ecole de medicine.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Совершенно иное представляет собой «предметная система учебного плана». Для ее иллюстрации автор ссылается на профессора П. Г. Виноградова, рассмотревшего этот вопрос в статье «Учебное дело в наших университетах» в октябрьском номере «Вестника Европы» за 1901 г. Вот суть рассуждений профессора: «Вместо общих планов по факультетам или хотя бы по отделениям, планы обязательных курсов должны быть групповые, на менее широком, но прочном фундаменте.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Например, на историко-филологическом факультете можно было бы разрешить следующие комбинации предметов, кроме логики и психологии, которые должны слушаться всеми: 1) древние языки (и литература), сравнительное языкознание, история древней философии, древняя история; 2) славянские наречия, славяноведение, сравнительное языкознание, история всеобщей литературы; 3) русский язык, история русской литературы, история философии, русская история; 4) германские (или романские) языки, сравнительное языкознание, история всеобщей литературы, история философии, всеобщая история; 5) история всеобщая и русская, история философии и древние языки; 6) философия, история всеобщей литературы, всеобщая история, древние языки; 7) история искусств, история философии, всеобщая история, история всеобщей литературы, древние языки».</span>
</p>
<p style="text-align: justify;">
 <span class="text">По-своему, с учетом профессиональной специфики, решали проблемы перехода на предметную систему технические вузы, но суть всего дела состояла в стремлении наилучшим образом организовать учебный процесс, с тем чтобы обеспечить максимальный педагогический эффект. В центре внимания педагогических коллективов стояла мобилизация внимания студентов, вопросы повышения их личной заинтересованности и ответственности за ход учения, наилучшей организации их самостоятельной работы. Использование богатого зарубежного опыта было важным подспорьем в этой работе.</span>
</p>
<p style="text-align: justify;">
 <span class="text">После основательной дискуссионной подготовки в начале 1901 г. министр народного просвещения обратился ко всем российским вузам с письмом, содержащим 15 вопросов относительно предстоящей реформы. Эти вопросы были обсуждены на заседаниях ученых советов, и в министерство поступила богатейшая информация о настроениях вузовской общественности. Идея перехода на предметную систему получила единодушную поддержку. И только после этого началась активная работа по ее реализации. В 1902 г. при министерстве была создана специальная комиссия по разработке проекта реформирования высшей школы, которая немедленно приступила к обобщению накопленных материалов. Материалы комиссии составили внушительный том ценнейших наблюдений, предложений, глубоких раздумий о судьбах отечественных университетов, и они доныне сохраняют актуальность и заслуживают самого тщательного исследования современными деятелями высшей школы. Многие из тех материалов содержат ценнейший опыт и могут послужить сегодняшней практике.</span>
</p>
<p style="text-align: justify;">
 <span class="text">В 1903 г. состоялся III съезд деятелей и специалистов по техническому и профессиональному образованию, на котором обсуждались конкретные проблемы предметной системы и практические шаги по ее претворению в жизнь. Подобные съезды проводились и по другим группам вузов. Заметим, кстати, что упомянутый съезд признал Московское высшее техническое училище лучшей из отечественных технических школ. Поэтому опыт училища по внедрению предметной системы организации учебного процесса служил примером для остальных технических вузов страны.</span>
</p>
