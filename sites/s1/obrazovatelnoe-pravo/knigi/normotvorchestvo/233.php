<p style="text-align: justify;">
	<span class="text"><b><i> <br>
 </i></b></span><span class="text"><b><i>Анализ содержания основных нормативных правовых актов об образовании в Свердловской области (на основе анализа Областного Закона “Об образовании в Свердловской области”).</i></b>Закон Свердловской области № 26-03 “Об образовании в Свердловской области” был принят 16 июля 1998 года. Он определил приоритеты образовательной политики на территории области, подходы к развитию содержания образования и управления им, закрепил принципиальные позиции по разграничению компетенции в сфере образования, интеграции областной системы образования, предоставлению обучающимся дополнительных прав и возможностей получения образования, государственному образовательному заказу и контролю за качеством образования и другие.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Принятие названного закона стало основой для дальнейшего развития нормативно-правового поля системы образования в Свердловской области.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Реализация в течение года Областного закона “Об образовании в Свердловской области” выявила несоответствие ряда его статей и отдельных положений реальной ситуации в образовании, которая достаточно быстро меняется, причем, ряд изменений непосредственно вызван действием закона.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Кроме того, за прошедшее время появились новые правовые акты федерального и областного уровня, которые требуют внесения изменений и дополнений в Закон Свердловской области “Об образовании в Свердловской области”.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Вступил в силу Налоговый Кодекс Российской Федерации, часть первая, от 31 июля 1998 года № 146-ФЗ, принят Закон Российской Федерации от 24 июля 1998 года № 124-ФЗ “Об основных гарантиях прав ребенка в Российской Федерации” (Собрание законодательства РФ, 1998, № 31, ст. 3802).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Приняты постановление Правительства Свердловской области от 03.08.99 г. № 897-ПП “Об утверждении Государственного образовательного стандарта (национально-региональный компонент) образования в период детства, основного общего и среднего (полного) общего образования Свердловской области в новой редакции”, постановление Правительства Свердловской области от 26.08.98 г. № 868-ПП “Об утверждении национально-регионального компонента государственного образовательного стандарта среднего педагогического образования”.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Идет работа над законопроектами “О внесении изменений и дополнений в Областной Закон “О защите прав ребенка”, предпринимаются попытки возобновления работы над законопроектом “О государственном образовательном заказе Свердловской области”.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В структуре областных органов исполнительной власти появились территориальные межотраслевые исполнительные органы государственной власти Свердловской области, утвержденные указом Губернатора Свердловской области от 01.10.99 г. № 475-УГ “О структуре исполнительной власти Свердловской области” (приложение 5). В функции этих органов входит и деятельность в сфере образования. Готовится проект Закона Свердловской области об управленческих округах.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Исходя из вышеизложенного, Закон “Об образовании в Свердловской области” требует внесения соответствующих изменений и дополнений. Например, в практике работы системы образования Свердловской области сложился и официально используется термин “образование в период детства”, разработана Концепция развития образования в период детства, принят Государственный образовательный стандарт (национально-региональный компонент) образования в период детства, этому термину соответствуют создаваемые на территории Свердловской области и Российской Федерации образовательные организации-комплексы “детский сад - начальная школа”, “прогимназия”, действующие на основе Типового положения об образовательном учреждении для детей дошкольного и младшего школьного возраста (утверждено постановлением Правительства Российской Федерации от 19.09.97г. № 1204), а в законе «Об образовании в Свердловской области этот термин не используется.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В связи с разработкой Государственного образовательного стандарта (федеральный компонент) и принятием Правительством Свердловской области Государственного образовательного стандарта (национально - региональный компонент) образования в период детства, основного общего и среднего (полного) общего образования появились точные ориентиры развития содержания образования, изменились подходы к сертификации образовательных программ.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В соответствии с пунктами 6 и 7 части 2 статьи 32 Закона Российской Федерации “Об образовании” образовательная организация самостоятельно разрабатывает и утверждает образовательные и учебные программы. Однако сложившаяся практика показывает, что образовательные организации не всегда способны самостоятельно оценить качество программ, которые им предлагаются. Для этого необходима экспертная оценка специалистов. С целью оградить обучающихся от некачественной образовательной продукции необходимо сохранить её обязательную сертификацию. В то же время, порядок сертификации периодически изменяется с учетом реальной ситуации в образовании. Он определяется Правительством Свердловской области.</span>
</p>
<p style="text-align: justify;">
	<span class="text">С принятием государственных образовательных стандартов в функции органов управления образованием необходимо ввести осуществление контроля за их исполнением.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В соответствии со статьей 45 Закона Российской Федерации “Об образовании” государственные и муниципальные образовательные организации могут оказывать населению платные дополнительные образовательные услуги, не предусмотренные государственными образовательными стандартами и не заменяющие образовательную деятельность, финансируемую из средств бюджета. Данная деятельность не считается предпринимательской при условии, что доход от нее реинвестируется в образовательную организацию. Некоторые руководители образовательных организаций и муниципальных органов управления образованием в условиях недостаточного финансирования восприняли право оказывать платные дополнительные образовательные услуги в качестве одного из важных источников дополнительного финансирования, стали навязывать их обучающимся, нередко вместо образовательной деятельности, финансируемой из бюджета. Предоставление дополнительных образовательных услуг, в том числе платных, требует упорядочивания на уровне образовательных организаций.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Учитывая, что в соответствии с пунктом 4 статьи 19 Закона Российской Федерации “Об образовании” требование обязательности основного общего образования применительно к конкретному обучающемуся сохраняет силу до достижения им возраста 15 лет, появляется категория лиц, не имеющих основного общего образования, не желающих получать начальное профессиональное образование, но нуждающихся в профессиональной подготовке. Это случаи исключительные. Проблемы таких детей должны быть решены на уровне органов местного самоуправления. В любом случае профессиональная подготовка должна быть для них бесплатной.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Одним из наиболее актуальных и развивающихся сегодня является коррекционное образование: идет обсуждение федерального закона “О коррекционном образовании”, стоит вопрос о развитии сети образовательных организаций коррекционного образования, приняты Типовые положения о психолого-медико-педагогических консультациях и комиссиях, центрах диагностики и консультирования, психолого-медико-социального сопровождения. Есть необходимость привести названия соответствующих служб, упоминаемых в Законе Свердловской области “Об образовании в Свердловской области”, в соответствие с Типовыми положениями о них, уточнить в соответствии с ними ряд положений названного закона.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Важнейшей задачей системы образования является защита прав граждан в образовании. Руководители негосударственных образовательных организаций, имеющих государственную аккредитацию, обращаются в Министерство общего и профессионального образования с просьбами решить проблему защиты прав обучающихся в соответствии с федеральным законодательством. Согласно пункту 20 статьи 50 Закона Российской Федерации “Об образовании”, обучающиеся негосударственных образовательных учреждений, имеющих государственную аккредитацию, имеют право на получение льгот, установленных законодательством Российской Федерации для обучающихся государственных и муниципальных образовательных учреждений.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В соответствии с п. 2 ст. 15 Закона Российской Федерации “Об основных гарантиях прав ребенка в Российской Федерации” предполагается осуществление ряда действий по защите прав детей, находящихся в трудной жизненной ситуации. Эти действия осуществляются исполнительными органами государственной власти Российской Федерации и Свердловской области в соответствии с их компетенцией</span>
</p>
<p style="text-align: justify;">
	<span class="text">В соответствии с пунктом 1 статьи 16 Закона РФ “Об образовании” государственные и муниципальные образовательные учреждения должны обеспечить прием всех граждан, которые проживают на данной территории.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В то же время, в соответствии с п. 13 ст. 50 Закона РФ “Об образовании”, органы государственной власти и органы управления образованием могут создавать нетиповые образовательные учреждения высшей категории для детей, подростков и молодых людей, проявивших выдающиеся способности.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Количество таких учреждений ограничено, при приеме в них необходимо выяснить уровень подготовки и обоснованность претензии поступающих на выдающиеся способности. Это делает возможным и необходимым конкурсный порядок приема. Но право на установление такого порядка дает только соответствующий статус образовательной организации, который определяется учредителем и подтверждается свидетельством о государственной аккредитации. Необходимо уточнить порядок приема обучающихся в образовательные организации, так как нередки случаи различных ограничений при приеме в муниципальные образовательные организации, создавшие профильные или специализированные классы. Образовательные организации незаконно устанавливают плату за образовательные услуги, которые должны финансироваться учредителем.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Комитетом по социальной политике Областной Думы Законодательного Собрания Свердловской области совместно с Министерством общего и профессионального образования Свердловской области в соответствии с Планом мероприятий по реализации Областного закона “Об образовании в Свердловской области”, утвержденным распоряжением Правительства Свердловской области от 12.10.98 г. № 784-РП, подготовлен проект Закона Свердловской области “Об образовательном заказе Свердловской области”. В соответствии с названным законопроектом необходимо уточнить роль органов управления образованием различных уровней в формировании и исполнении областного образовательного заказа[45].</span>
</p>
<p style="text-align: justify;">
	<span class="text">В связи с вступлением в силу с 1 января 1999 года Налогового Кодекса Российской Федерации отменен установленный Законом Свердловской области сбор на нужды образовательных организаций. Тем не менее, проблема привлечения дополнительных средств на развитие образования остается актуальной. Для решения этой проблемы идет работа по созданию Фонда развития образования в Свердловской области. Различные формы привлечения дополнительных, внебюджетных средств на развитие образования, на дополнительное финансирование областных целевых программ в сфере образования предусмотрены Уставом Свердловской области, Областным законом от 8 апреля 1997 года № 17-ОЗ “О благотворительной деятельности в Свердловской области” (“Областная газета”, 15 апреля 1997 года) и другими документами.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Одной из важнейших функций Министерства общего и профессионального образования Свердловской области является сбор, обработка и использование информации о состоянии и перспективах развития системы образования области. Для устранения разночтений в регулировании информационного обмена требуется уточнение текста Областного закона в части полномочий уполномоченного органа исполнительной власти (Министерства общего и профессионального образования Свердловской области).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Требуется уточнение в части регулирования взаимодействий Министерства общего и профессионального образования Свердловской области с муниципальными органами управления образованием.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В соответствии со статьей 59 Трудового кодекса РФ срочный трудовой договор (контракт) заключается, кроме перечисленных, также в случаях, непосредственно предусмотренных законом. Статьей 15 Закона Свердловской области от 8 апреля 1997 года № 18-ОЗ “О защите трудовых прав граждан на территории Свердловской области” (“Областная газета”, 15 апреля 1997 года, № 55) запрещается заключение срочного трудового договора (контракта), если это, помимо перечисленных обстоятельств, не предусмотрено федеральным и областным законодательством.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В соответствии с постановлением Правительства Свердловской области от 12.10.98 г. № 1046-П “Об утверждении положений о министерствах Свердловской области” Министерству общего и профессионального образования Свердловской области делегированы полномочия в части осуществления прав учредителя областных государственных образовательных организаций, назначения на должность и освобождения от должности руководителей областных государственных образовательных организаций. В соответствии с подпунктом 3.3.4. пункта 3.2. раздела 3 Положения о Министерстве общего и профессионального образования Свердловской области министерство назначает на должность и освобождает от должности, заключает контракты и контролирует выполнение условий контракта с руководителями областных государственных (подведомственных) образовательных организаций, учитывая, что руководители образовательных организаций являются организаторами реализации областной образовательной политики, а их статус определяется учредителем.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Согласно пункту 20 статьи 50 Закона Российской Федерации “Об образовании”, обучающиеся негосударственных образовательных учреждений, имеющих государственную аккредитацию, имеют право на получение льгот, установленных законодательством Российской Федерации для обучающихся государственных и муниципальных образовательных учреждений.</span>
</p>
<p style="text-align: justify;">
	<span class="text">То же можно сказать и о налоговых льготах. Например, о налоге с продаж. В соответствии с Законом Свердловской области “О налоге с продаж”, в подпункте 67 пункта 2 статьи 2 от налога с продаж освобождается только стоимость услуг, связанных с учебным процессом, осуществляемым государственными и муниципальными образовательными организациями. Однако в пункте 2 статьи 3 Налогового Кодекса Российской Федерации сказано, что “не допускается устанавливать... налоговые льготы в зависимости от форм собственности”. В соответствии с пунктом 3 статьи 40 Закона Российской Федерации “Об образовании” и с пунктом 4 статьи 27 Закона Свердловской области “Об образовании в Свердловской области” образовательные организации освобождаются независимо от их организационно-правовых форм в части непредпринимательской деятельности, предусмотренной уставом этих образовательных организаций, от уплаты всех видов налогов. В соответствии с пунктами 1 и 2 статьи 46 Закона Российской Федерации “Об образовании” платная образовательная деятельность негосударственных образовательных учреждений не рассматривается как предпринимательская, если получаемый от неё доход полностью идет на возмещение затрат на обеспечение образовательного процесса.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Таким образом, подробный контент-анализ Областного закона «Об образовании в Свердловской области» показывает, что динамичность ситуации в правовых отношениях требует не только приведения образовательной практики к соответствующим нормам закона, но и нормы закона следует совершенствовать в соответствии с изменяющейся образовательной практикой. Такая работа сейчас ведется и подготовлен законопроект «О внесении изменений и дополнений в Областной закон «Об образовании в Свердловской области».</span>
