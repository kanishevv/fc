  <h3 align="center">Реферат</h3>
 
  <p align="center"><b>итогового отчета за 2011 год по проекту</b></p>
 
  <p align="center"><b>&laquo;Исследование особенностей финансирования экспорта образовательных услуг&raquo;</b></p>
 
  <p> </p>
 
  <p align="justify"><b>Ключевые слова:</b> образовательные услуги, экспорт образовательных услуг, финансирование экспорта, международная торговля услугами</p>
 
  <p align="justify"> </p>
 
  <p align="justify"><b>Объект исследования </b></p>
 
  <p align="justify">Объектом исследования является система отношений, возникающих в процессе финансирования экспорта образовательных услуг.</p>
 
  <p align="justify"> </p>
 
  <p align="justify"><b>Цель работы.</b></p>
 
  <p align="justify">Проект нацелен на исследование особенностей финансирования и регламентации экспорта образовательных услуг.</p>
 
  <p align="justify">Основные задачи, решаемые при выполнении проекта: определение понятия экспорта образовательных услуг; анализ организационно-правовых основ экспорта образовательных услуг; анализ системы государственной поддержки экспорта образовательных услуг; исследование механизма финансирования экспорта образовательных услуг; выявление основных направлений, путей и средств повышения эффективности финансирования экспорта образовательных услуг; сравнительное исследование моделей финансирования академической мобильности в зарубежных странах и изучение организационно-правовых основ финансирования академической мобильности в Российской Федерации в новых условиях (создание в 2010 году таможенного союза ЕврАзЭС, углубление экономической интеграции в ЕврАзЭС).</p>
 
  <p align="justify"> </p>
 
  <p align="justify"><b>Методология исследования.</b></p>
 
  <p align="justify">В ходе реализации проекта предусматривается использование ряда методов исследования, одним из которых является <i>формально-догматический метод</i>, включающий такие приемы, как описание и анализ нормы права и формируемых в сфере финансирования образовательных услуг правоотношений образовательных правоотношений, их разъяснение и толкование, классификацию. </p>
 
  <p align="justify">Использование формально-догматического метода предполагает описание и анализ не только норм законодательства Российской Федерации об основах государственного регулирования внешнеторговой деятельности, гражданского и образовательного законодательства, но и блок международно-правовых источников, имеющих силу, превышающую силу актов российского законодательства.</p>
 
  <p align="justify">Анализ системы правового регулирования был бы неполным без рассмотрения основных тенденций правоприменительной практики, выраженных в судебной практике и решениях уполномоченных органов исполнительной власти.</p>
 
  <p align="justify">Наряду с формально-догматическим методом, при проведении исследования использовались также <i>статистический метод</i>, метод <i>конкретно-социологического исследования, </i>предполагающий использование таких приемов как личное наблюдение за деятельностью образовательных учреждений, устные опросы работников, анкетирование и собеседования. В некоторых случаях применяется <i>компаративный (сравнительно-правовой) метод.</i></p>
 
  <p align="justify"> </p>
 
  <p align="justify"><b>Результаты работы.</b></p>
 
  <p align="justify">По результатам работ, выполненных в течение четырех этапов проекта:</p>
 
  <p align="justify">&middot; дано понятие образовательной услуги;</p>
 
  <p align="justify">&middot; определена социальная ценность образовательных услуг;</p>
 
  <p align="justify">&middot; определено место образовательных услуг в системе общественных услуг;</p>
 
  <p align="justify">&middot; выявлены признаки образовательных услуг;</p>
 
  <p align="justify">&middot; показано место образовательных услуг в механизме реализации права на образование;</p>
 
  <p align="justify">&middot; дано определение образовательной услуги как правовой категории;</p>
 
  <p align="justify">&middot; даны основные характеристики образовательной услуги как экономической категории;</p>
 
  <p align="justify">&middot; показано значение образовательных услуг в современной экономике (экономике постиндустриального общества, экономике знаний);</p>
 
  <p align="justify">&middot; охарактеризованы нематериальный характер образовательных услуг и сопутствующая материальная инфраструктура образования. </p>
 
  <p align="justify">&middot; выявлены основные особенности предпринимательской деятельности в сфере образования; определена роль государства в механизме регулирования рынка образовательных услуг;</p>
 
  <p align="justify">&middot; определено понятие трансграничных образовательных услуг и их место в системе современных международных экономических отношений;</p>
 
  <p align="justify">&middot; дано понятие экспорта и импорта образовательных услуг;</p>
 
  <p align="justify">&middot; выявлены особенности правового режима трансграничных образовательных услуг;</p>
 
  <p align="justify">&middot; определено место, которое образование занимает на мировом рынке услуг, и влияние глобализации на развитие международного рынка образовательных услуг;</p>
 
  <p align="justify">&middot; рассмотрено развитие экспортного потенциала как приоритетное направление российской образовательной политики;</p>
 
  <p align="justify">&middot; определены образовательные услуги как объект государственной поддержки;</p>
 
  <p align="justify">&middot; определены понятие и формы экспорта образовательных услуг;</p>
 
  <p align="justify">&middot; определен механизм государственной поддержки экспорта образовательных услуг, его институциональная и функциональная составляющие;</p>
 
  <p align="justify">&middot; определены организационно-правовые основ экспорта образовательных услуг;</p>
 
  <p align="justify">&middot; определено понятие финансирования понятие финансирование экспорта образовательных услуг;</p>
 
  <p align="justify">&middot; выявлен и проанализирован механизм финансирования экспорта образовательных услуг; </p>
 
  <p align="justify">&middot; проанализировано влияние экспорта образовательных услуг на экономику страны, их экспортирующей: оценен финансовый эффект от экспортных операций, их влияние на развитии национального рынка труда;</p>
 
  <p align="justify">&middot; выявлены преимущества от активизации участия Российской Федерации в транснациональном (трансграничном) образовании (<i>transborder education</i>);</p>
 
  <p align="justify">&middot; проанализировано развитие механизмов финансирования экспорта образовательных услуг;</p>
 
  <p align="justify">&middot; определено направление для развития государственного финансирования образования - в направлении достижения большей координации научно-исследовательских и образовательных проектов; </p>
 
  <p align="justify">&middot; выявлены происходящие в институциональной структуре российского образования изменения, которые предполагают применение новых подходов к финансированию национальных исследовательских университетов, получающих свой статус по результатам конкурсного отбора программ развития университетов;</p>
 
  <p align="justify">&middot; сделан вывод<b>, </b>что скорейшее принятие комплексной программы содействия экспорту российских образовательных услуг, наряду с обновлением самого законодательства Российской Федерации об образовании будет содействовать созданию эффективной организационно-правовой базы управления экспортными операциями в сфере образования и финансирования соответствующих мероприятий;</p>
 
  <p align="justify">&middot; определено понятие академической мобильности. Академическая мобильность &ndash; это не просто перемещение студентов, преподавателей и исследователей в образовательных целях между ВУЗами Российской Федерации и зарубежными ВУЗами, не просто элемент международного сотрудничества &ndash; это скорее личностное качество участников образовательных правоотношений, развитию которого нужно способствовать как внутри университета (посредством проведения конференций, семинаров, на которых предоставляется нужная информация, создания специальных структур, которые занимаются этой организацией, а также посредством открытости ВУЗов к сотрудничеству), так и в рамках Российской Федерации в целом (посредством учреждения российских денежных фондов развития академической мобильности и привлечения в этих целях зарубежных);</p>
 
  <p align="justify">&middot; Выделены следующие особенности академической мобильности: во-первых необходимо, отличать академическую мобильность от мобильности профессиональной. Если академическая мобильность осуществляется по траекториям между учебными заведениями одного и того же государства или различных государств, то профессиональная мобильность предполагает трудоустройство на неакадемических рынках труда как в пределах одного государства, так и на международном уровне.</p>
 
  <p align="justify">Во-вторых, академическую мобильность следует отличать от зарубежных стажировок, в ходе которых студенты и исследователи направляются в зарубежные университеты, как правило, на непродолжительный срок. В рамках академической мобильности обучение длится один или несколько семестров. Еще одно отличие состоит в том, что программа стажировок предполагает обычно изучение одного или нескольких курсов, изучение языка государства пребывания. Программа академической мобильности предполагает освоение студентом в полном объеме учебного плана семестра или учебного года, который ему будет засчитан по возвращении в &laquo;базовый университет&raquo; &ndash; университет, в который студент поступил первоначально.</p>
 
  <p align="justify">В-третьих, следует исходить из того, что академическая мобильность &ndash; это всегда мобильность физическая. Виртуальная мобильность, дистантное трансграничное обучение не могут полностью заменить физическую мобильность, предполагающую проживание студента за рубежом и обучение в иностранном университете.</p>
 
  <p align="justify">В-четвертых, академическую мобильность, являющуюся существенной характеристикой Болонского процесса и предполагающую осуществление трансграничного образования, следует отличать от так называемой географической (внутренней) мобильности, под которой понимается возможность для студента продолжить обучение в университете, расположенном в другом регионе того же государства;</p>
 
  <p align="justify">&middot; определены участники академической мобильности: ими являются не только бакалавры, магистры и аспиранты, но и преподаватели, исследователи, администраторы и иные сотрудники высших учебных заведений. Академическая мобильность должна включаться в образовательную траекторию каждого успешного студента, стать обязательным атрибутом любого крупного исследовательского проекта. В конечном итоге, в европейском образовательном пространстве мобильность студентов, преподавателей и исследователей должна стать правом, а не оставаться привилегией;</p>
 
  <p align="justify">&middot; сделан вывод о том, что для формирования правового режима финансирования академической мобильности особое значение имеют норма, содержащаяся в п. 4 ст. 28 Федерального закона &laquo;О высшем и послевузовском профессиональном образовании&raquo;, в соответствии с которой &laquo;если порядком предоставления средств не установлено иное, высшие учебные заведения самостоятельно определяют направления использования средств, полученных ими из соответствующего бюджета и иных источников, не запрещенных законодательством Российской Федерации&raquo; Данная норма законодательно закрепляет возможность для вуза при наличии у него соответствующих средств финансировать как индивидуальную мобильность, так и совместные международные образовательные программы.</p>
 
  <p align="justify">Финансирование академической мобильности может осуществляться из разных источников:</p>
 
  <p align="justify">а) бюджетных средств вузов и научных организаций, в том числе выделяемых в рамках национальных проектов;</p>
 
  <p align="justify">б) внебюджетных средств вузов и научных организаций (в некоторых вузах, например МГУ, это могут быть централизованные внебюджетные средства вуза и внебюджетные средства его структурных подразделений);</p>
 
  <p align="justify">в) средств принимающей стороны, в том числе грантов международных организаций и частных фондов;</p>
 
  <p align="justify">г) личных средств участников программ академической мобильности;</p>
 
  <p align="justify">д) иных, не запрещенных законом источников.</p>
 
  <p align="justify">Успешное развитие академической мобильности в вузе во многом зависит от эффективности системы ее финансирования. Данная задача может быть решена в полном объеме и своевременно только в том случае, когда выделяемые на эти цели бюджетные средства дополняются привлеченными финансовыми ресурсами;</p>
 
  <p align="justify">&middot; сформулирована одна из важнейших задач по совершенствованию системы финансового обеспечения академической мобильности в Российской Федерации: она состоит в дальнейшей диверисификации источников финансирования, стимулировании грантовой и благотворительной политики в этом секторе (в том числе посредством льготного налогообложения), придания инструментам финансирования действенного характера, прежде всего путем предоставления финансовой поддержки на покрытие расходов не только на обучение, но и на переезд в принимающую страну, обустройство и проживание там.</p>
 
