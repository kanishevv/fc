<p><span class="text" style="text-align: justify;">Статья 34. Вступление в силу настоящего Федерального закона</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Порядок присуждения ученых степеней и присвоения ученых званий сохраняется до принятия соответствующих федеральных законов.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Предложить Президенту Российской Федерации и поручить Правительству Российской Федерации привести свои правовые акты в соответствие с настоящим Федеральным законом.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Настоящий Федеральный закон вступает в силу со дня его официального опубликования.</span></p>
 
