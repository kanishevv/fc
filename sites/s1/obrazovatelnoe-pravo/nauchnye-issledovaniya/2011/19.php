<p align="center"><b>РЕФЕРАТ</b></p>
 
<p align="center"><b>итогового отчета за 2011 год по проекту</b></p>
 
<p align="center"><b>&laquo;Разработка научно-методических основ правового обеспечения проведения государственной (итоговой) аттестации выпускников образовательных учреждений в условиях формирования единой независимой системы оценки качества образования&raquo;</b></p>
 
<p> </p>
 
<p align="justify"><b>Ключевые слова:</b> законодательство об образовании зарубежных стран, оценка качества образования, общероссийская система оценки качества образования, технологии и процедуры оценки качества образования, эффективность образования.</p>
 
<p align="justify"> </p>
 
<p align="justify"><b>Задачи проекта:</b></p>
 
<p align="justify">&bull; обеспечение развития системы образования за счет разработки критериев и принципов независимой оценки эффективности образования;</p>
 
<p align="justify">&bull; стимулирование развития образовательных учреждений и совершенствования образовательных программ путем непрерывного самообследования и планирования в соответствии с задачей достижения внешних эффектов, путем формулировки целей деятельности, социально-ориентированной и четко сформулированной &laquo;миссии&raquo; образовательного учреждения, разделяемой всеми участниками образовательного процесса и направленной на повышение качества образовательных услуг;</p>
 
<p align="justify">&bull; способствование повышению конкурентоспособности российского образования на мировом рынке образовательных услуг.</p>
 
<p align="justify"> </p>
 
<p align="justify"><b>Актуальность выполнения проекта:</b></p>
 
<p align="justify">Введение единой независимой системы оценки качества образования является одним из современных приоритетов модернизации образования и способствует повышению качества образовательных услуг на всех уровнях системы образования &ndash; от дошкольного до высшего и дополнительного. В настоящее время функцию независимой оценки качества образования, которая обеспечивает объективную информацию о системе образования города, выполняет лишь единый государственный экзамен (ЕГЭ). Однако единая независимая система оценки качества образования не ограничивается рамками общего образования. Актуальными остаются вопросы, связанные с оценкой эффективности деятельности образовательных учреждений всех уровней образования, качества различных образовательных программ, а также образовательных достижений обучающегося. Поиска решений требует и проблема обеспечения единства образовательного пространства России. Основной задачей формирования единой независимой системы оценки качества образования по-прежнему остается формулировка и апробация критериев эффективности образования. В настоящее время формирование и оценка таких критериев происходит внутри самой системы. При этом выпускники различных уровней образования зачастую не отвечают потребностям работодателя. Выходом из сложившейся ситуации видится формирование таких механизмов, при которых в процессе формулирования критериев качества образования и оценки эффективности образования принимал бы участие непосредственный &laquo;социальный заказчик&raquo;. Такой подход во многих развитых странах (в том числе в США, Великобритании, Австралии, Голландии и др.) обеспечивает соотношение результатов функционирования образовательных систем с запросами общества (потребностями инвесторов, государства, профессиональных сообществ, налогоплательщиков).</p>
 
<p align="justify"> </p>
 
<p align="justify"><b>В качестве объекта настоящего исследования</b> выступают общественные отношения, возникающие в связи с формированием единой независимой системой оценки качества образования в зарубежных странах, а также в России и в субъектах РФ. </p>
 
<p align="justify"> </p>
 
<p align="justify"><b>Предмет исследования</b> составляют законодательные акты зарубежных государств, федеральное законодательство и законодательство субъектов РФ, материалы совещаний, комиссий, комитетов, ассоциаций, объединений в сфере образования, содержащие сведения о формировании и практике внедрения единой независимой системой оценки качества образования. </p>
 
<p align="justify">Методической основой настоящего исследования послужил системный подход к изучению зарубежного и российского законодательства и правоприменительной практики по вопросам образования, а также исторический, логический, формально-юридический и сравнительно-правовой методы научного познания. </p>
 
<p align="justify"> </p>
 
<p align="justify"><b>Краткое описание работ, выполненных по проекту: </b></p>
 
