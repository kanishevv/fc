 <b>Статья 20. Реализация профессиональных образовательных программ</b></p>
 
<p>1. Профессиональные образовательные программы, в том числе для обучающихся, воспитанников с отклонениями в развитии и инвалидов, реализуются в образовательных учреждениях профессионального образования, в том числе в специальных (коррекционных) для обучающихся, воспитанников с отклонениями в развитии. Образовательные учреждения профессионального образования, имеющие государственную аккредитацию, реализуют указанные образовательные программы соответствующего уровня в целях подготовки работников квалифицированного труда (рабочих и служащих) и специалистов соответствующего уровня согласно перечням профессий и специальностей, устанавливаемым Правительством Российской Федерации, и уровням образования, установленным настоящим Законом.</p>
 
<p>2. Государственный образовательный стандарт среднего (полного) общего образования реализуется в пределах образовательных программ начального профессионального или среднего профессионального образования с учетом профиля получаемого профессионального образования.</p>
 
<p>3. Наличие любого другого профессионального образования не может служить основанием для отказа в приеме граждан в образовательное учреждение профессионального образования.</p>
 
