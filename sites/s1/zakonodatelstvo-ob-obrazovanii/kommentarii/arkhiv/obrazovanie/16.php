<p align="JUSTIFY"><span class="text">Статья 19. Право на образование и права в области культуры.</span></p>

<p align="JUSTIFY"><span class="text">1. Военнослужащие имеют право на обучение в военных образовательных учреждениях профессионального образования (в том числе на получение послевузовского образования) и на курсах (факультетах) подготовки, переподготовки и повышения профессиональной квалификации военнослужащих.</span></p>

<p align="JUSTIFY"><span class="text">Порядок приема военнослужащих в указанные образовательные учреждения и обучения в них определяется федеральными законами и иными нормативными правовыми актами.</span></p>

<p align="JUSTIFY"><span class="text">Военные образовательные учреждения высшего и среднего профессионального образования одновременно со специальной образовательной программой реализуют образовательные программы по подготовке специалистов в родственных (смежных) гражданских отраслях и выдают выпускникам соответствующие документы об образовании, если образование данного уровня получают военнослужащие впервые.</span></p>

<p align="JUSTIFY"><span class="text">2. Военнослужащие, проходящие военную службу по контракту, имеют право на обучение в гражданских образовательных учреждениях высшего ... профессионального образования и на подготовительных отделениях (курсах) указанных образовательных учреждений с освоением образовательных программ по очно-заочной (вечерней) или заочной форме обучения.</span></p>

<p align="JUSTIFY"><span class="text">3. Военнослужащим, проходящим военную службу по призыву, обучение в гражданских образовательных учреждениях высшего ... профессионального образования не разрешается.</span></p>

<p align="JUSTIFY"><span class="text">4. Военнослужащие, проходящие военную службу по контракту, общая продолжительность военной службы которых составляет пять лет и более (не считая времени обучения в военных образовательных учреждениях высшего профессионального образования), в год увольнения с военной службы по достижении ими предельного возраста пребывания на военной службе, истечении срока военной службы или состоянию здоровья имеют право пройти профессиональную переподготовку по одной из гражданских специальностей без взимания с них платы за обучение и с сохранением обеспечения всеми видами довольствия в порядке и на условиях, которые определяются Министерством обороны Российской Федерации (иным федеральным органом исполнительной власти, в котором федеральным законом предусмотрена военная служба), продолжительностью до трех месяцев, а при увольнении с военной службы в связи с организационно-штатными мероприятиями - шести месяцев. В случае увольнения указанных военнослужащих с военной службы в период обучения они имеют право на завершение учебы бесплатно.</span></p>

<p align="JUSTIFY"><span class="text">5. Гражданам, призванным на военную службу в период обучения в государственных образовательных учреждениях профессионального образования, при увольнении с военной службы сохраняется право на продолжение образования в том образовательном учреждении, в котором они обучались до призыва.</span></p>

<p align="JUSTIFY"><span class="text">Граждане, уволенные с военной службы, пользуются преимущественным правом на поступление в государственные образовательные учреждения высшего ... профессионального образования и на подготовительные отделения образовательных учреждений высшего профессионального образования.</span></p>

<p align="JUSTIFY"><span class="text">Граждане, уволенные с военной службы и поступающие в государственные образовательные учреждения высшего ... профессионального образования по рекомендациям командиров, пользуются правом внеконкурсного зачисления при условии получения ими положительных оценок на вступительных экзаменах.</span></p>

<p align="JUSTIFY"><span class="text">Граждане, проходившие военную службу по контракту и уволенные с военной службы по достижении ими предельного возраста пребывания на военной службе, состоянию здоровья или в связи с организационно-штатными мероприятиями, имеют право на:</span></p>

<p align="JUSTIFY"><span class="text">бесплатное первоочередное прохождение подготовки, переподготовки и повышение квалификации по направлению и за счет средств федеральной государственной службы занятости населения, а имеющие право на пенсию - на получение профессионального образования по направлению и за счет средств организаций, в которые они приняты на работу, с выплатой среднего заработка во время обучения;</span></p>

<p align="JUSTIFY"><span class="text">поступление в государственные образовательные учреждения профессионального образования без вступительных экзаменов:</span></p>

<p align="JUSTIFY"><span class="text">на первый курс и последующие курсы государственных образовательных учреждений высшего профессионального образования - имеющие незаконченное высшее или высшее военное профессиональное образование;</span></p>

<p align="JUSTIFY"><span class="text">на подготовительные отделения государственных образовательных учреждений высшего профессионального образования - окончившие образовательные учреждения среднего (полного) общего образования или среднего профессионального образования.</span></p>

<p align="JUSTIFY"><span class="text">Такое же право на поступление в государственные образовательные учреждения профессионального образования имеют военнослужащие, проходящие военную службу по контракту и имеющие общую продолжительность военной службы 15 лет и более.</span></p>

<p align="JUSTIFY"><span class="text">Прием указанных граждан в государственные образовательные учреждения профессионального образования проводится в течение всего учебного года, в том числе дополнительно к установленным этим образовательным учреждениям планам приема.</span></p>

<p align="JUSTIFY"><span class="text">Граждане, имеющие среднее военное или высшее военное профессиональное образование, проходят обучение в государственных образовательных учреждениях высшего профессионального образования бесплатно.</span></p>

<p align="JUSTIFY"><span class="text">Военнослужащие, имеющие высшее или среднее гражданское профессиональное образование, родственное по профилю подготовки военной специальности, при прохождении военной службы по этой специальности приравниваются по образованию к военнослужащим, окончившим соответствующие высшие или средние военные образовательные учреждения профессионального образования.</span></p>

<p align="JUSTIFY"><span class="text">6. При изменении места военной службы военнослужащих, проходящих военную службу по контракту, а также при увольнении с военной службы по достижении ими предельного возраста пребывания на военной службе, состоянию здоровья или в связи с организационно-штатными мероприятиями члены их семей, обучающиеся (воспитывающиеся) в государственных образовательных учреждениях, имеют право переводиться (приниматься) в образовательные учреждения, ближайшие к новому месту военной службы или месту жительства...</span></p>

<p align="JUSTIFY"><span class="text">Дети военнослужащих, проходящих военную службу по контракту и имеющих общую продолжительность военной службы 20 лет и более, дети граждан, уволенных с военной службы по достижении ими предельного возраста пребывания на военной службе, состоянию здоровья или в связи с организационно-штатными мероприятиями, общая продолжительность военной службы которых составляет 20 лет и более, дети военнослужащих, погибших при исполнении ими обязанностей военной службы или умерших вследствие увечья (ранения, травмы, контузии) либо заболевания, полученных ими при исполнении обязанностей военной службы, пользуются правом зачисления в суворовские военные, нахимовские военно-морские училища и кадетские корпуса вне конкурса, а при поступлении в военные образовательные учреждения высшего и среднего профессионального образования - преимущественным правом при условии успешной сдачи экзаменов и соответствия другим установленным для поступающих требованиям.</span></p>

