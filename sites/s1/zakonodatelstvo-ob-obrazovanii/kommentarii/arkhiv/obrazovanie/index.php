<p><b>Статья 5. Государственные гарантии прав граждан Российской Федерации в области образования</b></p>
 
<p>1. Гражданам Российской Федерации гарантируется возможность получения образования независимо от пола, расы, национальности, языка, происхождения, места жительства, отношения к религии, убеждений, принадлежности к общественным организациям (объединениям), возраста, состояния здоровья, социального, имущественного и должностного положения, наличия судимости.</p>
 
<p>Ограничения прав граждан на профессиональное образование по признакам пола, возраста, состояния здоровья, наличия судимости могут быть установлены только законом.</p>
 
<p>2. Государство обеспечивает гражданам право на образование путем создания системы образования и соответствующих социально-экономических условий для получения образования.</p>
 
<p>3. Государство гарантирует гражданам общедоступность и бесплатность дошкольного, начального общего, основного общего, среднего (полного) общего образования и начального профессионального образования, а также на конкурсной основе бесплатность среднего профессионального, высшего профессионального и послевузовского профессионального образования в государственных и муниципальных образовательных учреждениях в пределах государственных образовательных стандартов, если образование данного уровня гражданин получает впервые.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>4. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>5. В целях реализации права на образование граждан, нуждающихся в социальной поддержке, государство полностью или частично несет расходы на их содержание в период получения ими образования. Категории граждан, которым предоставляется данная поддержка, порядок и размеры ее предоставления устанавливаются федеральными законами для федеральных государственных образовательных учреждений, законами субъектов Российской Федерации для образовательных учреждений, находящихся в ведении субъектов Российской Федерации, и муниципальных образовательных учреждений.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>6. Государство создает гражданам с отклонениями в развитии условия для получения ими образования, коррекции нарушений развития и социальной адаптации на основе специальных педагогических подходов.</p>
 
<p>7. Государство оказывает содействие в получении образования гражданами, проявившими выдающиеся способности, в том числе посредством предоставления им специальных государственных стипендий, включая стипендии для обучения за рубежом. Критерии и порядок предоставления таких стипендий устанавливаются Правительством Российской Федерации.</p>
 
