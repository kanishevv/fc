  <p align="center"> </p>

  <p style="text-align: center;"><b>I. Нормативно-правовая основа по установлению учителям учебной нагрузки</b></p>

  <p><b>
      <br />
    </b></p>

  <p align="center"> </p>

  <p style="text-align: justify;"><span class="text">Основополагающими федеральными законодательными и иными нормативными правовыми актами, применяемыми при установлении учебной нагрузки учителям образовательных учреждений, реализующих общеобразовательные программы, являются:</span></p>

  <p style="text-align: justify;"><span class="text">- Трудовой кодекс РФ;</span></p>

  <p style="text-align: justify;"><span class="text">- Закон РФ &laquo;Об образовании&raquo;;</span></p>

  <p style="text-align: justify;"><span class="text">- Типовое положение об общеобразовательном учреждении, утвержденное постановлением Правительства РФ от 19 марта 2001 года № 196 «Об утверждении Типового положения об общеобразовательном учреждении» (с изменениями и дополнениями);</span></p>

  <p style="text-align: justify;"><span class="text">- постановление Правительства РФ от 3 апреля 2003 г. № 191 «О продолжительности рабочего времени (норме часов педагогической работы за ставку заработной платы) педагогических работников образовательных учреждений» (с изменениями и дополнениями) (далее - постановление Правительства РФ № 191);</span></p>

  <p style="text-align: justify;"><span class="text">- постановление Министерства труда и социального развития РФ от 30 июня 2003 г. № 41 «Об особенностях работы по совместительству педагогических, медицинских, фармацевтических работников и работников культуры» (далее &ndash; постановление Минтруда России № 41).</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b>II. Объем учебной нагрузки как обязательное условие трудового договора</b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">Необходимость указания в трудовом договоре педагогических работников объема учебной нагрузки определена Законом РФ «Об образовании» (п. 6 ст. 55) и Трудовым кодексом РФ (в редакции Федерального закона от 30 июня 2006 г. № 90-ФЗ) (ч. 2 ст. 333).</span></p>

  <p style="text-align: justify;"><span class="text">Это означает, что при заключении трудового договора учитель принимает на себя обязательство работать с оговоренным объемом учебной нагрузки, а работодатель на все время действия трудового договора обязан обеспечивать учителю предусмотренный в нем объем учебной нагрузки.</span></p>

  <p style="text-align: justify;"><span class="text">Учебная нагрузка является количественной обязательной составляющей трудовой функции, которую выполняет учитель, поэтому объем учебной нагрузки относится к обязательным условиям трудового договора с вытекающими отсюда правовыми последствиями (изменение только по соглашению сторон трудового договора, за исключением установленных случаев, и др.).</span></p>

  <p style="text-align: justify;"><span class="text">Если трудовой договор с учителем в письменной форме по каким-либо причинам отсутствует либо в нем не указан объем учебной нагрузки, то считается, что учитель работает с тем объемом учебной нагрузки, который ему был установлен приказом руководителя образовательного учреждения при приеме на работу, а изменение учебной нагрузки в текущем учебном году или на последующий период по инициативе работодателя допускается только в определенных случаях, о которых указывается в разделе IV настоящих Рекомендаций.</span></p>

  <p style="text-align: justify;"><span class="text">Комплектование кадров учителей в образовательных учреждениях имеет характерную особенность, которая заключается в том, что ни используемые в качестве примерных утвержденные ранее типовые штаты для общеобразовательных учреждений, ни штатные расписания учреждений не предусматривают нормативов для определения числа учителей различных специальностей.</span></p>

  <p style="text-align: justify;"><span class="text">В каждом образовательном учреждении, осуществляющем обучение по общеобразовательным программам, количество учителей устанавливается не на основе штатного расписания, как это имеет место при определении числа служащих в любом учреждении, в т.ч. в образовательных учреждениях при определении числа работников административно-хозяйственного, учебно-вспомогательного и обслуживающего персонала, а исходя из объема учебной нагрузки по специальности (математике, истории, русскому языку и литературе, географии и т.д.).</span></p>

  <p style="text-align: justify;"><span class="text">Такая особенность вызвана тем, что учителям устанавливаются не должностные оклады, а ставки заработной платы за определенную норму часов педагогической работы в неделю, являющиеся расчетными единицами при определении их месячной заработной платы в зависимости от установленного объема учебной нагрузки.</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b>III. Верхний предел учебной нагрузки</b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">Статьей 333 Трудового кодекса РФ (далее - ТК РФ) установлено, что учебная нагрузка педагогического работника, оговариваемая в трудовом договоре, может ограничиваться верхним пределом в случаях, предусмотренных типовым положением об образовательном учреждении соответствующих типа и вида, утверждаемым Правительством РФ.</span></p>

  <p style="text-align: justify;"><span class="text">Следует отметить, что Типовое положение об общеобразовательном учреждении не предусматривает случаев, связанных с какими-либо ограничениями при установлении учителям учебной нагрузки, тогда как, например, в Типовом положении об образовательном учреждении среднего профессионального образования (среднем специальном учебном заведении), утвержденном постановлением Правительства РФ от 3 марта 2001 г. № 160, определено, что учебная нагрузка преподавателей этих образовательных учреждений на учебный год, оговариваемая в трудовом договоре, не должна превышать 1440 часов, что соответствует двум ставкам заработной платы.</span></p>

  <p style="text-align: justify;"><span class="text">Это означает, что какие-либо решения органов управления образованием, иных органов исполнительной власти, предусматривающие для учителей ограничения при установлении учебной нагрузки, являются неправомерными.</span></p>

  <p style="text-align: justify;"><span class="text">В соответствии с подпунктом «з» пункта 2 постановления Минтруда России № 41 педагогическая работа в том же образовательном учреждении сверх установленной нормы часов педагогической работы за ставку заработной платы не является совместительством. При этом каких-либо ограничений такой работы не установлено, поскольку, как было отмечено выше, случаи, когда учебная нагрузка ограничивается верхним пределом, могут быть определены только типовым положением, утверждаемым Правительством РФ.</span></p>

  <p style="text-align: justify;"><span class="text">Вместе с тем, отсутствие нормативной основы для установления верхнего предела учебной нагрузки, которая может выполняться учителями в одном и том же образовательном учреждении, не означает, что не может быть установлено верхних пределов учебной нагрузки при работе учителей по совместительству в других образовательных учреждениях.</span></p>

  <p style="text-align: justify;"><span class="text">Регулирование работы по совместительству, в т.ч. педагогических работников, осуществляется Трудовым кодексом РФ. Так, в соответствии со статьями 282, 284 ТК РФ и принятыми в соответствии с ними нормативными правовыми актами продолжительность рабочего времени при работе по совместительству в течение одного месяца не должна превышать половины месячной нормы рабочего времени, установленной для соответствующей категории работников, что для учителей V-XI классов составляет 9 часов в неделю.</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b>IV. Порядок установления и изменения учебной нагрузки учителей</b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">В соответствии с пунктом 66 Типового положения об общеобразовательном учреждении объем учебной нагрузки учителям устанавливается исходя их количества часов по учебному плану и учебным программам, обеспеченности кадрами, других условий работы в данном общеобразовательном учреждении.</span></p>

  <p style="text-align: justify;"><span class="text">Учебная нагрузка, объем которой больше или меньше нормы часов за ставку заработной платы, устанавливается только с письменного согласия работника.</span></p>

  <p style="text-align: justify;"><span class="text">Установленный в начале учебного года объем учебной нагрузки <b>не может быть уменьшен в течение учебного года по инициативе администрации (работодателя)</b>, за исключением случаев уменьшения количества часов по учебным планам и учебным программам, сокращения количества классов.</span></p>

  <p style="text-align: justify;"><span class="text">При установлении учебной нагрузки на новый учебный год учителям, для которых данное общеобразовательное учреждение является местом основной работы, <b>как правило, сохраняется ее объем и преемственность предметов в классах.</b></span></p>

  <p style="text-align: justify;"><span class="text">Сохранение объема учебной нагрузки и ее преемственность у учителей выпускных классов могут быть обеспечены путем предоставления им учебной нагрузки в классах, в которых впервые начинается изучение преподаваемых этими учителями предметов.</span></p>

  <p style="text-align: justify;"><span class="text">По сложившейся практике, определение объема учебной нагрузки учителей общеобразовательных учреждений, кроме учителей вечерних (сменных) средних общеобразовательных школ (классов) с очно-заочной формой обучения, заочных школ, а также учителей, ведущих занятия с детьми, находящимися на длительном лечении в больнице, производится один раз в год раздельно по полугодиям.</span></p>

  <p style="text-align: justify;"><span class="text">Учебная нагрузка учителей вечерних (сменных) средних общеобразовательных школ (классов) с очно-заочной формой обучения, а также учителей, ведущих занятия с детьми, находящимися на длительном лечении в больнице, определяется дважды в год к началу первого и началу второго учебных полугодий.</span></p>

  <p style="text-align: justify;"><span class="text">Объем учебной нагрузки, установленный в текущем учебном году на следующий учебный год, может быть уменьшен по инициативе администрации (работодателя) также только по основаниям, связанным с уменьшением количества часов по учебным планам и учебным программам, а также сокращения количества классов.</span></p>

  <p style="text-align: justify;"><span class="text">В других случаях любое временное или постоянное изменение (увеличение или уменьшение) у учителей объема учебной нагрузки по сравнению с учебной нагрузкой, оговоренной в письменной форме трудового договора, либо по сравнению с учебной нагрузкой, установленной приказом руководителя образовательного учреждения при приеме на работу (если трудовой договор в письменной форме по каким-либо причинам отсутствует или в нем не указан объем учебной нагрузки), а также изменение характера работы возможно только по взаимному согласию сторон.</span></p>

  <p style="text-align: justify;"><span class="text">Таким образом, <b>руководители образовательных учреждений не имеют права без письменного согласия учителей (в т.ч. учителей, получающих трудовую пенсию) допускать уменьшение их учебной нагрузки,</b> например, в связи с приемом на работу других учителей, в т.ч. по совместительству, либо путем перераспределения учебной нагрузки между учителями, либо при предоставлении преподавательской работы лицам, выполняющим ее помимо основной работы в том же образовательном учреждении (включая руководителей и их заместителей).</span></p>

  <p style="text-align: justify;"><span class="text">В соответствии со статьей 74 ТК РФ в трудовой договор работника без его согласия могут вноситься изменения только по причинам, связанным с изменением организационных или технологических условий труда.</span></p>

  <p style="text-align: justify;"><span class="text">В образовательных учреждениях для учителей такими организационными или технологическими причинами, допускающими внесение в трудовой договор изменений без согласия работника, могут быть только причины уменьшения учебной нагрузки, вызванные уменьшением количества часов по учебным планам и учебным программам или сокращение количества классов.</span></p>

  <p style="text-align: justify;"><span class="text">Принимая во внимание, что о предстоящих изменениях определенных сторонами условий трудового договора, которые допускаются без согласия работника, а также о причинах, вызвавших необходимость таких изменений, работодатель обязан уведомить работника в письменной форме не позднее чем за два месяца, определение учителям объема учебной нагрузки на новый учебный год также необходимо осуществлять не менее чем за два месяца до его начала.</span></p>

  <p style="text-align: justify;"><span class="text">Для обеспечения соблюдения установленных сроков предупреждения учителей о возможном уменьшении объема учебной нагрузки по указанным причинам наиболее приемлемым является распределение учебной нагрузки учителей до ухода их в отпуск с тем, чтобы учителя знали, с какой учебной нагрузкой они будут работать в новом учебного году.</span></p>

  <p style="text-align: justify;"><span class="text">Если на новый учебный год устанавливать учителям учебную нагрузку не по окончании учебного года, а только после выхода учителей из отпуска, то при невозможности обеспечения учителям в новом учебном году прежнего объема учебной нагрузки по указанным выше причинам работодатель должен сохранять им ранее получаемый размер оплаты труда в течение не менее двух месяцев (т.е. в течение срока, за который он обязан предупредить работника об изменении учебной нагрузки).</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b>V. Установление учебной нагрузки учителям, находящимся в отпуске по уходу за ребенком до достижения им возраста трех лет</b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">В соответствии со статьей 256 ТК РФ на период отпуска по уходу за ребенком за работником сохраняется место работы (должность).</span></p>

  <p style="text-align: justify;"><span class="text">Поскольку выполнение трудовой функции по должности учителя характеризуется наличием учебной нагрузки, то обеспечение сохранения их должности на период нахождения в отпуске по уходу за ребенком до достижения им возраста трех лет возможно только путем установления им на общих основаниях объема учебной нагрузки на очередной учебный год, которая затем может быть передана для выполнения другим учителям на период нахождения работника в соответствующем отпуске.</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b>VI. Установление учебной нагрузки учителям на определенный срок</b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">Учебная нагрузка на определенный срок, в т.ч. только на учебный год, может быть установлена в следующих случаях:</span></p>

  <p style="text-align: justify;"><span class="text">для выполнения учебной нагрузки учителей, находящихся в отпуске по уходу за ребенком;</span></p>

  <p style="text-align: justify;"><span class="text">для выполнения учебной нагрузки учителей, отсутствующих в связи с болезнью и по другим причинам;</span></p>

  <p style="text-align: justify;"><span class="text">для выполнения временно преподавательской работы, которая ранее выполнялась постоянным учителем, с которым прекращены трудовые отношения, и на место которого работодатель намерен пригласить другого постоянного работника.</span></p>

  <p style="text-align: justify;"><span class="text">При этом необходимо учитывать, что увеличение учителю учебной нагрузки для замещения временно отсутствующего работника в связи с изменениями, внесенными в Трудовой кодекс РФ Федеральным законом от 30 июня 2006 г. № 90-ФЗ, фактически является временным переводом на необусловленную трудовым договором работу у того же работодателя, в соответствии с частью 1 статьи 72.2 ТК РФ требует согласия работника и оформляется письменным соглашением сторон трудового договора.</span></p>

  <p style="text-align: justify;"><span class="text">Без согласия работника замещение временно отсутствующего работника на срок до одного месяца допускается только в том случае, если это вызвано чрезвычайными обстоятельствами, исчерпывающий перечень которых указан в части 2 статьи 72.2. ТК РФ.</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b>VII. Учебная нагрузка учителей, обучающих детей на дому, а также при проведении занятий по физкультуре с обучающимися, отнесенными по состоянию здоровья к специальной медицинской группе</b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">При возложении на учителей общеобразовательных учреждений, для которых данное образовательное учреждение является местом основной работы, обязанностей по обучению детей на дому в соответствии с медицинским заключением, а также по проведению занятий по физкультуре с обучающимися, отнесенными по состоянию здоровья к специальной медицинской группе, учебные часы, предусмотренные на эти цели, включаются в их учебную нагрузку на общих основаниях.</span></p>

  <p style="text-align: justify;"><span class="text">Уменьшение учебной нагрузки учителей, осуществляющих указанную работу, по инициативе работодателя возможно только по причинам, установленным пунктом 66 Типового положения об общеобразовательном учреждении, с соблюдением порядка и сроков, предусмотренных статьей 74 ТК РФ.</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b><span class="text">VIII. Гарантии отдельных категорий учителей, которым не может быть обеспечена учебная нагрузка в полном объеме, а также при ее уменьшении в течение учебного года</span></b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">Для учителей в соответствии с постановлением Правительства РФ № 191 устанавливаются следующие гарантии.</span></p>

  <p style="text-align: justify;"><span class="text">1. Учителям, которым не может быть обеспечена полная учебная нагрузка, гарантируется выплата ставки заработной платы в полном размере при условии догрузки их до установленной нормы часов другой педагогической работой в следующих случаях:</span></p>

  <p style="text-align: justify;"><span class="text">учителям 1-4 классов при передаче преподавания уроков иностранного языка, музыки, изобразительного искусства и физической культуры учителям-специалистам;</span></p>

  <p style="text-align: justify;"><span class="text">учителям 1-4 классов сельских общеобразовательных учреждений с нерусским языком обучения, не имеющим достаточной подготовки для ведения уроков русского языка;</span></p>

  <p style="text-align: justify;"><span class="text">учителям русского языка сельских начальных общеобразовательных школ с нерусским языком обучения;</span></p>

  <p style="text-align: justify;"><span class="text">учителям физической культуры сельских общеобразовательных учреждений, учителям иностранного языка общеобразовательных учреждений, расположенных в поселках лесозаготовительных и сплавных предприятий и химлесхозов.</span></p>

  <p style="text-align: justify;"><span class="text">Например, если в 1-4 классах передача преподавания количества часов, предусмотренных учебным планом для проведения занятий по музыке, изобразительному искусству, физкультуре, привело к уменьшению учебной нагрузки учителей начальных классов, а оставшаяся учебная нагрузка составляет менее 20 часов в неделю, то этим учителям заработная плата должна выплачиваться в размере не ниже месячной ставки заработной платы при условии их догрузки до установленной нормы часов иной педагогической работой.</span></p>

  <p style="text-align: justify;"><span class="text">Если в школе не создана необходимая материальная база для осуществления преподавания указанных предметов учителями-специалистами или такие учителя-специалисты отсутствуют, а также в случаях, когда это нецелесообразно по другим причинам, учителя начальных классов вправе преподавать эти предметы сами, в т.ч. с соответствующей дополнительной оплатой за часы преподавательской работы, превышающей 20 часов в неделю.</span></p>

  <p style="text-align: justify;"><span class="text">Передача преподавания в начальных классах других предметов (например, уроков труда) без согласия учителей не допускается.</span></p>

  <p style="text-align: justify;"><span class="text">2. Учителям, у которых по независящим от них причинам в течение учебного года учебная нагрузка уменьшается по сравнению с установленной нагрузкой, до конца учебного года выплачивается:</span></p>

  <p style="text-align: justify;"><span class="text">заработная плата за фактическое число часов, если оставшаяся нагрузка выше установленной нормы за ставку;</span></p>

  <p style="text-align: justify;"><span class="text">заработная плата в размере ставки, если оставшаяся нагрузка ниже установленной нормы за ставку и если их невозможно догрузить другой педагогической работой;</span></p>

  <p style="text-align: justify;"><span class="text">заработная плата, установленная до снижения учебной нагрузки, если она была установлена ниже нормы за ставку и если их невозможно догрузить другой педагогической работой.</span></p>

  <p style="text-align: justify;"><span class="text">Гарантии сохранения заработной платы за учителями в указанных случаях фактически означают, что с этими работниками до конца учебного года не может быть расторгнут трудовой договор при любом объеме оставшейся после уменьшения учебной нагрузки, даже при ее полном отсутствии.</span></p>

  <p style="text-align: justify;"><span class="text">Об уменьшении учебной нагрузки работники должны быть уведомлены работодателем в письменной форме не позднее чем за два месяца, в течение которых работнику выплачивается заработная плата в прежнем размере (несмотря на то, что учебная нагрузка в прежнем объеме в период после уведомления выполняться уже не будет).</span><span class="text">
      <br />
    </span></p>

  <p> </p>

  <p style="text-align: center;"><b>IX. Участие выборного органа первичной профсоюзной организации при установлении учебной нагрузки учителям</b></p>

  <p><b>
      <br />
    </b></p>

  <p> </p>

  <p style="text-align: justify;"><span class="text">Объем учебной нагрузки учителей является основой для определения размера оплаты их труда и включается как необходимый элемент в систему оплаты труда педагогических работников.</span></p>

  <p style="text-align: justify;"><span class="text">В соответствии со статьей 135 ТК РФ локальные нормативные акты, устанавливающие системы оплаты труда, принимаются работодателем с учетом мнения представительного органа работников, к которому относится первичная профсоюзная организация.</span></p>

  <p style="text-align: justify;"><span class="text">Установление учебной нагрузки на новый учебный год (т.е. определение ее конкретного объема по сравнению с установленной нормой часов педагогической работы за ставку заработной платы, составляющей 18 часов в неделю для учителей 5-11 классов и 20 часов – для учителей 1-4 классов) осуществляется локальным нормативным актом образовательного учреждения (приказом, распоряжением), который принимается с учетом мнения (по согласованию) выборного органа первичной профсоюзной организации.</span></p>

  <p style="text-align: justify;"><span class="text">Порядок учета мнения выборного органа первичной профсоюзной организации при принятии локального нормативного акта определен статьей 372 ТК РФ, в соответствии с которой работодатель в случаях, предусмотренных ТК РФ, другими федеральными законами и иными нормативными правовыми актами РФ, коллективным договором, соглашениями (при принятии, например, локального нормативного акта об установлении учителям объема учебной нагрузки, от которого зависит размер их заработной платы), перед принятием решения направляет проект локального нормативного акта и обоснование по нему в выборный орган первичной профсоюзной организации, представляющий интересы всех или большинства работников.</span></p>

  <p style="text-align: justify;"><span class="text">Выборный орган первичной профсоюзной организации не позднее пяти рабочих дней со дня получения проекта указанного локального нормативного акта направляет работодателю мотивированное мнение по проекту в письменной форме.</span></p>

  <p style="text-align: justify;"><span class="text">В случае если мотивированное мнение выборного органа первичной профсоюзной организации не содержит согласия с проектом локального нормативного акта либо содержит предложения по его совершенствованию, работодатель может согласиться с ним либо обязан в течение трех дней после получения мотивированного мнения провести дополнительные консультации с выборным органом первичной профсоюзной организации работников в целях достижения взаимоприемлемого решения.</span></p>

  <p style="text-align: justify;"><span class="text">При недостижении согласия возникшие разногласия оформляются протоколом, после чего работодатель имеет право принять локальный нормативный акт, который может быть обжалован выборным органом первичной профсоюзной организации в соответствующую государственную инспекцию труда или в суд. Выборный орган первичной профсоюзной организации также имеет право начать процедуру коллективного трудового спора в порядке, установленном ТК РФ.</span></p>

  <p style="text-align: justify;"><span class="text">Государственная инспекция труда при получении жалобы (заявления) выборного органа первичной профсоюзной организации обязана в течение одного месяца со дня получения жалобы (заявления) провести проверку и в случае выявления нарушения выдать работодателю предписание об отмене указанного локального нормативного акта, обязательное для исполнения.</span></p>

