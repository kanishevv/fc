<p><b>Статья 9. Образовательные программы</b></p>
 
<p>1. Образовательная программа определяет содержание образования определенного уровня и направленности. В Российской Федерации реализуются образовательные программы, которые подразделяются на:</p>
 
<p>1) общеобразовательные (основные и дополнительные);</p>
 
<p>2) профессиональные (основные и дополнительные).</p>
 
<p>2. Общеобразовательные программы направлены на решение задач формирования общей культуры личности, адаптации личности к жизни в обществе, на создание основы для осознанного выбора и освоения профессиональных образовательных программ.</p>
 
<p>3. К общеобразовательным относятся программы:</p>
 
<p>1) дошкольного образования;</p>
 
<p>2) начального общего образования;</p>
 
<p>3) основного общего образования;</p>
 
<p>4) среднего (полного) общего образования.</p>
 
<p>4. Профессиональные образовательные программы направлены на решение задач последовательного повышения профессионального и общеобразовательного уровней, подготовку специалистов соответствующей квалификации.</p>
 
<p>5. К профессиональным относятся программы:</p>
 
<p>1) начального профессионального образования;</p>
 
<p>2) среднего профессионального образования;</p>
 
<p>3) высшего профессионального образования;</p>
 
<p>4) послевузовского профессионального образования.</p>
 
<p>6. Обязательный минимум содержания каждой основной общеобразовательной программы или основной профессиональной образовательной программы (по конкретной профессии, специальности) устанавливается соответствующим государственным образовательным стандартом.</p>
 
<p>7. Нормативные сроки освоения основных образовательных программ в государственных и муниципальных образовательных учреждениях определяются настоящим Законом и (или) типовыми положениями об образовательных учреждениях соответствующих типов и видов или соответствующим государственным образовательным стандартом.</p>
 
