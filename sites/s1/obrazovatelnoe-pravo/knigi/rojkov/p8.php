	обеспечивать безопасность в радиусе двух метров от места проведе­ния работ. Перечень конкретных действий, которые необходимо осу­ществить Заказчику для исполнения этой обязанности, согласовыва­ется между ним и Подрядчиком в устной форме;
</p>
<p style="text-align: justify;">
	<span style="font-size: 13px;"><span class="text"></span></span>
</p>
<p>
</p>
<p style="text-align: justify;">
	<span class="text">б) иным образом содействовать Подрядчику, в том числе выпол­нять его указания, отвечать на его предупреждения и давать ему указа­ния в предусмотренных законом или настоящим договором случаях;</span>
</p>
<p style="text-align: justify;">
	<span class="text">в)принять результат работ, выполненный Подрядчиком, при отсут­ствии в нем явных недостатков;</span>
</p>
<p style="text-align: justify;">
	<span class="text">г)в случае обнаружения скрытых недостатков в результате работ со­общить об этом Подрядчику в течение десяти дней с момента обнару­жения;</span>
</p>
<p style="text-align: justify;">
