<p><span class="text" style="text-align: justify;">Статья 1. Правовое регулирование отношений в области высшего и послевузовского профессионального образования</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Правовое регулирование отношений в области высшего и послевузовского профессионального образования осуществляется настоящим Федеральным законом, другими законами и иными нормативными правовыми актами Российской Федерации, а также законами и иными нормативными правовыми актами субъектов Российской Федерации и муниципальными правовыми актами.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Если международным договором Российской Федерации установлены иные правила, чем те, которые предусмотрены настоящим Федеральным законом, применяются правила международного договора.</span></p>
 
