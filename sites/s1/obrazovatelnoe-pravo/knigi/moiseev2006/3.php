<p style="text-align: justify;"><span style="font-size: 13px;"><span class="text">Предлагаемые рекомендации посвящены решению актуальной научной и практической задачи &ndash; обеспечению планомерного и эффективно управляемого введения профильного обучения на старшей ступени общего образования.</span></span></p>

<p style="text-align: justify;"><span class="text">В качестве современного и действенного средства такого управленческого сопровождения в данных рекомендациях рассматриваются<b>целевые комплексные программы перехода на профильное обучение,</b>разрабатываемые с учетом идей и принципов программно-целевого, стратегического и индикативного планирования на трех основных иерархических уровнях региональной образовательной системы –<b>региональном, муниципальном и межшкольном</b>(включая межшкольные сети, созданные для организации профильного обучения по сетевой модели),<b>уровне общеобразовательного учреждения.</b></span></p>

<p style="text-align: justify;"><span class="text">Ориентация на разработку целевых комплексных программ введения профильного обучения требует<b>краткого экскурса в методологию программно-целевого управления.</b></span></p>

<p style="text-align: justify;"><span class="text">Появление программно-целевого подхода тесно связано и по времени, и по содержанию с переходом менеджмента на системные и ситуационные концепции. Можно утверждать, что программно-целевое управление является конкретизацией системного подхода, рассматривающего и объект управления, и субъекта управления как сложные целеустремленные системы, имеющие свои функции, структуру, связи и т.д., вносящие свой вклад в функционирование более широких систем.</span></p>

<p style="text-align: justify;"><span class="text">Программно-целевое управление, учитывающее изменчивость внешней и внутренней среды, обеспечивает наряду с системным, и ситуационное видение реальности, способствуя выработке решений, максимально адекватных конкретным ситуациям и, зачастую, в опережающем режиме реагирующих на различные изменения.</span></p>

<p style="text-align: justify;"><span class="text">В этом смысле важно, что в последние годы происходит сближение и взаимообогащение между программно-целевым подходом и стратегическим управлением. Целевые программы становятся инструментом соединения стратегического анализа, разработки стратегии и планов ее реализации, появляется большая гибкость и неформальность, которой не отличались более ранние версии программно-целевого управления.</span></p>

<p style="text-align: justify;"><span class="text">В качествепредпосылок и условий выбора и использованияименно программно-целевого управления обычно называют:</span></p>

<ul>
  <li>
