<p><span class="text" style="text-align: justify;">Статья 14. Объединения юридических лиц (ассоциации, союзы) в системе высшего и послевузовского профессионального образования</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Образовательные учреждения высшего профессионального образования независимо от их организационно-правовых форм и другие организации и учреждения, действующие в системе высшего и послевузовского профессионального образования, вправе создавать и вступать в объединения юридических лиц (ассоциации, союзы), которые могут иметь права юридических лиц и действовать на основании своих уставов или, не являясь юридическими лицами, действовать на основании договоров о совместной деятельности.</span></p>
 
<p><span class="text" style="text-align: justify;">Объединения юридических лиц (ассоциации, союзы) в системе высшего и послевузовского профессионального образования являются некоммерческими организациями, создаются и действуют в соответствии с Гражданскими кодексом Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">Наименование объединения юридических лиц (ассоциации, союза) должно содержать указание на характер деятельности его участников и включать слова &quot;объединение&quot;, &quot;ассоциация&quot; или &quot;союз&quot;.</span></p>
 
<p><span class="text" style="text-align: justify;">2 - 3. Утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">4. Управление объединением юридических лиц (ассоциацией, союзом) в системе высшего и послевузовского профессионального образования осуществляется в соответствии с законодательством Российской Федерации и учредительными документами указанного объединения (ассоциации, союза).</span></p>
 
