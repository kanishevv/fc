?><span class="textmar"><b>Статья L.914-1</b> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Общие правила, которые определяют условия службы и прекращения деятельности штатных учителей публичного образования, а также социальные преимущества и возможности подготовки, которыми они пользуются, применяются в равной мере и одновременно к учителям, подтверждающим тот же уровень подготовки, обладающим полномочиями на основании признания их статуса или посредством контракта осуществлять их функции в частных образовательных учреждениях, связанных контрактом с Государством. Эти учителя пользуются в равной мере преимуществами, касающимися служебного продвижения и выслуги, как и учителя публичного образования.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Документалисты, выполняющие свои функции содействия учащимся классов по контракту с частным образовательным учреждением второй ступени, пользуются контрактом на тех же условиях, что и учителя, работающие в классах.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Учителя, связанные с Государством признанием их статуса или контрактом, которые выполняют функции директора частного образовательного учреждения первой ступени на условиях контракта, пользуются освобождениями от нагрузки на тех же условиях, что и директора публичных школ.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
