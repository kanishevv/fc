<p>2. Высшее профессиональное образование может быть получено в образовательных учреждениях высшего профессионального образования (высших учебных заведениях).</p>
 
<p>3. Лица, имеющие начальное профессиональное образование соответствующего профиля, могут получать высшее профессиональное образование по сокращенным ускоренным программам.</p>
 
<p>4. Лица, имеющие среднее профессиональное образование соответствующего профиля, могут получать высшее профессиональное образование по сокращенным ускоренным программам.</p>
 
