<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">В рамках миссий, которые на них возложены настоящим Кодексом и с тем, чтобы информировать об их достижениях как в национальном, так и в интернациональном плане, эти учреждения могут обеспечивать посредством соглашений, одобренных административным советом в условиях, устанавливаемых в статьях L.712-3, L.715-2, L.716-1,L.717-1 и L.718-1, предоставление услуг на платной основе, использовать свидетельства и лицензии и коммерциализировать результаты своей деятельности. Они могут создавать в этих целях службы промышленной и коммерческой деятельности, на условиях, предусмотренных в статье L.123-5. В пределах средств, высвобождаемых от этой деятельности, учреждения могут участвовать в объединениях и создавать филиалы на условиях, устанавливаемых декретом Государственного Совета. Они могут прибегать к арбитражу в случае разногласий, связанных с исполнением контрактов, заключенных с иностранными организациями. Они могут приходить к согласию в смысле статьи 2024 Гражданского кодекса на условиях, определяемых декретом.</span></p>
