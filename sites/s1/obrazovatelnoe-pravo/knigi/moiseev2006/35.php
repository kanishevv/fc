<p style="text-align: justify;"><span class="text" style="text-align: center;"><b>3.5.2. Информационно-аналитическая справка о ходе и результатах подготовки к переходу на профильное обучение</b></span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">В Информационно-аналитической справке, которая как предполагается, должна быть предельно конкретной, достаточно лаконичной и должна носить именно информативный характер, рекомендуется давать краткие ответы на следующие примерные вопросы (с учетом конкретного уровня системы):</span></p>
 
<ul> 
  <p style="text-align: justify;"><span class="text">каковы существенные с точки зрения задач перехода на профильное обучение особенности действующей региональной, муниципальной образовательной системы, образовательной системы школы, каковы важнейшие характеристики их состояния в данное время (суперкратко, только для создания общего контекста понимания специфики образовательной системы конкретного уровня)?</span></p>
 
  <p style="text-align: justify;"><span class="text">какие факторы широкой социально-экономической, социокультурной, политической и технологической среды значимы для реализации Программы и создают для нее благоприятные возможности, а также ограничения, риски и угрозы, требующие прогнозирования и учета при реализации Программы?</span></p>
 
  <p style="text-align: justify;"><span class="text">в каком состоянии находится решение главных инвариантных задач перехода на профильное обучение, и каков уровень готовности образовательной системы к началу массового перехода на профильное обучение?</span></p>
 
  <p style="text-align: justify;"><span class="text">что и за какой период удалось сделать в плане подготовки к переходу на профильное обучение?</span></p>
 
  <p style="text-align: justify;"><span class="text">какие конкретно предпосылки и заделы созданы для развертывания такого перехода, какие ресурсы (информационные, методические, кадровые, нормативно-правовые, мотивационные, административные и др.) для развертывания проекта введения профильного обучения уже имеются в регионе, муниципальных образованиях, образовательных учреждениях?</span></p>
 
  <p style="text-align: justify;"><span class="text">каковы в обобщенном виде сильные и слабые стороны образовательной системы перед началом массового перехода на профильное обучение?</span></p>
 
  <p style="text-align: justify;"><span class="text">что можно отнести к достижениям действующей образовательной системы в плане подготовки к переходу на профильное обучение, и каковы причины этих достижений?</span></p>
 
  <p style="text-align: justify;"><span class="text">какие ресурсы являются дефицитными, и как предполагается преодолеть или компенсировать их недостаточность?</span></p>
 
  <p style="text-align: justify;"><span class="text">какие проблемы перехода на профильное обучение можно прогнозировать, каковы их возможные причины, и как предлагается обеспечить их решение?</span></p>
 </ul>
 
<p style="text-align: justify;"><span class="text">Для ответа на эти вопросы рекомендуется использовать методы анализа, принятые в практике стратегического менеджмента.</span></p>
 
<p style="text-align: justify;"><span class="text">Состояние ситуации в стране, регионе, конкретной системе образования и ее изменения, естественно, оказывают и будут оказывать определенные влияния на реализацию нашего проекта по введению профильного обучения. Характер этих влияний во многом определяет внешние условия осуществления проекта, требует со стороны разработчиков Программ учета и выбора адекватной ответной или даже упреждающей реакции, позволяющей использовать возникающие благоприятные возможности и уменьшить (нейтрализовать, компенсировать) угрозы и риски.</span></p>
 
<p style="text-align: justify;"><span class="text">Для решения задач такого рода в стратегическом менеджменте рекомендуют использовать метод PEST-анализа, нацеленный на анализ нынешнего состояния и прогнозирование вероятных изменений влияния на интересующие нас проекты со стороны минимального количества наиболее крупных факторов внешней среды (политических, экономических, социальных и технологических).</span></p>
 
<p style="text-align: justify;"><span class="text">Для проведения такого анализа и прогнозирования необходимо последовательно ответить в ходе коллективной «мозговой атаки» на следующие вопросы (фиксируя ответы наглядно, например, на доске или больших листах ватмана):</span></p>
 
<p style="text-align: justify;"><span class="text">1. Какие факторы (явления, события) внешнего окружения с большой вероятностью будут действовать (происходить, иметь место) в период реализации нашего проекта (2004-2007 гг.) и окажут значимое влияние на осуществление проекта?</span></p>
 
<ul> 
  <p style="text-align: justify;"><span class="text"><b>политические</b>факторы (например, федеральные, региональные и местные выборы, реальные приоритеты политики государства и место образования в их ряду, образовательная политика государства и Министерства образования и науки РФ, тенденции развития модернизации образования, ориентиры новой Федеральной программы развития образования, в частности - сохранение развития государственно-общественного управления в качества приоритета образовательной политики и практики, принятие Государственной Думой изменений в Закон РФ «Об образовании» и соответствующих изменений в Типовое Положение об общеобразовательном учреждении и иные документы, развитие в обществе (в ту или иную сторону) тенденций демократизации, стимулирование укрепления гражданского общества или отсутствие такового, развитие местного самоуправления);</span></p>
 
  <p style="text-align: justify;"><span class="text"><b>экономические</b>факторы (например, развитие среднего класса, повышение благосостояния населения, занятость населения и др.);</span></p>
 
  <p style="text-align: justify;"><span class="text">факторы<b>социального окружения</b>(ситуация в области социального расслоения, уровень напряженности, демографические факторы, изменения в образе и стиле жизни населения, развитие культуры, популярность или непопулярность гражданской активности, в частности - в виде участия в решение проблем образования);</span></p>
 
  <p style="text-align: justify;"><span class="text">факторы<b>технологического окружения</b>(развитие информатизации и новых информационно-коммуникативных технологий и др.).</span></p>
 </ul>
 
<p style="text-align: justify;"><span class="text">2. Влияние каких из этих факторов окажет на осуществление проекта введения профильного обучения благоприятное влияние? Какие конкретно новые благоприятные возможности будут созданы?</span></p>
 
<p style="text-align: justify;"><span class="text">3. Что надо сделать, чтобы использовать эти возможности на благо проекта? </span></p>
 
<p style="text-align: justify;"><span class="text">4. Влияние каких из перечисленных факторов окажет на осуществление проекта отрицательное, негативное влияние? Какие конкретно ограничения, угрозы, опасности и риски могут возникнуть?</span></p>
 
<p style="text-align: justify;"><span class="text">5. Что надо сделать, чтобы минимизировать, нейтрализовать, компенсировать эти угрозы?</span></p>
 
<p style="text-align: justify;"><span class="text">Таким образом, «следами» анализа, видимыми в Программе, окажутся только обобщенные и очень краткие выводы из анализа, значимые для Программы перехода на профильное обучение.</span></p>
 
<p style="text-align: justify;"><span class="text">В ходе реализации проекта полезно постоянно, в динамике отслеживать и оценивать состояние готовности конкретной образовательной системы к реализации задач Программы<b>(в виде сильных и слабых сторон),</b>соотнося ее с влияниямивнешней среды<b>(благоприятными - в виде возможностей и неблагоприятными - в виде угроз).</b>Аббревиатура этих четырех английских слов (сила, слабость, возможности, угрозы) - SWOT дала наименование простому и очень широко применяемому в стратегическом планировании методу анализа (и прогнозирования).</span></p>
 
<p style="text-align: justify;"><span class="text">Работая с этим методом, важно помнить, что:</span></p>
 
<p style="text-align: justify;"><span class="text">1. Оценка производится всегда по отношению к конкретной решаемой задаче и конкретным отрезкам времени (например, мы можем провести SWOT-анализ применительно к решению задачи начать не позднее 2005 г. активную работу по подготовке массового перехода на профильное обучение. Соответственно, сильные и слабые стороны, а также возможности и угрозы (опасности, риски) будут оцениваться именно по отношению к успешности решения этой задачи).</span></p>
 
<p style="text-align: justify;"><span class="text">2. Данный метод не дает большой точности ответов, но точность эта может быть повышена, если до проведения анализа участники анализа конкретно и подробно проработают списки переменных, которые могут включаться в оценку, а также если анализ будет проводиться многократно (благо, его трудоемкость невелика).</span></p>
 
<p style="text-align: justify;"><span class="text">Для проведения такого анализа и обеспечения наглядности его хода и результатов поле доски или лист ватмана делится на 4 части (квадранта). В верхней части обозначаются сильные и слабые стороны, в нижней части, соответственно - возможности и угрозы.</span></p>
 
<p style="text-align: justify;"><span class="text">Первая часть работы связана с поочередным заполнением всех квадрантов.</span></p>
 
<p style="text-align: justify;"><span class="text">Определяются<b>сильные стороны</b>(например, сильные стороны нашей готовности к переходу на профильное обучение могут выражаться в наличии мощного научно-методического потенциала, сильной системы повышения квалификации, высокой мотивации участников работы, ясном понимании смысла и необходимости перехода на профильное обучение).</span></p>
 
<p style="text-align: justify;"><span class="text">Затем определяются<b>слабые стороны</b>(ими, как показывает практика, могут оказаться отсутствие опыта, слабая мотивация, отсутствие квалификации, недостатки программ, учебников, элективных курсов, отсутствие опыта и механизмов построения образовательных сетей и т.д.).</span></p>
 
<p style="text-align: justify;"><span class="text">Например, управление образования г. Жуковского Московской области выявило следующие недостатки сложившейся на сегодня системы.</span></p>
 
<p style="text-align: justify;"><span class="text">Это:</span></p>
 
<ul> 
  <p style="text-align: justify;"><span class="text">несоответствие возможностей школ в плане открытия классов и групп профильного обучения социальному заказу, потребностям родителей и учащихся;</span></p>
 
  <p style="text-align: justify;"><span class="text">отсутствие целостной концепции содержания профильного обучения, реализуемой через школьные учебные планы и программы преподавания профильных дисциплин;</span></p>
 
  <p style="text-align: justify;"><span class="text">неподготовленность учителей к использованию новых технологий в преподавании профильных дисциплин;</span></p>
 
  <p style="text-align: justify;"><span class="text">неподготовленность старшеклассников к осуществлению выбора профильных предметов и направлений обучения;</span></p>
 
  <p style="text-align: justify;"><span class="text">увеличение объема содержания знаний, подлежащих усвоению, и, как следствие, перегрузка учащихся;</span></p>
 
  <p style="text-align: justify;"><span class="text">отсутствие эффективного взаимодействия школ с учреждениями профессионального образования и, в частности, - с вузами в целях в решении задач профилизации старшей школы и подготовки ее выпускников к получению профессии.</span></p>
 </ul>
 
<p style="text-align: justify;"><span class="text">Далее выявляются благоприятные внешние<b>возможности</b>, возникающие благодаря ожидаемой внешней поддержке проекта введения профильного обучения и его участников (речь может идти о поддержке на федеральном, региональном и местном уровнях, о нормативно-правовых ресурсах, научно-методических рекомендациях, услугах повышения квалификации, административной поддержке органов управления образованием и местных властей).</span></p>
 
<p style="text-align: justify;"><span class="text">После этого определяются<b>внешние угрозы, опасности, риски</b>(связанные, в частности, с недостаточностью поддержки и ресурсов, инертным отношением общественности к задачам Программы).</span></p>
 
<p style="text-align: justify;"><span class="text">На следующем шаге работы проводится соотнесение сильных и слабых сторон нашей (нашей команды) готовности к проекту, которое позволяет выявить, что превалирует на сегодняшний день, а также задуматься над тем, что надо сделать для усиления сильных сторон и ослабления, минимизации слабых.</span></p>
 
<p style="text-align: justify;"><span class="text">Сходным образом соотносятся возможности и угрозы.</span></p>
 
<p style="text-align: justify;"><span class="text">Далее можно интегрировать действие и увидеть взаимоусиление своих сильных сторон и внешних возможностей, а также - возможный негативный результат сложения наших слабостей и внешних угроз.</span></p>
 
<p style="text-align: justify;"><span class="text">Итогом работы, который и отразится кратко в тексте Программы, будут выводы о готовности к эффективной реализации проекта на профильное обучение и действиях, которые надо предпринять для ее повышения.</span></p>
 
<p style="text-align: justify;"><span class="text">Реализация Программы предполагает максимально возможную опору на имеющийся позитивный опыт. Поэтому в ходе анализа рекомендуется анализировать и оценивать прежние, имеющиеся<b>достижения</b>участников Программы в этой сфере (или достижения, имеющиеся в регионе и доступные для творческого воспроизведения и развития участникам проекта) и их причины.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом предполагается, что:</span></p>
 
<p style="text-align: justify;"><span class="text">1) достижения - это прежде всего конкретные и при этом достаточно высокие и устойчивые позитивные<b>результаты</b>,</span></p>
 
<p style="text-align: justify;"><span class="text">2) достижения определяются конкретными<b>достоинствами</b>проделанной работы - качеством инновационной практики обучения, качеством и количеством вовлекаемых в этот процесс ресурсов, качеством управления и т.д.</span></p>
 
<p style="text-align: justify;"><span class="text">В Программе в кратком виде можно зафиксировать перечень важнейших достижений образовательного учреждения, муниципальной, региональной образовательной системы и участников проекта в плане готовности к переходу на профильное обучение, их причины (достоинства системы работы) и сделать выводы о том, как эти достижения следует использовать и развивать в ходе осуществления Программы.</span></p>
 
<p style="text-align: justify;"><span class="text">Наиболее важной, но и достаточно сложной задачей анализа является выявление (прогнозирование)<b>ключевых проблем</b>, на решение которых следует направить работу, и<b>причин,</b>порождающих эти проблемы и подлежащих устранению.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом под<b>проблемами</b>понимаются разрывы между желаемыми (сегодня и завтра) и имеющимися сегодня<b>результатами,</b>преодоление которых позволяет продвинуться вперед и достичь необходимых результатов, сделать шаг развития. Так, если желаемым результатом является массовый переход на профильное обучение на старшей ступени общего образования, а на сегодняшний день до таких результатов очень далеко, то в таком несоответствии и заключается проблема, которую надо решить.</span></p>
 
<p style="text-align: justify;"><span class="text"><b>Причинами проблем</b>являются недостатки, дефициты, изъяны в содержании, организации и технологии процесса обучения, недостаток ресурсного обеспечения, отсутствие разумной образовательной политики на разных уровнях. Решение проблем предполагает устранение их причин, но оно не должно превращаться в самоцель. Главным является именно решение проблемы, то есть - достижение нового качества образовательных результатов.</span></p>
 
<p style="text-align: justify;"><span class="text">Для выявления проблем нужно иметь определенные представления о главных целях Программы и их «получателях».</span></p>
 
<p style="text-align: justify;"><span class="text">По ходу реализации Программы ее информационно-аналитическая часть может дополняться данными анализа достижения поставленных целей.</span></p>
 
