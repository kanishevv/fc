 <b>Статья 28. Компетенция Российской Федерации в области образования</b></p>
 
<p>В области образования ведению Российской Федерации в лице ее федеральных органов государственной власти и органов управления образованием подлежат:</p>
 
<p>1) формирование и осуществление федеральной политики в области образования;</p>
 
<p>2) правовое регулирование отношений в области образования в пределах своей компетенции;</p>
 
<p>2.1) обеспечение государственных гарантий прав граждан в области образования;</p>
 
<p>(пп. 2.1 введен Федеральным законом от 07.07.2003 № 123-ФЗ)</p>
 
<p>3) разработка и реализация федеральных целевых и международных программ в области образования;</p>
 
<p>(пп. 3 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>4) формирование федеральных государственных органов управления образованием, а также ведомственных органов управления образованием и руководство ими, назначение руководителей этих органов;</p>
 
<p>5) согласование назначений руководителей образовательных учреждений федерального подчинения, если иное не предусмотрено законом и типовыми положениями об образовательных учреждениях соответствующих типов и видов;</p>
 
<p>6) установление порядка создания, реорганизации и ликвидации федеральных государственных образовательных учреждений;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>7) установление перечней профессий и специальностей, по которым ведутся профессиональная подготовка и профессиональное образование;</p>
 
<p>8) создание, реорганизация и ликвидация образовательных учреждений федерального подчинения, аттестация и государственная аккредитация образовательных учреждений, создание независимой от органов управления образованием государственной системы аттестационно-диагностических центров (государственной аттестационной службы);</p>
 
<p>9) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>10) разработка и утверждение типовых положений об образовательных учреждениях;</p>
 
<p>11) установление порядка лицензирования, аттестации и государственной аккредитации образовательных учреждений;</p>
 
<p>12) установление порядка аттестации педагогических работников государственных и муниципальных образовательных учреждений и работников органов управления образованием;</p>
 
<p>13) установление норм труда и нормативов его оплаты в федеральных государственных образовательных учреждениях;</p>
 
<p>(пп. 13 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>14) установление федеральных компонентов государственных образовательных стандартов и эквивалентности документов об образовании на территории Российской Федерации, нострификация на территории Российской Федерации документов об образовании иностранных государств;</p>
 
<p>15) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>16) установление:</p>
 
<p>федеральных нормативов финансирования образования обучающихся, воспитанников федеральных государственных образовательных учреждений;</p>
 
<p>общих принципов финансирования образовательных услуг;</p>
 
<p>образовательного ценза педагогических работников;</p>
 
<p>ставок заработной платы и должностных окладов работников федеральных государственных образовательных учреждений соответствующих профессионально-квалификационных групп;</p>
 
<p>социальной поддержки различным категориям обучающихся, воспитанников и педагогических работников федеральных государственных образовательных учреждений, а также видов и норм материального обеспечения указанных обучающихся, воспитанников и педагогических работников в соответствии с законодательством Российской Федерации;</p>
 
<p>порядка предоставления гражданам и погашения ими государственного образовательного кредита;</p>
 
<p>федеральных требований к образовательным учреждениям в части строительных норм и правил, санитарных норм, охраны здоровья обучающихся, воспитанников, защиты населения и территорий от чрезвычайных ситуаций природного и техногенного характера, минимальной оснащенности учебного процесса и оборудования учебных помещений;</p>
 
<p>порядка разработки и использования дистанционных образовательных технологий;</p>
 
