</p>
<p style="text-align: justify;">
	<span class="text">4.2.Расчеты за товар Плательщиком производятся перечислением денежных средств на расчетный счет Подрядчика.</span>
</p>
<p>
</p>
<p style="text-align: justify;">
	<b style="font-size: 13px;"><span class="text"> <br>
 </span><span class="text">5. Качество электроприборов, порядок приемки выполненных</span></b>
</p>
 <b>
<p style="text-align: justify;">
	<b style="font-size: 13px;"><span class="text">работ</span></b>
</p>
 </b>
<p>
</p>
