<p><b>Статья 50. Права и социальная поддержка обучающихся, воспитанников</b></p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p> 
  <br />
 1. Права и обязанности обучающихся, воспитанников образовательного учреждения определяются уставом данного образовательного учреждения и иными предусмотренными этим уставом локальными актами.</p>
 
<p>2. Совершеннолетние граждане Российской Федерации имеют право на выбор образовательного учреждения и формы получения образования.</p>
 
<p>3. Граждане, получившие образование в неаккредитованных образовательных учреждениях в форме семейного образования и самообразования, имеют право на аттестацию в форме экстерната в аккредитованных образовательных учреждениях соответствующего типа.</p>
 
<p>4. Обучающиеся всех образовательных учреждений имеют право на получение образования в соответствии с государственными образовательными стандартами, на обучение в пределах этих стандартов по индивидуальным учебным планам, на ускоренный курс обучения, на бесплатное пользование библиотечно-информационными ресурсами библиотек, на получение дополнительных (в том числе платных) образовательных услуг, на участие в управлении образовательным учреждением, на уважение своего человеческого достоинства, на свободу совести, информации, на свободное выражение собственных мнений и убеждений.</p>
 
<p>Обучение граждан по индивидуальным учебным планам в пределах государственного образовательного стандарта и формы участия обучающихся в управлении образовательным учреждением регламентируются уставом данного образовательного учреждения.</p>
 
<p>5. Выпускники образовательных учреждений независимо от их организационно-правовых форм, имеющих государственную аккредитацию, обладают равными правами при поступлении в образовательное учреждение следующего уровня.</p>
 
<p>6. Обучающихся, воспитанников государственного или муниципального образовательного учреждения учредитель в пределах своей компетенции обеспечивает стипендиями, местами в общежитиях и интернатах, а также осуществляет иные меры социальной поддержки.</p>
 
<p>(п. 6 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>7. Граждане Российской Федерации имеют право на получение впервые бесплатного начального общего, основного общего, среднего (полного) общего, начального профессионального образования и на конкурсной основе среднего профессионального, высшего профессионального и послевузовского профессионального образования в государственных или муниципальных образовательных учреждениях в пределах государственных образовательных стандартов.</p>
 
<p>Граждане Российской Федерации вправе в установленном порядке неоднократно получать бесплатное профессиональное образование по направлению государственной службы занятости, в случае потери возможности работать по профессии, специальности, в случае профессионального заболевания и (или) инвалидности, в иных случаях, предусмотренных законодательством Российской Федерации.</p>
 
<p>8. Обучающиеся в образовательных учреждениях по очно-заочной (вечерней) и заочной форме, выполняющие учебный план, имеют право на дополнительный оплачиваемый отпуск по месту работы, на сокращенную рабочую неделю и на другие льготы, которые предоставляются в порядке, устанавливаемом законодательством Российской Федерации.</p>
 
<p>9. В образовательных учреждениях содержание и обучение детей-сирот и детей, оставшихся без попечения родителей (законных представителей), осуществляются на основе полного государственного обеспечения.</p>
 
<p>10. Для детей и подростков с отклонениями в развитии органы управления образованием создают специальные (коррекционные) образовательные учреждения (классы, группы), обеспечивающие их лечение, воспитание и обучение, социальную адаптацию и интеграцию в общество.</p>
 
<p>Абзац утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>Категории обучающихся, воспитанников, направляемых в указанные образовательные учреждения, а также содержащихся на полном государственном обеспечении, определяются Правительством Российской Федерации.</p>
 
<p>Дети и подростки с отклонениями в развитии направляются в указанные образовательные учреждения органами управления образованием только с согласия родителей (законных представителей) по заключению психолого-медико-педагогической комиссии.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>11. Для подростков с девиантным (общественно опасным) поведением, достигших возраста одиннадцати лет, нуждающихся в особых условиях воспитания и обучения и требующих специального педагогического подхода, создаются специальные учебно-воспитательные учреждения, обеспечивающие их медико-социальную реабилитацию, образование и профессиональную подготовку.</p>
 
<p>Направление таких подростков в эти образовательные учреждения осуществляется только по решению суда.</p>
 
<p>12. Для граждан, содержащихся в воспитательно-трудовых и исправительно-трудовых учреждениях, администрацией этих учреждений и органами государственной власти субъектов Российской Федерации создаются условия для получения основного общего и начального профессионального образования, для профессиональной подготовки, а также для самообразования.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>13. Органы государственной власти и органы управления образованием могут создавать нетиповые образовательные учреждения высшей категории для детей, подростков и молодых людей, проявивших выдающиеся способности.</p>
 
<p>Абзац утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>Категории отбора таких детей, подростков и молодых людей в указанные образовательные учреждения определяются учредителем и доводятся до сведения общественности.</p>
 
<p>14. Привлечение обучающихся, воспитанников гражданских образовательных учреждений без согласия обучающихся, воспитанников и их родителей (законных представителей) к труду, не предусмотренному образовательной программой, запрещается.</p>
 
<p>15. Принуждение обучающихся, воспитанников к вступлению в общественные, общественно-политические организации (объединения), движения и партии, а также принудительное привлечение их к деятельности этих организаций и участию в агитационных кампаниях и политических акциях не допускаются.</p>
 
<p>16. Обучающиеся, воспитанники гражданских образовательных учреждений имеют право на свободное посещение мероприятий, не предусмотренных учебным планом.</p>
 
<p>17. В случае прекращения деятельности общеобразовательного учреждения или образовательного учреждения начального профессионального образования орган управления образованием, которому подведомственно данное образовательное учреждение, обеспечивает перевод обучающихся, воспитанников с согласия родителей (законных представителей) в другие образовательные учреждения соответствующего типа.</p>
 
<p>18. В случае прекращения деятельности государственного гражданского образовательного учреждения среднего профессионального или высшего профессионального образования студенты переводятся в другие образовательные учреждения.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>Абзац утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>19. Обучающиеся, воспитанники имеют право на перевод в другое образовательное учреждение, реализующее образовательную программу соответствующего уровня, при согласии этого образовательного учреждения и успешном прохождении ими аттестации.</p>
 
<p>20. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>21. Обучающиеся в образовательных учреждениях профессионального образования по очной форме обучения имеют право на получение отсрочки от призыва на военную службу в соответствии с Федеральным законом &quot;О воинской обязанности и военной службе&quot;.</p>
 
<p>(п. 21 в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>22. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
