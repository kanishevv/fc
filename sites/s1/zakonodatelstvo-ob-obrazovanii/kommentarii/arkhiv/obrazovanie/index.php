 <b>Статья 16. Общие требования к приему граждан в образовательные учреждения</b></p>
 
<p>1. Порядок приема граждан в образовательные учреждения в части, не урегулированной настоящим Законом, определяется учредителем и закрепляется в уставе образовательного учреждения.</p>
 
<p>Учредитель устанавливает порядок приема в государственные и муниципальные образовательные учреждения на ступени начального общего, основного общего, среднего (полного) общего и начального профессионального образования, обеспечивающий прием всех граждан, которые проживают на данной территории и имеют право на получение образования соответствующего уровня.</p>
 
<p>2. При приеме гражданина в образовательное учреждение последнее обязано ознакомить его и (или) его родителей (законных представителей) с уставом образовательного учреждения, лицензией на право ведения образовательной деятельности, со свидетельством о государственной аккредитации образовательного учреждения и другими документами, регламентирующими организацию образовательного процесса.</p>
 
<p>(в ред. Федерального закона от 20.07.2000 № 102-ФЗ)</p>
 
<p>3. Прием граждан в государственные образовательные учреждения для получения среднего профессионального, высшего профессионального и послевузовского профессионального образования проводится на конкурсной основе по заявлениям граждан. Условия конкурса должны гарантировать соблюдение прав граждан на образование и обеспечить зачисление граждан, наиболее способных и подготовленных к освоению образовательной программы соответствующего уровня.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>Вне конкурса при условии успешной сдачи вступительных экзаменов в государственные образовательные учреждения среднего профессионального и высшего профессионального образования принимаются:</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>дети-сироты и дети, оставшиеся без попечения родителей, а также лица в возрасте до 23 лет из числа детей-сирот и детей, оставшихся без попечения родителей;</p>
 
<p>(в ред. Федерального закона от 05.03.2004 № 9-ФЗ)</p>
 
<p>дети-инвалиды, инвалиды I и II групп, которым согласно заключению учреждения Государственной службы медико-социальной экспертизы не противопоказано обучение в соответствующих образовательных учреждениях;</p>
 
<p>граждане в возрасте до 20 лет, имеющие только одного родителя - инвалида I группы, если среднедушевой доход семьи ниже величины прожиточного минимума, установленного в соответствующем субъекте Российской Федерации;</p>
 
<p>граждане, уволенные с военной службы и поступающие в соответствующие образовательные учреждения на основании рекомендаций командиров воинских частей, участники боевых действий и инвалиды боевых действий.</p>
 
<p>Преимущественным правом на поступление в государственные образовательные учреждения среднего профессионального и высшего профессионального образования пользуются граждане, уволенные с военной службы.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>Победители и призеры заключительного этапа Всероссийской олимпиады школьников и члены сборных команд Российской Федерации, участвовавших в международных олимпиадах по общеобразовательным предметам и сформированных в порядке, определяемом Правительством Российской Федерации, принимаются без вступительных испытаний в государственные образовательные учреждения среднего профессионального и высшего профессионального образования для обучения по направлениям подготовки (специальностям), соответствующим профилю олимпиады.</p>
 
<p>(абзац введен Федеральным законом от 25.06.2002 № 71-ФЗ, в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>(п. 3 в ред. Федерального закона от 20.07.2000 № 102-ФЗ)</p>
 
