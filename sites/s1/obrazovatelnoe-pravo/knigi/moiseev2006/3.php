  </li>

  <li>
    <p style="text-align: justify;"><span class="text">объективно межфункциональный (межведомственный) характер этих проблем, требующий координации усилий и ресурсов с выходом за рамки существующих функциональных и ведомственных структур, и требующий участия субъектов власти и управления более высоких уровней;</span></p>
  </li>

  <li>
    <p style="text-align: justify;"><span class="text">существование реальной сильной потребности в решении данной проблемы и получении максимально полезного (оптимального) результата за соответствующий промежуток времени;</span></p>
  </li>

