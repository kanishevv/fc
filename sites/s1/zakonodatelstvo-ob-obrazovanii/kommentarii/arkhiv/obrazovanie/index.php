 <b>Статья 17. Реализация общеобразовательных программ</b></p>
 
<p>1. Общеобразовательные программы реализуются в дошкольных образовательных учреждениях, образовательных учреждениях начального общего, основного общего, среднего (полного) общего образования, в том числе в специальных (коррекционных) образовательных учреждениях для обучающихся, воспитанников с отклонениями в развитии, в образовательных учреждениях для детей-сирот и детей, оставшихся без попечения родителей (законных представителей).</p>
 
<p>2. Образовательные программы специальных (коррекционных) образовательных учреждений для обучающихся, воспитанников с отклонениями в развитии разрабатываются на базе основных общеобразовательных программ с учетом особенностей психофизического развития и возможностей обучающихся, воспитанников.</p>
 
<p>3. Образовательные программы дошкольного, начального общего, основного общего и среднего (полного) общего образования являются преемственными, то есть каждая последующая программа базируется на предыдущей.</p>
 
<p>4. Обучающиеся на ступени начального общего и основного общего образования, не освоившие программу учебного года и имеющие академическую задолженность по двум и более предметам, по усмотрению их родителей (законных представителей) оставляются на повторное обучение, переводятся в классы компенсирующего обучения с меньшим числом обучающихся на одного педагогического работника образовательного учреждения или продолжают обучение в форме семейного образования. Обучающиеся на указанных ступенях образования, имеющие по итогам учебного года академическую задолженность по одному предмету, переводятся в следующий класс условно. Ответственность за ликвидацию обучающимися академической задолженности в течение следующего учебного года возлагается на их родителей (законных представителей).</p>
 
<p>Перевод обучающегося в следующий класс в любом случае производится по решению органа управления образовательного учреждения.</p>
 
<p>5. Обучающиеся, не освоившие образовательную программу предыдущего уровня, не допускаются к обучению на следующей ступени общего образования.</p>
 
