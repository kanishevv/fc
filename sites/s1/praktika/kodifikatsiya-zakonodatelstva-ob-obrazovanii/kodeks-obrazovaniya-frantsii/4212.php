<p><span class="textmar"><b>Статья L.421-12</b></span></p>

<p> </p>

<p><span class="textmar">За исключением даты, приведённой в п. a статьи L.421-11, положения этой статьи применимы к модифицируемым бюджетам.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.421-13</b></span></p>

<p> </p>

<p><span class="textmar">I. - Регулируя бюджет учреждения во исполнение положений третьего абзаца статьи L.1612-5 Общего кодекса территориальных объединений и третьего абзаца статьи L.1612-15 того же Кодекса, представитель Государства по отношению к предыдущему бюджету, кроме исключений, связанных с эволюцией числа учащихся или поддержанием запаса материалов или состоянием помещений, увеличить долю, приходящуюся на объединение, только в пропорции, не превышающей ни эволюцию продукта прямого налогообложения этого объединения, ни эволюцию поступлений, ассигнованных Государством и предназначенных для удовлетворения педагогических расходов этого учреждения.</span></p>

<p> </p>

<p><span class="textmar">II. - В применение положений статей L.1612-1, L.1612-5, L.1612-9, L.1612-12, первый абзац, L.1612-15, L.1612-16 Общего кодекса территориальных объединений и L. 242-2 Кодекса финансовой юрисдикции полномочия мэра и муниципального совета исполняются соответственно главой учреждения и административным советом.</span></p>

<p> </p>

<p><span class="textmar">Однако если бюджет установлен в соответствии с первым абзацем пункта е) статьи L.421-11 настоящего Кодекса и не сбалансирован, совместное решение прикрепленного объединения и органов управления образованием заменяет новое решение, упоминаемое во втором абзаце статьи L.1612-5 Общего кодекса территориальных объединений.</span></p>

<p> </p>

<p><span class="textmar">III. - Во изменение положений первого абзаца статьи L.1612-12 Общего кодекса территориальных объединений финансовый счет представляется руководителем учреждения административному совету прежде, чем истечет шестой месяц после завершения исполнения.</span></p>

<p> </p>

<p><span class="textmar">Другие положения статьи L.1612-12 Общего кодекса территориальных объединений и положения статьи L.1612-14 того же Кодекса не применяются.</span></p>

<p> </p>

<p><span class="textmar">Бюджет учреждения принимается к исполнению при реальной сбалансированности.</span></p>

<p> </p>

<p><span class="textmar">IV. - Во исполнение положений настоящей статьи и статьи L.421-11 и статьи L.421-12 настоящего Кодекса общий или региональный совет может передать все или часть своих полномочий своему бюро, за исключением тех полномочий, которые относятся к утверждению суммы, вносимой прикрепленным объединением, упоминаемой в статье L.421-11 настоящего Кодекса.</span></p>

<p> </p>

<p><span class="textmar"><b>Статья L.421-14</b></span></p>

<p> </p>

<p><span class="textmar">1. За исключением особых положений, применимых к бюджету и к решениям, его изменяющим, акты административного совета, относящиеся к заключению сделок, и в частности торговых, а также акты, относящиеся к функционированию учреждения и не затрагивающие содержания или организации педагогической деятельности, подлежат обязательной передаче представителю Государства, прикрепленному объединению и органам управления образованием. Они становятся обязательными к исполнению спустя пятнадцать дней после этой передачи.</span></p>

<p> </p>

<p><span class="textmar">В течение срока, указанного в предыдущем абзаце, прикрепленное объединение и органы управления образованием могут потребовать повторного рассмотрения.</span></p>

<p> </p>

<p><span class="textmar">Акты, упомянутые в первом абзаце, подлежат проверке их законности со стороны представителя Государства в соответствии с положениями общего кодекса территориальных объединений.</span></p>

<p> </p>

<p><span class="textmar">Акты, относящиеся к содержанию и организации образовательной деятельности, принимаются к исполнению через пятнадцать дней после их передачи в органы управления образованием. В течение этого срока органы управления образованием могут заявить об отмене этих актов, если они противоречат законам и регламентациям или могут нанести вред функционированию публичной службы образования. Мотивированное решение об этом должно быть безотлагательно сообщено административному совету.</span></p>

<p> </p>

<p><span class="textmar">2. Во изменение положений статей с L.2131-1 по L.2131-5 Общего кодекса территориальных объединений акты руководителя учреждения, принятые по установлению или исполнению соглашений, в том числе торговых, подлежат обязательной передаче представителю Государства, прикрепленному объединению и органам управления образованием. Они принимаются к исполнению спустя пятнадцать дней после этой передачи.</span></p>

<p> </p>

<p><span class="textmar">Для этих актов и в срок, предусмотренный в предыдущем абзаце, и не нарушая положений, предусмотренных Общим кодексом территориальных объединений по контролю законности представителем Государства, прикрепленное объединение или орган управления образованием может сопроводить свое ходатайство просьбой об отсрочке исполнения в соответствии с положениями третьего абзаца статьи L.2131-6 Общего кодекса территориальных объединений.</span></p>

<p> </p>

<p><span class="textmar">Акты, отличающиеся от уже упомянутых в двух абзацах выше и относящиеся к содержанию или организации образовательной деятельности, приводятся в исполнение спустя пятнадцать дней после их передачи в органы управления образованием. В течение этого срока органы управления образованием могут объявить об аннулировании этих актов, если они противоречат законам или могут нанести ущерб функционированию публичной службы образования.</span></p>

<p> </p>

<p><span class="textmar">3. Органы управления образованием и прикрепленное объединение регулярно информируются о финансовой ситуации учреждения, а также о заключении всякого соглашения финансовой направленности.</span></p>

<p> </p>

<p><span class="textmar">Прикрепленное объединение запрашивает в случае необходимости органы управления образованием о проведении государственной инспекции функционирования учреждения.</span></p>

