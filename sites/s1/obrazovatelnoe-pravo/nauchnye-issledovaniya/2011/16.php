  <p align="justify"><b>Рекомендации по внедрению результатов НИР.</b></p>
 
  <p align="justify">Результаты исследования могут быть применены при модернизации существующей в вузах инфраструктуры международного образования через выработку рекомендаций и подготовку методических указаний по организационно-правовому международного сотрудничества в сфере образования, составление и использование типовых внешнеэкономических договоров на оказание образовательных услуг, а также путем подготовки предложений по диверсификации направлений экспорта образовательных услуг и совершенствованию системы его финансирования.</p>
 
  <p align="justify"> </p>
 
  <p align="justify"><b>Область применения.</b></p>
 
  <p align="justify">Результаты данного исследования могут применяться:</p>
 
  <p align="justify"> в системе государственного управления образованием и сфере внутривузовского управления, в том числе в системе переподготовки и повышения квалификации управленческих кадров;</p>
 
  <p align="justify">экономическими и финансовыми службами, занятыми в процессе государственного регулирования внешнеэкономических операций, в том числе экспорта образовательных услуг;</p>
 
  <p align="justify"> в учебном процессе по подготовке бакалавров и магистров по экономике, юриспруденции и педагогике, для организации практических занятий по ряду учебных курсов &laquo;Внешнеэкономическое право&raquo;, &laquo;Образовательное право&raquo; и др.)</p>
 
  <p align="justify"> </p>
 
  <p align="justify"><b>Экономическая эффективность или значимость работы.</b></p>
 
  <p align="justify">Исследование особенностей финансирования экспорта образовательных услуг позволит создать благоприятные условия для существенного укрепления экспортного потенциала российского образования. Экспорт образовательных услуг Российской Федерацией представляет собой не только важную экономическую проблему, значимость которой особенно отчетливо проявляется в современном постиндустриальном обществе, но и актуальную и многогранную политическую проблему, связанную с развитием интеграционных процессов в рамках СНГ и ШОС, а также с реализацией государственной политики по поддержке соотечественников за рубежом.</p>
 
  <p align="justify"> </p>
 
  <p align="justify"><b>Прогнозные предположения о развитии объекта исследования.</b></p>
 
  <p align="justify">Экспорт образовательных услуг становится на протяжении последних десятилетий одним из наиболее перспективных направлений развития внешнеэкономических связей и, скорее всего, эта тенденция сохранится на будущее. Обучение иностранных граждан практически во всех развитых становится существенным фактором стимулирования экономического роста. </p>
 
  <p align="justify">Проблема развития экспорта образования связана и с демографическими проблемами российского общества. &laquo;Демографическая яма&raquo;, из-за которой, по прогнозам, уже к 2018 году число студентов снизится до 40% относительно нынешних показателей, заставит российские вузы конкурировать не только между собой в борьбе за студентов на внутреннем рынке, но и на международном рынке с зарубежными вузами. В этой связи исследователи отмечают, что стратегической целью государственной политики должно стать обеспечение высокого качества подготовки и конкурентоспособности выпускников российских образовательных учреждений на мировом рынке образования и труда<a title="" name="_ftnref1" href="http://lexed.ru/ni/2011/?16.html#_ftn1">[1]</a>.</p>
 
  <p align="justify">Наконец, экспорт образования имеет для любой развитой страны мира большое геополитическое значение, поскольку от того, где и как будут учиться иностранные студенты, во многом будет зависеть, какие люди встанут у руля власти в разных странах мира через несколько лет. Исследователи отмечают, что &laquo;деструктуризация&raquo; системы подготовки кадров для зарубежных стран в России в начале 1990-х годов в значительной степени ослабила перспективы реализации такого инструмента развития и защиты геополитических интересов страны, как подготовка специалистов для зарубежных стран и формирование &laquo;института&raquo; выпускников российских вузов, многие из которых, со временем, занимают значимые, а иногда и ключевые, позиции в управлении этих государств, в решении важных вопросов экономического, политического и иных видов сотрудничества, имеющих или могущих иметь для России стратегическое значение.</p>
 
