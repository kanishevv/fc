<p>в образовательных учреждениях дополнительного образования (в учреждениях повышения квалификации, на курсах, в центрах профессиональной ориентации, музыкальных и художественных школах, школах искусств, домах детского творчества, на станциях юных техников, станциях юных натуралистов и в иных учреждениях, имеющих соответствующие лицензии);</p>
 
