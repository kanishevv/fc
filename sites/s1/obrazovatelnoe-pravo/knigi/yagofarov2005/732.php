	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">на бесплатное пользование библиотеками, информационными ресурсами, услугами учебных, социально-бытовых, лечебных и других подразделений среднего специального учебного заведения в порядке, установленном его уставом;</span>
