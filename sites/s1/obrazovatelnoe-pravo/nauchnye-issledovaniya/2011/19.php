<p align="justify">Для подготовки настоящего отчета использовались зарубежные и российские издания, посвященные проблемам формирования образовательной политики, оригиналы законодательных актов, если не указано иное, находящиеся в общем доступе в зарубежных справочных правовых системах, на официальных сайтах органов государственной власти зарубежных стран. Также для проведения исследования использовались нормативные правовые акты по вопросам создания региональных систем оценки качества образования в субъектах РФ.</p>
 
