<p><b>Статья 2. Принципы государственной политики в области образования</b></p>
 
<p>Государственная политика в области образования основывается на следующих принципах:</p>
 
<p>1) гуманистический характер образования, приоритет общечеловеческих ценностей, жизни и здоровья человека, свободного развития личности. Воспитание гражданственности, трудолюбия, уважения к правам и свободам человека, любви к окружающей природе, Родине, семье;</p>
 
<p>2) единство федерального культурного и образовательного пространства. Защита и развитие системой образования национальных культур, региональных культурных традиций и особенностей в условиях многонационального государства;</p>
 
<p>3) общедоступность образования, адаптивность системы образования к уровням и особенностям развития и подготовки обучающихся, воспитанников;</p>
 
<p>4) светский характер образования в государственных и муниципальных образовательных учреждениях;</p>
 
<p>5) свобода и плюрализм в образовании;</p>
 
<p>6) демократический, государственно-общественный характер управления образованием. Автономность образовательных учреждений.</p>
 
