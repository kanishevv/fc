<p>Dear authors,</p>
 
<p align="center"><b> </b></p>
 
<p>While inviting you for cooperation, we draw your attention to the fact that for publication in the Yearbook we accept only those articles that comply with the general theme of the journal and are of relevance and scientific novelty.</p>
 
<p> </p>
 
<p>The decision to include articles and other materials in the next issue of the Yearbook is made by the Editorial Board that does not guarantee publication of all submitted materials.</p>
 
<p> </p>
 
<p>Materials published in the Yearbook may not reflect the views of the Editorial Board.</p>
 
<p> </p>
 
<p>The Editorial Board reserves the right to editorial changes of the manuscripts.</p>
 
<p>The author shall provide the following personal information: name and surname, scientific degree, title, honour title, position and the place of work (study), contact telephone numbers, e-mail and mailing address.</p>
 
<p>Authors are fully responsible for the choice and the accuracy of these facts, quotations, sociological, statistical and other data, proper names and other information, as well as for the use of information not intended for the press.</p>
 
<p>Papers are accepted only if they meet the following requirements:</p>
 
<p>- Volume: 10 to 45 standard A4 pages (5,000 – 10,000 words without footnotes); </p>
 
<p>- Font: Times New Roman, size 14.</p>
 
<p>- Spacing: 1.5.</p>
 
<p>- Margins: 2 cm.</p>
 
<p>- Indent: 1 cm.</p>
 
<p>- All abbreviations and acronyms should be deciphered when first used in the text.</p>
 
<p>- Links to the sources used (normative legal acts, books and archival documents, multivolume editions, articles, theses, dissertation abstracts, and electronic resources) are required.</p>
 
<p> </p>
 
<p>The authors are reminded that in accordance with the standard ‘Bibliographic references’ GOST R 7.0.5-2008 we use auto-formatted footnotes standard in most text packages, sequential numbering for the entire document (font - Times New Roman, size 10). </p>
 
<p> </p>
 
<p>The prescribed sign of dots and dashes that separates different areas of the bibliographic description may be replaced by a dot. The list of bibliographical references should include only the literature used in the article, but not normative legal acts, the mark (dot and dash, not a hyphen) - is needed. Either the total volume of the book (e.g. 250 pages) or the location of an article referred to (e.g. p. 15) should be given. </p>
 
<p> </p>
 
<p>Articles may be either submitted in person (hard and electronic version) or sent by email celmo@mail.ru.</p>
 
<p> </p>
 
<p>Editorial Board of the Yearbook is hoping for your understanding and looking forward to fruitful cooperation.</p>
