		 В случае реорганизации Учредителя его права переходят к соответствующему правопреемнику.
	</p>
	<p>
		 1.3. Центр создан приказом Министерства общего и профессионального образования Российской Федерации от 12 января 1999 г. № 40 «Об организации Центра образовательного законодательства» (в редакции приказа Министерства общего и профессионального образования Российской Федерации&nbsp;<br>
