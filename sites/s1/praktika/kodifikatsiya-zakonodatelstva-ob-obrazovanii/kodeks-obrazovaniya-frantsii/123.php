<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-1</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Публичная служба высшего образования включает совокупность видов послесредней подготовки, находящихся в ведении различных министерских департаментов.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-2</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Публичная служба высшего образования содействует:</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">1. Развитию научных исследований, необходимой поддержке осуществляемой подготовки и повышению научного, культурного и профессионального уровня нации и индивидуумов, которые ее составляют;</p>
 
<p style="margin-left: 70.8pt; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">2. Региональному и национальному росту в рамках планирования, экономическому подъему и реализации политики занятости, учитывающей актуальные потребности и их предвидимую эволюцию;</p>
 
<p style="margin-left: 88.8pt; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">3. Сокращению социального и культурного неравенств и реализации равенства между мужчинами и женщинами, обеспечивая всем тем, кто имеет желание и способность, доступ к самым высоким формам культуры и научных исследований.</p>
 
<p style="margin-left: 0cm; text-align: justify;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-3</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Задачи публичной службы высшего образования суть следующие:</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">начальная и непрерывная подготовка;</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">научные и технические исследования, а также практическое применение их результатов;</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">распространение культуры, научной и технической информации;</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">международное сотрудничество.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-4</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Публичная служба высшего образования предлагает виды подготовки, одновременно научные, культурные и профессиональные.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;">Для этого публичная служба:</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 53.4pt; text-align: justify; text-indent: -18pt;">1. Принимает студентов и содействует их ориентации;</p>
 
<p style="margin-left: 35.4pt; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 53.4pt; text-align: justify; text-indent: -18pt;">2. Осуществляет начальную подготовку;</p>
 
<p style="margin-left: 53.4pt; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 53.4pt; text-align: justify; text-indent: -18pt;">3. Участвует в непрерывной подготовке;</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 53.4pt; text-align: justify; text-indent: -18pt;">4. Обеспечивает подготовку преподавателей.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Ориентация студентов включает информацию о ходе обучения, о последующих возможностях и о возможных переходах с одного вида подготовки на другой.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Непрерывная подготовка предназначена всем лицам, занятым или не занятым в активной жизни. Организованная как ответ на индивидуальные и коллективные потребности, она включает открытие для взрослых циклов начальной подготовки, а также организацию видов профессиональной подготовки или подготовки в области культуры.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-5</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Публичная служба высшего образования стремится развивать и продвигать во всех дисциплинах, особенно в гуманитарных и социальных науках, фундаментальные исследования, прикладные исследования и технологии.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Она обеспечивает необходимую связь между образовательной деятельностью и научными исследованиями. Она предлагает привилегированный способ подготовки к научным исследованиям и посредством научных исследований.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Она участвует в политике научного и технологического развития, признанной в качестве национального приоритета, во взаимосвязи с ведущими национальными исследованиями. Она содействует осуществлению целей, определенных Законом № 82-610 от 15 июля 1982г. об ориентации и планировании научных исследований и технологического развития Франции.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Она содействует политическому обустройству территории путем внедрения и развития в регионах групп с высоким научным уровнем. Она усиливает связи с социально-экономическими публичными и частными секторами.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Она улучшает научный потенциал нации, поощряя работу молодых исследователей и новых групп вместе с уже утвердившимися группами, содействуя сближению между группами, относящимися к взаимодополняющим дисциплинам или различным учреждениям, развивая различные формы ассоциаций с ведущими публичными исследовательскими организациями, проводя политику сотрудничества и прогресса с промышленными исследованиями и совокупностью секторов производства.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Условия, на которых учреждения, участвующие в этой публичной службе, осуществляют посредством соглашения предоставление услуг, используют свидетельства и лицензии и коммерциализуют результаты их деятельности, определены их уставами. С целью практического применения результатов исследований в областях их деятельности они могут посредством соглашения и на ограниченный срок и с информированием компетентных научных инстанций предоставить предприятиям или физическим лицам средства функционирования, а именно предоставив в их распоряжение помещения, оборудование и материалы на условиях, установленных декретом; этот декрет определяет, в частности, предоставление услуг, которые могут быть объектом соглашений, условия их оценки и условия вознаграждения учреждения.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Вышеуказанная деятельность может управляться промышленными и коммерческими службами. Для функционирования этих служб и реализации их деятельности учреждения при необходимости могут нанять на условиях, определенных декретом Государственного Совета, агентов, не обладающих контрактами публичной службы, на определенный или неопределенный срок.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-6</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Публичная служба высшего образования имеет своей задачей развитие культуры и распространение знаний и результатов научных исследований.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Она поощряет инновации, индивидуальное и коллективное творчество в области искусства, литературы, науки и техники. Она осуществляет развитие физкультуры и спорта и подготовки, которая с ними связана.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Она следит за продвижением и обогащением французского языка и региональных языков и культур. Она участвует в изучении и освоении элементов национального и регионального культурного наследия. Она обеспечивает сохранение и обогащение коллекций, доверенных учреждениям.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Учреждения, участвующие в этой публичной службе, могут предоставлять услуги с тем, чтобы содействовать социально-экономическому развитию своей окружающей среды. Они могут осуществлять издание и коммерциализацию научных, технических или научно-популярных работ и периодики, а также обеспечивать создание, обновление, расширение музеев, центров информации и документации и банков данных. Им разрешено заключать сделки согласно статье 2044 Гражданского Кодекса и прибегать к арбитражу в случае споров, связанных с исполнением контрактов, заключенных с иностранными организациями в условиях, определяемых декретом.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-7</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Публичная служба высшего образования содействует в рамках международного научного и культурного сообщества обсуждению идей, прогрессу научных исследований и встрече культур. Она обеспечивает прием и подготовку иностранных студентов. Она поддерживает развитие французских учреждений за границей. Она содействует развитию центров подготовки и исследований в странах, которые этого желают. Программы сотрудничества, которые она осуществляет, позволяют французскому и иностранному персоналу получить подготовку в области новых технологий и практике научных исследований.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">В рамках, определяемых публичными властями, учреждения, участвующие в этой публичной службе, заключают соглашения с иностранными и международными институтами, а именно с институтами высшего образования различных государств, и завязывают особые связи с институтами государств-членов Европейского Сообщества и с иностранными учреждениями, которые осуществляют частично или полностью своё обучение на французском языке.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-8</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Учреждения высшего образования несут ответственность за начальную и непрерывную подготовку всех учителей национального образования и содействуют, совместно с соответствующими министерскими департаментами, подготовке других преподавателей. Эта подготовка является одновременно научной и педагогической. Она включает конкретные контакты с различными циклами образования. Для этой деятельности учреждения высшего образования развивают научные исследования, касающиеся образования, и содействуют контакту учителей с экономической и социальной реальностью.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 123-9</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">По отношению к преподавателям &ndash; исследователям, преподавателям и исследователям университеты и учреждения высшего образования должны обеспечить средства для осуществления их преподавательской и исследовательской деятельности в условиях независимости и уверенности, необходимых для размышления и интеллектуального творчества.</p>
