  <br />
 <b>Статья 21. Профессиональная подготовка</b></p>
 
<p>1. Профессиональная подготовка имеет целью ускоренное приобретение обучающимся навыков, необходимых для выполнения определенной работы, группы работ. Профессиональная подготовка не сопровождается повышением образовательного уровня обучающегося.</p>
 
<p>2. Государство в необходимых случаях создает лицам, не имеющим основного общего образования, условия для получения ими профессиональной подготовки.</p>
 
<p>3. Профессиональная подготовка может быть получена в образовательных учреждениях начального профессионального образования и других образовательных учреждениях: межшкольных учебных комбинатах, учебно-производственных мастерских, учебных участках (цехах), а также в образовательных подразделениях организаций, имеющих соответствующие лицензии,</p>
 
<p>и в порядке индивидуальной подготовки у специалистов или рабочих, имеющих соответствующую квалификацию.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
