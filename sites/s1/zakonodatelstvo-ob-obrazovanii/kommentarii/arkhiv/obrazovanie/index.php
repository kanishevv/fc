 <b>Статья 41. Финансирование образовательных учреждений</b></p>
 
<p>1. Деятельность образовательного учреждения финансируется в соответствии с законодательством.</p>
 
<p>(п. 1 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>2. Финансирование федеральных государственных образовательных учреждений осуществляется на основе федеральных нормативов финансирования государственных образовательных учреждений, находящихся в ведении субъектов Российской Федерации, и муниципальных образовательных учреждений - на основе федеральных нормативов и нормативов субъекта Российской Федерации. Данные нормативы определяются по каждому типу, виду и категории образовательного учреждения в расчете на одного обучающегося, воспитанника, а также на иной основе.</p>
 
<p>Для малокомплектных сельских и рассматриваемых в качестве таковых органами государственной власти и органами управления образованием образовательных учреждений норматив финансирования должен учитывать затраты, не зависящие от количества обучающихся.</p>
 
<p>За счет средств федерального бюджета финансируется обучение в федеральных государственных образовательных учреждениях высшего профессионального образования не менее чем ста семидесяти студентов на каждые десять тысяч человек, проживающих в Российской Федерации.</p>
 
<p>(п. 2 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>3. Нормативы финансирования федеральных государственных образовательных учреждений устанавливаются Правительством Российской Федерации.</p>
 
<p>(п. 3 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>4. Нормативы финансирования образовательных учреждений субъекта Российской Федерации и муниципальных образовательных учреждений в части, предусмотренной пунктом 6.1 статьи 29 настоящего Закона, устанавливаются органами государственной власти субъектов Российской Федерации.</p>
 
<p>Органами местного самоуправления могут быть установлены нормативы финансирования муниципальных образовательных учреждений за счет средств местных бюджетов (за исключением субвенций, предоставляемых из бюджетов субъектов Российской Федерации в соответствии с пунктом 6.1 статьи 29 настоящего Закона).</p>
 
<p>(п. 4 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>5 - 7. Утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>8. Образовательное учреждение вправе привлекать в порядке, установленном законодательством Российской Федерации, дополнительные финансовые средства за счет предоставления платных дополнительных образовательных и иных предусмотренных уставом образовательного учреждения услуг, а также за счет добровольных пожертвований и целевых взносов физических и (или) юридических лиц, в том числе иностранных граждан и (или) иностранных юридических лиц.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>9. Привлечение образовательным учреждением дополнительных средств, указанных в пункте 8 настоящей статьи, не влечет за собой снижение нормативов и (или) абсолютных размеров его финансирования за счет средств учредителя.</p>
 
<p>10. Государственные образовательные учреждения среднего профессионального и высшего профессионального образования вправе осуществлять сверх финансируемых за счет средств учредителя заданий (контрольных цифр) по приему обучающихся подготовку и переподготовку работников квалифицированного труда (рабочих и служащих) и специалистов соответствующего уровня образования по договорам с физическими и (или) юридическими лицами с оплатой ими стоимости обучения.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>Абзац утратил силу. - Федеральные законы от 20.07.2004 № 68-ФЗ, от 22.08.2004 № 122-ФЗ.</p>
 
