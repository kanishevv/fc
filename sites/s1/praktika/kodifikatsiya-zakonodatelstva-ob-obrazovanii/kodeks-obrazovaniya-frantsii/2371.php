<p align="justify"> </p>
 
<p align="justify">Каждый региональный комитет информируется в первую очередь о контрактах, заключенных между Государством и этими двумя органами, и запрашивается по проектам трехсторонних соглашений между Государством, регионом и каждым из этих органов в целях адаптации этих контрактов к особой ситуации региона. Он запрашивается по проектам инвестиций и способам участия, которыми располагают региональные службы этих органов.</p>
 
<p align="justify"> </p>
 
