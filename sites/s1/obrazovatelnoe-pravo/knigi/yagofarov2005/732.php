	К обучающимся в учреждениях среднего профессионального образования (средних специальных учебных заведениях) относятся студенты (курсанты), слушатели и другие категории обучающихся. Основными актами, определяющими правовой статус этой группы обучающихся, выступают, помимо Закона ОРФ «Об образовании», Типовое положение об учреждении среднего профессионального образования (среднем специальном учебном заведении), а также Устав это образовательного учреждения, Правила внутреннего распорядка, договором об образовании и иные предусмотренные Уставом локальные нормативно-правовые акты, разрабатываемые учреждением.
</p>
<p style="text-align: justify;">
	<span class="text">В соответствии с п.35 Типового положения&nbsp;<i>студентом (курсантом)</i>является лицо, зачисленное приказом директора в среднее специальное учебное заведение для обучения по образовательной программе среднего профессионального образования; с<i>лушатель -</i>лицо, зачисленное приказом директора в среднее специальное учебное заведение для обучения на подготовительных курсах или освоения дополнительной профессиональной образовательной программы (повышения квалификации). Правовое положение слушателя в части получения образовательных услуг соответствует статусу студента соответствующей формы обучения.</span>
