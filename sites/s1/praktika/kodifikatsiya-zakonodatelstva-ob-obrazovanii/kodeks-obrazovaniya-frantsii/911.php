<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.911-3</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">В том, что касается распределения ставок, политика уменьшения неравенств, констатируемых между академиями и между департаментами, направлена на то, чтобы устранить различие в охвате обучением за счет улучшения условий кадрового обеспечения учащихся и студентов. Она учитывает специфические трудности зон социального неблагополучия и зон разрозненного проживания. В этом отношении принимаются меры в пользу заморских департаментов и других территориальных объединений. Разрыв, существующий между этими департаментами или этими объединениями и метрополией, с точки зрения уровня кадрового обеспечения и охвата обучением, будет устранен.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L. 911-4</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Во всех случаях, когда ответственность членов публичного образования оказывается затронутой в результате или по случаю совершенного факта нанесения ущерба либо учащимися или студентами, которые им доверены в силу их обязанностей, либо в отношении этих учащихся или этих студентов в тех же условиях, ответственность Государства замещает ответственность вышеупомянутых членов образования, которые никогда не могут быть обвинены в гражданском суде пострадавшим или его представителями.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Так обстоит дело во всех случаях, когда во время учебы или вне учебы в целях обучения или физического воспитания, не запрещенных правилами, учащиеся и студенты, доверенные членам публичного образования, находятся под наблюдением этих последних.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Иск может быть выдвинут Государством либо против члена публичного образования, либо против третьих лиц в соответствии с общим правом.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">В основном процессе члены публичного образования, против которых Государство могло бы в известных случаях произвести обвинительное действие, не могут быть заслушаны в качестве свидетелей.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Иск, предъявляемый пострадавшим, его родителями или его правомочными представителями, выдвинутый против Государства как ответчика за нанесенный ущерб, рассматривается судом юридической инстанции по месту, где ущерб был нанесен, и направлен против представителя Государства в департаменте.</span></p>
