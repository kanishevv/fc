<p><b>Статья 36. Управление негосударственным образовательным учреждением</b></p>
 
<p>1. Руководство негосударственным образовательным учреждением осуществляет непосредственно его учредитель или по его поручению попечительский совет, формируемый учредителем.</p>
 
<p>2. Правомочия попечительского совета и схема внутреннего управления негосударственным образовательным учреждением, а также процедура назначения или выборов руководителя указанного образовательного учреждения и правомочия данного руководителя определяются учредителем (попечительским советом) этого образовательного учреждения по согласованию с педагогическим коллективом и фиксируются в уставе негосударственного образовательного учреждения.</p>
 
