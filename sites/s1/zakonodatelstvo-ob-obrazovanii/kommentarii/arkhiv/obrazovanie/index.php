<p><b>Статья 4. Задачи законодательства Российской Федерации в области образования</b></p>
 
<p>Задачами законодательства Российской Федерации в области образования являются:</p>
 
<p>1) разграничение компетенции в области образования между федеральными органами исполнительной власти, органами исполнительной власти субъектов Российской Федерации и органами местного самоуправления;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>2) обеспечение и защита конституционного права граждан Российской Федерации на образование;</p>
 
<p>3) создание правовых гарантий для свободного функционирования и развития системы образования Российской Федерации;</p>
 
<p>4) определение прав, обязанностей, полномочий и ответственности физических и юридических лиц в области образования, а также правовое регулирование их отношений в данной области.</p>
 
