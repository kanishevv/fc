<p><span class="text" style="text-align: justify;"> Статья 2.1. Интеграция высшего и послевузовского профессионального образования и науки</span></p>
 
<p><span class="text" style="text-align: justify;">(введена Федеральным законом от 01.12.2007 № 308-ФЗ)</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Интеграция высшего и послевузовского профессионального образования и науки имеет целями кадровое обеспечение научных исследований, а также развитие и совершенствование системы образования путем использования новых знаний и достижений науки и техники.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Интеграция высшего и послевузовского профессионального образования и науки может осуществляться в разных формах, в том числе:</span></p>
 
<p><span class="text" style="text-align: justify;">1) проведения высшими учебными заведениями научных исследований и экспериментальных разработок за счет грантов или иных источников финансирования;</span></p>
 
<p><span class="text" style="text-align: justify;">2) привлечения высшими учебными заведениями работников научных организаций и научными организациями работников высших учебных заведений на договорной основе для участия в образовательной и (или) научной деятельности;</span></p>
 
<p><span class="text" style="text-align: justify;">3) осуществления высшими учебными заведениями и научными организациями совместных научно-образовательных проектов, научных исследований и экспериментальных разработок, а также иных совместных мероприятий на договорной основе;</span></p>
 
<p><span class="text" style="text-align: justify;">4) реализации научными организациями образовательных программ послевузовского профессионального образования, а также образовательных программ дополнительного профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">5) создания на базе высших учебных заведений научными организациями лабораторий, осуществляющих научную и (или) научно-техническую деятельность, в порядке, установленном уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 23.07.2008 № 160-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6) создания высшими учебными заведениями на базе научных организаций кафедр, осуществляющих образовательный процесс, в порядке, установленном уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 23.07.2008 № 160-ФЗ)</span></p>
 
