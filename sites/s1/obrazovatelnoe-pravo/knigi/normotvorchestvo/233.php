<p style="text-align: justify;">
	<span class="text"><b><i> <br>
 </i></b></span><span class="text"><b><i>Оценка качества нормативных правовых актов, регулирующих отношения в сфере образования в Свердловской области.</i></b>Теория правового качества законов выделяет три основных критерия оценки их качества:</span>
</p>
<p style="text-align: justify;">
	<span class="text">1)качество правовой легальности закона (качество компетенционной, содержательно-правовой и процедурной легальности);</span>
</p>
<p style="text-align: justify;">
	<span class="text">2)инструментально-правовое качество закона (качество статусной регламентации, качество обеспечения закона правовыми средствами защиты прав участников правоотношений, а также процедурами его применения, качество законодательной регламентации юрисдикционных правовых средств);</span>
</p>
<p style="text-align: justify;">
	<span class="text">3)технико-юридическое качество закона (качество структуры, соотношения формального и оценочного в законе, лингвистическое качество).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Используя неофициальный статус нашей оценки, мы выделим самостоятельные критерии оценки состояния образовательного законодательства в Свердловской области, такие как, показатель продуктивности (широта охвата вопросов регулирования) и показатель эффективности (степень применимости в правоотношениях).</span>
</p>
<p style="text-align: justify;">
	<span class="text">По критерию продуктивности вышеприведенные нормативные правовые акты Свердловской области, регулирующие отношения в сфере образования, можно оценить как недостаточные в связи с тем, что, несмотря на наличие порядка десяти законов и около сотни подзаконных актов, как показывалось выше, ряд вопросов остается неурегулированным (например, вопросы деятельности негосударственных образовательных учреждений, учреждений дошкольного образования, начального профессионального образования, дополнительного образования детей, вопросы собственности образовательных учреждений, налоговые, земельные отношения, международные контакты, обеспеченность ряда социальных гарантий и др.). Вместе с тем, ряд вопросов, несмотря на большое количество подзаконных актов, требуют создания механизма их реализации, например, вопросы финансирования, предоставления социальных льгот, административные и трудовые отношения и др. По ряду проблем принимаются ежегодные акты (например, целевые программы образования, их финансирование, стандартизация образования, летний отдых детей и пр.), возможно, требующие принятия нормативных правовых актов, в том числе законов, носящих рамочный характер, включающих процедурные вопросы, такие как, закон о целевых программах и программах развития образования в Свердловской области, закон о досуговой деятельности детей и молодежи и др.</span>
</p>
<p style="text-align: justify;">
	<span class="text">По критерию эффективности, также качество нормативных правовых актов об образовании в Свердловской области имеет невысокие показатели.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Анализ исполнения законов, регулирующих отношения в сфере образования, тоже свидетельствует о декларативности ряда положений и невозможности их выполнения, с одной стороны, в силу отсутствия механизмов реализации, с другой - по причине несовершенства подзаконных актов, с третьей – из-за отсутствия правовой культуры у конкретных исполнителей. Постараемся продемонстрировать это на примерах противоречий некоторых основных законов (табл. 4)</span>
</p>
<p style="text-align: justify;">
	<span class="text">Таким образом, даже приблизительный анализ законодательства об образовании в Свердловской области по двум условным критериям свидетельствует о невысоком качестве нормативных правовых актов и требует более тщательного контент-анализа и рекомендаций по его совершенствованию.</span>
</p>
