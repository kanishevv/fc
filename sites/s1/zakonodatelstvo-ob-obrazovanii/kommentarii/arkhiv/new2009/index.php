<p><span class="text" style="text-align: justify;">Статья 3. Автономия образовательных учреждений высшего профессионального образования и академические свободы</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 31.12.2005 № 199-ФЗ)</span></p>
 
