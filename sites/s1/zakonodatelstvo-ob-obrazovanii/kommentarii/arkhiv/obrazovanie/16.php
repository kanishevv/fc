<p align="JUSTIFY"><span class="text">XI. Федеральный закон от 27 мая 1998 года N 76-ФЗ &quot;О статусе военнослужащих&quot; (Собрание законодательства Российской Федерации, 1998, N 22, ст. 2331; 2000, N 1 (ч. II), ст. 12, N 26, ст. 2729, N 33, ст. 3348; 2001, N 1 (часть I), ст. 2, N 31, ст. 3173, N 53 (ч. I), ст. 5030; 2002, N 1 (ч. I), ст. 2, N 19, ст. 1794, N 21, ст. 1919, N 26, ст. 2521, N 48, ст. 4740, N 52 (ч. I), ст. 5132)</span></p>

<p align="JUSTIFY"><span class="text">Статья 2. Граждане, имеющие статус военнослужащих</span></p>

<p align="JUSTIFY"><span class="text">1. Военнослужащие проходят военную службу по контракту или военную службу по призыву в соответствии с федеральным законом о воинской обязанности и военной службе.</span></p>

<p align="JUSTIFY"><span class="text">К военнослужащим относятся:</span></p>

<p align="JUSTIFY"><span class="text">офицеры, прапорщики и мичманы, курсанты военных образовательных учреждений профессионального образования, сержанты и старшины, солдаты и матросы, проходящие военную службу по контракту (далее - военнослужащие, проходящие военную службу по контракту);</span></p>

<p align="JUSTIFY"><span class="text">офицеры, призванные на военную службу в соответствии с указом Президента Российской Федерации;</span></p>

<p align="JUSTIFY"><span class="text">сержанты, старшины, солдаты и матросы, проходящие военную службу по призыву, курсанты военных образовательных учреждений профессионального образования до заключения с ними контракта (далее - военнослужащие, проходящие военную службу по призыву).</span></p>

<p align="JUSTIFY"><span class="text">Офицеры, призванные на военную службу в соответствии с указом Президента Российской Федерации, по своему правовому положению приравниваются к офицерам, проходящим военную службу по контракту, если иное не предусмотрено федеральными законами и иными нормативными правовыми актами Российской Федерации.</span></p>

<p align="JUSTIFY"><span class="text">Условия контракта о прохождении военной службы определяются федеральными конституционными законами, федеральными законами и иными нормативными правовыми актами Российской Федерации.</span></p>

<p align="JUSTIFY"><span class="text">2. Граждане приобретают статус военнослужащих с началом военной службы и утрачивают его с окончанием военной службы.</span></p>

<p align="JUSTIFY"><span class="text">На граждан, призванных на военные сборы, статус военнослужащих распространяется в случаях и порядке, которые предусмотрены настоящим Федеральным законом, федеральными законами и иными нормативными правовыми актами Российской Федерации.</span></p>

<p align="JUSTIFY"><span class="text">3. За гражданами, проходившими военную службу в воинских частях Вооруженных Сил Союза ССР, других воинских формированиях Союза ССР и государств - участников Содружества Независимых Государств до принятия указанных воинских формирований под юрисдикцию Российской Федерации и перешедшими на военную службу в войска или иные воинские формирования, организации других государств, ранее входивших в состав Союза ССР, сохраняются льготы, гарантии и компенсации, предусмотренные настоящим Федеральным законом, федеральными законами и иными нормативными правовыми актами Российской Федерации, при условии заключения и ратификации в установленном порядке соответствующих международных договоров Российской Федерации.</span></p>

<p align="JUSTIFY"><span class="text">4. Статус военнослужащих, проходящих военную службу на территориях государств, не указанных в пункте 3 настоящей статьи, а также военнослужащих, направленных в вооруженные силы указанных государств в порядке военного сотрудничества, определяется в соответствии с международными договорами Российской Федерации.</span></p>

