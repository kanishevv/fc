<p style="margin-left: 0cm; text-indent: 0cm;"><b>Статья L.151-1</b></p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Государство декларирует и уважает свободу образования и гарантирует ее осуществление в частных учреждениях, открытых согласно существующим правилам.</p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"><b>Статья L.151-2</b></p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Территориальные объединения Республики способствуют свободе образования в условиях, предусматриваемых статьями L. 442-6 и L.442-7.</p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"><b>Статья L.151-3</b></p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Образовательные учреждения первой и второй ступени могут быть публичными или частными.</p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Публичные учреждения создаются и поддерживаются Государством, регионами, департаментами и коммунами.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Частные учреждения учреждаются и поддерживаются частными лицами или ассоциациями.</p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"><b>Статья L. 151-4</b></p>
 
<p style="margin-left: 0cm; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Частные общеобразовательные учреждения второй ступени могут получать от коммун, департаментов, регионов и Государства помещения и дотацию, не превосходящую одну десятую долю ежегодных расходов учреждения. Академический совет национального образования дает свое предварительное заключение по поводу целесообразности таких дотаций.</p>
 
<p style="margin-left: 0cm; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"><b>Статья L.151-5</b></p>
 
<p style="margin-left: 0cm; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Учреждения технического образования суть публичные и частные.</p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"><b>Статья L.151-6</b></p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Высшее образование свободно.</p>
 
