<p><span class="text" style="text-align: justify;">Статья 33. Осуществление международного сотрудничества Российской Федерации в области высшего и послевузовского профессионального образования</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Высшие учебные заведения вправе участвовать в международном сотрудничестве Российской Федерации в области высшего и послевузовского профессионального образования посредством:</span></p>
 
<p><span class="text" style="text-align: justify;">1) участия в программах двустороннего и многостороннего обмена студентами, аспирантами, докторантами, педагогическими и научными работниками;</span></p>
 
<p><span class="text" style="text-align: justify;">2) проведения совместных научных исследований, а также конгрессов, конференций, симпозиумов и других мероприятий;</span></p>
 
<p><span class="text" style="text-align: justify;">3) осуществления фундаментальных и прикладных научных исследований, а также опытно-конструкторских работ по заказам иностранных юридических лиц;</span></p>
 
<p><span class="text" style="text-align: justify;">4) участия в международных программах совершенствования высшего и послевузовского профессионального образования.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Подготовка, переподготовка и повышение квалификации иностранных граждан в Российской Федерации, преподавательская и научно-исследовательская работа граждан Российской Федерации за пределами территории Российской Федерации в пределах установленной Правительством Российской Федерации квоты осуществляются по межгосударственным договорам, договорам между федеральными органами исполнительной власти, органами исполнительной власти субъектов Российской Федерации и органами местного самоуправления городских округов, муниципальных районов, в ведении которых находятся высшие учебные заведения, и соответствующими органами управления образованием иностранных государств, а также по прямым договорам, заключенным высшими учебными заведениями с иностранными гражданами и иностранными юридическими лицами.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ, от 29.12.2006 № 258-ФЗ, от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. В целях участия в международной деятельности Российской Федерации высшие учебные заведения имеют право:</span></p>
 
<p><span class="text" style="text-align: justify;">вступать в неправительственные международные организации;</span></p>
 
<p><span class="text" style="text-align: justify;">заключать с иностранными партнерами договоры о совместной деятельности, которые не могут рассматриваться как международные договоры Российской Федерации;</span></p>
 
<p><span class="text" style="text-align: justify;">создавать с участием иностранных партнеров структурные подразделения (центры, лаборатории, технические парки и другие подразделения).</span></p>
 
<p><span class="text" style="text-align: justify;">4. Высшие учебные заведения в соответствии с законодательством Российской Федерации вправе заниматься внешнеэкономической деятельностью, предусмотренной уставами высших учебных заведений и направленной на выполнение задач, которые определены настоящим Федеральным законом, а также на развитие международных контактов.</span></p>
 
<p><span class="text" style="text-align: justify;">5. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
