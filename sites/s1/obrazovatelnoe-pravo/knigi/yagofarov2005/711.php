<p style="text-align: justify;">
	<span class="text">Есть еще одна категория обучающихся, которая не может быть отнесена к названным категориям - обучающиеся в семье, поскольку их обучение осуществляется вне стен образовательного учреждения, хотя остальные основные требования (соответствие содержания обучения образовательным программам и государственным образовательным стандартам) сохраняются.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В зависимости<i>от способа финансирования образовательных услуг</i>обучающихся можно разделить на следующие категории:</span>
</p>
<p style="text-align: justify;">
	<span class="text">1) обучающиеся, обучение которых финансируется из федерального бюджета, бюджетов субъектов федерации, муниципальных бюджетов;</span>
</p>
<p style="text-align: justify;">
	<span class="text">2) обучающиеся, обучение которых финансируется за счёт средств физических и юридических лиц. К этой же категории могут быть отнесены обучающиеся, заключившие договоры с юридическими лицами, которые оплачивают их обучение в образовательных учреждениях (предприятия, учреждения, различного рода общественные объединения и организации). Образование данной категории обучающихся регламентируется, кроме нормативно-правовых актов Российской Федерации, договорами, заключёнными между обучающимся и образовательным учреждением.</span>
