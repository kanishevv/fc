</p>
<p style="text-align: justify;">
	<span class="text">Студенты очной формы обучения имеют право в свободное от учебы время работать на предприятиях, в учреждениях и организациях любых организационно-правовых форм и пользоваться при этом льготами, установленными законодательством РФ о труде и об образовании.</span>
