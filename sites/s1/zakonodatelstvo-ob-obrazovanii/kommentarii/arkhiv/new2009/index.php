<p><span class="text" style="text-align: justify;">Статья 11. Прием в высшее учебное заведение и подготовка специалистов с высшим и послевузовским профессиональным образованием</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Прием в государственные и муниципальные высшие учебные заведения для обучения по программам бакалавриата и программам подготовки специалиста проводится по заявлениям лиц, имеющих среднее (полное) общее или среднее профессиональное образование, на конкурсной основе по результатам единого государственного экзамена. Правом на участие в конкурсе для обучения по программам магистратуры пользуются лица, успешно завершившие обучение по программам бакалавриата.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Условиями конкурса должны быть гарантированы соблюдение прав граждан на образование и зачисление граждан, наиболее способных и подготовленных к освоению образовательных программ соответствующего уровня.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">При приеме в государственные и муниципальные высшие учебные заведения для обучения по программам бакалавриата и программам подготовки специалиста по направлениям подготовки (специальностям), требующим наличия у поступающих лиц определенных творческих способностей, физических и (или) психологических качеств, указанные высшие учебные заведения вправе проводить по предметам, по которым не проводится единый государственный экзамен, дополнительные вступительные испытания творческой и (или) профессиональной направленности, результаты которых учитываются наряду с результатами единого государственного экзамена при проведении конкурса. Перечень направлений подготовки (специальностей), по которым при приеме для обучения по программам бакалавриата и программам подготовки специалиста могут проводиться дополнительные испытания творческой и (или) профессиональной направленности, утверждается в порядке, определяемом Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Государственным высшим учебным заведениям может быть предоставлено право проводить дополнительные вступительные испытания профильной направленности при приеме для обучения по программам бакалавриата и программам подготовки специалиста по иным направлениям подготовки (специальностям). Перечень указанных высших учебных заведений, при приеме в которые могут проводиться дополнительные вступительные испытания профильной направленности, и направлений подготовки (специальностей) формируется на основании предложений государственных высших учебных заведений. Порядок, критерии отбора и перечень указанных высших учебных заведений и направлений подготовки (специальностей), по которым могут проводиться дополнительные вступительные испытания профильной направленности, утверждаются Правительством Российской Федерации.</span></p>
 
