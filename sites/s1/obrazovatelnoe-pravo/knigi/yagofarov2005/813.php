<p style="text-align: justify;">
 <span class="text" style="text-align: center;"><b>VII. ОБЯЗАННОСТИ И ОТВЕТСТВЕННОСТЬ ПРЕПОДАВАТЕЛЬСКИХ</b></span>
</p>
<p style="text-align: justify;">
 <span class="text" style="text-align: center;"><b>КАДРОВ УЧРЕЖДЕНИИ ВЫСШЕГО ОБРАЗОВАНИЯ</b></span>
</p>
<p style="text-align: justify;">
 <span class="text"> <br>
 </span>
</p>
<p style="text-align: justify;">
 <span class="text">33. Необходимо, чтобы преподавательские кадры учреждений высшего образования признавали, что осуществление их прав влечет за собой особые обязанности и ответственность, включая обязанность уважать академическую свободу других членов академического сообщества и обеспечивать честное обсуждение противоположных мнений. Академической свободе сопутствует обязанность использовать ее в соответствии с долгом ученого, заключающимся в проведении исследовательской деятельности на основе добросовестного поиска истины. Необходимо, чтобы преподавательская, исследовательская и научная деятельность проводилась в полном соответствии с этическими и профессиональными нормами и была нацелена, по возможности, на решение современных проблем, стоящих перед обществом, а также обеспечивала сохранение исторического и культурного наследия мира.</span>
</p>
<p style="text-align: justify;">
 <span class="text">34. Индивидуальные обязанности преподавательских кадров учреждений высшего образования, неразрывно связанные с их академической свободой, заключаются, в частности, в следующем:</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 1) эффективно обучать студентов в рамках средств, предоставляемых учебным заведением и государством, быть честным и справедливым к студентам мужского и женского пола и одинаково обращаться со студентами всех рас и религий, а также инвалидами, поощрять свободный обмен идеями между самими преподавателями и студентами и предоставлять себя в распоряжение последних, с тем, чтобы направлять учебу студентов.</span>
</p>
<p style="text-align: justify;">
 <span class="text"> Преподавательские кадры учреждений высшего образования должны, в случае необходимости, обеспечивать охват того минимума материала по каждому предмету, который предусмотрен в учебной программе;</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 2) проводить научно-исследовательскую деятельность и распространять ее результаты или, когда проведение оригинальных исследований не требуется, поддерживать и повышать уровень своих знаний по конкретному предмету с помощью учебных курсов и исследовательской деятельности, а также посредством разработки учебной методологии, нацеленной на совершенствование педагогических навыков:</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 3) осуществлять исследовательскую и научную деятельность на основе честного поиска знаний, должным образом обеспечивая доказательность, беспристрастность суждений и добросовестность при сообщении результатов;</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 4) соблюдать этику научных исследований, касающихся человека, животных, наследия или окружающей среды;</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 5) уважать и признавать научную работу своих коллег и студентов, и в частности обеспечивать, чтобы в число авторов публикуемых работ входили все, кто внес в них реальный вклад и разделяет ответственность за их содержание;</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 6) воздерживаться от использования новой информации, концепций или данных, изначально полученных вследствие доступа к конфиденциальным рукописям или запросам о предоставлении средств на исследовательскую деятельность или подготовку, ознакомиться с которыми можно было в результате такой деятельности, как, например, внешнее рецензирование, за исключением тех случаев; когда автор дал на это разрешение;</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 7) обеспечивать, чтобы исследовательская деятельность осуществлялась в соответствии с законами и нормами того государства, в котором она проводится, чтобы она не нарушала международных правовых актов в области прав человека и чтобы се результаты и данные, на которых она основывается, были реально доступными для ученых и исследователей в принимающем учреждении, за исключением тех случаев; когда информация могла бы поставить под угрозу респондентов или когда гарантируется анонимность;</span>
</p>
<p style="text-align: justify;">
 <span class="text"> 8) избегать столкновения интересов и решать спорные вопросы на основе открытости и всесторонних консультаций с учреждением высшего образования, в котором они работают, с тем, чтобы получить поддержку этого заведения:</span>
