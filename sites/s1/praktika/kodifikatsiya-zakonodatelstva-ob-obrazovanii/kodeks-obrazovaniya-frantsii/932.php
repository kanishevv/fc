<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">В местных публичных образовательных учреждениях, подведомственных министру, ответственному за образование, по дисциплинам технологического и профессионального образования могут быть призваны для работы ассоциированные преподаватели.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Ассоциированные преподаватели обеспечивают службу на полной ставке или службу на неполной ставке, как максимум, равной половине ставки преподавателя.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
