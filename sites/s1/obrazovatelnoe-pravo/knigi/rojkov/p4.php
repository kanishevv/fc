 <span class="text">7.1.Исполнитель несет ответственность за недостатки оказанных услуг, предусмотренных разделом 1 настоящего договора, в том чис­ле за оказание их не в полном объеме, предусмотренном учебным пла­ном, за нарушение сроков оказания указанных услуг, в соответствии с законодательством Российской Федерации.</span>
</p>
<p style="text-align: justify;">
 <span class="text">7.2.Заказчик обязан полностью оплатить услуги, указанные в разде­ле 1 настоящего договора, при невозможности их исполнения, воз­никшей по его вине и (или) вине Потребителя, в том числе при пропус­ке Потребителем занятий по любым причинам в соответствии с п. 5.4.настоящего договора.</span>
</p>
