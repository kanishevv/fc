 <b>Статья 37. Органы управления образованием в Российской Федерации</b></p>
 
<p> 
  <br />
 1. В Российской Федерации могут создаваться и действовать государственные и муниципальные органы управления образованием:</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>федеральные (центральные) государственные органы управления образованием;</p>
 
<p>федеральные ведомственные органы управления образованием;</p>
 
<p>государственные органы управления образованием субъектов Российской Федерации;</p>
 
<p>муниципальные органы управления образованием.</p>
 
<p>(абзац введен Федеральным законом от 22.08.2004 № 122-ФЗ)</p>
 
<p>2. Государственные органы управления образованием создаются решением соответствующего органа исполнительной власти по согласованию с соответствующим законодательным (представительным) органом государственной власти.</p>
 
<p>3. Муниципальные органы управления образованием могут создаваться и действовать по решению соответствующих органов местного самоуправления.</p>
 
<p>(п. 3 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>4. Деятельность органов управления образованием направлена на обеспечение государственных образовательных стандартов и функционирования системы образования на уровне государственных нормативов.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>5. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>6. Органам управления образованием подконтрольны подведомственные им образовательные учреждения. В случае нарушения образовательным учреждением законодательства Российской Федерации в области образования и (или) своего устава государственные органы управления образованием вправе своим предписанием приостановить в этой части деятельность образовательного учреждения до решения суда.</p>
 
