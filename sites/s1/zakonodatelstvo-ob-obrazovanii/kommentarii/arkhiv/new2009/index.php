<p><span class="text" style="text-align: justify;">Статья 29. Платная деятельность высшего учебного заведения</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Высшее учебное заведение в соответствии со своим уставом может осуществлять платную деятельность в области образования и в других областях, если это не идет в ущерб его основной деятельности.</span></p>
 
<p><span class="text" style="text-align: justify;">Платная образовательная деятельность высшего учебного заведения не может быть осуществлена взамен и в рамках образовательной деятельности, финансируемой за счет средств бюджета. В противном случае средства, заработанные в результате такой деятельности, изымаются учредителем высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Высшее учебное заведение вправе в пределах, установленных лицензией, осуществлять сверх финансируемых за счет средств соответствующего бюджета контрольных цифр приема студентов подготовку специалистов по соответствующим договорам с оплатой стоимости обучения физическими и (или) юридическими лицами в объеме, согласованном с органом исполнительной власти или исполнительно-распорядительным органом городского округа, муниципального района, в ведении которого это высшее учебное заведение находится.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ, от 29.12.2006 № 258-ФЗ, от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Высшее учебное заведение самостоятельно решает вопросы по заключению договоров, определению обязательств и иных условий, не противоречащих законодательству Российской Федерации и уставу данного высшего учебного заведения.</span></p>
 
