<p><span class="text" style="text-align: justify;">5) осуществление контроля и надзора за деятельностью советов по защите диссертаций на соискание ученой степени кандидата наук, на соискание ученой степени доктора наук, ученых (научно-технических) советов высших учебных заведений, образовательных учреждений дополнительного профессионального образования и научных организаций по вопросам присвоения ученых званий;</span></p>
 
<p><span class="text" style="text-align: justify;">6) выдача разрешений на создание советов по защите диссертаций на соискание ученой степени кандидата наук, на соискание ученой степени доктора наук, установление полномочий этих советов и определение перечня специальностей, по которым этим советам предоставляется право приема диссертаций для защиты, а также приостановление деятельности и закрытие таких советов;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 18.10.2007 № 230-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">7) рассмотрение в установленном порядке вопросов присвоения ученых званий профессора по специальности, профессора по кафедре, доцента по специальности, доцента по кафедре, присуждения ученых степеней кандидата наук, доктора наук; выдача соответствующих аттестатов и дипломов установленного образца, а также рассмотрение вопросов лишения, восстановления указанных ученых званий и ученых степеней;</span></p>
 
<p><span class="text" style="text-align: justify;">8) признание и установление эквивалентности на территории Российской Федерации документов иностранных государств о высшем или послевузовском профессиональном образовании, об ученых степенях и ученых званиях;</span></p>
 
<p><span class="text" style="text-align: justify;">9) информационно-методическое обеспечение деятельности органов исполнительной власти субъектов Российской Федерации, осуществляющих функции по контролю и надзору в сфере образования;</span></p>
 
<p><span class="text" style="text-align: justify;">10) подтверждение документов государственного образца о высшем или послевузовском профессиональном образовании, об ученых степенях и ученых званиях.</span></p>
 
<p><span class="text" style="text-align: justify;">(пп. 10 введен Федеральным законом от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 4 в ред. Федерального закона от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Разграничение полномочий между федеральными органами исполнительной власти, в ведении которых находятся высшие учебные заведения, устанавливается положениями о соответствующих органах, утвержденными Президентом Российской Федерации или Правительством Российской Федерации.</span></p>
 
