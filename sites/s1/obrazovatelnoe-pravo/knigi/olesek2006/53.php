<p style="text-align: justify;">
 <span class="text">Переход высшей школы на предметную систему</span>
</p>
<p style="text-align: justify;">
 <span class="text">и первые его результаты</span>
</p>
<p style="text-align: justify;">
 <span class="text"> <br>
 </span>
</p>
<p style="text-align: justify;">
 <span class="text">Основательная подготовительная работа, проведенная в стране, обеспечила успешный старт реформы, который был дан опубликованием 12 июля 1906 г. решения о переходе на новую систему. С этого момента по всей стране прокатилась волна создания новых отношений в российских университетах. Как и подобало признанному флагману высшей инженерной школы, подошли к перестройке учебного процесса в Московском высшем техническом училище. Учебный комитет вуза считал, что старая, до мелочей заорганизованная курсовая система изжила себя, и переход на новую, предметную систему представлялся всем назревшим и необходимым. Этот переход рассматривали в училище в свете политехнизации высшего технического образования.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Широко задуманная перестройка учебного процесса представляла собой многоплановое мероприятие, включавшее ряд взаимосвязанных акций, в числе которых были: а) политехнизация высшего технического образования за счет изменения состава обязательных дисциплин на старших курсах; б) переход в преподавании наук на предметную систему и разработка метода контроля за обучением студентов в установленные сроки; в) развитие экспериментального метода обучения студентов, создание новых и расширение старых лабораторий; г) перестройка системы практического обучения и подготовка студентов в соответствии с требованиями быстро развивающейся промышленности.</span>
</p>
<p style="text-align: justify;">
 <span class="text">В училище переход на новую систему произошел организованно и планомерно. Прежде всего было разрешено свободное посещение лекций и других занятий, кроме работы в лабораториях и учебных мастерских. Наиболее существенные перемены произошли в организации системы контроля за ходом учебной работы. Экзамены поначалу было решено принимать в течение всего учебного года. Но затем сочли целесообразным ограничить их особыми экзаменационными сессиями в мае, сентябре и декабре.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Все дисциплины учебного плана делились на три разряда:</span>
</p>
<p style="text-align: justify;">
 <span class="text">а) общеобязательные, к которым относились математика, физика, теоретическая механика, химия и некоторые другие; в этот же разряд входило машиноведение, представлявшее концентрированное изложение технических предметов, относящихся к разным специальностям;</span>
</p>
<p style="text-align: justify;">
 <span class="text">б) специально-обязательные предметы, которые включали дисциплины, формирующие инженера в избранном направлении техники. Изучение этих дисциплин, выполнение лабораторных работ и проектирование, включая дипломный проект, экскурсии на промышленные предприятия и производственную практику - все это углубляло и расширяло знания будущего специалиста;</span>
</p>
<p style="text-align: justify;">
 <span class="text">в) необязательные дисциплины - это дисциплины общественно-экономического цикла или дисциплины из второго разряда, относящиеся к другим специальностям. Например, студентам, избравшим для себя специальность «Тепловые двигатели», необязательно было изучать дисциплины, относящиеся к специальностям электротехнического направления.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Вначале общий порядок и продолжительность курса учения предоставлялись на выбор студента со свободным посещением, причем общая продолжительность курса устанавливалась в 5 лет, или 10 семестров. Впоследствии учебный комитет сам устанавливал порядок прохождения курсов, исходя из их логической последовательности.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Немало новаций имелось непосредственно в организации учебного процесса. По ряду наиболее трудных дисциплин, таких, как математика, теоретическая механика и другие, были установлены обязательные консультации, так называемые репетиции, с текущей проверкой знаний. При всех сданных репетициях их оценка засчитывалась как экзаменационная. Таким образом, студентам предоставлялась возможность сдавать экзамен по частям. Особенно широко это практиковалось среди студентов младших курсов. Этот опыт заслуживает и ныне самого пристального внимания, поскольку способствует планомерной и систематической организации самостоятельной работы студента.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Вслед за высшим техническим училищем на новый порядок учебной работы переходили другие вузы. В литературе имеются богатые материалы на эту тему, пока совершенно не изученные и ждущие своего исследователя.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Интересные данные опубликованы в журнале «Техническое образование» (1906. № 1) под заголовком «Об изменении учебного строя в Горном имени императрицы Екатерины Великой институте (к вопросу о введении предметной системы прохождения курсов в высших технических учебных заведениях)». В статье говорится: «В июле 1905 г. соединенная комиссия из профессоров и преподавателей Горного института закончила разработку проекта нового учебного плана. Поводом к изменению учебного плана послужили, с одной стороны, уже признанные и другими высшими техническими учебными заведениями преимущества предметной системы, с другой - давно назревший и уже разработанный несколько лет назад вопрос о разделении Горного института на два разряда - Горный и Заводской».</span>
</p>
<p style="text-align: justify;">
 <span class="text">Все преподаваемые в институте предметы распределялись на четыре группы: I группа - основные предметы, II группа - общие предметы, III группа - специальные предметы и IV группа - предметы дополнительные. Распределение часов практических и теоретических занятий в неделю по предметам отдельных групп видно из таблицы.</span>
