<p align="justify">Исполнители НИР: Безносикова С.В.; Власова Ю.Ю., к.п.н.; Голов В.П., д.п.н., проф.; Захарова Т.Б., д.п.н.; Коротков Е.И., к.п.н.; Макарова Т.Д.; Падалкина Л.В.; Панеева И.В.; Рыжаков М.В., д.п.н., проф.; Сомова Л.Я.; Цыпляева Т.А.</p>
 
