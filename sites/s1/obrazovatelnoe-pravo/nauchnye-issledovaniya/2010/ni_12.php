  <li> - формирование предложений по технологии разработки государственных требований к минимуму содержания и уровню подготовленности выпускников с дополнительной квалификацией &laquo;Педагог дополнительного образования&raquo;; </li>

  <li> - формирование предложений по разработке перечня и примерных программ по дисциплинам, способствующим формированию инновационной культуры выпускников вузов. </li>
</ul>
 
<br />
 
