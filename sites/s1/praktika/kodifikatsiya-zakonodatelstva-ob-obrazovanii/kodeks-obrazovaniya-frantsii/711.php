<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar"><b>Статья L.711-3</b></span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Университеты технологии являются публичными учреждениями научного, культурного и профессионального характера, созданными на условиях, предусматриваемых в статье L.711-4, и которые имеют главной задачей подготовку инженеров, развитие исследований и технологии. Эти учреждения являются либо институтами и школами, внешними по отношению к университетам и определяемыми главой V, либо Большими учреждениями, определяемыми главой VII настоящего раздела.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Учреждения высшего образования могут быть трансформированы в университеты технологии при условии, что ежегодный набор на их технологические направления подготовки составляет по крайней мере пятьсот студентов.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar"><b>Статья L.711-4</b></span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">I. - Публичные учреждения научного, культурного и профессионального характера создаются декретом по получении заключения Национального Совета высшего образования и исследований.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">II. - Декреты, касающиеся создания публичных учреждений научного, культурного и профессионального характера, могут содержать отклонения, на период в пять лет, от положений статей с L.712-1 по L.712-3, с L.712-5 по L.712-7, L.713-1, L.714-1, c L.715-1 по L.715-3, c L.719-1 по L.719-3.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Отклонения имеют своей единственной целью экспериментирование в новых учреждениях со способами организации и управления, отличающимися от способов, предусматриваемых вышеперечисленными статьями. Они обеспечивают независимость профессоров и других преподавателей-исследователей посредством адекватного и подлинного представительства каждой из этих двух групп и относительной значительностью их представительства в составе принимающего решения органа учреждения. Они обеспечивают также адекватное и подлинное представительство других категорий персонала и пользователей. Они не могут наносить ущерб принципу выборности представителей этих различных категорий внутри руководящего органа.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Эксперименты, предусматриваемые в предыдущем абзаце, являются объектом оценки со стороны Национального комитета оценки публичных учреждений научного, культурного и профессионального характера. Комитет составляет по каждому учреждению отчет, который он направляет в Парламент и министру, ответственному за высшее образование, не позже чем за шесть месяцев до окончания эксперимента.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">В случае, если учреждение намерено закончить эксперимент до истечения вышеупомянутых пяти лет, исполнительный орган учреждения обращается с просьбой к министру, ответственному за высшее образование поручить приступить проведение оценки эксперимента Национальному комитету по оценке; этот последний направляет свой отчет министру и в исполнительный орган учреждения в течение срока в шесть месяцев, начиная с даты подачи прошения исполнительного органа; он делает заключение о целесообразности продолжения эксперимента; после получения этого заключения учреждение имеет право принять решение о продолжении эксперимента до окончания пятилетнего срока или о его приостановлении.</span></p>
<span class="textmar"> </span>
