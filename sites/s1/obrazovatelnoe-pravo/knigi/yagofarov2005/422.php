<p style="text-align: justify;">
	 Под компетенцией государственного органа в правовой науке понимается совокупность юридических прав и обязанностей (полномочий) конкретного государственного органа, органа местного самоуправления или должностного лица, определяющих его место в системе государственных органов (органов местного самоуправления) и, тем самым, возможность и способность решения этим органом (должностным лицом) определенного круга вопросов (дел). Применительно к сфере образования термином «компетенция» обозначается совокупность юридических прав и обязанностей (полномочий) государственных и/или уполномоченных негосударственных органов, учреждений, организаций по принятию правовых решений в ходе законотворческой или правоприменительной деятельности по вопросам образования.
</p>
<p>
</p>
<p style="text-align: justify;">
 <span class="text">Установленная ст. 28 и 29 Закона РФ "Об образовании" компетенция федеральных органов и органов государственной власти субъектов РФ является исчерпывающей и может быть изменена только законом. В последний раз компетенция государственных органов в сфере образования была изменена федеральным законом от 22.08.2004 года № 122-ФЗ. Так, из области компетенции РФ было исключено два пункта и введено два, из области компетенции субъектов РФ исключено четыре пункта.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Разграничение компетенции между федеральными органами исполнительной власти определяется Правительством Российской Федерации. Разграничение компетенции между органами законодательной и исполнительной власти субъектов Российской Федерации, а также между их органами регулируется законодательством соответствующих субъектов.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Принцип федерализма в сфере образовательно-правовой законодательной компетенции имеет три уровня<i>: конституционный, федеральный и региональный .</i>Прежде чем раскрыть эти уровни, заметим следующее.</span>
</p>
<p style="text-align: justify;">
 <span class="text">На уровне Российской Федерации наиболее важное, принципиальное значение имеют положения, требования, ориентированные на реализацию<i>интегративной функции</i>принципа федерализма в законодательстве об образовании. Это, в частности, проявляется в следующем:</span>
</p>
<p style="text-align: justify;">
 <span class="text">- Российская Федерация имеет исключительные полномочия в нормативно-правовом регулировании правовых институтов, которые находятся на стыке образовательного права и отрасли права, названной в ст. 71 Конституции РФ;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- на уровне общих вопросов образования предмет ведения Российской Федерация ограничивается сферой непосредственно образовательных отношений между обучающимися, педагогическими работниками и образовательным учреждениям;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- относительно отношений, находящихся на стыке образовательного законодательства и трудового, административного, налогового и других отраслей права, Российская Федерация правомочна вырабатывать принимать как общие, так и конкретные нормативно-правовые установления и предписания;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- Российская Федерация правомочна устанавливать и конкретизировать общую компетенцию субъектов Российской Федерации и органов местного самоуправления, а также устанавливать общие положения относительно структуры органов управления образованием. </span>
</p>
<p style="text-align: justify;">
 <span class="text">Деятельность упомянутых органов призвана обеспечить реализацию Федеральную программу развития образования до 2010г. и Национальной доктрины развития образования в РФ, государственные образовательные стандарты и функционирование системы образования на уровне государственных нормативов.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Благодаря принятым в 90-х годах прошлого столетия и в первом пятилетии нынешнего столетия федеральным законам по вопросам образования в РФ:</span>
</p>
<p style="text-align: justify;">
 <span class="text">1) была установлена и/или уточнена компетенция и ответственность в области образования федеральных органов государственной власти и органов государственной власти субъектов Российской Федерации;</span>
</p>
<p style="text-align: justify;">
 <span class="text">2) получили единообразное нормативное закрепление общие вопросы образования, которые должны одинаково решаться всеми субъектами РФ;</span>
</p>
<p style="text-align: justify;">
 <span class="text">3) введены общие установочные нормы по вопросам, которые относятся к компетенции субъектов РФ и, в соответствии с которыми, последние могут осуществлять собственное правовое регулирование в сфере образования.</span>
</p>
<p style="text-align: justify;">
 <span class="text"><i>Конституционная компетенция.</i>В соответствии с пунктом «е» ч.1 ст. 72 Конституции РФ общие вопросы образования находятся в совместном ведении Российской Федерации и субъектов Российской Федерации. Рассуждая формально-логически, можно предположить, что субъекты образовательного правотворчества как на федеральном уровне, так и на уровне субъектов РФ совместно определяют, какие именно общие вопросы образования наиболее целесообразно им урегулировать совместно<sup>11</sup>. Вместе с тем, ни в Конституции, ни в Законе РФ «Об образовании» не определен легально и однозначно круг вопросов, подлежащих совместному правовому урегулированию. Это несомненный пробел в законодательстве, требующий своего восполнения. В отдельных работах такой перечень предлагается (например, в<i>Аналитическом отчете Центра образовательного законодательства Минобразования России за 2002</i>). В частности, этот перечень должен, по мнению авторов отчета, включать:</span>
</p>
<p style="text-align: justify;">
 <span class="text">- вопросы, связанные с закреплением и конкретизацией компетенции Федерации и ее субъектов в сфере образования, установлением полномочий органов управления образованием на уровне Федерации, субъектов Российской Федерации и органов местного самоуправления;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- вопросы, связанные с определением предмета, метода, целей и задач, источников, принципов и основных понятий законодательства об образовании;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- вопросы, связанные с определением основных компонентов системы образования, правового статуса обучающихся, образовательных учреждений, педагогических работников и иных участников образовательных отношений, а также установлением основ нормативно-правового регулирования образовательного процесса;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- вопросы, связанные с порядком и пределами применения в законодательстве об образовании норм и принципов административного, трудового и других отраслей права;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- установление перечней профессий и специальностей, по которым ведутся профессиональная подготовка и профессиональное образование;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- разработка и утверждение типовых положений об образовательных учреждениях;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- установление порядка лицензирования, аттестации и государственной аккредитации образовательных учреждений;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- информационное и научно-методическое обеспечение системы образования, разработка в пределах своей компетенции примерных учебных планов и программ курсов, дисциплин, организация издания учебной литературы и производства учебных пособий, создание единой для Российской Федерации системы информации в области образования;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- установление государственных образовательных стандартов и их регионально-национальных компонентов.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Данное предложение нашло отклик у авторского коллектива проекта Общей части Кодекса Российской Федерации об образовании и приведенный перечень был включен в п.2 ч.1 ст. 17 указанного проекта.</span>
</p>
