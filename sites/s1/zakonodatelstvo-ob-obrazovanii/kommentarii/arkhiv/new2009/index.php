<p><span class="text" style="text-align: justify;">Статья 30. Оплата труда работников высшего учебного заведения</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Высшее учебное заведение в пределах имеющихся у него средств на оплату труда работников самостоятельно определяет размеры доплат, надбавок, премий и других мер материального стимулирования, а также размеры должностных окладов (ставок) всех категорий работников (без установления предельных размеров должностных окладов (ставок)).</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Должностной оклад (ставка) выплачивается работнику высшего учебного заведения за выполнение им функциональных обязанностей и работ, предусмотренных трудовым договором (контрактом).</span></p>
 
<p><span class="text" style="text-align: justify;">3. Высшее учебное заведение самостоятельно формирует фонд оплаты труда работников за счет средств бюджета, направляемых на содержание высшего учебного заведения, и (или) иных источников, не запрещенных законодательством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 3 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">5. Научно-педагогическим работникам высших учебных заведений устанавливаются надбавки к должностным окладам (ставкам) в размере:</span></p>
 
<p><span class="text" style="text-align: justify;">сорока процентов за должность доцента;</span></p>
 
<p><span class="text" style="text-align: justify;">шестидесяти процентов за должность профессора;</span></p>
 
<p><span class="text" style="text-align: justify;">3000 рублей за ученую степень кандидата наук;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 07.08.2000 № 122-ФЗ, от 05.04.2003 № 41-ФЗ, от 16.10.2006 № 161-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">7000 рублей за ученую степень доктора наук.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 07.08.2000 № 122-ФЗ, от 05.04.2003 № 41-ФЗ, от 16.10.2006 № 161-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6 - 9. Утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
