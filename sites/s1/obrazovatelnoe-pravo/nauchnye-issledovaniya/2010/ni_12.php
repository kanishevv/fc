<div><b><i>Новизна, научная и практическая значимость проекта и его результатов:</i></b></div>
 
<div>Проведенный анализ о потребностях системы дополнительного образования по качеству и номенклатуре подготовки специалистов позволил выделить ряд направлений для дополнительных образовательных программ. Определены особенности, задачи и специфика интеграции общего и дополнительного образования, которая должна позволить более эффективно решать ряд серьезных социальных задач. Выделены четыре модели организации дополнительного образования в общеобразовательном учреждении. </div>
 
<div>Сформулирован подход к созданию программ дополнительной квалификации, предполагающий необходимость обоснования актуальности разработки программы, цели программы и задач ее реализации, адресности программы, функционала специалиста, а также проектирования содержания программы. </div>
 
<div>Раскрыто понятие &laquo;инновационной культуры&raquo;, показано значение инновационной культуры для выпускников по педагогических специальностям. Обозначен механизм управление процессом становления, формирования и развития инновационной культуры педагогов. </div>
 
<div>Разработаны и представлены проекты Государственных требований к минимуму содержания и уровню профессиональной подготовки для получения дополнительной квалификации: &laquo;Педагог дополнительного образования&raquo;, а также Примерный учебный план дополнительной квалификации &laquo;Педагог дополнительного образования&raquo;.</div>
 
