<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Образовательные учреждения, получившие право выдавать дипломы инженера или которые выдают диплом инженера в соответствии со статьей L.641-5, подлежат в том, что касается условий, в которых обеспечивается профессиональная подготовка инженера, инспекции со стороны инспекторов или лиц, которым поручена инспекция.</span></p>
