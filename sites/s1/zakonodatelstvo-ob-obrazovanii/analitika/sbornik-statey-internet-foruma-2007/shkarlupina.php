  <p><span class="text">Практика показывает, что необходимо установление устойчивых межбюджетных отношений между региональной властью и муниципальной, это будет способствовать развитию бюджетов муниципалитетов в решении вопросов образования:</span></p>
 
  <p><span class="text">- разработку и осуществление программ, определяющих структуру сегодняшних потребностей населения в образовательных услугах;</span></p>
 
  <p><span class="text">- выполнения сложного комплекса мер, включающих, прежде всего, разработку целей и содержания новых типов общего образования, состава или форм организации образовательного процесса, методов диагностики результатов обучения, механизмов перехода между различными ступенями и типами образования, создания диверсификационной системы непрерывного профессионального образования;</span></p>
 
  <p><span class="text">- сохранение и развитие региональной (муниципальной) образовательной и управленческой инфраструктур;</span></p>
 
  <p><span class="text">- создание условий для адаптации системы образования к новым социально-экономическим реалиям и дальнейшего преодоления отраслевой замкнутости системы;</span></p>
 
  <p><span class="text">- стимулирование развития сферы дополнительного образования как средства расширения рынка дополнительных образовательных услуг в целях самореализации личности.</span></p>
 
  <p><span class="text">Выявление специфики развития муниципальных органов управления позволяет сделать вывод о том, что структура и функции муниципальных образований имеет свою специфику организационного устройства.</span></p>
 
  <p><span class="text">Это требует от государственных служащих субъектов РФ дополнительных усилий по выполнению должностных инструкций, т.к. они должны охватывать все необходимые функции государственного управления, предписанные нормативно-правовыми актами, количество функций, выполняемое государственным служащим, увеличивается в несколько раз. Государственные служащие муниципальных образований в большей степени подвержены подобным перегрузкам. Должностные инструкции муниципальных госслужащих максимально охватывают те виды деятельности, по которым ведется отчетность перед вышестоящими органами исполнительной власти: департаментами, управлениями, отделами субъектов Федерации, предоставляя информацию для отчетности служащим этого уровня перед федеральными службами, агентствами и министерством. При этом редко учитывается тот факт, что в штате муниципального образования насчитывается не более двух десятков специалистов разных категорий. Растет бюрократизация государственного управления, она внедряется не только в системы государственного управления, но и в управление образовательных учреждений, руководство которых также вынуждено постоянно готовить информацию для управления системы образования при администрации муниципальных образований. Необходима унификация основных управленческих структур различных уровней исполнительной власти в области образования.</span></p>
 
  <p><span class="text">Таким образом, регионам России предстоит создать нормативно-правовую базу осуществления местного самоуправления, в пределах переданных ему полномочий научиться решать вопросы обеспечения жизнедеятельности местного сообщества, выстраивать систему местного самоуправления, позволяющую избежать противостояния органов власти муниципального и государственного уровня. Реализация этих мер позволит осуществлять власть муниципального уровня в интересах граждан, в том числе и в области непрерывного образования.</span></p>
 
