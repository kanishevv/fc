</p>
<p style="text-align: justify;">
 <span class="text">4.2.Исполнитель вправе приостановить оказание услуг, указанных в разделе 1 настоящего договора, если Заказчик не произвел оплату в срок, установленный п. 6.2 настоящего договора.</span>
