<p align="LEFT"> 
  <br />
 <b>Статья 47. Предпринимательская и иная приносящая доход деятельность образовательного учреждения</b></p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>1. Образовательное учреждение вправе вести предпринимательскую и иную приносящую доход деятельность, предусмотренную его уставом.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>2. К предпринимательской деятельности образовательного учреждения относятся:</p>
 
<p>абзац утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>торговля покупными товарами, оборудованием;</p>
 
<p>оказание посреднических услуг;</p>
 
<p>долевое участие в деятельности других учреждений (в том числе образовательных) и организаций;</p>
 
<p>приобретение акций, облигаций, иных ценных бумаг и получение доходов (дивидендов, процентов) по ним;</p>
 
<p>ведение приносящих доход иных внереализационных операций, непосредственно не связанных с собственным производством предусмотренных уставом продукции, работ, услуг и с их реализацией.</p>
 
<p>3 - 4. Утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>5. Учредитель или органы местного самоуправления вправе приостановить предпринимательскую деятельность образовательного учреждения, если она идет в ущерб образовательной деятельности, предусмотренной уставом, до решения суда по этому вопросу.</p>
 
