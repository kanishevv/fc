</p>
<p style="text-align: justify;">
	<span class="text">2.2.Обеспечить для проведения занятий помещения, соответствующие санитарным и гигиеническим требованиям, а также оснащение, соответствующее обязательным нормам и правилам, предъявляемым к образовательному процессу.</span>
</p>
<p style="text-align: justify;">
	<i><span class="text">* На оказание услуг, связанных с обеспечением содержания обучающегося в образовательном учреждении во время оказания дополнительных образовательных услуг (по организации питания, медицинского обслуживания, охраны, доставки обучающегося в образовательное учреждение и домой транспортом Исполнителя и т.д.), составляется отдельный договор.</span></i>
</p>
<p style="text-align: justify;">
	<span class="text"> <br>
 </span>
</p>
<p style="text-align: justify;">
	<span class="text">2.3.Во время оказания дополнительных образовательных услуг про­являть уважение к личности Потребителя, оберегать его от всех форм физического и психологического насилия, обеспечить условия укреп­ления нравственного, физического и психологического здоровья, эмоционального благополучия Потребителя с учетом его индивиду­альных особенностей.</span>
</p>
<p style="text-align: justify;">
	<span class="text">2.4.Сохранить место за Потребителем (в системе оказываемых об­щеобразовательным учреждением дополнительных образовательных услуг) в случае его болезни, лечения, карантина, отпуска родителей, каникул и в других случаях пропуска занятий по уважительным причи­нам.</span>
</p>
<p style="text-align: justify;">
	<span class="text">2.5.Уведомить Заказчика о нецелесообразности оказания Потреби­телю образовательных услуг в объеме, предусмотренном разделом 1 настоящего договора, вследствие его индивидуальных особенностей, делающих невозможным или педагогически нецелесообразным ока­зание данных услуг.</span>
</p>
<p style="text-align: justify;">
	<b><span class="text"> <br>
 </span><span class="text">3. Обязанности Заказчика</span></b>
</p>
<p style="text-align: justify;">
	<span class="text">3.1.Своевременно вносить плату за предоставленные услуги, указан­ные в разделе 1 настоящего договора.</span>
</p>
<p style="text-align: justify;">
	<span class="text">3.2.При поступлении Потребителя в общеобразовательное учрежде­ние и в процессе его обучения своевременно предоставлять все необ­ходимые документы, предусмотренные уставом общеобразователь­ного учреждения.</span>
</p>
<p style="text-align: justify;">
	<span class="text">3.3.Незамедлительно сообщать руководителю Исполнителя об изме­нении контактного телефона и места жительства.</span>
</p>
<p style="text-align: justify;">
	<span class="text">3.4.Извещать руководителя Исполнителя об уважительных причинах отсутствия Потребителя на занятиях.</span>
</p>
<p style="text-align: justify;">
	<span class="text">3.5.По просьбе Исполнителя приходить для беседы при наличии пре­тензий Исполнителя к поведению Потребителя или его отношению к получению дополнительных образовательных услуг.</span>
</p>
<p style="text-align: justify;">
