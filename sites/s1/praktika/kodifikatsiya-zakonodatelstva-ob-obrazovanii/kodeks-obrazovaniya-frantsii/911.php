<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">2. Те, кто были лишены судом всех или части гражданских, политических и семейных прав, упомянутых в статье 131-26 Уголовного кодекса, или кто был лишен родительских прав.</span></p>
<span class="textmar"> </span>
<p> </p>
