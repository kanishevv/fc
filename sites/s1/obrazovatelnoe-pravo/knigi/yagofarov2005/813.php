<p style="text-align: justify;">
	<span style="text-align: justify;">Общие обязанности педагогических работников.&nbsp;Общий круг основных общих обязанностей педагогических работников представлен в Законе РФ «Об образовании». Однако, в отличие от прав и свобод педагогических работников непосредственного перечня таких обязанностей закон не содержит. Их можно выделить лишь путем анализа содержания норм всего этого закона, прежде всего, текста ст.ст. 2 «Принципы государственной политики в области образования», 4 «Задачи законодательства Российской Федерации в области образования»,5«Государственные гарантии прав граждан Российской Федерации в области образования», 32 «Компетенция и ответственность образовательного учреждения», 56 «Трудовые отношения в системе образования» и др. Такой подход законодателя к установлению правового статуса одного из центральных субъектов образовательной системы и образовательных отношений в целом представляется не совсем удачным, поскольку обязанности субъектов права, наряду с их правами и свободами, являются ключевыми элементами такого статуса. В этом отношении правильно поступили авторы проекта Общей части Кодекса РФ об образовании, выделив в качестве самостоятельной ст. 60 «Основные обязанности педагогических работников образовательного учреждения (организации)», которые в целом соответствуют ныне закрепленным в законе. Комплексный анализ этих двух документов позволяет свести все основные (общие) обязанности педагогических работников в следующий перечень:</span>
</p>
<p style="text-align: justify;">
	<span class="text">- обязанность соблюдать действующее российское законодательство, в том числе образовательное законодательство, Устав и локальные акты образовательного учреждения (организации):</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность обеспечивать качественное проведение учебного и воспита­тельного процесса, высокую эффективность педагогической и мето­дической деятельности;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность соблюдать все требования, предъявляемые к педагогическим работникам в тарифно-квалификационных характеристиках, утвер­жденных в установленном порядке;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность обеспечивать качественное освоение обучающими образова­тельных программ и выполнение государственных образовательных стандартов в соответствии с учебным планом и графиком занятий;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность строго следовать профессиональной этике;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность систематически повышать свою профессиональную квалификацию;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность развивать у обучающихся (воспитанников) самостоятельность, инициативу, творческие способности, а также содействовать гражданам с отклонениями благоприятных психолого-педагогических условий для получения ими образования;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность формировать у обучающихся (воспитанников) здоровый образ жизни, вести профилактику вредных привычек;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность уважать, защищать и обеспечивать права, свободы и законные интересы обучающегося (воспитанника) в образовательном процессе, недопустимость произвольного ограничения этих прав, свобод и законных интересов;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность периодически обновлять образовательные программы, учебные планы;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность осуществления систематического текущего контроля успеваемости и промежуточной аттестации обучающихся образовательного учреждения в соответствии с его Уставом и требованиями образовательного законодательства;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обязанность содействия формированию и подержанию благоприятного морально-психологического климата среди обучающихся (воспитанников) и в целом в коллективе работников образовательного учреждения.</span>
</p>
<p style="text-align: justify;">
 <span class="text"><i>Обязанности педагогических работников общеобразовательных учреждений.</i>Анализ трудовых и образовательно-профессиональных обязанностей, конкретизирующие и уточняющие названные выше обязанности Трудовым кодексом РФ, Типовым положением об общеобразовательном учреждении, их уставами и иными локальными актами, а также трудовыми договорами (контрактами) педагогических работников (учителей школы) позволяет выделить следующие их обязанности:</span>
</p>
<p style="text-align: justify;">
 <span class="text">- добросовестно выполнять трудовые обязанности;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- соблюдать трудовую дисциплину;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- выполнять установленные нормы труда;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- стремиться к повышению качества выполняемой работы, проявлять творческую инициативу, направленную на до­стижение высоких результатов трудовой деятельности;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- соблюдать требования по охране труда, технике без­опасности, производственной санитарии (гигиене труда) и противопожарной охране;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- проходить один раз в пять лет аттестацию на соответствие занимаемой должности;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- систематически повышать свой теоретический и культурный уровень, профессиональную квалификацию;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- соблюдать установленный порядок хранения материальных ценностей и документов;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- проходить периодически бесплатное медицинское обследование.</span>
</p>
<p style="text-align: justify;">
 <span class="text">- осуществлять обучение и воспитание учащихся с учетом специфики преподаваемого предмета;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- принимать меры к немедленному устранению причин и ус­ловий, препятствующих или затрудняющих осуществление педагогического процесса;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- формировать общую культуру личности учащихся, условия для осознанного выбора и последующего освоения профессио­нальных образовательных программ учащимися;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- реализовать образовательную программу и обеспечивать уровень подготовки учащихся, соответствующего Госу­дарственному образовательному стандарту;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- участвовать в деятельности методических объединений и других формах методической работы;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обеспечивать охрану жизни и здоровья обучающихся в период образовательного процесса;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- сотрудничать с коллегами, органами управления об­разования в интересах учащихся;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- постоянное проявлять внимание и вежливое отноше­ние к детям, родителям учащихся и членам коллектива;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- участвовать в мероприятиях с учащимися и их родителями, выходящими за рамки учебного плана образовательного учреждения.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Упоминавшаяся выше «Рекомендация МОТ/ЮНЕСКО о положении учителей» называет такие обязанности учителей, признание и обеспечение которых является одной из важнейших задач органов народного образования:</span>
</p>
<p style="text-align: justify;">
 <span class="text">- все учителя должны стремиться к достижению максимально высокого уровня всей своей профессиональной работы;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- учителя и учительские организации должны стремиться к установлению широкого сотрудничества с органами народного образования в интересах учащихся, развития образования и общества;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- учительскими организациями должен быть выработан кодекс этики учителя или кодекс поведения, ибо такое кодексы вносят значительный вклад в обеспечение престижа профессии учителя и выполнение профессионального долга в соответствии с согласованными принципами;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- учителя должны проявлять готовность к участию в мероприятиях с учащимися и взрослыми, выходящих за рамки учебного плана школы.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Как можно заметить, ряд этих рекомендаций уже нашел свое отражение в российском образовательном законодательстве.</span>
</p>
<p style="text-align: justify;">
 <span class="text"><i>Обязанности педагогических работников педагогических работников высших учебных заведении</i> не отличаются принципиально от обязанностей учителей общеобразовательных школ, хотя четкого перечня таких обязанностей ни ФЗ «О высшем и послевузовском профессиональном образовании», ни Типовое положение о высшем учебном заведении не приводят. Скажем, как и любому педагогическому работнику, преподавателю высшей школы запрещается использование антигуманных, а также опасных для жизни или здоровья обучающихся методов обучения (п. 38 . Типового положении о высшем учебном заведении)</span>
</p>
<p style="text-align: justify;">
 <span class="text">То же время в, уставах вузов ряд обязанностей актуализируются и уточняются, исходя из специфики высшей школы вообще и конкретного вуза, в частности. Так¸ из положений пунктов 45 и 46 Типового положения явно вытекают обязанности профессорско-преподавательского состава активно заниматься научной деятельностью, по результатам оценки которой дается характеристика как индивидуальной профессиональной научно-педагогической работы каждого преподавателя, но и всего учебного заведения в целом. Это, в частности, имеет отношение и к так называемой «грантовой» научной деятельности, удельный вес и значимость которой для оценки научно-педагогического профессионализма преподавателя и вуза в целом в современный период повышаются.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Пункт 49 Устава государственного образовательного учреждения высшего профессионального образования Уральский университет предусматривает следующие, помимо общих, указанных выше, обязанности научно-педагогических работников Университета:</span>
</p>
<p style="text-align: justify;">
 <span class="text">- всемерно своим трудом и поведением способствовать повышению престижа Университета как научно-образовательного, просветительного и культурного центра.</span>
</p>
<p style="text-align: justify;">
 <span class="text">- обеспечивать высокую эффективность педагогического и научного процессов;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- формировать у обучающихся профессиональные качества по избранному направлению подготовки (специальности), гражданскую позицию, способность к труду и жизни в условиях современной цивилизации и демократии;</span>
</p>
<p style="text-align: justify;">
 <span class="text">- выполнять требования по защите государственной, служебной и коммерческой тайны, сведений конфиденциального характера, а также правовой охране интеллектуальной собственности Университета.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Пункт 6.25 Устава Института дополнительного профессионального образования – негосударственного образовательного учреждения «Европейский университет в Санкт-Петербурге предусматривает также такие (кроме общих) обязанности своих преподавателей, как обязанности ответственно и творчески относиться к работе, а также соблюдать нормы профессионального поведения и этики.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Уместно вновь привести извлечение из «Рекомендации ЮНЕСКО/МОТо статусе преподавательских кадров учреждений высшего образования» (Париж, 1997г.) с перечислением обязанностей преподавателей высшей школы. Представляется, что этот перечень, признаваемый мировым сообществом, для российского законодателя и самих вузов страны, равно как и для самих преподавателей, имеет исключительно большое значение.</span>
</p>
