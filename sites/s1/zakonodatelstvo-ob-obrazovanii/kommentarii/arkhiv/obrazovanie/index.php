<p><b>Статья 32. Компетенция и ответственность образовательного учреждения</b></p>
 
<p>1. Образовательное учреждение самостоятельно в осуществлении образовательного процесса, подборе и расстановке кадров, научной, финансовой, хозяйственной и иной деятельности в пределах, установленных законодательством Российской Федерации, типовым положением об образовательном учреждении соответствующих типа и вида и уставом образовательного учреждения.</p>
 
<p>2. К компетенции образовательного учреждения относятся:</p>
 
<p>1) материально-техническое обеспечение и оснащение образовательного процесса, оборудование помещений в соответствии с государственными и местными нормами и требованиями, осуществляемые в пределах собственных финансовых средств;</p>
 
<p>2) привлечение для осуществления деятельности, предусмотренной уставом образовательного учреждения, дополнительных источников финансовых и материальных средств, в том числе использование негосударственным образовательным учреждением банковского кредита;</p>
 
<p>(пп. 2 в ред. Федерального закона от 30.06.2004 № 61-ФЗ)</p>
 
<p>3) предоставление учредителю и общественности ежегодного отчета о поступлении и расходовании финансовых и материальных средств;</p>
 
<p>4) подбор, прием на работу и расстановка кадров, ответственность за уровень их квалификации;</p>
 
<p>5) использование и совершенствование методик образовательного процесса и образовательных технологий, в том числе дистанционных образовательных технологий. Под дистанционными образовательными технологиями понимаются образовательные технологии, реализуемые в основном с применением информационных и телекоммуникационных технологий при опосредованном (на расстоянии) или не полностью опосредованном взаимодействии обучающегося и педагогического работника.</p>
 
<p>Образовательное учреждение вправе использовать дистанционные образовательные технологии при всех формах получения образования в порядке, установленном федеральным (центральным) государственным органом управления образованием;</p>
 
<p>(пп. 5 в ред. Федерального закона от 10.01.2003 № 11-ФЗ)</p>
 
<p>6) разработка и утверждение компонента образовательного учреждения государственного образовательного стандарта общего образования, образовательных программ и учебных планов;</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>7) разработка и утверждение рабочих программ учебных курсов и дисциплин;</p>
 
<p>8) разработка и утверждение по согласованию с органами местного самоуправления годовых календарных учебных графиков;</p>
 
<p>9) установление структуры управления деятельностью образовательного учреждения, штатного расписания, распределение должностных обязанностей;</p>
 
<p>10) установление заработной платы работников образовательного учреждения, в том числе надбавок и доплат к должностным окладам, порядка и размеров их премирования;</p>
 
<p>(пп. 10 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>11) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>12) разработка и принятие устава коллективом образовательного учреждения для внесения его на утверждение;</p>
 
<p>(пп. 12 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>13) разработка и принятие правил внутреннего распорядка образовательного учреждения, иных локальных актов;</p>
 
<p>14) самостоятельное формирование контингента обучающихся, воспитанников в пределах оговоренной лицензией квоты, если иное не предусмотрено типовым положением об образовательном учреждении соответствующих типа и вида и настоящим Законом;</p>
 
<p>15) самостоятельное осуществление образовательного процесса в соответствии с уставом образовательного учреждения, лицензией и свидетельством с государственной аккредитации;</p>
 
<p>16) осуществление текущего контроля успеваемости и промежуточной аттестации обучающихся образовательного учреждения в соответствии со своим уставом и требованиями настоящего Закона;</p>
 
<p>17) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>18) обеспечение в образовательном учреждении интернатного типа условий содержания воспитанников не ниже нормативных;</p>
 
<p>19) создание в образовательном учреждении необходимых условий для работы подразделений организаций общественного питания и медицинских учреждений, контроль их работы в целях охраны и укрепления здоровья обучающихся, воспитанников и работников образовательного учреждения;</p>
 
<p>20) содействие деятельности учительских (педагогических) организаций (объединений) и методических объединений;</p>
 
<p>21) координация в образовательном учреждении деятельности общественных (в том числе детских и молодежных) организаций (объединений), не запрещенной законом;</p>
 
<p>22) осуществление иной деятельности, не запрещенной законодательством Российской Федерации и предусмотренной уставом образовательного учреждения;</p>
 
<p>23) выбор учебников из утвержденных федеральных перечней учебников, рекомендованных (допущенных) к использованию в образовательном процессе.</p>
 
<p>(пп. 23 введен Федеральным законом от 25.06.2002 № 71-ФЗ)</p>
 
<p>3. Образовательное учреждение несет в установленном законодательством Российской Федерации порядке ответственность за:</p>
 
<p>1) невыполнение функций, отнесенных к его компетенции;</p>
 
<p>2) реализацию не в полном объеме образовательных программ в соответствии с учебным планом и графиком учебного процесса; качество образования своих выпускников;</p>
 
<p>3) жизнь и здоровье обучающихся, воспитанников и работников образовательного учреждения во время образовательного процесса;</p>
 
<p>4) нарушение прав и свобод обучающихся, воспитанников и работников образовательного учреждения;</p>
 
<p>5) иные действия, предусмотренные законодательством Российской Федерации.</p>
 
