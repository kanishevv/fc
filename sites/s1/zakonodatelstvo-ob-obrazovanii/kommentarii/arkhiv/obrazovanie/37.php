<p align="JUSTIFY"><span class="text">4. Органы управления образованием в субъектах Российской Федерации.</span></p>

<p align="LEFT" style="text-align: justify;"><span class="text">Такие органы учреждаются под разными названиями (главное управление образования администрации области, министерство образования республики или области и т.д.).</span></p>

<p align="LEFT" style="text-align: justify;"><span class="text">Статус этих органов определяется, как правило, в законах об образовании субъекта Российской Федерации.</span></p>

<p align="JUSTIFY"><span class="text">Органы управления образованием в субъектах Российской Федерации контролируют исполнение законодательства в области образования и реализацию прав обучающихся, осуществляют межведомственную координацию по вопросам образования, осуществляют управление государственными образовательными учреждениями данного субъекта Российской Федерации, разрабатывают национально-региональный компонент государственных образовательных стандартов, лицензирование образовательных учреждений и т.д.</span></p>

