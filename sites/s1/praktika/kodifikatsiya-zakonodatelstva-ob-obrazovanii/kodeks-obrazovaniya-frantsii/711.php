<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Они являются многодисциплинарными и объединяют преподавателей-исследователей, преподавателей и исследователей по различным специальностям с тем, чтобы обеспечить развитие знаний и научной, культурной и профессиональной подготовки, готовящей к исполнению профессии.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Они автономны. Исполняя миссии, доверенные им законом, они определяют свою политику подготовки, исследований и документации в рамках национальной регламентации и при соблюдении взятых на себя контрактных обязательств.</span></p>
<span class="textmar"> </span>
  <p> </p>
<span class="textmar"> </span>
