<p style="text-align: justify;"><span class="text">Возможный вариант конкретного определения благополучателей Региональной Программы и их возможные интересы мы привели выше.</span></p>
 
<p style="text-align: justify;"><span class="text">Понимание благополучателей Программ позволяет перейтик рассмотрению их<b>главных целей.</b></span></p>
 
<p style="text-align: justify;"><span class="text"><b>Под целью понимают идеальный (находящийся в сознании субъекта деятельности) образ желаемого и возможного будущего результата, для достижения которого субъект и осуществляет свою деятельность</b>.</span></p>
 
<p style="text-align: justify;"><span class="text">Иными словами, любое предложение, выступающее под именем цели, должно содержать в себе<b>как можно более четкое указание на тот результат, который должен быть получен.</b>В современном менеджменте к цели помимо ясности<b>проектируемого</b><b>результата</b>добавляются также<b>сроки, временные рамки его достижения, а зачастую и предельно допустимые затраты ресурсов по пути к цели.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Важно подчеркнуть, что все цели должны иметь ясные параметры и индикаторы своего достижения, позволяющие в любой момент реализации Программы осуществить проверку степени их достижения!</span></p>
 
<p style="text-align: justify;"><span class="text">Мы знаем, что в образовательной практике эти требования соблюдаются далеко не всегда, но в Программах перехода на профильное обучение они должны выполняться достаточно строго.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом Программа объективно связана<b>с различными уровнями целей и целеполагания.</b>Таких уровней можно выделить пять.</span></p>
 
<ol> 
  <p style="text-align: justify;"><span class="text">В Программе должны быть абсолютно четко поставлены ее главные цели - то есть<b>непосредственные цели массового перехода образовательной системы</b>на профильное обучение, которые следует формулировать в терминах абсолютного количества и удельного веса (процента) образовательных учреждений, осуществляющих программы общего образования и перешедших на профильное обучение учащихся на старшей ступени (в целом по региону и по отдельным муниципальным образованиям за определенный плановый период, не слишком любимого нами, но достаточно важного показателя - «охвата» учащихся различными вариантами профильного обучения и различными профилями (включая и универсальный вариант без выраженной профилизации).</span></p>
 
  <p style="text-align: justify;"><span class="text">Данные непосредственные цели конкретизируются через формулирование<b>обеспечивающих их достижение более частных целей<sup>17</sup></b>(результатов), связанных, в частности, с обозначением</span></p>
 </ol>
 
<ul> 
  <p style="text-align: justify;"><span class="text">номенклатуры новых нормативно-правовых документов, регламентирующих переход на профильное обучение;</span></p>
 
  <p style="text-align: justify;"><span class="text">количественных и качественных параметров местных образовательных сетей;</span></p>
 
  <p style="text-align: justify;"><span class="text">состава, структуры и количества профильных классов по разным направлениям;</span></p>
 
  <p style="text-align: justify;"><span class="text">организуемых в регионе новых структур, нацеленных на научно-методическое, консультационное, профориентационное и иное сопровождение перехода на профильное обучение;</span></p>
 
  <p style="text-align: justify;"><span class="text">создаваемых информационных банков, программ, учебников (в том числе - по элективным курсам), учебно-методических разработок, образовательных технологий, включая технологии формирования учебных классов и групп, диагностики, контроля и оценки достижений учащихся;</span></p>
 
  <p style="text-align: justify;"><span class="text">количественных и качественных параметров организуемой в регионе подготовки, переподготовки и повышения квалификации педагогических и управленческих кадров в интересах освоения профильного обучения (по различным категориям и видам образовательных программ основного и дополнительного профессионального образования);</span></p>
 
  <p style="text-align: justify;"><span class="text">разрабатываемых в условиях городской и сельской местности новых организационно-экономических механизмов, призванных решить новые проблемы финансирования образовательных учреждений и организаций, оплаты труда педагогов и др.</span></p>
 </ul>
 
<ol start="3"> 
  <p style="text-align: justify;"><span class="text">В рамках Программ могут и должны формулироваться также<b>цели главных этапов работы (в рамках описания стратегий реализации Программы перехода).</b></span></p>
 
  <p style="text-align: justify;"><span class="text">В Программах рекомендуется<b>также ставить цели (обозначать продукты, результаты, эффекты, последствия) наиболее значимых программных мероприятий</b>, то есть наиболее конкретные и детализированные цели, достижение которых должно гарантировать и достижение более высоких и значимых целей Программ<b>(в разделе, посвященном конкретному плану программных мер, действий и мероприятий, обеспечивающих переход на профильное обучение)</b>.</span></p>
 
  <p style="text-align: justify;"><span class="text">Однако постановка перечисленных групп целей еще не делает Программы перехода стратегическими документами и<b>сохраняет нежелательную возможность превращения массового перехода на профильное обучение из мощного средства развития образования, повышения его качества, доступности и эффективности - в самодостаточное начинание, в самоцель</b>. Чтобы избежать этого, необходимо обозначить в Программах и<b>более отдаленные, глобально-стратегические (не в плане политической демагогии, а во вполне конкретном плане) цели массового перехода на профильное обучение.</b>Такие цели могут формулироваться в терминах качественных и (по возможности) количественных изменений («цели прироста») или желаемого состояния («цели состояния»)<b>в:</b></span></p>
 </ol>
 
<ul> 
  <p style="text-align: justify;"><span class="text"><b>качестве образования школьников</b>(включая и его мотивационные, профориентационные составляющие, развитие важнейших компетентностей);</span></p>
 
  <p style="text-align: justify;"><span class="text"><b>качестве образовательной системы</b>(с точки зрения роста ее многообразия, умножения возможностей в плане удовлетворения разных и все более дифференцированных потребностей учащихся, накопления в ней в ходе осуществления данного нововведения новых полезных инновационных компетентностей и навыков);</span></p>
 
  <p style="text-align: justify;"><span class="text">качестве связей между общим и профессиональным образованием, образованием и наукой, образованием и обществом, социальными партнерами;</span></p>
 
  <p style="text-align: justify;"><span class="text">качестве социальных последствий перехода на профильное обучение в виде улучшения общей ситуации на рынке труда и занятости, снижения остроты криминогенной ситуации<sup>18</sup>.</span></p>
 </ul>
 
<p style="text-align: justify;"><span class="text">Ясно, что именно эти цели должны являться точкой отсчета при стратегическом планировании массового перехода образовательных систем на профильное обучение.</span></p>
 
<p style="text-align: justify;"><span class="text">Предполагается, что главные цели общерегионального масштаба послужат ориентирами для проектирования целей перехода на профильное обучение на местах.</span></p>
 
<p style="text-align: justify;"><span class="text">Резюмируя сказанное, можно рекомендовать разработчикам Региональных Программ:</span></p>
 
<ol> 
  <p style="text-align: justify;"><span class="text">Положить в основу Программ<b>иерархию взаимосвязанных разноуровневых целей</b>- от стратегических целей, описывающих желаемое состояние региональной образовательной системы и ее главных результатов после освоения профильного обучения на старшей ступени общего образования, и непосредственных целей, связанных собственно с осуществлением в разумные сроки и при ограниченных ресурсах массового перехода на профильное обучение<b>(именно эти цели разумно формулировать в соответствующем, «целевом» разделе Региональной Программы)</b>- до целей, описывающих желаемое состояние отдельных сторон образовательной системы и результаты отдельных программных действий и мероприятий (<b>эти цели (результаты) могут формулироваться в последующих разделах Программы как результаты реализации главных инвариантных задач (направлений) перехода на профильное обучение, действий, мероприятий</b>).</span></p>
 
  <p style="text-align: justify;"><span class="text">Формулировать цели как&nbsp;<b>качественные, так и, по возможности, количественные, вместе с индикаторами их достижения.</b></span></p>
 
  <p style="text-align: justify;"><span class="text">Четко увязывать формулируемые&nbsp;<b>цели со сроками, отводимыми на их достижение и имеющимися ресурсами.</b></span></p>
 </ol>
 
