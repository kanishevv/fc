  <li>2. предложения по технологии разработки государственных требований к минимуму содержания и уровню подготовленности выпускников с дополнительной квалификацией &laquo;Педагог дополнительного образования&raquo;; </li>

  <li>3. предложения по формированию перечня и примерных программ по дисциплинам, способствующим формированию инновационной культуры выпускников вузов </li>
</ol>
 
