<p><strong>Статья 6. Язык (языки) обучения</strong></p>
 
<p>1. Общие вопросы языковой политики в области образования регулируются Законом РСФСР &quot;О языках народов РСФСР&quot;.</p>
 
<p>2. Граждане Российской Федерации имеют право на получение основного общего образования на родном языке, а также на выбор языка обучения в пределах возможностей, предоставляемых системой образования.</p>
 
<p>Право граждан на получение образования на родном языке обеспечивается созданием необходимого числа соответствующих образовательных учреждений, классов, групп, а также условий для их функционирования.</p>
 
<p>3. Язык (языки), на котором ведутся обучение и воспитание в образовательном учреждении, определяется учредителем (учредителями) образовательного учреждения и (или) уставом образовательного учреждения.</p>
 
<p>4. Государство в соответствии с международными договорами Российской Федерации оказывает содействие представителям народов Российской Федерации, проживающим вне ее территории, в получении ими основного общего образования на родном языке.</p>
 
<p>5. Во всех имеющих государственную аккредитацию образовательных учреждениях, за исключением дошкольных, изучение русского языка как государственного языка Российской Федерации регламентируется государственными образовательными стандартами.</p>
 
<p>6. Вопросы изучения государственных языков республик в составе Российской Федерации регулируются законодательством этих республик.</p>
 
<p>7. Государство оказывает содействие в подготовке специалистов для осуществления образовательного процесса на языках народов Российской Федерации, не имеющих своей государственности.</p>
 
