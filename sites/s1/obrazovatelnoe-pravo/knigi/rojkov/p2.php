</p>
<p style="text-align: justify;">
	<span class="text">3.7.Возмещать ущерб, причиненный Потребителем имуществу Исполнителя в соответствии с законодательством Российской Федера­ции.</span>
</p>
<p style="text-align: justify;">
	<span class="text">3.8.Обеспечить Потребителя за свой счет предметами, необходимы­ми для надлежащего исполнения Исполнителем обязательств по ока­занию дополнительных образовательных услуг, в количестве, соответ­ствующем возрасту и потребностям Потребителя.</span>
</p>
<p style="text-align: justify;">
