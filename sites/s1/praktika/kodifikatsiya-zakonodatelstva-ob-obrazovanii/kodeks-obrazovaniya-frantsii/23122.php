  <p><b>Статья L.231-12</b></p>
 
  <p> </p>
 
  <p>Если заинтересованное лицо может подтвердить, что оно было наказано за проступки, которые затем подпадают под закон об амнистии, или за правонарушения, аннулированные впоследствии в результате постановления пересмотра, необходимость срока после первого обращения отменяется, но не сроки подачи последующих обращений, если прошение отклонено.</p>
 
