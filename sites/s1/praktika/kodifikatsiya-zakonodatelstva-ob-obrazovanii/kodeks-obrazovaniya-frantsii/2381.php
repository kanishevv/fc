<p align="justify"> </p>
 
<p align="justify">&laquo;Ст. L.814-2. &ndash; Национальный Совет сельскохозяйственного образования может быть запрошен на предмет получения заключения по любому вопросу из его компетенции четвертью его членов или Правительством. Он дает в обязательном порядке заключение по любому предварительному проекту закона или декрета, касающихся сельскохозяйственного образования.</p>
 
<p align="justify"> </p>
 
<p align="justify">Он дает заключение по проекту национальной прогнозируемой схемы видов подготовок сельскохозяйственного образования. Эта схема, которая учитывается на пятилетний период министром сельского хозяйства. Руководство национальным аппаратом общего, технологического и профессионального образования и сельскохозяйственной профессиональной подготовки обеспечивается Государством на основе этой схемы.</p>
 
<p align="justify"> </p>
 
<p align="justify">В случае существенных изменений в течение периода действия схемы базовых данных, которые использовались для ее установления, эта схема может быть подвергнута частичным модификациям по предложению Национального Совета сельскохозяйственного образования.</p>
 
