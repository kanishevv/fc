<p><span class="textmar"><b>Статья L.911-5</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Неправоспособны руководить образовательным учреждением первой и второй ступени или учреждением технического образования, являются ли они публичными или частными, и быть там занятыми на работе в каком бы то ни было качестве:</span></p>
