<p style="text-align: justify;"><span class="text">После того как определены заинтересованные стороны, с ними проводятся консультации для выяснения стоящих перед ними проблем. Для этого необходим сбор информации об актуальном положении, возможно, посредством проведения обследования, а также ряда встреч или использования какой-либо иной формы оценки. По завершении сбора и обработки достаточного количества информации, наступает время встречи заинтересованных сторон, которая проводится в виде рабочей группы и имеет целью дальнейшую идентификацию проблем и начало процесса разработки проекта.</span></p>
 
<p style="text-align: justify;"><span class="text">Целью анализа заинтересованных сторон являются опреде­ление основных из них, оценка их интереса в осуществлении проекта или «выгоды» от него, а также того, каким образом их интересы сказываются на жизнеспособности и рискованности проекта. Для этого необходимо выяснить следующее:</span></p>
 
<ol start="6"> 
  <p style="text-align: justify;"><span class="text">интересы заинтересованных сторон в отношении подлежащих решению проблем, а также основные допущения, которые следует сделать об их участии в проекте</span></p>
 
  <p style="text-align: justify;"><span class="text">столкновение интересов заинтересованных сторон, сказывающееся на рискованности проекта</span></p>
 
  <p style="text-align: justify;"><span class="text">существующие и потенциальные отношения между заинтересованными сторонами и возможность их использования в целях расширения перспектив успеха проекта</span></p>
 
  <p style="text-align: justify;"><span class="text">адекватные формы участия в проекте различных заинтересованных сторон.</span></p>
 </ol>
 
<p style="text-align: justify;"><span class="text">Лучше всего проводить анализ заинтересованных сторон во время рабочей встречи типа «мозговой атаки», в которой участвуют основные заинтересованные стороны. Задача такой встречи - обсудить проблемы, с которыми заинтересованные стороны сталкиваются в области основного внимания проекта, например: повышение доли профессионально занятых социальной деятельностью, увеличение возможностей обучения на долгосрочных и краткосрочных курсах по социальному законодательству и социальной деятельности.</span></p>
 
