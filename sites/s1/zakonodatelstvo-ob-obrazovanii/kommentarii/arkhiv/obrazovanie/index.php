 <b>Статья 42. Особенности экономики среднего профессионального и высшего профессионального образования</b></p>
 
<p>1. Получение на конкурсной основе бесплатного среднего профессионального и высшего профессионального образования в государственных образовательных учреждениях среднего профессионального и высшего профессионального образования в пределах федерального компонента государственного образовательного стандарта осуществляется в федеральных государственных образовательных учреждениях и в образовательных учреждениях субъектов Российской Федерации в соответствии с заданиями (контрольными цифрами) по приему студентов на бесплатное обучение.</p>
 
<p>(п. 1 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>2. Объемы и структура приема обучающихся на обучение за счет средств федерального бюджета определяются в порядке, устанавливаемом Правительством Российской Федерации.</p>
 
<p>Объем и структура приема обучающихся за счет средств бюджетов субъектов Российской Федерации определяются в порядке, устанавливаемом органами исполнительной власти субъектов Российской Федерации.</p>
 
