<p><b>Статья 23. Среднее профессиональное образование</b></p>
 
<p>1. Среднее профессиональное образование имеет целью подготовку специалистов среднего звена, удовлетворение потребностей личности в углублении и расширении образования на базе основного общего, среднего (полного) общего или начального профессионального образования.</p>
 
<p>2. Граждане, имеющие среднее (полное) общее или начальное профессиональное образование соответствующего профиля, получают среднее профессиональное образование по сокращенным ускоренным программам.</p>
 
<p>3. Среднее профессиональное образование может быть получено в образовательных учреждениях среднего профессионального образования (средних специальных учебных заведениях) или на первой ступени образовательных учреждений высшего профессионального образования.</p>
 
<p>4. Образовательное учреждение среднего профессионального образования может реализовывать образовательные программы начального профессионального образования при наличии соответствующей лицензии.</p>
 
