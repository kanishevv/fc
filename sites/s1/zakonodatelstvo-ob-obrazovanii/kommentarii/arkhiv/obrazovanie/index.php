<p><b>Статья 7. Государственные образовательные стандарты</b></p>
 
<p>1. В Российской Федерации устанавливаются государственные образовательные стандарты, включающие в себя федеральный и региональный (национально-региональный) компоненты, а также компонент образовательного учреждения.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>Российская Федерация в лице федеральных органов государственной власти в пределах их компетенции устанавливает федеральные компоненты государственных образовательных стандартов, определяющие в обязательном порядке обязательный минимум содержания основных образовательных программ, максимальный объем учебной нагрузки обучающихся, требования к уровню подготовки выпускников.</p>
 
<p>Государственные образовательные стандарты могут устанавливаться по отдельным дополнительным образовательным программам в порядке, определяемом федеральным законом.</p>
 
<p>(абзац введен Федеральным законом от 25.06.2002 № 71-ФЗ)</p>
 
<p>2. При реализации образовательных программ для обучающихся с отклонениями в развитии могут быть установлены специальные государственные образовательные стандарты.</p>
 
<p>3. Порядок разработки, утверждения и введения государственных образовательных стандартов определяется Правительством Российской Федерации, за исключением случаев, предусмотренных законом.</p>
 
<p>4. Основные положения государственных образовательных стандартов начального общего, основного общего и среднего (полного) общего образования, порядок их разработки и утверждения устанавливаются федеральным законом.</p>
 
<p>(п. 4 в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>5. Государственные образовательные стандарты утверждаются не реже одного раза в десять лет.</p>
 
<p>(п. 5 в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>6. Государственные образовательные стандарты являются основой объективной оценки уровня образования и квалификации выпускников независимо от форм получения образования.</p>
 
