		 от 12 января 1999 г. № 40) с наименованием государственное учреждение «Центр образовательного законодательства Министерства общего и профессионального образования Российской Федерации».&nbsp;
	</p>
	<p>
		 В соответствии с приказом Министерства образования Российской Федерации от 11 сентября 2001 г. № 3123 «О переименовании государственного учреждения «Центр образовательного законодательства Министерства общего и профессионального образования Российской Федерации» государственное учреждение «Центр образовательного законодательства Министерства общего и профессионального образования Российской Федерации» переименовано в государственное учреждение «Центр образовательного законодательства Министерства образования Российской Федерации».&nbsp;
	</p>
	<p>
		 Согласно приказу Федерального агентства по образованию&nbsp;<br>
		 от 5 октября 2004 г. № 155 «О переименовании государственного учреждения «Центр образовательного законодательства Министерства образования Российской Федерации» государственное учреждение «Центр образовательного законодательства Министерства образования Российской Федерации» переименовано в государственное учреждение «Федеральный центр образовательного законодательства».&nbsp;
	</p>
	<p>
		 Приказом Федерального агентства по образованию от 17 октября 2005 г.&nbsp;<br>
		 № 1190 «О переименовании государственного учреждения «Федеральный центр образовательного законодательства» государственное учреждение «Федеральный центр образовательного законодательства» переименовано в Федеральное государственное учреждение «Федеральный центр образовательного законодательства».&nbsp;
	</p>
	<p>
		 Приказом Министерства образования и науки Российской Федерации&nbsp;<br>
		 от 5 апреля 2011 г. № 1456 Федеральное государственное учреждение «Федеральный центр образовательного законодательства» переименовано в федеральное государственное бюджетное учреждение «Федеральный центр образовательного законодательства», которое приказом Министерства образования и науки Российской Федерации от «14» июля 2016 г. № 864&nbsp;переименовано в федеральное государственное бюджетное научное учреждение «Федеральный центр образовательного законодательства».&nbsp;
	</p>
	<p>
		 1.4. Полное официальное наименование Центра на русском языке: федеральное государственное бюджетное научное учреждение «Федеральный центр образовательного законодательства».&nbsp;
	</p>
	<p>
		 Сокращенное наименование Центра: ФГБНУ «ФЦОЗ».&nbsp;
	</p>
	<p>
		 Полное наименование Центра на английском языке: «The Federal Centre of Education Legislation».&nbsp;
	</p>
	<p>
		 1.5. Место нахождения Центра: г. Москва.
	</p>
	<p>
		 1.6. Центр руководствуется в своей деятельности Конституцией Российской Федерации, федеральными законами, актами Президента Российской Федерации, Правительства Российской Федерации, Учредителя, другими нормативными правовыми актами и настоящим уставом.&nbsp;
	</p>
	<p>
		 1.7. Центр является юридическим лицом с момента его государственной регистрации.
	</p>
	<p>
		 Центр может от своего имени приобретать и осуществлять гражданские права и нести гражданские обязанности, быть истцом и ответчиком в суде.
	</p>
	<p>
		 Центр осуществляет операции с поступающими ему в соответствии с законодательством Российской Федерации средствами через лицевые счета, открываемые в территориальном органе Федерального казначейства в порядке, установленном законодательством Российской Федерации (за исключением случаев, установленных федеральными законами).
	</p>
	<p>
		 Центр имеет круглую печать со своим полным наименованием и изображением Государственного герба Российской Федерации, штамп, бланки и иные реквизиты юридического лица и товарный знак.
	</p>
	<p>
		 1.8.&nbsp;Центр вправе участвовать в других юридических лицах&nbsp;<br>
		 в установленном законодательством Российской Федерации порядке.
	</p>
	<p>
		 Центр вправе выступать учредителем (соучредителем) средств массовой информации, включая иностранные, в том числе научных журналов, обеспечивающих распространение накопленного научного и образовательного опыта работников Центра на российском и международном уровнях.
	</p>
	<p>
		 1.9.&nbsp;Центр проводит и обеспечивает необходимые мероприятия по мобилизационной подготовке, гражданской обороне, предупреждению и ликвидации чрезвычайных ситуаций в соответствии с законодательством Российской Федерации.
	</p>
	<p>
		 1.10.&nbsp;Центр осуществляет в соответствии с законодательством Российской Федерации работы по комплектованию, хранению, учету и использованию архивных документов, образовавшихся в процессе его деятельности.
	</p>
	<p>
		 1.11.&nbsp;Устав Центра, а также изменения, вносимые в него, утверждаются Учредителем и подлежат государственной регистрации в соответствии с законодательством Российской Федерации.
	</p>
	<p>
		 1.12.&nbsp;Центр обеспечивает защиту сведений, составляющих государственную тайну, в соответствии с возложенными на него задачами и в пределах своей компетенции. Ответственность за организацию защиты сведений, составляющих государственную тайну, в Центре возлагается на директора Центра. В Центре может быть создано структурное подразделение по защите государственной тайны, функции которого определяются директором Центра в соответствии с нормативными документами, утверждаемыми Правительством Российской Федерации, и с учетом специфики проводимых Центром работ с использованием сведений, составляющих государственную тайну. Защита государственной тайны является видом основной деятельности Центра.
	</p>
	<p>
		 Проведение работ, связанных с использованием сведений, составляющих государственную тайну, созданием средств защиты информации, а также осуществлением мероприятий и (или) оказанием услуг по защите государственной тайны, осуществляется Центром на основании лицензии, получаемой в порядке, установленном законодательством Российской Федерации.
	</p>
