	<span style="font-size: 16px;"><span class="text">Имущественные права на цифровые учебные ресурсы возни-кают с момента создания и не требуют выполнения дополнитель-ных формальностей. Основанием для закрепления имуществен-ных прав на является договор о создании цифрового учебного ресурса, договор об уступке прав на цифровой учебный ресурс, лицензионный договор об использовании цифрового учебного ресурса.</span></span>
</p>
<p>
</p>
<p style="text-align: justify;">
	<span style="font-size: 16px;"><span class="text">Правообладатель в течение срока действия авторского права может по своему желанию зарегистрировать программу для ЭВМ или базу данных в федеральном органе исполнительной власти по интеллектуальной собственности, за исключением программ для ЭВМ и баз данных, содержащих сведения, составляющих го-сударственную тайну. Правила оформления заявки на регистра-цию определяет федеральный орган исполнительной власти по интеллектуальной собственности. Действующие правила утвер-ждены приказом Роспатента от 25.02.2003 № 25 «О Правилах со-ставления, подачи и рассмотрения заявки на официальную реги-страцию программы для электронных вычислительных машин и заявки на официальную регистрацию базы данных». Порядок официальной регистрации программ для ЭВМ и баз данных, формы свидетельств об официальной регистрации, перечень ука-зываемых в них сведений, а также перечень сведений, публикуе-мых в официальном бюллетене, устанавливаются федеральным ор-ганом исполнительной власти по интеллектуальной собственности.</span></span>
</p>
<p style="text-align: justify;">
	<span style="font-size: 16px;"><span class="text">Договоры о полной или частичной передаче исключитель-ного права на программы для ЭВМ и базы данных могут быть зарегистрированы по соглашению сторон в федеральном органе исполнительной власти по интеллектуальной собственности. Сведения об изменении правообладателя на основе зарегистри-рованного договора вносятся в Реестр программ для ЭВМ или Реестр баз данных и публикуются в официальном бюллетене фе-дерального органа исполнительной власти по интеллектуальной собственности. Сведения, внесенные в Реестр программ для ЭВМ или Реестр баз данных, считаются достоверными до тех пор, пока не доказано обратное. Ответственность за достоверность указан-ных сведений несет заявитель.</span></span>
</p>
<p style="text-align: justify;">
	<span style="font-size: 16px;"><span class="text">За осуществление действий, связанных с официальной реги-страцией программ для ЭВМ и баз данных, договоров, и публи-кацию сведений взимаются регистрационные сборы. Размеры, сроки уплаты регистрационных сборов, а также основания для освобождения от их уплаты или уменьшения их размеров уста-навливаются Правительством Российской Федерации.</span></span>
</p>
<p style="text-align: justify;">
	<span style="font-size: 16px;"><span class="text">В соответствии с Положением по ведению бухгалтерского учета и бухгалтерской отчетности в Российской Федерации права на цифровые учебные ресурсы подлежат включению в нематери-альные активы организации, если они соответствуют следующим условиям: во-первых, объект нематериальных активов должен использоваться предприятием более 12 месяцев в производствен-ных целях или для управленческих нужд и при этом не иметь ма-териально-вещественную структуру; во-вторых, необходимо, чтобы данный объект можно было идентифицировать. Организа-ция должна иметь документы, подтверждающие существование самого актива и исключительного права на результаты интеллек-туальной деятельности (договор, свидетельство о регистрации программы для ЭВМ, свидетельство о регистрации базы данных и т. п.).</span></span>
