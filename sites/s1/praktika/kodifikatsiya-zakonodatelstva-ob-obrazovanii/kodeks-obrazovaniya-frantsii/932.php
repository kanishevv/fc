  <p> </p>
<span class="textmar"> </span>
  <p><span class="textmar">Служащие преподавательского состава учреждений технологического образования, обеспечивающие общее образование, нанимаются и готовятся на тех же условиях, что и преподаватели, призванные осуществлять преподавание такого же уровня в общеобразовательных учреждениях.</span></p>
