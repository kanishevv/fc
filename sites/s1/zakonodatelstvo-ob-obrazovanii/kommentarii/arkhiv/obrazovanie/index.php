 <b>Статья 34. Реорганизация и ликвидация образовательного учреждения</b></p>
 
<p>1. Образовательное учреждение может быть реорганизовано в иную образовательную организацию в соответствии с законодательством Российской Федерации.</p>
 
<p>Порядок реорганизации федеральных государственных образовательных учреждений устанавливается Правительством Российской Федерации, государственных образовательных учреждений, находящихся в ведении субъекта Российской Федерации, - органом исполнительной власти субъекта Российской Федерации, муниципальных образовательных учреждений - органом местного самоуправления.</p>
 
<p>(п. 1 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>2. При реорганизации (изменении организационно-правовой формы, статуса) образовательного учреждения его устав, лицензия и свидетельство о государственной аккредитации утрачивают силу.</p>
 
<p>3. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>4. Ликвидация образовательного учреждения может осуществляться:</p>
 
<p>в соответствии с законодательством Российской Федерации в установленном Правительством Российской Федерации порядке для федеральных государственных образовательных учреждений, органом исполнительной власти субъекта Российской Федерации - для государственных образовательных учреждений, находящихся в ведении субъекта Российской Федерации, органами местного самоуправления - для муниципальных образовательных учреждений;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>по решению суда в случае осуществления деятельности без надлежащей лицензии, либо деятельности, запрещенной законом, либо деятельности, не соответствующей его уставным целям.</p>
 
<p>5. Ликвидация сельского дошкольного образовательного или общеобразовательного учреждения допускается только с согласия схода жителей населенных пунктов, обслуживаемых данным учреждением.</p>
 
