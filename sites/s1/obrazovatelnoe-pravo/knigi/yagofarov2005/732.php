	</p>
 </li>
</ul>
<p style="text-align: justify;">
	<span class="text">Студенты очной формы обучения, получающие среднее профессиональное образование за счет бюджетных средств, в установленном порядке обеспечиваются стипендиями. Студенты вправе получать стипендии, выплачиваемые физическими или юридическими лицами, а также иные стипендии.</span>
