<p align="justify">1. Сбор научно-практического материала;</p>
 
<p align="justify">2. Анализ и исследование собранного материала;</p>
 
<p align="justify">3. Подготовка и опубликование научных статей;</p>
 
<p align="justify">4. Подготовка рукописи монографии по итогам проекта;</p>
 
<p align="justify">5. Подготовка отчета проведенного исследования. </p>
 
<p align="justify">В рамках данного проекта проведено исследование правовых аспектов международного сотрудничества независимых государств, образованных на постсоветском пространстве в денежно-кредитной сфере (исторический, организационный и правовой аспект) и выявлено реальное положение дел в данной сфере. Важнейшим направлением межгосударственного сотрудничества в денежно-кредитной сфере на постсоветском пространстве следует признать провозглашенные на межгосударственном уровне идеи о создании единой денежно-кредитной системы, единого платежного и валютного союза, включая введение единой валюты. Кроме того, проводится работа по унификации правового регулирования банковской деятельности и банковского надзора, например, Межпарламентской Ассамблеей СНГ разработаны и приняты модельные законодательные акты; координируются совместные действия по противодействию использования денежно-кредитных институтов в преступных целях; осуществляется сотрудничество и по другим направлениям. Однако, учитывая, что государства-участники не готовы даже на частичное ограничение своих суверенных полномочий в денежно-кредитной сфере, значительная часть интеграционных проектов осталась нереализованной. Реальная межгосударственная интеграция в денежно-кредитной сфере может быть реализована исключительно в рамках конституционных процедур </p>
 
