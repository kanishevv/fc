<p style="text-align: justify;"><span class="text">Законодательство об образовании за последние годы уверенно заняло место среди наиболее динамично обновляющихся отраслей российского законодательства. Частые и существенные изменения, затрагивающие основные источники образовательного права, позволяют утверждать, что реформа образовательного законодательства в России стремительно набирает обороты.</span></p>
 
<p style="text-align: justify;"><span class="text">Темпы и масштабы этой реформы, помимо главной ее цели – оптимизации и совершенствования нормативно-правового регулирования отношений в сфере образования – обусловлены также иными факторами: вовлеченностью России в Болонский процесс и участием в формировании единого европейского пространства; комплексным характером самого образовательного законодательства, находящегося в непосредственной зависимости от изменений, вносимых в гражданское, трудовое, бюджетное, административное и другие &laquo;смежные&raquo; отрасли российского законодательства; организационно-правовыми реформами в административной, бюджетной сфере, в сфере разграничения полномочий между Российской Федерацией, ее субъектами и муниципальными образованиями.</span></p>
 
<p style="text-align: justify;"><span class="text">Все сказанное об образовании в целом относится в полной мере к высшему и послевузовскому профессиональному образованию. В основной источник права, регулирующий комплекс отношений в этой сфере, – Федеральный закон от 22 августа 1996 года №125-ФЗ «О высшем и послевузовском профессиональном образовании» – было внесено более двадцати поправок с момента его принятия. При этом десять «корректирующих» законов было принято только в течение 2007 года. Комментируемый закон неоднократно «подправлялся» федеральными законами о федеральных бюджетах на соответствующие годы, а также вследствие принятия отдельных постановлений Конституционного Суда РФ. Если же к этому добавить многочисленные поправки, вносившиеся в Закон РФ от 10 июля 1992 года №3266-1 «Об образовании», а также так называемые косвенные изменения, которые не находят отражения непосредственно в текстах законодательных актов, но присутствуют в подзаконных актах, регулирующих отношения в сфере образования, то совершенно очевидным становится вывод о необходимости упорядочения и систематизации действующего образовательного законодательства.</span></p>
 
<p style="text-align: justify;"><span class="text">Актуальность систематизации законодательства об образовании вызвана и тем, что большинство вносившихся поправок основывалось на различных концептуальных положениях либо на идеях, отличных от тех подходов, которых придерживались разработчики первоначальных редакций законодательных актов. Такое «обновление» образовательного законодательства сегодня, очевидно, уже исчерпало себя: многие проблемы законодательного регулирования отношений в сфере образования в последнее время только усугубились; крайне низкую эффективность осуществляемого таким образом правового регулирования демонстрирует и правоприменительная практика.</span></p>
 
<p style="text-align: justify;"><span class="text">На данный момент обозначилось несколько подходов к решению проблемы систематизации образовательного законодательства. Подготовлено несколько проектов Образовательного кодекса. Параллельно с этим ведутся законопроектные работы по совершенствованию двух основных законодательных источников российского образовательного права – законов «Об образовании» и «О высшем и послевузовском профессиональном образовании». Существуют предложения по объединению двух существующих законов в единый федеральный закон об образовании в Российской Федерации. Не ставя перед собой задачу во вступительной статье рассуждать об оптимальных формах систематизации образовательного законодательства, выскажем лишь несколько соображений по этому вопросу.</span></p>
 
<p style="text-align: justify;"><span class="text">Представляется, что постановка вопроса о кодификации образовательного права на данный момент является преждевременной. Во-первых, не завершен процесс «вживания» России в Болонский процесс, завершить который намечено к 2010 году. Во-вторых, продолжается реформирование административного, бюджетного, гражданского, трудового и иных отраслей российского законодательства, сопряженных в образовательным правом. Как следствие – до конца не определились окончательные контуры управления образованием, не сформировалась организационная и нормативная база регулирования административных, экономических, трудовых отношений в сфере образования. Наконец, до сих пор не появилось концепции кодификации образовательного законодательства, а ведь именно правовая доктрина должна зажечь зеленый свет кодификации.</span></p>
 
<p style="text-align: justify;"><span class="text">Разработка и принятие образовательного кодекса – задача, скорее, среднесрочной перспективы, текущей же задачей является совершенствование действующего образовательного законодательства, его «актуализация», скорейшее приведение в соответствие с задачами государственной политики в сфере образования, международно-правовыми стандартами образования и вновь принятыми нормами «смежных» отраслей российского законодательства. Таким образом, задача на ближайшую перспективу – работа с действующим законодательством, его развитие и совершенствование, дополнение новыми нормами, нацеленными на обеспечение реализации национального проекта в сфере образования, модернизацию экономики образовательного комплекса (именно в этом направлении движутся разработчики законопроектов в сфере образования из Федерального центра образовательного законодательства и Государственного университета – Высшей школы экономики). Обновленное образовательное законодательство должно стать нормативно-правовой базой продолжающейся образовательной реформы, по завершении которой и может ставиться вопрос о кодификации образовательного права.</span></p>
 
<p style="text-align: justify;"><span class="text">До тех пор пока законодательство об образовании остается несистематизированным, сохраняется потребность в его комментировании. Эта потребность становится очевидной в период интенсивной корректировки законодательной базы. В этом смысле 2007 год оказался особенно «результативным» для образовательного законодательства. И дело тут вовсе не в одном количестве принимаемых новых законов. Важны направления, по которым ведется работа по совершенствованию образовательного законодательства, ведь именно они и позволяют говорить о реформаторском характере принятых поправок. В течение только одного (2007) года приняты федеральные законы о едином государственном экзамене, обязательности общего образования, двухуровневой системе высшего образования, интеграции образования и науки, разрешении работодателям вмешиваться в государственную политику в области профессионального образования. И это, не считая поправок, принимаемых практически ежегодно и нацеленных на обеспечение более высокой степени социальной защищенности обучающихся, о совершенствовании разграничения полномочий в области управления в сфере образования и т.д.</span></p>
 
