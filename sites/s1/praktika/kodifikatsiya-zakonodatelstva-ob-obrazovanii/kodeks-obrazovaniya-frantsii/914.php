<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.914-6</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">Всякое лицо, связанное с преподаванием или надзором частного образовательного учреждения первой или второй ступени или частного высшего образовательного учреждения, может по жалобе инспектора академии, ректора, представителя Государства в департаменте или прокуратуры быть вызвано в академический совет национального образования за серьезное нарушение в исполнении своих функций, за безнравственность или аморальность, или если его преподавание нарушает мораль и законы, или, если это касается профессора частного высшего образовательного учреждения, за серьезное нарушение порядка, вызванное или допущенное им на своём занятии.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
