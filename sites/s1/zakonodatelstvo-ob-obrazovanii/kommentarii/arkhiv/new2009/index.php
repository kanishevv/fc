<p><span class="text" style="text-align: justify;">Статья 27. Отношения собственности в системе высшего и послевузовского профессионального образования</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;"> 1. За высшим учебным заведением в целях обеспечения деятельности, предусмотренной его уставом, учредитель закрепляет на праве оперативного управления здания, сооружения, имущественные комплексы, оборудование, а также иное необходимое имущество потребительского, социального, культурного и иного назначения.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Негосударственные высшие учебные заведения могут быть собственниками имущества в соответствии с законодательством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 2 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. За высшим учебным заведением закрепляются в постоянное (бессрочное) пользование выделенные ему в установленном порядке земельные участки.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Высшие учебные заведения вправе выступать в качестве арендатора и (или) арендодателя имущества.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Высшие учебные заведения с согласия учредителя (собственника) и на основании договоров имеют право предоставлять научным организациям в пользование движимое и недвижимое имущество, а также использовать на основании договоров движимое и недвижимое имущество, принадлежащее научным организациям на праве собственности, оперативного управления или хозяйственного ведения. Между такими государственными некоммерческими организациями указанные отношения могут осуществляться на безвозмездной основе.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 01.12.2007 № 308-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Высшие учебные заведения с согласия учредителя (собственника) или самостоятельно, если они вправе распоряжаться соответствующим имуществом самостоятельно, на основании договора между высшим учебным заведением и медицинским учреждением имеют право предоставлять медицинским учреждениям в пользование движимое и недвижимое имущество для медицинского обслуживания обучающихся и работников высших учебных заведений и прохождения ими медицинского обследования. Между такими государственными некоммерческими организациями и (или) муниципальными некоммерческими организациями указанные отношения могут осуществляться на безвозмездной основе.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 25.12.2008 № 286-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Использование высшим учебным заведением природных ресурсов осуществляется в порядке, установленном законодательством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">6. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
