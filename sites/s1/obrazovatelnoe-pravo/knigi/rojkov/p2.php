</p>
<p style="text-align: justify;">
	<span class="text">4.3.Соблюдать учебную дисциплину и общепринятые нормы поведе­ния, в частности, проявлять уважение к педагогам, администрации и техническому персоналу Исполнителя и другим обучающимся, не по­сягать на их честь и достоинство.</span>
</p>
<p style="text-align: justify;">
