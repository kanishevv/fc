<div align="justify"><b>Статья L.231-10</b> 
  <p> </p>
 
  <p>Участники публичного или частного образования могут быть освобождены от санкций лишения прав или профессиональной непригодности, явившихся результатом решений, принятых относительно лишения их прав преподавать или приостановке их права руководить частным образовательным учреждением.</p>
 
