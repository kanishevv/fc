<p align="JUSTIFY"><span class="text">5) решает в соответствии с законодательством Российской Федерации о государственной службе вопросы, связанные с прохождением федеральной государственной службы в Агентстве;</span></p>

<p align="JUSTIFY"><span class="text">6) утверждает структуру и штатное расписание аппарата Агентства в пределах установленных Правительством РФ численности и фонда оплаты труда работников, смету расходов на его содержание в пределах утвержденных на соответствующий период ассигнований, предусмотренных в федеральном бюджете;</span></p>

<p align="JUSTIFY"><span class="text">7) в установленном порядке назначает на должность и освобождает от должности руководителей находящихся в ведении Агентства организаций, заключает, изменяет, расторгает с указанными руководителями трудовые договоры;</span></p>

