<p>(пп. 17 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>18) ежегодное утверждение на основе экспертизы федеральных перечней учебников, рекомендованных (допущенных) к использованию в образовательном процессе в образовательных учреждениях, реализующих образовательные программы общего образования и имеющих государственную аккредитацию;</p>
 
<p>(пп. 18 введен Федеральным законом от 25.06.2002 № 71-ФЗ)</p>
 
<p>19) информационное и научно-методическое обеспечение системы образования, разработка в пределах своей компетенции базисных учебных планов и примерных программ курсов, дисциплин, учебной литературы и учебных пособий. Создание единой для Российской Федерации системы информации в области образования;</p>
 
<p>(в ред. Федеральных законов от 25.06.2002 № 71-ФЗ, от 22.08.2004 № 122-ФЗ)</p>
 
