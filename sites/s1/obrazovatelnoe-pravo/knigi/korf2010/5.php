?><p style="text-align: justify;">
	Большую частьXXвека федеральная политика в сфере высшего образования, а также контроль и надзор за деятельностью высших учебных заведений в США определялись Департаментом здравоохранения, высшего образования и социального обеспечения. Так было вплоть до 1979 года, когда впервые был учрежден Департамент образования и начался новый этап в развитии системы управления высшим образованием в США. В полной мере это относится и к системе управления публичными расходами на образование, поскольку по представлению именно этого департамента, в котором создана собственная «бюджетная служба», Конгресс США утверждает, в каком объеме и какие бюджетные целевые программы в сфере образования требуется финансировать в предстоящем финансовом году.
</p>
<p style="text-align: justify;">
	<span class="text">Участие федерального бюджета США в финансировании высших учебных заведений реализуется по следующим основным направлениям:</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – прямая финансовая помощь студентам;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – финансовая помощь, направленная на укрепление финансового благополучия высших учебных заведений;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – законодательное установление на федеральном уровне налоговых льгот высшим учебным заведениям;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – финансовая помощь, направленная на улучшение подготовки в вузах педагогических кадров;</span>
</p>
<p style="text-align: justify;">
	<span class="text"> – содержание специальных целевых научно-исследо­ватель­ских фондов США (финансовая поддержка научных исследований в определенных отраслях науки и техники, содержание государственных научных центров фундаментальных исследований).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Примерно треть университетов в США являются частными учебными заведениями, поэтому экономическая, стимулирующая и перераспределительная функции публичных финансов для высшего образования в США имеют особое значение. При этом основными проводниками бюджетных средств в данном случае выступают региональные, а не местные органы власти, что объясняется меньшим количеством высших учебных заведений по сравнению с количеством школ.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Федеральные ассигнования выделяются исключительно аккредитованным вузам. Именно в этом контексте рассматривается институт аккредитации высшего учебного заведения, анализируются правовые основы использования основных каналов федеральной финансовой помощи в сфере высшего образования: институт налоговых льгот и освобождений, государственный образовательный кредит, федеральные гранты и ваучерные программы.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Прямое финансирование высших учебных заведений из федерального бюджета США – явление сравнительно редкое, и в 2005 году существовало всего две целевых программы: поддержки вузов, в которых учатся представители расовых меньшинств, и программа развития вузов, в которых большинство студентов составляют испаноговорящие[1].</span>
</p>
<p style="text-align: justify;">
	<span class="text">Удельный вес расходов федерального бюджета в общих затратах на все виды образования невелик и в 2008 году, по данным Департамента образования США,[2]не превышал 12%. Это означает, что основную нагрузку по финансовому обеспечению системы высшего образования несут штаты, местные власти и частные лица.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Вместе с тем, федеральные финансовые ресурсы в определенных отраслях науки жизненно необходимы, особенно там, где решаются общегосударственные задачи[3]либо имеются капиталоемкие проекты, из которых выгода конкретного штата, муниципального образования, корпорации не очевидна.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Федеральная финансовая помощь вузам реализуется в следующих основных формах[4]:</span>
</p>
<p style="text-align: justify;">
	<span class="text">I.Прямое финансирование:</span>
</p>
<p style="text-align: justify;">
	<span class="text">1)договорной бюджет,</span>
</p>
<p style="text-align: justify;">
	<span class="text">2)финансирование по формуле,</span>
</p>
<p style="text-align: justify;">
	<span class="text">3)конкурсное финансирование,</span>
</p>
<p style="text-align: justify;">
	<span class="text">4)софинансирование.</span>
</p>
<p style="text-align: justify;">
	<span class="text">II.Опосредованное финансирование:</span>
</p>
<p style="text-align: justify;">
	<span class="text">1)образовательный кредит,</span>
</p>
<p style="text-align: justify;">
	<span class="text">2)стипендии,</span>
</p>
<p style="text-align: justify;">
	<span class="text">3)предоставление налоговых льгот, скидок и освобождений,</span>
</p>
<p style="text-align: justify;">
	<span class="text">4)исследовательские гранты.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Образовательный кредит является разновидностью финансовой помощи, предоставляемой получателю на возвратной и возмездной основе, в отличие от стипендий, которые предоставляются на безвозвратной и безвозмездной основе.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Существует три главных категории образовательных займов: студенческий заем (например, заем Стаффорда и Перкинса), родительский заем (например, займы PLUS) и частные студенческие займы, также именуемые альтернативными студенческими займами. Четвертый тип образовательного займа – консолидирующий заем, позволяющий заемщику соединить все прочие займы в один для упрощения выплат, а также уменьшения или фиксации процентной ставки по отдельным кредитам.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Недавнее нововведение, которое может рассматриваться как пятый тип образовательного займа – частные образовательные займы между физическими лицами, по субъектному составу оказывается за рамками предмета настоящего исследования.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Федеральное законодательство устанавливает ограничение на максимальную ставку процента, которую вправе устанавливать займодатель в отношении образовательных займов, гарантированных федеральным правительством. Однако займодатель вправе выдать образовательный кредит под более низкий процент. Многие займодатели предлагают различные планы скидок по студенческим кредитам для привлечения заемщиков.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Лишь немногие студенты могут позволить себе оплачивать обучение в колледже без привлечения дополнительного финансирования. Две трети (65,7%) от общего числа студентов, окончивших 4-летние колледжи, имеют определенные задолженности по образовательным кредитам (исключая займы PLUS, но включая Стаффорд, Перкинс, государственные кредиты, кредиты колледжей и частных лиц) в среднем в сумме 19 237 долл. США на одного студента согласно данным, приведенным в Государственном исследовании финансовой помощи студентам 2003–2004 гг., обучающимся по программам после­среднего образования.[5]Средняя сумма задолженности студента, не закончившего обучение, составляет 17 120 долл. США, четверть всех студентов получили кредит на сумму, превышающую 24 936 долл. США, задолженность одной десятой части студентов превысила сумму в 35 213 долл. США. Число студентов-заемщиков по федеральному образовательному кредиту (без учета займовPLUS) составляет 62,2%, в среднем – 17 036 долл. США на одного студента[6].</span>
</p>
