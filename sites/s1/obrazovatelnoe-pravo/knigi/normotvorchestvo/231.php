<p align="center" style="text-align: justify;"><span class="text">Рис. 5.Инновационный учебно-научно-производственный комплекс</span></p>
 
<p style="text-align: justify;"><span class="text">
    <br>
  </span><span class="text">Практическая реализация модели построения университетского комплекса представлена сегодня в Уральском государственном техническом университете - УПИ развитием ряда взаимодополняющих проектов интеграции.</span></p>
 
<p style="text-align: justify;"><span class="text"><b>А) Объединение с профильными колледжами и средними специальными учреждениями</b>вписывается в известную<b><i>схему образовательных комплексов,</i></b>где выстраивается<b><i>схема непрерывного образования «школа-техникум-вуз»</i></b>.</span></p>
 
<p style="text-align: justify;"><span class="text">Образовательные комплексы решают следующие основные задачи:</span></p>
 
<p style="text-align: justify;"><span class="text">•создание необходимых условий дляреализации концепции непрерывного образования и преемственных образовательных программ между ступенями общего и профессионального образованиявсоответствующих профессионально-образовательных центрах;</span></p>
 
<p style="text-align: justify;"><span class="text">•создание интегрированных учебных программ, в том числе экспериментальных планов обучения учащихся лицейских (гимназических) классов, учреждений начального профессионального образования, студентов учреждений среднего профессионального образования и УГТУ-УПИ, учитывающих интересы конкретных потребителей специалистов: производственных предприятий всех форм собственности, научно-исследовательских организаций, учреждений образования и социальной сферы;</span></p>
 
<p style="text-align: justify;"><span class="text">•развитие интеграции учебного процесса с производством;</span></p>
 
<p style="text-align: justify;"><span class="text">•привлечение профессорско-преподавательского состава, студентов, магистров, аспирантов, докторантов к проведению научно-педагогической деятельности в профессионально-образовательных центрах УК;</span></p>
 
<p style="text-align: justify;"><span class="text">•привлечение представителей работодателей к непосредственному участию в осуществлении и совершенствовании учебно-методического обеспечения учебного процесса;</span></p>
 
<p style="text-align: justify;"><span class="text">•обеспечение использования материально-технической базы сторон для проведения учебного процесса и осуществления совместных мероприятий;</span></p>
 
<p style="text-align: justify;"><span class="text">•организация совместной инновационной деятельности.</span></p>
 
<p style="text-align: justify;"><span class="text"><b>Б)</b><b>Взаимодействие с профильными академическими институтами Уральского отделения российской академии наук</b>развивается по линии организации<b><i>научно-образовательных центров – совместных структурных подразделений университета и академических институтов</i></b>.</span></p>
 
<p style="text-align: justify;"><span class="text">Целью деятельности НОЦ является повышение уровня образовательной и научной деятельности за счет объединения педагогического и научного потенциала академического института и вуза. Для достижения этой цели создаются условия (организационные, финансовые, материальные, информационные) для подготовки высококвалифицированных специалистов на основе тесного сотрудничества, интеграции и координации планов научной и образовательной деятельности, поддержки ведущих научных школ, проведения научных исследований в области фундаментальных наук, трансформации результатов фундаментальных исследований в новые технологии, подготовки специалистов, способных реализовать эти технологии.</span></p>
 
<p style="text-align: justify;"><span class="text"><b>В)</b><b>Примером интеграции университета с промышленными предприятиями</b>является проект целевой подготовки специалистов для нужд промышленных предприятий региона. С крупными предприятиями и промышленными объединениями заключены долговременные соглашения, созданы и развиваются филиалы кафедр, совместные лаборатории и образовательные центры. Целевая подготовка дает предприятию возможность пополнить кадровый потенциал молодыми специалистами, обладающими профессиональными навыками и умениями, соответствующими требованиям предприятия, обладающими корпоративной культурой, готовыми без длительного адаптационного периода включиться в процесс управления производством.</span></p>
 
<p style="text-align: justify;"><span class="text"><b><i>В качестве организационной модели выбран вариант создания образовательно-производственных инновационных комплексов,</i></b>которые обеспечивают непрерывный цикл подготовки специалистов. Модель позволяет достичь наиболее развитых связей высшего учебного заведения и предприятия. Участие преподавателей университета в такой деятельности обогащает их новыми методическими идеями, стимулирует создание новых педагогических технологий, больше внимания уделяется связи содержания курсов с реальным производством. Студент становится активным соучастником процесса обучения, приобретает опыт решения реальных производственных задач, работы в команде, ответственности за принимаемые решения.</span></p>
 
<p style="text-align: justify;"><span class="text">В целом, опыт построения отдельных элементов организационных структур в УГТУ-УПИ позволяет с уверенностью говорить о целесообразности объединения их в общую систему – университет интеграционного типа, развивающийся как учебно-научно-производственный комплекс.</span></p>
 
<p style="text-align: justify;"><span class="text">Примеров взаимодействия субъектов образования только в Екатеринбурге можно насчитать множество. Интересным является опыт создания университетских комплексов Уральского горно-геологического университета и Уральской сельскохозяйственной академии. Этот опыт полезно и необходимо изучать, чтобы на уже отработанных механизмах выходить на новый виток развития.</span></p>
 
<p style="text-align: justify;"><span class="text">Таким образом:</span></p>
 
<p style="text-align: justify;"><span class="text">- при решении вопросов, связанных с перспективами развития региональных систем образования, следует учитывать сложившуюся практику, прогнозы социально-экономического развития регионов и мировой опыт интеграционных процессов, закрепленных в нормативных, научно-исследовательских и программных документах;</span></p>
 
<p style="text-align: justify;"><span class="text">- необходимо объединить усилия органов исполнительной и законодательной власти субъектов Российской Федерации, органов местного самоуправления с образовательными учреждениями, предприятиями и организациями различных форм собственности, выступающими заказчиками на подготовку специалистов;</span></p>
 
<p style="text-align: justify;"><span class="text">- актуально создание различных моделей региональных образовательных комплексов, механизмов их нормативного закрепления и вариантов практического воплощения в деятельности систем образования различных иерархических уровней.</span></p>
 
<p align="center" style="text-align: justify;"><b><span class="text">
      <br>
    </span><span class="text" style="text-align: center;">Рынок образовательных услуг в Уральском федеральном округе</span></b></p>
 
<p align="center" style="text-align: justify;"><b><span class="text" style="text-align: center;">и его влияние на степень сформированности единого образовательного</span></b></p>
 
<p align="center" style="text-align: justify;"><b><span class="text" style="text-align: center;">пространства федерального округа</span><span class="text">
      <br>
    </span></b></p>
 
<p style="text-align: justify;"><span class="text">Условия глобальной конкуренции требуют усиления практической направленности образования, а это в первую очередь означает повышение требований к профессиональному образованию в широком смысле этого слова.</span></p>
 
<p style="text-align: justify;"><span class="text">Задача развития профессионального образования в Уральском федеральном округе является приоритетной сегодня не только по требованию изменившегося законодательства, рынка труда, работодателей и самих работников, но и по требованиям изменившегося содержания и образовательных технологий, инновационных и интеграционных процессов, происходящих в условиях социально-экономических реформ.</span></p>
 
<p style="text-align: justify;"><span class="text">Как было показано в предыдущем параграфе настоящей работы, происходит несбалансированное изменение сети образовательных учреждений. Закрываются общеобразовательные школы на селе и расширяется сеть негосударственных учреждений и филиалов. Только за 2003-2004 учебный год сократилось общее число образовательных учреждений в УрФО на 414 единиц.</span></p>
 
<p style="text-align: justify;"><span class="text">Это тревожные тенденции и они требуют пристального внимания всех заинтересованных лиц.</span></p>
 
<p style="text-align: justify;"><span class="text">Отсюда, несмотря на то, что ситуация в образовании в целом меняется, и многие негативные тенденции удалось переломить, в целом можно констатировать, что действующая система образования в Уральском федеральном округе существенно отстает от процессов, происходящих в обществе.</span></p>
 
<p style="text-align: justify;"><span class="text">Сегодня со всей очевидностью становится ясно, что управление профессиональным образованием и вся его деятельность нуждается в серьезном реформировании. Несоответствие профессионально-квалификационной структуры выпускаемых специалистов потребностям экономики не только ложится тяжелым бременем на бюджет страны, но и обрекает молодежь на безрадостную перспективу пополнить ряды безработных, лишает молодое поколение их социальных прав и гарантий. При этом известно, что уровень образования является одним из главных факторов, определяющих позиции работника на рынке труда. Чем больше запас накопленного человеческого капитала, тем меньше риск попадания в ряды «лишних людей». Эта закономерность отчетливо прослеживается и на российском рынке труда. Так, уровень общей безработицы среди лиц с высшим образованием значительно ниже. Напротив, среди лиц с низким образованием (не закончивших среднюю школу) он всегда выше. Та же тенденция наблюдается и в Уральском федеральном округе (табл. 1,2).[38]</span></p>
 
