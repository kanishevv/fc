<p align="justify"><b>Статья L.231-4</b></p>
 
<p align="justify"> </p>
 
<p align="justify">Каждый министр может с согласия министра, ответственного за образование, назначить представителя, который будет допущен в Высший Совет образования, чтобы присутствовать с правом совещательного голоса на заседаниях, которые интересуют его департамент.</p>
 
