<p><span class="text" style="text-align: justify;">Статья 9. Виды и наименования высших учебных заведений</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. В Российской Федерации устанавливаются следующие виды высших учебных заведений: университет, академия, институт.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Университет - высшее учебное заведение, которое:</span></p>
 
<p><span class="text" style="text-align: justify;">реализует образовательные программы высшего и послевузовского профессионального образования по широкому спектру направлений подготовки (специальностей);</span></p>
 
<p><span class="text" style="text-align: justify;">осуществляет подготовку, переподготовку и (или) повышение квалификации работников высшей квалификации, научных и научно-педагогических работников;</span></p>
 
<p><span class="text" style="text-align: justify;">выполняет фундаментальные и прикладные научные исследования по широкому спектру наук;</span></p>
 
<p><span class="text" style="text-align: justify;">является ведущим научным и методическим центром в областях своей деятельности.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Академия - высшее учебное заведение, которое:</span></p>
 
<p><span class="text" style="text-align: justify;">реализует образовательные программы высшего и послевузовского профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">осуществляет подготовку, переподготовку и (или) повышение квалификации работников высшей квалификации для определенной области научной и научно-педагогической деятельности;</span></p>
 
<p><span class="text" style="text-align: justify;">выполняет фундаментальные и прикладные научные исследования преимущественно в одной из областей науки или культуры;</span></p>
 
<p><span class="text" style="text-align: justify;">является ведущим научным и методическим центром в области своей деятельности.</span></p>
 
<p><span class="text" style="text-align: justify;">4. Институт - высшее учебное заведение, которое:</span></p>
 
<p><span class="text" style="text-align: justify;">реализует образовательные программы высшего профессионального образования, а также, как правило, образовательные программы послевузовского профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">осуществляет подготовку, переподготовку и (или) повышение квалификации работников для определенной области профессиональной деятельности;</span></p>
 
<p><span class="text" style="text-align: justify;">ведет фундаментальные и (или) прикладные научные исследования.</span></p>
 
<p><span class="text" style="text-align: justify;">5. Статус высшего учебного заведения определяется в зависимости от его вида, организационно-правовой формы, наличия или отсутствия государственной аккредитации. Статус высшего учебного заведения включается в его наименование.</span></p>
 
<p><span class="text" style="text-align: justify;">6. Наименование высшего учебного заведения устанавливается при создании и изменяется в обязательном порядке при изменении его статуса. В случае, если в наименовании высшего учебного заведения употребляется специальное название (консерватория, высшее училище и другие названия), наряду с ним указывается вид высшего учебного заведения.</span></p>
 
