<p style="text-align: justify;"><span class="text" style="text-align: center;"><b>Региональная стратегия перехода на профильное обучение</b></span></p>
 
<p style="text-align: justify;"><span class="text" style="text-align: center;"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">Для достижения поставленных стратегических целей в регионе реализуется стратегия управляемого перехода на профильное обучение, предполагающая&nbsp;<b>три взаимосвязанных логических этапа работ, частично перекрывающимся по срокам осуществления</b>.</span></p>
 
<p style="text-align: justify;"><span class="text"><b>На первом этапе (… 200... г. - …200... г.)</b>в качестве главной задачи выделяется&nbsp;<b>массированное создание и накопление всех видов необходимых ресурсов (прежде всего - нормативно-право­вых, человеческих-кадровых, программно-методических) и объединение всех сил для подготовки к переходу на профильное обучение.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Одновременно на основе опыта регионов-участников федерального эксперимента по профильному обучению (прежде всего - опыта ….областей, наиболее близких к нашему региону по своим особенностям) начинается экспериментальная апробация моделей перехода на предпрофильную подготовку и профильное обучение в ограниченном числе … муниципальных образований и ….обра­зовательных учреждений. В ходе этой работы усиленно формируется общественное мнение в пользу введения профильного обучения, а в профессиональном сообществе педагогов активно обсуждается необходимость существенной трансформации подготовки и работы учителя и команд учителей в условиях профильного обучения.</span></p>
 
<p style="text-align: justify;"><span class="text"><b>На втором этапе (… 200. г. - …200. г.)</b>в качестве главной задачи выдвигается<b>задача создания критической массы готовности к переходу на профильное обучение, когда региональная образовательная система окажется в состоянии по преимуществу самостоятельно производить и воспроизводить необходимые ресурсы для осуществления профильного обучения. Работа по введению профильного обучения охватит в той или иной степени все муниципальные образования области.</b></span></p>
 
<p style="text-align: justify;"><span class="text">В ситуации, когда не удастся компенсировать ряд рисков, связанных с недополучением некоторых необходимых ресурсов, возможна определенная коррекция темпов и продолжительности первых двух этапов в сторону увеличения с тем, чтобы обеспечить максимальные гарантии успешности массового перехода на профильное обучение</span></p>
 
<p style="text-align: justify;"><span class="text">Соответственно, в случае более высоких темпов создания готовности к массовому переходу на профильное обучение, возможна коррекция темпов работы в противоположном направлении, но - без искусственного форсирования процесса.</span></p>
 
<p style="text-align: justify;"><span class="text"><b>На третьем этапе (… 200... г. - …200... г.)</b>на основе заделов двух предыдущих этапов в повестку дня становится<b>задача обеспечения массового перехода на профильное обучение с выходом на показатели, зафиксированные в цели Программы (см. выше). На финише этого этапа региональная образовательная система накопит опыт, значимый для диссеминации (распространения) в другие регионы РФ.</b></span></p>
 
<p style="text-align: justify;"><span class="text">Считая неэффективной для нашей области моновариантную модель стратегии перехода на профильное обучение, разработчики Программы предлагают<b>разные темпы и способы решения поставленной задачи</b>для разных:</span></p>
 
<p style="text-align: justify;"><span class="text"><b>типов поселений</b>(сверхкрупный город, крупный город, малый город, сельские поселения) - в частности, для малых городов и сельских поселений особое значение приобретают дистанционные формы поддержки педагогов и учащихся из крупных учебно-методических центров; темпы перехода на профильное обучение в сельской местности будут несколько ниже;</span></p>
 
<p style="text-align: justify;"><span class="text"><b>групп муниципальных образований -</b>в частности, в качестве авангардной группы, которая раньше других муниципальных образований начнет работу по введению профильного обучения, предлагается выделить следующие муниципальные образования (список прилагается). На втором и третьем этапах реализации стратегии перехода эти муниципальные образования будут играть роль ресурсных центров по отношению к другим муниципалитетам.</span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">
    <br>
  </span></p>
 
<p style="text-align: justify;"><span class="text">Стратегия организационно-управленческой деятельности по обеспечению перехода на профильное обучение предполагает:</span></p>
 
<p style="text-align: justify;"><span class="text">государственно-общественный, вневедомственный, коллегиальный и командный (работа постоянных и временных, гибких управленческих команд) тип управления;</span></p>
 
<p style="text-align: justify;"><span class="text">использование подходов стратегического, проектного, программно-целевого управления;</span></p>
 
<p style="text-align: justify;"><span class="text">понимание управления введением профильного обучения как частного случая управления инновационными процессами системного, комплексного, масштабного и радикального характера;</span></p>
 
<p style="text-align: justify;"><span class="text">наукоемкость работы, опору в принятии управленческих решений на специально организуемые практико-ориентированные исследования и анализ своего опыта и опыта других регионов и стран;</span></p>
 
<p style="text-align: justify;"><span class="text">открытость работы по подготовке и реализации Программы и широкое вовлечение в эти процессы общественности, властных структур, бизнеса, широких слоев населения.</span></p>
 
<p style="text-align: justify;"><span class="text">Собственно образовательная стратегия перехода на профильное обучение в нашей области предполагает:</span></p>
 
<p style="text-align: justify;"><span class="text">полное, комплексное и глубокое освоение всех сторон и направлений концепции профильного обучения при приоритетном внимании к разработке индивидуальных учебных планов учащихся, элективных курсов, развитию профессиональной ориентации;</span></p>
 
<p style="text-align: justify;"><span class="text">переход на профильное обучение не «для учащихся», а «вместе с учащимися»;</span></p>
 
<p style="text-align: justify;"><span class="text">коллективную рефлексию, критическую самооценку рождающегося опыта и поддержку его жизнеспособных моментов;</span></p>
 
<p style="text-align: justify;"><span class="text">становление в регионе локальных образовательных сетей;</span></p>
 
<p style="text-align: justify;"><span class="text">широкое использование возможностей Интернета и дистанционных образовательных технологий;</span></p>
 
<p style="text-align: justify;"><span class="text">опору на опыт и разработки передовых педагогических, методических и научных коллективов и авторов;</span></p>
 
<p style="text-align: justify;"><span class="text">сопряженные изменения образовательных программ и технологий - с одной стороны и образовательной среды, взаимодействия учителей и учащихся, общего уклада школьной жизни - с другой стороны;</span></p>
 
<p style="text-align: justify;"><span class="text">особое внимание к реальным результатам образования, его качеству при высокой мотивации обучения.</span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;">Стратегия ресурсообеспечивающей деятельности для массового перехода на профильное обучение предполагает в наших условиях:</p>
 
<p style="text-align: justify;"><span class="text">максимально возможное опережение создания и наполнения ресурсной базы по отношению к запуску собственно профильного обучения;</span></p>
 
<p style="text-align: justify;"><span class="text">приоритетное внимание опережающей разъяснительной и концептуально-мировоззренческой работы с управленческими, методическими и педагогическими кадрами, от позиции которой успех профильного обучения зависит в максимальной степени;</span></p>
 
<p style="text-align: justify;"><span class="text">опережающее создание и запуск системы кадрового обеспечения Программы через стимулирование и привлечение к работе системы среднего, высшего и дополнительного профессионального образования;</span></p>
 
<p style="text-align: justify;"><span class="text">активизацию связей с научно-исследовательскими структурами и организациями, специализирующимися на оказании профессиональных услуг (психологическое, социологическое и организационно-управленческое консультирование, реклама и PR, информационные технологии, профориентация и профконсультация и др.);</span></p>
 
<p style="text-align: justify;"><span class="text">определение «цены вопроса», необходимых финансовых затрат на любые программные действия и своевременный поиск источников их покрытия, увязывание введения профильного обучения с освоением новых механизмов финансирования образования и оплаты труда педагогов;</span></p>
 
<p style="text-align: justify;"><span class="text">выбор наиболее перспективных поставщиков программно-методического и научно-консультационного сопровождения Программы при сочетании необходимой конкурентности и свободы выбора с разумной централизацией и экономией ресурсов<sup>19</sup>.</span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text" style="text-align: center;"><b>3.5.4. Конкретный план программных мер, действий и мероприятий, обеспечивающих переход образовательной системы&nbsp;</b><b style="font-size: 13px;">на профильное обучение и достижение поставленных целей</b></span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">Начиная с данного фрагмента Программ, идет изложение<b>конкретного</b>(тактического и оперативного)<b>плана</b>реализации стратегии перехода образовательного учреждения, муниципальной, региональной образовательных систем на профильное обучение.</span></p>
 
<p style="text-align: justify;"><span class="text">Описание плана осуществляется сначала<b>в содержательно-тематической (план действий), а затем во временной (календарный план, план-график) логике.</b></span></p>
 
<p style="text-align: justify;"><span class="text">В первом плане описание действий строится вокруг трех взаимосвязанных групп выделенных ранее главных задач перехода на профильное обучение - собственно образовательных задач (1), задач ресурсного обеспечения перехода (2) и задач управления реализацией Программы, управления переходом на профильное обучение (3).</span></p>
 
<p style="text-align: justify;"><span class="text">Смысл этого документа - четкое планирование всего комплекса действий, обеспечивающих выход на запланированные ранее контрольные количественные показатели перехода образовательной системы на профильное обучение в заданные сроки и при достаточном качестве образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Как уже отмечалось выше, конкретный план действий не может быть разработан сколько-нибудь точно сразу на весь период действия программы (3-4 года); предполагается, что такой план строится на один год с последующей конкретизацией.</span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text"><b>Меры, действия и мероприятия по изменению образования&nbsp;</b><b style="font-size: 13px;">на старшей ступени школы</b></span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p style="text-align: justify;"><span class="text">Содержание фрагмента «монтируется» вокруг главных линий и составляющих перехода на профильное обучение как образовательного нововведения, например, таких как:</span></p>
 
<ol> 
  <p style="text-align: justify;"><span class="text">Проектирование и формирование локальных образовательных сетей для организации предпрофильной подготовки в основной школе и профильного обучения на старшей ступени школы.</span></p>
 
  <p style="text-align: justify;"><span class="text">Диагностика возможностей, интересов и склонностей учащихся и формирование профильных классов.</span></p>
 
  <p style="text-align: justify;"><span class="text">Проектирование и запуск учебных планов и образовательных программ образовательных организаций и других субъектов образования, осуществляющих программы предпрофильной подготовки и профильного обучения на основе новых вариантов БУПа.</span></p>
 
  <p style="text-align: justify;"><span class="text">Изменения в целевых установках образовательного процесса в духе компетентностного подхода.</span></p>
 
  <p style="text-align: justify;"><span class="text">Изменения в образовательной среде и укладе жизни образовательных организаций, осуществляющих программы предпрофильной подготовки и профильного обучения.</span></p>
 
  <p style="text-align: justify;"><span class="text">Изменения в технологиях и формах организации обучения, связанные с широким использованием проектных методов и т.п.).</span></p>
 
  <p style="text-align: justify;"><span class="text">Изменения в технологиях оценки качества обучения (портфолио, ЕГЭ и т.п.).</span></p>
 </ol>
 
<p style="text-align: justify;"><span class="text">Эти и возможные иные направления работы «разбиваются» далее на конкретные действия и мероприятия, по которым в духе целевого подхода также определяются конкретные ожидаемые результаты.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом предполагается, что, например, на региональном уровне не достигается уровень полной конкретизации этих планов (который можно ожидать скорее на муниципальном и школьном уровнях планирования), но задается «общая рамка» для соответствующих планов нижележащих уровней.</span></p>
 
