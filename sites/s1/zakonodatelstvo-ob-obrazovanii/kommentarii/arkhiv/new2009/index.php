<p><span class="text" style="text-align: justify;">Перечень дополнительных вступительных испытаний и правила их проведения в государственных и муниципальных высших учебных заведениях объявляются такими высшими учебными заведениями не позднее 1 февраля текущего года.</span></p>
 
<p><span class="text" style="text-align: justify;">Правила приема граждан в высшие учебные заведения в части, не урегулированной настоящим Федеральным законом, другими федеральными законами, нормативными правовыми актами уполномоченных федеральных органов исполнительной власти, определяются учредителем и закрепляются в уставе высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">Высшее учебное заведение вправе объявлять прием граждан только при наличии лицензии. Высшее учебное заведение обязано ознакомить абитуриента с указанной лицензией, а также со свидетельством о государственной аккредитации по каждому из направлений подготовки (специальности), дающим право на выдачу документа государственного образца о высшем профессиональном образовании. Об ознакомлении абитуриента со свидетельством о государственной аккредитации по выбранному им направлению подготовки (специальности) или об отсутствии указанного свидетельства в приемные документы вносится запись, которая заверяется личной подписью абитуриента.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 1 в ред. Федерального закона от 09.02.2007 № 17-ФЗ)</span></p>
 
