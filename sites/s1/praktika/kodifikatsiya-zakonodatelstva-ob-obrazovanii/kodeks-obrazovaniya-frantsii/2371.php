<p align="justify"> </p>
 
<p align="justify">Институции профессиональной подготовки организуются в соответствии с положением статей L.910-1 и L.910-2 Кодекса труда, воспроизводимых ниже:</p>
 
<p align="justify"> </p>
 
<p align="justify">&laquo;Ст. L.910-1 &ndash; Профессиональная подготовка и социальное развитие являются предметом политики, скоординированной и согласованной в первую очередь, с представительными организациями работодателей и наемных работников, а также с независимыми работниками.</p>
 
<p align="justify"> </p>
 
<p align="justify">Для этой цели создается при Премьер-министре межминистерский комитет, вице-президентом которого является министр, ответственный за национальное образование, и постоянная группа ответственных сотрудников, президент которой назначается Премьер-министром. Этим органам оказывает содействие в выработке и осуществлении политики профессиональной подготовки, социального развития и занятости Национальный Совет профессиональной подготовки, социального развития и занятости, объединяющий в первую очередь, представителей публичных властей и заинтересованных профессиональных и профсоюзных организаций.</p>
 
<p align="justify"> </p>
 
<p align="justify">На тех же принципах создаются региональные и департаментские комитеты профессиональной подготовки, социального развития и занятости.</p>
 
<p align="justify"> </p>
 
<p align="justify">В каждом из заморских регионов региональный комитет профессиональной подготовки, социального развития и занятости исполняет всю совокупность прерогатив, приданных региональному комитету и департаментскому комитету профессиональной подготовки, социального развития и занятости.</p>
 
<p align="justify"> </p>
 
