 <b>Статья 30. Порядок разграничения компетенции органов государственной власти и органов управления образованием</b></p>
 
<p>1. Установленная статьями 28 и 29 настоящего Закона компетенция в области образования федеральных органов государственной власти, федеральных органов управления образованием и органов государственной власти субъектов Российской Федерации является исчерпывающей и не может быть изменена иначе как законом.</p>
 
<p>2. Разграничение компетенции в области образования между федеральными органами законодательной и исполнительной власти в области образования определяется в соответствии с Конституцией Российской Федерации законом, регулирующим деятельность Правительства Российской Федерации.</p>
 
<p>3. Разграничение компетенции в области образования между федеральными органами исполнительной власти определяется Правительством Российской Федерации.</p>
 
<p>4. Разграничение компетенции в области образования между органами законодательной и исполнительной власти субъектов Российской Федерации, а также между их органами регулируется законодательством субъектов Российской Федерации.</p>
 
<p>5. Федеральные органы исполнительной власти не вправе самостоятельно принимать к своему рассмотрению вопросы, отнесенные настоящим Законом к компетенции органов исполнительной власти субъектов Российской Федерации и местных органов управления образованием, за исключением случаев, предусмотренных законодательством Российской Федерации и касающихся обеспечения государственной и общественной безопасности, охраны здоровья и санитарно-эпидемиологического благополучия населения, защиты прав и свобод граждан.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>6. Федеральные государственные органы управления образованием вправе в пределах своей компетенции инспектировать на территории Российской Федерации любые образовательные учреждения и любой орган управления образованием в порядке надзора, а также могут предоставлять это право другим государственным органам управления образованием.</p>
 
