  <li>
    <p style="text-align: justify;"><span class="text">отделения государственной аттестационной службы по месту нахождения образовательного учреждения.</span></p>
  </li>
</ul>

<p style="text-align: justify;"><span class="text">По отношению к образовательным учреждениям, реализующим профессиональные образовательные программы, вопрос о рекламациях решается:</span></p>

<ul>
  <li>
    <p style="text-align: justify;"><span class="text">общим собранием обучающихся;</span></p>
