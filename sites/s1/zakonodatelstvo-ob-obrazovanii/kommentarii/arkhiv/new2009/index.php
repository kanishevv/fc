<p><span class="text" style="text-align: justify;">Статья 6. Уровни высшего профессионального образования, сроки и формы его получения</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Основные образовательные программы высшего профессионального образования реализуются по уровням.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 1 в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. В Российской Федерации устанавливаются следующие уровни высшего профессионального образования:</span></p>
 
<p><span class="text" style="text-align: justify;">высшее профессиональное образование, подтверждаемое присвоением лицу, успешно прошедшему итоговую аттестацию, квалификации (степени) &quot;бакалавр&quot; - бакалавриат;</span></p>
 
<p><span class="text" style="text-align: justify;">высшее профессиональное образование, подтверждаемое присвоением лицу, успешно прошедшему итоговую аттестацию, квалификации (степени) &quot;специалист&quot; или квалификации (степени) &quot;магистр&quot; - подготовка специалиста или магистратура.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 2 в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. Абзац утратил силу. - Федеральный закон от 24.10.2007 № 232-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">Лицам, не завершившим освоение основной образовательной программы высшего профессионального образования, выдаются академические справки установленного образца.</span></p>
 
<p><span class="text" style="text-align: justify;">4. Нормативные сроки освоения основных образовательных программ высшего профессионального образования по очной форме обучения составляют:</span></p>
 
<p><span class="text" style="text-align: justify;">для получения квалификации (степени) &quot;бакалавр&quot; - четыре года;</span></p>
 
<p><span class="text" style="text-align: justify;">для получения квалификации (степени) &quot;специалист&quot; - не менее пяти лет;</span></p>
 
<p><span class="text" style="text-align: justify;">для получения квалификации (степени) &quot;магистр&quot; - два года.</span></p>
 
<p><span class="text" style="text-align: justify;">Перечень направлений подготовки (специальностей), по которым могут быть установлены иные нормативные сроки освоения основных образовательных программ высшего профессионального образования (программ бакалавриата, программ подготовки специалиста или программ магистратуры), устанавливается Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 4 в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Лица, получившие документы государственного образца о соответствующем уровне высшего профессионального образования, подтверждаемого присвоением лицу квалификации (степени) &quot;бакалавр&quot;, имеют право продолжить на конкурсной основе обучение по образовательной программе высшего профессионального образования - программе магистратуры.</span></p>
 
<p><span class="text" style="text-align: justify;">Получение образования по следующим образовательным программам высшего профессионального образования рассматривается как получение второго высшего профессионального образования:</span></p>
 
<p><span class="text" style="text-align: justify;">по программам бакалавриата или программам подготовки специалиста - лицами, имеющими диплом бакалавра, диплом специалиста, диплом магистра;</span></p>
 
<p><span class="text" style="text-align: justify;">по программам магистратуры - лицами, имеющими диплом специалиста, диплом магистра.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 5 в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6. Утратил силу. - Федеральный закон от 24.10.2007 № 232-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">7. Освоение лицом образовательной программы высшего профессионального образования соответствующего уровня в высшем учебном заведении, имеющем государственную аккредитацию, является условием для занятия им в государственной, муниципальной организации определенной должности, получения должностного оклада и надбавок к нему. Для лиц, освоивших образовательные программы высшего медицинского и высшего фармацевтического образования, условием для занятия ими указанных должностей является первичная годичная послевузовская подготовка (интернатура), подтверждаемая удостоверениями установленного образца.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Квалификация (степень) &quot;бакалавр&quot;, квалификация (степень) &quot;специалист&quot;, квалификация (степень) &quot;магистр&quot; при поступлении на работу дают гражданину право претендовать на занятие должности, для которой квалификационными требованиями предусмотрено высшее профессиональное образование, если иное не установлено федеральными законами.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 24.10.2007 № 232-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Занятие должностей государственных и муниципальных служащих осуществляется в порядке, устанавливаемом соответствующим федеральным законом.</span></p>
 
<p><span class="text" style="text-align: justify;">8. Основные образовательные программы высшего профессионального образования обучающимися могут осваиваться в различных формах в зависимости от объема обязательных занятий педагогического работника высшего учебного заведения с обучающимися: очной, очно-заочной (вечерней), заочной, в форме экстерната. Допускается сочетание различных форм получения высшего профессионального образования. Перечень направлений подготовки (специальностей), по которым получение высшего профессионального образования в очно-заочной (вечерней), заочной формах обучения или в форме экстерната не допускается, устанавливается уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 23.07.2008 № 160-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">9. Экстернат - самостоятельное изучение обучающимся дисциплин согласно основной образовательной программе высшего профессионального образования по избранному направлению подготовки (специальности) с последующей аттестацией (текущей и итоговой) в высшем учебном заведении.</span></p>
 
<p><span class="text" style="text-align: justify;">Положение об экстернате утверждается федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">10. Лица, обучающиеся в не имеющих государственной аккредитации высших учебных заведениях или успешно окончившие их, имеют право на текущую и итоговую государственную аттестацию в высших учебных заведениях, имеющих государственную аккредитацию, на условиях экстерната.</span></p>
 
