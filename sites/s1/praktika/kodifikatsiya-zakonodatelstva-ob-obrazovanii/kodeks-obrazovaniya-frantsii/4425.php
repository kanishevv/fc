<p><span class="textmar">Статьи L.111-1, L.111-2, L.111-3, L.112-2,первый абзац статьи L.113-1, статей L.121-1, L.121-3, c L.122-1 по L.122-5, L.131-1, c L.311-1по L.311-5, L.311-7, L.312-10, L.313-1, L.321-1, первый абзац статьи L.321-2, статьи L.321-3,L.321-4, L.331-1, L.331-4, L.331-7,L.331-8, c L.332-1 по L.332-4, c L.333-1 по L.333-3, L.334-1, L.337-2, L.337-3, L.511-3, первая фраза статьи L.521-1 и статья L.551-1 применяются к частным образовательным учреждениям в режиме контракта с учетом положений настоящей Главы.</span></p>

