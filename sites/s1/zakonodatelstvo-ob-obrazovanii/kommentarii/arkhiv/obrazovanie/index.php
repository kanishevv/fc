<p>22) установление и присвоение государственных наград и почетных званий работникам образования;</p>
 
<p>23) издание нормативных документов в пределах своей компетенции;</p>
 
<p>24) лицензирование образовательных учреждений по программам высшего профессионального и послевузовского профессионального образования и выдача лицензии на право ведения образовательной деятельности по указанным программам.</p>
 
