<p style="text-align: justify;">
 <span class="text">Исполнитель предоставляет в отношении Ларионова Ивана Андрее­вича, сына Заказчика, 1996 года рождения, ученика 5-го класса «А»(именуемого в дальнейшем — «Потребитель»), а Заказчик оплачивает дополнительные образовательные услуги, наименование и количество которых определено в приложении 1, являющемся неотъемлемой ча­стью настоящего договора. Срок обучения в группе в соответствии с рабочим учебным планом составляет восемь месяцев (с октября 2005года по май 2006 года включительно).</span>
