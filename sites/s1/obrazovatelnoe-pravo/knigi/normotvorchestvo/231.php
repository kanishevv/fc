<p align="center" style="text-align: justify;"><span class="text">Рис. 8</span></p>
 
<p style="text-align: justify;"><span class="text">
    <br>
  </span><span class="text">С началом переходного периода спрос на рабочую силу сместился из отраслей материального производства в сферу торговли и бытового обслуживания, коммуникационного обеспечения и кредитно-финансовых отношений. Одновременно стали меняться квалификационные требования со стороны работодателей в тех отраслях, которые не только выжили в условиях кризиса, но и получили бурное развитие. Образовательные учреждения системы начального и среднего профессионального образования стали все больше и больше отставать от требований рынка труда, разработанные 10 – 15 лет назад профессиональные образовательные стандарты оказались устаревшими, квалификационные характеристики на профессии перестали соответствовать требованиям работодателей. В результате, выпускники учреждений профессионального образования все чаще стали сталкиваться с проблемой трудоустройства после окончания учебы, причем даже по тем профессиям, которые, казалось бы, должны пользоваться наибольшим спросом на рынке труда.</span></p>
 
<p style="text-align: justify;"><span class="text">Другой причиной глубоких изменений стало значительное сокращение государственных расходов на образование, переход от достаточного, а в отдельных случаях и избыточного, к дефицитному финансированию. Сегодня страны с переходной экономикой объективно могут позволить себе финансирование лишь обязательного основного общего образования и некоторые виды профессионального образования, востребованность которых экономикой не вызывает сомнений.</span></p>
 
<p style="text-align: justify;"><span class="text">В целом, в числе факторов, связанных с рынком труда и существенным образом влияющих на деятельность учреждений профессионального образования, можно выделить:</span></p>
 
<p style="text-align: justify;"><span class="text">•изменение отраслевой структуры занятости населения;</span></p>
 
<p style="text-align: justify;"><span class="text">•изменение профессиональной и квалификационной структуры спроса на рабочую силу;</span></p>
 
<p style="text-align: justify;"><span class="text">•несоответствие профессионально-квалификационного потенциала населения структуре спроса на рынке труда в сочетании с высоким общим образовательным уровнем;</span></p>
 
<p style="text-align: justify;"><span class="text">•усиление конкуренции со стороны негосударственных образовательных учреждений;</span></p>
 
<p style="text-align: justify;"><span class="text">•появление на рынке труда свободной рабочей силы, имеющей те же профессионально-квалификационные характеристики, что и выпускники образовательных учреждений;</span></p>
 
<p style="text-align: justify;"><span class="text">•наличие общих трансформационных феноменов в сфере занятости, таких как сочетание умеренно открытой и высокой скрытой безработицы, гипертрофия дополнительной и скрытой занятости и т.п.</span></p>
 
<p style="text-align: justify;"><span class="text">Однако фиксация проблемных зон сегодня недостаточна для формирования адекватной стратегии развития профессиональной школы на будущее. Развитие, например, демографической ситуации в сторону убывания численности молодежи в ближайшие 5-10 лет будет неизбежно сказываться на сокращении контингента абитуриентов, что вызывает необходимость изменения структуры приема в государственные учебные заведения отдельных уровней образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Исходя из общих тенденций изменения спроса населения на получение определенного уровня профессионального образования, спроса экономики на кадры различного профиля, с учетом опыта развитых стран, можно ориентироваться на некоторое снижение доли подготовки кадров на уровне начального профессионального образования, при возрастании ее для среднего и высшего образования.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом имеется в виду общая объективная тенденция постепенного продвижения содержательной стороны деятельности этих уровней образования в сторону более высоких: начального – к среднему, среднего – к высшему. В связи с этим можно предположить, что определенная часть учебных заведений начального профессионального образования (лицеи) начнет к 2010 году реализовывать программы среднего профессионального образования, а часть учебных заведений среднего профессионального образования (колледжи) – программы высшего образования.</span></p>
 
<p style="text-align: justify;"><span class="text">При условии приема на обучение всего контингента выпускников текущего года из общеобразовательной школы, сохранения сложившейся доли приема в общем приеме выпускников ПУ – в средние специальные учебные заведения и выпускников ПУ и ССУЗов – в высшие, снижения до 10 % доли приема за счет выпускников из учебных заведений прошлых лет (в 2000 г. – 25 % – снижение доли данной категории абитуриентов определяется постепенным уменьшением их контингента по мере снижения выпуска из общеобразовательной школы) можно прогнозировать следующие структурные сдвиги (табл. 10).</span></p>
 
