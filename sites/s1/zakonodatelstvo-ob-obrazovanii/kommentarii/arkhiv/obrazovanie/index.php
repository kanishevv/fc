<p><b>Статья 31. Полномочия органов местного самоуправления муниципальных районов и городских округов в области образования</b></p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>1. К полномочиям органов местного самоуправления муниципальных районов и городских округов в области образования относятся:</p>
 
<p>1) организация предоставления общедоступного и бесплатного начального общего, основного общего, среднего (полного) общего образования по основным общеобразовательным программам, за исключением полномочий по финансовому обеспечению образовательного процесса, отнесенных к полномочиям органов государственной власти субъектов Российской Федерации;</p>
 
<p>2) организация предоставления дополнительного образования детям и общедоступного бесплатного дошкольного образования на территории муниципального района;</p>
 
<p>3) создание, реорганизация и ликвидация муниципальных образовательных учреждений;</p>
 
<p>4) обеспечение содержания зданий и сооружений муниципальных образовательных учреждений, обустройство прилегающих к ним территорий;</p>
 
<p>5) учет детей, подлежащих обязательному обучению в образовательных учреждениях, реализующих образовательные программы основного общего образования;</p>
 
<p>6) опека и попечительство.</p>
 
