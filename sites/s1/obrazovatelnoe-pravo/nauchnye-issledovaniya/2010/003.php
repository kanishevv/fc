<div align="justify"> 1.1.6. обоснование критериев отбора нормативных правовых актов, подлежащих систематизации (по юридической силе, по относимости к образованию и по действию во времени и пространстве), схемы группировки; </div>

<div align="justify"> 1.1.7. предложения по инкорпорации, консолидации и кодификации образовательного законодательства. </div>
 
<div align="justify">1.2. Учет (сбор) действующего законодательства и нормативных правовых актов об образовании РФ, включая: </div>
 
