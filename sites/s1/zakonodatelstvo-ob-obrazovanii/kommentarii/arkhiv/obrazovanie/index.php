<p><b>Статья 11. Учредитель образовательного учреждения</b></p>
 
<p><b>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</b></p>
 
<p>1. Учредителем образовательного учреждения (далее - учредитель) могут быть:</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>1) органы государственной власти, органы местного самоуправления;</p>
 
<p>2) отечественные и иностранные организации всех форм собственности, их объединения (ассоциации и союзы);</p>
 
<p>3) отечественные и иностранные общественные и частные фонды;</p>
 
<p>4) общественные и религиозные организации (объединения), зарегистрированные на территории Российской Федерации;</p>
 
<p>5) граждане Российской Федерации и иностранные граждане.</p>
 
<p>Допускается совместное учредительство негосударственных образовательных учреждений.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>Абзац утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>В случае реорганизации органов государственной власти, органов управления образованием и (или) органов местного самоуправления права учредителя переходят к соответствующим правопреемникам.</p>
 
<p>2. Учредителем образовательных учреждений всех типов и видов, реализующих военные профессиональные образовательные программы, может быть только Правительство Российской Федерации.</p>
 
<p>Учредителем специального учебно-воспитательного учреждения закрытого типа для детей и подростков с девиантным (общественно опасным) поведением могут быть только федеральные органы исполнительной власти и (или) органы исполнительной власти субъектов Российской Федерации.</p>
 
<p>3. Отношения между учредителем и образовательным учреждением определяются договором, заключенным между ними в соответствии с законодательством Российской Федерации.</p>
 
