 <span class="text">Готовность<b>субъектов преобразований</b><b>на всех уровнях</b>к решению задач введения профильного обучения, которая в свою очередь, включает<b>ценностную готовность</b>(субъект разделяет и принимает ценности и цели работы),<b>мотивационную готовность</b>(субъект стремится к достижению поставленных целей и задач) и<b>исполнительскую готовность</b>(субъект обладает требуемой компетентностью – знаниями, умениями, навыками, качествами, необходимыми для решения задач).</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
 <span class="text"><b>Готовность ресурсов и условий</b>, используемых субъектами (основные группы таких ресурсов и условий рассмотрены выше).</span>
	</p>
 </li>
</ol>
<p style="text-align: justify;">
 <span class="text">Готовность образовательной системы (школы) к введению профильного обучения имеет смысл рассматривать в строгой привязке к конкретному времени и на различных качественных уровнях.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Предлагается выделить три таких качественных уровня:</span>
</p>
<ol>
	<li>
