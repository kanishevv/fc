<p><span class="text" style="text-align: justify;">Статья 17. Гарантии и компенсации, предоставляемые лицам, совмещающим учебу в высшем учебном заведении с работой</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">1. Лицам, успешно обучающимся в имеющих государственную аккредитацию высших учебных заведениях независимо от их организационно-правовых форм по заочной и очно-заочной (вечерней) формам, по месту их работы предоставляются дополнительные отпуска с сохранением средней заработной платы, начисляемой в порядке, установленном для ежегодных отпусков (с возможностью присоединения дополнительных учебных отпусков к ежегодным отпускам) для:</span></p>
 
<p><span class="text" style="text-align: justify;">сдачи зачетов и экзаменов на первом и втором курсах соответственно по сорок календарных дней, на последующих курсах соответственно по пятьдесят календарных дней;</span></p>
 
<p><span class="text" style="text-align: justify;">подготовки и защиты дипломного проекта (работы) со сдачей государственных экзаменов четыре месяца;</span></p>
 
<p><span class="text" style="text-align: justify;">сдачи государственных экзаменов один месяц.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Дополнительные отпуска без сохранения заработной платы по месту работы предоставляются:</span></p>
 
<p><span class="text" style="text-align: justify;">лицам, допущенным к вступительным испытаниям в высшие учебные заведения, пятнадцать календарных дней;</span></p>
 
<p><span class="text" style="text-align: justify;">слушателям подготовительных отделений при высших учебных заведениях для сдачи выпускных экзаменов пятнадцать календарных дней;</span></p>
 
<p><span class="text" style="text-align: justify;">студентам высших учебных заведений очной формы обучения, совмещающим учебу с работой, для сдачи зачетов и экзаменов пятнадцать календарных дней в учебном году; для подготовки и защиты дипломного проекта (работы) со сдачей государственных экзаменов четыре месяца; для сдачи государственных экзаменов один месяц.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Студентам, обучающимся по заочной форме в высших учебных заведениях, которые имеют государственную аккредитацию, один раз в учебном году организация-работодатель оплачивает проезд к месту нахождения высшего учебного заведения и обратно для выполнения лабораторных работ, сдачи зачетов и экзаменов, а также для сдачи государственных экзаменов, подготовки и защиты дипломного проекта (работы).</span></p>
 
<p><span class="text" style="text-align: justify;">4. Форма справки-вызова, дающей право на предоставление по месту работы дополнительного отпуска и других гарантий, связанных с обучением в высшем учебном заведении, которые имеют государственную аккредитацию, утверждается федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Для работников предприятий, учреждений и организаций независимо от их организационно-правовых форм и форм собственности, совмещающих учебу в высших учебных заведениях с работой, коллективным договором или трудовым договором (контрактом) могут предусматриваться увеличение продолжительности дополнительных отпусков, предоставление отпуска с сохранением заработной платы вместо отпуска без сохранения заработной платы и иные меры поддержки.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">7. При обучении студента, совмещающего учебу с работой одновременно в двух высших учебных заведениях, указанные выше гарантии и компенсации могут иметь место только по одному из этих высших учебных заведений (по выбору студента).</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
