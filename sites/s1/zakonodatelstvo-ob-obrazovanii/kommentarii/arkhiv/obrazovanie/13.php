  </li>

  <li>
    <p align="JUSTIFY"><span class="text">Типовое положение об образовательном учреждении высшего профессионального образования (высшем учебном заведении) Российской Федерации, утвержденное постановлением Правительства РФ от 5 апреля 2001 г. № 264 (в ред. постановлений Правительства РФ от 17.09.2001 № 676, от 03.10.2002 № 731, от 23.12.2002 № 919, от 31.03.2003 № 175);</span></p>
  </li>

  <li>
    <p align="JUSTIFY"><span class="text">Типовое положение об образовательном учреждении среднего профессионального образования (среднем специальном учебном заведении), утвержденное постановлением Правительства РФ от 3 марта 2001 г. № 160 (в ред. постановлений Правительства РФ от 23.12.2002 № 919, от 31.03.2003 № 175);</span></p>
