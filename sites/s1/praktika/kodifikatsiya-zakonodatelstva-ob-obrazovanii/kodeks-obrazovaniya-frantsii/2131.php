?><b>Статья L.213-1</b> 
<p> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Генеральный совет устанавливает после согласия каждой из коммун или, в ином случае, каждого из публичных учреждений межкоммунального сотрудничества, имеющих отношение к проектам, расположенным на их территории, прогнозируемую программу инвестиций, относящихся к колледжам, которая вытекает из прогнозируемой схемы видов подготовки, упомянутой в статье L.214-1 настоящего Кодекса.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Для этого генеральный совет определяет места расположения учреждений, их возможность приёма и способ обеспечения проживания учащихся.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-2</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Департамент имеет на своём содержании колледжи. Для этого он обеспечивает их строительство, реконструкцию, расширение, крупный ремонт, оборудование и функционирование, за исключением, с одной стороны, образовательных расходов, подлежащих возмещению Государством, перечень которых установлен декретом, и, с другой стороны, расходов на персонал, с оговорками положений статьи L.216-1.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">В том, что касается строительства, реконструкции, расширения, крупного ремонта так же, как оборудования этих учреждений, департамент может доверить Государству на условиях, определяемых статьями L.3 и 5 закона № 85-704 от 12 июля 1985, касающегося руководства строительством публичных сооружений и его отношений с руководством частного производства, исполнение от его имени и за его счёт, полностью или частично, некоторые прерогативы руководства строительством.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">В этом случае департамент использует компенсационные фонды для налога на добавленную стоимость по статье соответствующих инвестиционных расходов.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Департамент использует также компенсационные фонды для налога на добавленную стоимость по статье инвестиционных расходов, которые он несёт, на публичные местные образовательные учреждения, которые к нему прикреплены, с целью строительства, реконструкции и крупного ремонта этих учреждений.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L. 213-3</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Департамент является собственником помещений, строительство и реконструкцию которых он обеспечивает.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-4</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Положения статей с L. 1321-1 по L.1321-6 Общего кодекса территориальных объединений, относящихся к исполнению полномочий и к предоставлению в распоряжение имущества, используемого для исполнения переданных полномочий, применяются к существующим строениям с оговорками положений, приведенных ниже.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Во изменение положений последнего абзаца статьи L.1321-1 и статей L.1321-4 и L.1321-5 Общего кодекса территориальных объединений правила, предусмотренные в статьях L.213-4 по L.213-6 настоящего Кодекса, применяются к исполнению полномочий и к предоставлению в распоряжение департамента колледжей, существующих на дату передачи полномочий в области образования, и которые не принадлежат Государству.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">I. Движимое и недвижимое имущество на законном основании предоставляется бесплатно, начиная с даты передачи полномочий, в распоряжение департамента.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Департамент осуществляет весь комплекс обязанностей собственника. Он обладает всеми правами управления. Он обеспечивает обновление движимого имущества; с оговорками положений cтатей L.216-1 и L.212-15 настоящего Кодекса он может разрешить использование переданного имущества. Он выступает перед законом в качестве собственника.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Департамент может предпринимать все работы по крупному ремонту, реконструкции, сносу, надстройке и пристройке строений, которые не нарушают предназначения имущества.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">С оговорками положений параграфа II, приведенного ниже, в том, что касается произведенных займов, департамент замещает объединение-собственника в его правах и обязательствах, вытекающих из сделок и контрактов, которые объединение-собственник могло заключить для обустройства, поддержания и сохранения переданного имущества так же, как и для функционирования служб. Объединение-собственник признает это замещение и уведомляет о нем другие стороны контрактов.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Протокол, констатирующий передачу в распоряжение, предусматриваемую в статье L.1321-1 Общего кодекса территориальных объединений, устанавливается в результате прений между представителями Государства, департамента и объединения-собственника.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Операции, производившиеся на момент передачи полномочий, заканчиваются в том юридическом и финансовом режиме, в котором они были начаты.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">II. Объединение-собственник сохраняет ответственность за возмещение займов, на которых оно заключило контракт до передачи полномочий, как за имущество, переданное в распоряжение.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-5</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Соглашение между департаментом и территориальным объединением-собственником, заключаемое после консультаций с паритетными полномочными инстанциями, определяет положение персонала, который объединение-собственник наняло в состав своих служб до передачи полномочий, для поддержания и крупного ремонта имущества, предоставленного в распоряжение. Это соглашение уточняет также дальнейшее применение материальных средств, используемых для их вознаграждения. Оно предусматривает предоставление в распоряжение департамента персонала и материальных средств и возможность их окончательной передачи в результате соглашения сторон. Оно устанавливает также финансовые условия предоставления в распоряжение или передачи. В случае отсутствия соглашения в течение одного года, начиная с момента передачи полномочий, производится предоставление в распоряжение персонала и материальных средств представителем Государства в департаменте на условиях, определяемых декретом Государственного Совета, и после получения заключения полномочных паритетных инстанций.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">До введения соглашения или в его отсутствии, решение представителя Государства в департаменте этот персонал и эти средства передаются в распоряжение департамента.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-6</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">I. &ndash; Положения статьи L.1321-3 Общего кодекса территориальных объединений, касающиеся дальнейшего использования имущества в случае полного или частичного неиспользования, применяются к имуществу, находящемуся в распоряжении департамента.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">II. &ndash; По соглашению между департаментом и объединением-собственником, имущество, переданное в распоряжение департамента, может быть передано ему в полную собственность.</p>
 
<p style="margin-left: 108pt; text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Отдельное соглашение устанавливает условия передачи собственности.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Эта передача не сопровождается выплатой на основании закона налога или гонорара.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">III. Департамент также замещает Государство в правах и обязанностях, которыми оно обладало в качестве пользователя имущества, переданного в распоряжение.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Департамент замещает Государство в контрактах любого рода, которые оно заключило на оборудование, поддержание и сохранение имущества, а также на функционирование служб. Государство признает это замещение и уведомляет о нем другие стороны контрактов.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">IV. Если государственное учреждение межкоммунального сотрудничества получило полномочия территориального объединения-собственника, то эта группа исполняет прерогативы, возлагаемые на местное объединение-собственника статьями от L.213-4 до L.213-6 настоящего Кодекса.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-7</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Условия, на которых выплата на аренду жилья и вспомогательные выплаты обеспечиваются некоторым категориям Государственного персонала учреждений, находящихся в ведении департаментов, устанавливаются декретом Государственного Совета.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-8</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Если, по меньшей мере 10 % учащихся колледжа проживают в департаменте, отличном от того, к которому относится это учреждение, то участие в затратах на функционирование может быть потребовано от департамента проживания. Сумма этого участия устанавливается по соглашению между заинтересованными департаментами.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">В случае разногласий представитель Государства в регионе устанавливает условия этого участия. Если департаменты принадлежат к разным регионам, эти условия устанавливаются совместно представителями Государства в заинтересованных регионах.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-9</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Департаментская дотация на оборудование колледжей регулируется положениями статьи L.3334-16 Общего кодекса территориальных объединений, воспроизводимыми ниже:</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">&laquo;Ст. L.3334-16. &ndash; Департаментская дотация на оборудование колледжей изменяется также как, взятая в целом дотация на оборудование.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Доля совокупности департаментов каждого региона во взятой в целом дотации на оборудование определяется на условиях, устанавливаемых декретом действующего Государственного Совета, а именно из эволюции населения школьного возраста и возможностей приема учреждений.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Она распределяется между департаментами по согласованию президентов генеральных советов после сообщения представителем Государства в регионе списка операций по строительству и расширению, предусматриваемого в статье L.211-3 Кодекса образования.</p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">В отсутствии согласия между президентами генеральных советов она распределяется представителем Государства в регионе на условиях, определяемых декретом Государственного Совета.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Дотация вписывается в бюджет каждого департамента, который её использует на реконструкцию, крупный ремонт, оборудование, и, если эти операции фигурируют в списке, установленном в приложение статьи L.211-2 Кодекса образования, &ndash; на расширение и строительство колледжей.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Во изменение статей L.1614-4 и L.1614-5, кредиты, упомянутые в настоящей статье, не включаются в общую дотацию на цели децентрализации&raquo;.</p>
 
<p style="text-align: justify; text-indent: 27pt;"> </p>
 
<p style="text-align: justify;"><b>Статья L.213-10</b></p>
 
<p style="text-align: justify;"> </p>
 
<p style="text-align: justify; text-indent: 27pt;">Условия охвата школьным обучением переезжающих детей являются объектом департаментской схемы в соответствии с положениями статьи 28 Закона №90-449 от 31 мая 1990 года, касающегося осуществления права на жилище.</p>
