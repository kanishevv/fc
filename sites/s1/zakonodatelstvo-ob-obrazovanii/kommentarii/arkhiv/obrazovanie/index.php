 <b>Статья 14. Общие требования к содержанию образования</b></p>
 
<p> 
  <br />
 1. Содержание образования является одним из факторов экономического и социального прогресса общества и должно быть ориентировано на:</p>
 
<p>обеспечение самоопределения личности, создание условий для ее самореализации;</p>
 
<p>развитие общества;</p>
 
<p>укрепление и совершенствование правового государства.</p>
 
<p>2. Содержание образования должно обеспечивать:</p>
 
<p>адекватный мировому уровень общей и профессиональной культуры общества;</p>
 
<p>формирование у обучающегося адекватной современному уровню знаний и уровню образовательной программы (ступени обучения) картины мира;</p>
 
<p>интеграцию личности в национальную и мировую культуру;</p>
 
<p>формирование человека и гражданина, интегрированного в современное ему общество и нацеленного на совершенствование этого общества;</p>
 
<p>воспроизводство и развитие кадрового потенциала общества.</p>
 
<p>3. Профессиональное образование любого уровня должно обеспечивать получение обучающимся профессии и соответствующей квалификации.</p>
 
<p>4. Содержание образования должно содействовать взаимопониманию и сотрудничеству между людьми, народами независимо от расовой, национальной, этнической, религиозной и социальной принадлежности, учитывать разнообразие мировоззренческих подходов, способствовать реализации права обучающихся на свободный выбор мнений и убеждений.</p>
 
<p>5. Содержание образования в конкретном образовательном учреждении определяется образовательной программой (образовательными программами), разрабатываемой, принимаемой и реализуемой этим образовательным учреждением самостоятельно.</p>
 
<p>Государственные органы управления образованием обеспечивают разработку на основе государственных образовательных стандартов примерных образовательных программ.</p>
 
<p>6. Образовательное учреждение в соответствии со своими уставными целями и задачами может реализовывать дополнительные образовательные программы и оказывать дополнительные образовательные услуги (на договорной основе) за пределами определяющих его статус образовательных программ.</p>
 
<p>7. Военная подготовка в гражданских образовательных учреждениях может проводиться только на факультативной основе с согласия обучающихся и (или) их родителей (законных представителей) за счет средств и силами заинтересованного ведомства.</p>
 
<p>8. Образовательное учреждение при реализации образовательных программ использует возможности учреждений культуры.</p>
 
