<p><span class="text" style="text-align: justify;">Статья 15. Общественные организации и государственно-общественные объединения в системе высшего и послевузовского профессионального образования</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Общественные организации и государственно-общественные объединения в системе высшего и послевузовского профессионального образования действуют в соответствии с законодательством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Интересы обучающихся высших учебных заведений могут представлять общественные организации обучающихся. Конкретные взаимоотношения администрации высшего учебного заведения и общественных организаций обучающихся определяются уставом высшего учебного заведения и договором между его администрацией и общественной организацией обучающихся.</span></p>
 
<p><span class="text" style="text-align: justify;">3. В системе высшего и послевузовского профессионального образования могут создаваться без образования юридических лиц государственно-общественные объединения типа учебно-методических объединений высших учебных заведений, научно-методических, научно-технических и других советов и комиссий.</span></p>
 
<p><span class="text" style="text-align: justify;">4. Государственно-общественные объединения создаются федеральными органами исполнительной власти и осуществляют свою деятельность в соответствии с положениями, утверждаемыми этими органами власти. Типовые положения о государственно-общественных объединениях утверждаются федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования. В состав государственно-общественных объединений на добровольных началах входят научно-педагогические и другие работники высших учебных заведений, работники учреждений и организаций, действующих в системе высшего и послевузовского профессионального образования. Государственно-общественные объединения могут привлекать к участию в своей работе иностранных граждан, лиц без гражданства и иностранных юридических лиц в соответствии с законодательством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 29.12.2006 № 258-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Органы исполнительной власти, исполнительно-распорядительные органы городских округов, муниципальных районов, ученые советы высших учебных заведений рассматривают и учитывают в своей деятельности рекомендации общественных организаций и государственно-общественных объединений в системе высшего и послевузовского профессионального образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ, от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6 - 7. Утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
