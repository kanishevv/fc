</p>
<p style="text-align: justify;">
 <span class="text">В широком смысле процесс введения профильного обучения включает в себя различные подпроцессы:</span>
</p>
<p style="text-align: justify;">
 <span class="text"><b>1) в содержательном плане</b>– подпроцессы:<b>а) собственно введение профильного обучения</b>как определенных изменений в реальной практике образования, в образовательном процессе в конкретных образовательных учреждениях;<b>б) ресурсное обеспечение введения профильного обучения</b>;<b>в) управленческое сопровождения введения профильного обучения;</b></span>
</p>
<p style="text-align: justify;">
 <span class="text"><b>2) в динамическом плане</b>– подпроцессы :а) создание условий и ресурсов, обеспечивающих готовность к введению профильного обучения или<b>стадию готовности к введению профильного обучения</b>; б) собственно введение профильного обучения или<b>стадию непосредственного введения профильного обучения.</b></span>
</p>
<p style="text-align: justify;">
 <span class="text"><b>Введение профильного обучения может быть выборочным и массовым.</b>О массовом введении профильного обучения можно говорить, когда не менее 70 % образовательных учреждений территории перешли на профильное обучение.</span>
</p>
<p style="text-align: justify;">
 <span class="text"><b>Ресурсное обеспечение введения профильного обучения –</b>деятельность субъектов образовательных систем и их социальных партнеров по созданию, распространению, поставке, использованию всех видов<b>ресурсов</b>, необходимых для успешного и полноценного введения профильного обучения.</span>
</p>
<p style="text-align: justify;">
 <span class="text"><b>Управленческое сопровождение введения профильного обучения –</b>деятельность субъектов управления образовательными и более широкими социальными системами, направленная на постановку (конкретизацию) и достижение целей введения профильного обучения с помощью анализа, планирования, организации, руководства и контроля. В качестве объектов сопровождения выступают все обозначенные выше подпроцессы процесса введения профильного обучения.</span>
</p>
<p style="text-align: justify;">
 <span class="text"><b>Инвариантные задачи введения профильного обучения –</b>общие, не зависящие от особенностей регионов, муниципалитетов и школ задачи и направления деятельности, реализация которых обеспечивает переход на профильное обучение.</span>
</p>
<p style="text-align: justify;">
 <span class="text"><b>Готовность к введению профильного обучения</b>– состояние образовательной системы (школы), характеризуемое наличием в этой системе необходимых и достаточных условий (ресурсов, возможностей) внутреннего и внешнего характера, позволяющих в разумные сроки обеспечить на достойном качественном уровне и при рациональном расходовании ресурсов, времени и усилий участников образовательного процесса успешное и массовое введение профильного обучения в общеобразовательных школах.</span>
</p>
<p style="text-align: justify;">
