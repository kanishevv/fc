<p><b>Статья 8. Понятие системы образования</b></p>
 
<p>Система образования в Российской Федерации представляет собой совокупность взаимодействующих:</p>
 
<p>преемственных образовательных программ и государственных образовательных стандартов различного уровня и направленности;</p>
 
<p>сети реализующих их образовательных учреждений независимо от их организационно-правовых форм, типов и видов;</p>
 
<p>органов управления образованием и подведомственных им учреждений и организаций.</p>
 
