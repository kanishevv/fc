<p><span class="text" style="text-align: justify;">Статья 19. Докторанты, аспиранты (адъюнкты) и соискатели (в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Докторантом является лицо, имеющее ученую степень кандидата наук и зачисленное в докторантуру для подготовки диссертации на соискание ученой степени доктора наук.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Аспирантом является лицо, имеющее высшее профессиональное образование и обучающееся в аспирантуре и подготавливающее диссертацию на соискание ученой степени кандидата наук.</span></p>
 
<p><span class="text" style="text-align: justify;">Адъюнктом является военнослужащий, имеющий высшее профессиональное образование, обучающийся в адъюнктуре и подготавливающий диссертацию на соискание ученой степени кандидата наук.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. Соискателем является лицо, имеющее высшее профессиональное образование, прикрепленное к организации или учреждению, которые имеют аспирантуру (адъюнктуру) и (или) докторантуру, и подготавливающее диссертацию на соискание ученой степени кандидата наук без обучения в аспирантуре (адъюнктуре), либо лицо, имеющее ученую степень кандидата наук и подготавливающее диссертацию на соискание ученой степени доктора наук.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Положения о докторантах, об аспирантах (адъюнктах) и о соискателях утверждаются уполномоченным Правительством Российской Федерации федеральным органом исполнительной власти.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 20.04.2007 № 56-ФЗ, от 23.07.2008 № 160-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Положения о докторантах, об аспирантах (адъюнктах) и о соискателях высших учебных заведений, реализующих военные профессиональные образовательные программы, и высших учебных заведений, реализующих образовательные программы, содержащие сведения, составляющие государственную тайну, утверждаются в порядке, установленном Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 23.07.2008 № 160-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Лицам, допущенным к вступительным испытаниям в аспирантуру, предоставляются отпуска продолжительностью тридцать календарных дней с сохранением средней заработной платы по месту работы.</span></p>
 
<p><span class="text" style="text-align: justify;">6. Лица, обучающиеся в аспирантуре по очной форме обучения за счет средств бюджета, обеспечиваются государственными стипендиями и пользуются ежегодно каникулами продолжительностью два месяца.</span></p>
 
<p><span class="text" style="text-align: justify;">7. Аспиранты, обучающиеся в аспирантуре по заочной форме обучения, имеют право на ежегодные дополнительные отпуска по месту работы продолжительностью тридцать календарных дней с сохранением средней заработной платы.</span></p>
 
<p><span class="text" style="text-align: justify;">К ежегодному дополнительному отпуску аспиранта добавляется время, затраченное на проезд от места работы до места нахождения аспирантуры и обратно с сохранением средней заработной платы. Указанный проезд оплачивает организация-работодатель.</span></p>
 
<p><span class="text" style="text-align: justify;">Аспиранты, обучающиеся в аспирантуре по заочной форме обучения, имеют право соответственно на один свободный от работы день в неделю с оплатой его в размере пятидесяти процентов получаемой заработной платы, но не ниже 100 рублей. Организация-работодатель вправе предоставлять аспирантам по их желанию на четвертом году обучения дополнительно не более двух свободных от работы дней в неделю без сохранения заработной платы.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 07.08.2000 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Аспирантам, обучающимся в аспирантуре по заочной форме обучения, предоставляются места в общежитии на период сдачи экзаменов и выполнения работ по диссертации.</span></p>
 
<p><span class="text" style="text-align: justify;">8. Утратил силу с 1 июля 2006 года. - Федеральный закон от 06.07.2006 № 104-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">9. Докторантам выплачиваются государственные стипендии и предоставляются ежегодные каникулы продолжительностью два месяца.</span></p>
 
<p><span class="text" style="text-align: justify;">За докторантами сохраняются все права по месту работы, которые они имели до поступления в докторантуру (права на получение жилой площади, на присвоение ученого звания и другие права), а также право на возвращение на прежние места работы.</span></p>
 
<p><span class="text" style="text-align: justify;">10. Аспиранты и докторанты пользуются бесплатно оборудованием, лабораториями, учебно-методическими кабинетами, библиотеками, а также правом на командировки, в том числе в высшие учебные заведения и научные центры иностранных государств, участие в экспедициях для проведения работ по избранным темам научных исследований наравне с научно-педагогическими работниками высших учебных заведений и научными работниками научно-исследовательских учреждений (организаций).</span></p>
 
<p><span class="text" style="text-align: justify;">11. Для приобретения научной литературы каждому аспиранту и докторанту, обучающемуся за счет средств федерального бюджета, выдается ежегодное пособие в размере двух месячных стипендий.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">12. Для завершения диссертаций на соискание ученой степени кандидата наук или доктора наук работникам предприятий, учреждений и организаций по месту работы предоставляются отпуска с сохранением заработной платы продолжительностью соответственно три или шесть месяцев в порядке, установленном положениями об аспирантах, докторантах и соискателях.</span></p>
 
<p><span class="text" style="text-align: justify;">13. Соискатели, работающие над диссертациями на соискание ученой степени доктора наук и имеющие значительные научные результаты по актуальным социально-экономическим проблемам или приоритетным направлениям фундаментальных научных исследований, могут быть переведены на должности научных работников на срок до двух лет для подготовки диссертации на соискание ученой степени доктора наук. За соискателями, переведенными на должности научных работников, сохраняются получаемые ими должностные оклады и право на возвращение на прежние места работы.</span></p>
 
