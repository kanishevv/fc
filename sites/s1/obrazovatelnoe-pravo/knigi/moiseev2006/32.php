<p style="text-align: justify;"><span style="font-size: 13px;"><span class="text" style="text-align: center;"><b>Требования к Программам и пути их обеспечения.</b></span></span></p>
 
<p style="text-align: justify;"><span style="font-size: 13px;"><span class="text" style="text-align: center;"><b>Какими должны быть и какими не должны быть</b></span></span></p>
 
<p style="text-align: justify;"><span style="font-size: 13px;"><span class="text" style="text-align: center;"><b>разрабатываемые Программы?</b></span></span></p>
 
<p></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br />
   </span></p>
 
<p style="text-align: justify;"><span class="text">Программы перехода на профильное обучение должны соответствовать некоторым принципиальным общим<b>требованиям.</b>Эти требования могут носить как<b>позитивный</b>(&quot;что желательно, что должно быть?&quot;), так и<b>негативный</b>(&quot;чего не должно быть?&quot;) характер.</span></p>
 
<p style="text-align: justify;"><span class="text">В частности, в позитивном плане предполагается, что Программы должны быть:</span></p>
 
<ul> 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>предельно конкретными, краткими, компактными</b>(за счет минимизации объема концептуальных и аналитических текстов);</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>отражать специфику</b>ситуации, традиций, возможностей и ограничений конкретного образовательного учреждения, муниципального образования, региона;<b>исходить из актуальных потребностей</b>региональной, муниципальной образовательной системы и ее потребителей;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>стратегическими</b>, подчиняющими частные действия реализации общих целей и стратегий Программ;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>целевыми и контролируемыми</b>, то есть задающими ясные, доходящие до результатов на уровне качества образования учащихся, стратегические, конкретные цели,<b>допускающими несложное сопоставление реально полученных эффектов с заданными целями с помощью операциональных, понятных, проверяемых индикаторов;</b></span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>логичными</b>и четко обозначать действия, связанные с осуществлением в рамках перехода на профильное обучение изменений в образовательных процессах, ресурсном обеспечении и в управлении;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>комплексными</b>и четко увязывать поставленные<b>цели, людей, организации, их действия и ресурсы;</b></span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>полными, информативными, информационно достаточными</b>, то есть отражающими все направления работы по переходу на профильное обучение и отвечающими на все главные вопросы введения профильного обучения - что, с какими целями и ожидаемыми результатами, кем, когда, где, в какой последовательности, с какими и откуда поступающими ресурсами предполагается осуществить, и почему это приведет к приемлемым конечным результатам, эффектам и последствиям.</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>выполнимыми, реализуемыми</b>, то есть ресурсно (прежде всего - кадрово, программно, учебнико-, финансово, материально), просчитанными и ресурсно обеспеченными;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>открытыми</b>, предполагающими кооперацию множества субъектов внутри системы образования и за ее пределами;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>средством консолидации</b><b>профессионального и более широкого образовательного сообщества,</b>обращаться не только к профессионалам от образования;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>мотивирующими и привлекательными</b>(в том числе - инвестиционно);</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>ориентированными на связи и сетевое взаимодействие</b>по проблеме профильного обучения;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">надежно<b>управляемыми</b>и иметь встроенный механизм управления реализацией Программы, включая мониторинг и оценку.</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>доступными</b>любому заинтересованному в получении информации о них пользователю в школе, муниципалитете, регионе;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text"><b>гибкими и мобильными</b>.</span></p>
   </li>
 </ul>
 
<p style="text-align: justify;"><span class="text">Одновременно Программы перехода на профильное обучение<b>не должны</b>:</span></p>
 
<ul> 
  <li> 
    <p style="text-align: justify;"><span class="text">носить <b>наукообразного характера</b>, подменять собой концепции и другие документы теоретического и доктринального типа;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">развернуто излагать концептуальные положения о профильном обучении;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">содержать <b>развернутых</b>обоснований предлагаемой в тексте стратегии и тактики перехода на профильное обучение (предполагается, что разработчики Программ будут обосновывать, причем кратко, только те положения, который носят спорный и дискуссионный характер);</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">носить по преимуществу <b>политизированного и идеологического </b>характера;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">ориентировать образовательные системы на<b>искусственное, нарушающее принцип добровольности и достаточной ресурсной обеспеченности, форсированное ускорение введения профильного обучения</b>;</span></p>
   </li>
 
  <li> 
    <p style="text-align: justify;"><span class="text">быть <b>избыточно детализированными </b>(так как это приведет к резкому расширению роста объема Программ и сделает их неинструментальными). В любом случае детализация может следовать только после раскрытия принципиального подхода, целей, стратегии.</span></p>
   </li>
 </ul>
 
