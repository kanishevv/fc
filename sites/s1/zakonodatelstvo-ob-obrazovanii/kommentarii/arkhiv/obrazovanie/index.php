<p><b>Статья 55. Права работников образовательных учреждений и меры их социальной поддержки</b></p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>1. Работники образовательных учреждений имеют право на участие в управлении образовательным учреждением, на защиту своей профессиональной чести и достоинства.</p>
 
<p>2. Дисциплинарное расследование нарушений педагогическим работником образовательного учреждения норм профессионального поведения и (или) устава данного образовательного учреждения может быть проведено только по поступившей на него жалобе, поданной в письменной форме. Копия жалобы должна быть передана данному педагогическому работнику.</p>
 
<p>3. Ход дисциплинарного расследования и принятые по его результатам решения могут быть преданы гласности только с согласия заинтересованного педагогического работника образовательного учреждения, за исключением случаев, ведущих к запрещению заниматься педагогической деятельностью, или при необходимости защиты интересов обучающихся, воспитанников.</p>
 
<p>4. При исполнении профессиональных обязанностей педагогические работники имеют право на свободу выбора и использования методик обучения и воспитания, учебных пособий и материалов, учебников в соответствии с образовательной программой, утвержденной образовательным учреждением, методов оценки знаний обучающихся, воспитанников.</p>
 
<p>(в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
<p>5. Для педагогических работников образовательных учреждений устанавливается сокращенная продолжительность рабочего времени - не более 36 часов в неделю.</p>
 
<p>В зависимости от должности и (или) специальности педагогическим работникам образовательных учреждений с учетом особенностей их труда продолжительность рабочего времени, а также минимальная продолжительность ежегодного оплачиваемого отпуска устанавливаются Кодексом законов о труде Российской Федерации и иными нормативными правовыми актами Российской Федерации.</p>
 
<p>Педагогические работники образовательных учреждений в порядке, установленном законодательством Российской Федерации, пользуются правом на получение пенсии за выслугу лет до достижения ими пенсионного возраста, на бесплатную жилую площадь с отоплением и освещением в сельской местности, рабочих поселках (поселках городского типа), на первоочередное предоставление жилой площади. Размер, условия и порядок возмещения расходов, связанных с предоставлением указанных мер социальной поддержки, устанавливаются законодательными актами субъектов Российской Федерации.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>Педагогические работники образовательного учреждения не реже чем через каждые 10 лет непрерывной преподавательской работы имеют право на длительный отпуск сроком до одного года, порядок и условия предоставления которого определяются учредителем и (или) уставом данного образовательного учреждения.</p>
 
<p>6. Учебная нагрузка педагогического работника образовательного учреждения, оговариваемая в трудовом договоре (контракте), ограничивается верхним пределом, определяемым типовым положением об образовательном учреждении соответствующих типа и вида.</p>
 
<p>7. Педагогический работник образовательного учреждения высшего профессионального образования, имеющий ученую степень по соответствующей специальности, имеет право безвозмездно читать учебный курс, параллельный существующему. Руководство образовательного учреждения обязано создать для этого необходимые условия.</p>
 
<p>8. Педагогическим работникам федеральных государственных образовательных учреждений (в том числе руководящим работникам, деятельность которых связана с образовательным процессом) в целях содействия их обеспечению книгоиздательской продукцией и периодическими изданиями выплачивается ежемесячная денежная компенсация в размере 150 рублей в федеральных государственных образовательных учреждениях высшего профессионального образования и соответствующего дополнительного профессионального образования, в размере 100 рублей - в других федеральных государственных образовательных учреждениях.</p>
 
<p>Педагогическим работникам государственных образовательных учреждений, находящихся в ведении субъектов Российской Федерации, указанная денежная компенсация выплачивается по решению органа государственной власти субъекта Российской Федерации в размере, устанавливаемом указанным органом, муниципальных образовательных учреждений - органом местного самоуправления.</p>
 
<p>(п. 8 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>9 - 11. Утратили силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</p>
 
<p>12. На занятых на работах с осужденными работников образовательных учреждений и учебно-консультационных пунктов, которые создаются при учреждениях, исполняющих уголовные наказания в виде лишения свободы, распространяются порядок и условия установления пенсии в связи с особыми условиями труда, предусмотренные для занятых на работах с осужденными работников учреждений, исполняющих уголовные наказания в виде лишения свободы.</p>
 
