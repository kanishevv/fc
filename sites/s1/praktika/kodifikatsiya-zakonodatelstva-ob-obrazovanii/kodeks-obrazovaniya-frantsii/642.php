<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.642-6</b></span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar">По ходатайству министра, ответственного за высшее образование, может производиться отзыв права выдавать дипломы инженера. Решение об отзыве принимается в формах и органами, предусмотренными статьями L.642-4 и L.642-5. Однако решение об отзыве может быть принято только после предупреждения, данного по докладу инспектора, специально назначенного для этой цели комиссией по инженерным званиям, после которого при прохождении новой инспекции с годичным интервалом констатируется неэффективность. Комиссия принимает все необходимые меры, чтобы защитить права учащихся, находящихся на обучении с целью получения диплома инженера.</span></p>
<span class="textmar"> </span>
<p> </p>
<span class="textmar"> </span>
<p><span class="textmar"><b>Статья L.642-7</b></span></p>
