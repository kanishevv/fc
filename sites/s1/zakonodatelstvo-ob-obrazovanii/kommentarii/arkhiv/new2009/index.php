<p><span class="text" style="text-align: justify;">другом высшем учебном заведении, если они параллельно получают второе высшее профессиональное образование;</span></p>
 
<p><span class="text" style="text-align: justify;">ординатуре или интернатуре медицинских высших учебных заведений.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Статус слушателей учреждений системы высшего и послевузовского профессионального образования в части получения образовательных услуг приравнивается к статусу студента высшего учебного заведения соответствующей формы обучения.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Слушатели подготовительных отделений федеральных государственных высших учебных заведений, определенных в порядке, предусмотренном пунктом 3.3 статьи 11 настоящего Федерального закона, из числа граждан, проходивших в течение не менее трех лет военную службу по контракту в Вооруженных Силах Российской Федерации, других войсках, воинских формированиях и органах на воинских должностях, подлежащих замещению солдатами, матросами, сержантами, старшинами, и уволенных с военной службы по основаниям, предусмотренным подпунктами &quot;б&quot; - &quot;г&quot; пункта 1, подпунктом &quot;а&quot; пункта 2 и пунктом 3 статьи 51 Федерального закона &quot;О воинской обязанности и военной службе&quot;, обучающиеся по очной форме обучения за счет средств федерального бюджета, обеспечиваются стипендиями в размере, повышенном на 50 процентов по сравнению с размером, установленным для студентов федеральных государственных высших учебных заведений, обучающихся по очной форме обучения.</span></p>
 
