<p><span class="text" style="text-align: justify;">Контрольные цифры приема граждан, обучающихся за счет средств федерального бюджета, а также квоты по целевому приему в высшие учебные заведения, находящиеся в ведении федеральных органов исполнительной власти, устанавливаются ими ежегодно по согласованию с федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования.</span></p>
 
