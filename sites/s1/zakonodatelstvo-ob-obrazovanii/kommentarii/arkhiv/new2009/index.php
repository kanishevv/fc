<p><span class="text" style="text-align: justify;">Статья 10. Порядок создания и реорганизации высших учебных заведений, лицензирования их деятельности и аккредитации</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Вопросы создания и реорганизации высшего учебного заведения регулируются Законом Российской Федерации &quot;Об образовании&quot;.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Учредителем высших учебных заведений, реализующих военные профессиональные образовательные программы (военно-учебных заведений), может быть только Правительство Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Государственные высшие учебные заведения, находящиеся в ведении Российской Федерации, создаются, реорганизуются и ликвидируются Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">Органы государственной власти субъектов Российской Федерации вправе осуществлять управление высшими учебными заведениями, находящимися в ведении субъектов Российской Федерации по состоянию на 31 декабря 2004 года, реорганизацию и ликвидацию указанных высших учебных заведений. Такие реорганизация и ликвидация осуществляются органами исполнительной власти субъектов Российской Федерации по согласованию с федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">Муниципальные высшие учебные заведения создаются, реорганизуются и ликвидируются органами местного самоуправления городских округов в соответствии с законодательством в порядке, установленном муниципальными правовыми актами.</span></p>
 
<p><span class="text" style="text-align: justify;">Органы местного самоуправления муниципальных районов осуществляют управление высшими учебными заведениями, находящимися в ведении муниципальных районов по состоянию на 31 декабря 2008 года, а также вправе осуществлять реорганизацию и ликвидацию указанных высших учебных заведений.</span></p>
 
<p><span class="text" style="text-align: justify;">(абзац введен Федеральным законом от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Негосударственные высшие учебные заведения создаются учредителем (учредителями) в порядке, установленном законодательством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 2 в ред. Федерального закона от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. Право юридического лица в части ведения финансово-хозяйственной деятельности, предусмотренной уставом, возникает у высшего учебного заведения с момента его государственной регистрации. Право на реализацию образовательных программ высшего и послевузовского профессионального образования возникает с момента выдачи ему лицензии.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Лицензия на ведение образовательной деятельности по образовательным программам высшего и послевузовского профессионального образования выдается федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в сфере образования, на основании заключения экспертной комиссии, которая формируется с участием представителей органов исполнительной власти, органа местного самоуправления, на территории которого расположено высшее учебное заведение, представителей образовательных учреждений, научных организаций, экспертов из числа лиц, прошедших соответствующую подготовку в порядке, установленном федеральным органом исполнительной власти, осуществляющим функции по выработке государственной политики и нормативно-правовому регулированию в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">Предметом и содержанием экспертизы является установление соответствия условий осуществления образовательного процесса в высшем учебном заведении требованиям органов государственной власти Российской Федерации и органа местного самоуправления, на территории которого расположено высшее учебное заведение, к обеспеченности такого высшего учебного заведения учебной площадью, санитарным и гигиеническим нормам, требованиям к охране здоровья обучающихся и работников, оборудованию учебных помещений, оснащенности учебного процесса, образовательному цензу педагогических работников и укомплектованности штатов.</span></p>
 
<p><span class="text" style="text-align: justify;">Лицензирование образовательной деятельности высшего учебного заведения по новым для него образовательным программам осуществляется на общих основаниях независимо от наличия лицензии.</span></p>
 
<p><span class="text" style="text-align: justify;">Лицензии на ведение образовательной деятельности по военным профессиональным образовательным программам выдаются только федеральным государственным высшим учебным заведениям в порядке, установленном Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">Лицензии высшим учебным заведениям независимо от их организационно-правовых форм могут быть выданы только при наличии у них на правах собственности, оперативного управления, безвозмездного пользования или аренды необходимой учебно-материальной базы.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 28.02.2008 № 14-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Особенности предмета и содержания экспертизы, проводимой в отношении высших учебных заведений, реализующих образовательные программы частично или в полном объеме посредством дистанционных образовательных технологий, устанавливаются федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в сфере образования, если иное не установлено федеральными законами.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 4 в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Право выдачи документов государственного образца об образовании появляется у высшего учебного заведения с момента его государственной аккредитации, проводимой федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в сфере образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 5 в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">6. Утратил силу. - Федеральный закон от 20.04.2007 № 56-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">7. Государственная аккредитация высшего учебного заведения проводится федеральным органом исполнительной власти, осуществляющим функции по контролю и надзору в сфере образования, по заявлению высшего учебного заведения, согласованному с учредителем (учредителями), в порядке, установленном Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Свидетельство о государственной аккредитации устанавливает (подтверждает на очередной срок) статус высшего учебного заведения, перечень направлений подготовки (специальностей), которые прошли государственную аккредитацию и по которым высшее учебное заведение имеет право выдавать выпускникам соответствующие документы государственного образца об образовании.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">8. Высшие учебные заведения могут получать общественную аккредитацию. Общественной аккредитацией является признание уровня деятельности высшего учебного заведения, отвечающего критериям и требованиям соответствующих общественных образовательных, профессиональных, научных и промышленных организаций. Общественная аккредитация не влечет за собой финансовых или иных обязательств со стороны государства.</span></p>
 
<p><span class="text" style="text-align: justify;">9. Порядок лицензирования образовательной деятельности и порядок государственной аккредитации высших учебных заведений, реализующих образовательные программы, содержащие сведения, составляющие государственную тайну, - высших учебных заведений федерального органа исполнительной власти, уполномоченного в области внешней разведки, федерального органа исполнительной власти, осуществляющего государственное управление в области обеспечения безопасности Российской Федерации, федерального органа исполнительной власти, осуществляющего функции по выработке государственной политики, нормативно-правовому регулированию, контролю и надзору в сфере государственной охраны, устанавливаются Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 9 введен Федеральным законом от 20.04.2007 № 56-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">10. Порядок лицензирования образовательной деятельности и порядок государственной аккредитации высших учебных заведений, реализующих образовательные программы высшего профессионального образования в соответствии с пунктом 4 статьи 5 настоящего Федерального закона, устанавливаются Правительством Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 10 введен Федеральным законом от 01.12.2007 № 309-ФЗ)</span></p>
 
