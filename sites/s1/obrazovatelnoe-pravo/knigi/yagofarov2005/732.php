		<span class="text">на образование в соответствии с государственными стандартами и приобретение знаний, адекватных современному уровню развития науки, техники и культуры;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">на обучение в рамках государственных образовательных стандартов по индивидуальным учебным планам;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">на ускоренный курс обучения;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">на получение дополнительных (в том числе платных) образовательных услуг;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">на государственную (итоговую) аттестацию по завершении освоения образовательных программ среднего профессионального образования независимо от формы их освоения;</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">на участие в обсуждении и решении важнейших вопросов деятельности среднего специального учебного заведения, в том числе через органы самоуправления и общественные организации;</span>
