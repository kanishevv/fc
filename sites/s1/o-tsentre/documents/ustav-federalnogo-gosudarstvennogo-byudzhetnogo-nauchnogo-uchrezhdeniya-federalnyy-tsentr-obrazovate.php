	<p align="center">
		 I. Общие положения
	</p>
	<p align="center">
 <br>
	</p>
	<p>
		 1.1. Федеральное государственное бюджетное научное учреждение «Федеральный центр образовательного законодательства» (далее – Центр) является унитарной некоммерческой организацией, созданной для осуществления научных&nbsp;и иных функций.
	</p>
	<p>
		 1.2.&nbsp;Учредителем Центра является Российская Федерация.
	</p>
