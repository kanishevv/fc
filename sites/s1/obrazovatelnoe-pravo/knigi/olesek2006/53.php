<p style="text-align: justify;">
 <span class="text">Деление на курсы с введением в действие настоящего проекта отменялось начиная с прежнего второго курса. Первый же курс составлял исключение; предметная система на него не распространялась, так как на него приходились так называемые «длинные курсы», лежащие в основе дальнейших занятий, а также потому что считалось целесообразным дать студенту, только что вышедшему из школы, возможность осмотреться и свыкнуться с характером вузовской учебы. Все изучаемые предметы и выполняемые студентами практические занятия распределялись по семестрам и по порядку их последовательности и взаимозависимости с таким расчетом, чтобы полный курс мог быть пройден в течение 5 лет, разделенных на первый младший курс (1-2 семестры), второй общий курс (3-6 семестры) и третий специальный курс (7-10 семестры).</span>
</p>
<p style="text-align: justify;">
 <span class="text">Предметы, связанные между собой в своей последовательности, составляли отдельные группы. Прохождение предметов одной группы и сдача экзаменов могли не зависеть от предметов другой группы, чтобы студент, заинтересовавшийся известным циклом предметов, имел возможность отдаться ему предпочтительно, не прерывая своих занятий посторонними предметами.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Прослушивание курсов, сдача экзаменов и выполнение отдельных работ ограничивались конкретными сроками семестров; при этом студентам предоставлялась возможность избрать самостоятельно как период сдачи экзаменов, так и сроки выполнения работ.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Студенты старших семестров заявляли письменно заведующему учебной частью за две недели до начала экзаменационного периода о своем желании приступить к экзаменам по тем предметам, которые они имели возможность прослушать, при условии соблюдения последовательности прохождения предметов и выполнения соответствующих практических занятий. К каждому экзаменационному периоду составлялись списки студентов на каждый предмет и определялось число экзаменационных дней сообразно с числом записавшихся.</span>
</p>
<p style="text-align: justify;">
 <span class="text">В литературе имеются материалы о поэтапном введении нового метода в Киевском политехническом институте, где на предметную систему сначала были переведены старшие курсы, в то время как первые два продолжали заниматься по прежней курсовой системе. Имеются материалы о практике Рижского политехнического института, который одним из первых среди российских технических вузов перешел на новую систему.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Примерно такая же практика характеризовала деятельность других вузов. Материалы о характере и ходе реформы освещены в довольно обширной специальной и общей литературе (правда, как правило, старой; к сожалению, в современной литературе следа той реформы не сохранилось, и отыскать его практически невозможно). Богатые конкретные данные на этот счет можно почерпнуть из публикаций И. Озерова «К реформе преподавания» (М., 1907), доктора зоологии Санкт-Петербургского университета И. Вагнера «Чем должен быть университет?» (СПб., 1907), в «Отчете совета Высших женских курсов за 1907-1908 гг.» (СПб., 1909) и др.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Материалы, дающие представление о ходе вузовской реформы в отдельных регионах и учебных заведениях можно найти в публикациях Ивановского «Предметная система в наших университетах применительно к философским наукам» (Журнал Министерства народного просвещения. Новая серия. Ч. ХII. 1907, ноябрь), А. А. Воронова «О положении высшего технического образования в связи с предметной системой» (Техническое образование. 1911. № 1).</span>
</p>
<p style="text-align: justify;">
 <span class="text">Пожалуй, наиболее полные данные ожидают заинтересованного исследователя в архивных фондах. Нам удалось обнаружить таковые в Государственном историческом архиве Ленинградской области (ГИАЛО). Наибольший интерес представляют следующие материалы: «Дело о разработке предметной системы преподавания. Переписка с департаментом народного просвещения, протоколы предметной комиссии при университете, положения об организации преподавания, представления, учебные планы» (ГИАЛО, ф. 14, оп. 1, д. 10028) и «Представления факультетов о порядке прохождения университетского курса и об учебных планах факультетов; представление ректора в департамент народного просвещения о порядке осуществления предметной системы в университете; переписка о новых учебных планах» (ГИАЛО, ф. 14, оп. 1, д. 10510, 10671).</span>
</p>
<p style="text-align: justify;">
 <span class="text">К сожалению, в силу объективных и субъективных обстоятельств, среди которых были и такие, как первая русская революция и последовавшая за ней реакция, а затем начавшаяся Первая мировая война с вытекающими из нее последствиями, и т. д., реформа не получила должного завершения. В литературе, как об этом можно судить по приведенным выше публикациям и иным материалам, отчетливо отложились моменты подготовки и начала реформы (Правила о переходе на новую систему были опубликованы 12 июня 1906 г.), но практически не обнаружено материалов с подведением ее итогов.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Имеются лишь отдельные косвенные свидетельства происходившего. Как известно, в 1906-1909 гг. на посту министра народного просвещения был Кауфман фон Туркестанский, отличавшийся либеральными взглядами и настроениями. При нем и были осуществлены основные мероприятия по реализации планов преобразования российских университетов, которое рассматривалось как непосредственное продолжение и развитие университетской демократии по примеру западных университетов. В 1908 г. его сменил на посту министра А. Н. Шварц, оставивший о себе в истории русской школы нелестную славу реакционера и ретрограда. Он и отменил как вредные, по его мнению, глубокие преобразования университетской системы. Во всяком случае, так основательно подготовленная и активно начатая реформа, принесшая обнадеживающие результаты, по не выясненным до настоящего времени причинам не получила достойного завершения и, по существу, была забыта.</span>
</p>
<p style="text-align: justify;">
 <span class="text">Возможно, уже тогда ощущалось приближение глобальных тектонических разломов и, таким образом, на судьбах реформы так или иначе сказались крупные социально-политические катаклизмы всемирного масштаба, которые круто изменили историческую судьбу России</span>.
