 <b>Статья 3. Законодательство Российской Федерации в области образования</b></p>
 
<p>1. Законодательство Российской Федерации в области образования включает в себя Конституцию Российской Федерации, настоящий Федеральный закон, принимаемые в соответствии с ним другие законы и иные нормативные правовые акты Российской Федерации, а также законы и иные нормативные правовые акты субъектов Российской Федерации в области образования.</p>
 
<p>2. Федеральные законы в области образования, включая настоящий Закон:</p>
 
<p>разграничивают компетенцию и ответственность в области образования федеральных органов государственной власти, органов государственной власти субъектов Российской Федерации и органов местного самоуправления;</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>регулируют в рамках установленной федеральной компетенции вопросы отношений в области образования, которые должны решаться одинаково всеми субъектами Российской Федерации. В этой части федеральные законы в области образования являются законами прямого действия и применяются на всей территории Российской Федерации;</p>
 
<p>вводят общие установочные нормы по вопросам, которые относятся к компетенции субъектов Российской Федерации и в соответствии с которыми последние осуществляют собственное правовое регулирование в области образования.</p>
 
<p>3. Субъекты Российской Федерации в соответствии с их статусом и компетенцией могут принимать в области образования законы и иные нормативные правовые акты, не противоречащие федеральным законам в области образования.</p>
 
<p>Законы и иные нормативные правовые акты субъектов Российской Федерации в области образования не могут ограничивать права физических и юридических лиц по сравнению с законодательством Российской Федерации в области образования.</p>
 
<p>4. Физические и юридические лица, нарушившие законодательство Российской Федерации в области образования, несут ответственность в порядке, установленном законодательством Российской Федерации.</p>
 
