<p><b>Статья 11.1. Государственные и негосударственные образовательные организации</b></p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>1. Государственные и негосударственные образовательные организации могут создаваться в организационно-правовых формах, предусмотренных гражданским законодательством Российской Федерации для некоммерческих организаций.</p>
 
<p>2. Деятельность государственных и негосударственных образовательных организаций в части, не урегулированной настоящим Законом, регулируется законодательством Российской Федерации.</p>
 
