<p>Под образованием в настоящем Законе понимается целенаправленный процесс воспитания и обучения в интересах человека, общества, государства, сопровождающийся констатацией достижения гражданином (обучающимся) установленных государством образовательных уровней (образовательных цензов).</p>
 
<p>Под получением гражданином (обучающимся) образования понимается достижение и подтверждение им определенного образовательного ценза, которое удостоверяется соответствующим документом.</p>
 
<p>Право на образование является одним из основных и неотъемлемых конституционных прав граждан Российской Федерации.</p>
 
<p>Образование в Российской Федерации осуществляется в соответствии с законодательством Российской Федерации и нормами международного права.</p>
 
