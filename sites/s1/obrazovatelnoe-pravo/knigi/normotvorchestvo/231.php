  </span><span class="text">Так, на 10 тыс. населения в УрФО приходится 129 обучающихся в учреждениях начального профессионального образования (в России – 114), в учреждениях среднего профессионального образования эта цифра достигает 170 (в России – 159), в высших учебных заведениях – 291 студент (в России – 294).[39]</span></p>
 
<p style="text-align: justify;"><span class="text">При этом динамику роста студентов в высших учебных заведениях за последние годы можно проследить на примере вузов Челябинской области: количество студентов только в государственных вузах увеличилось более чем в 2 раза (с 56 до 140 тыс.). В 1999 г. показатель численности студентов всех форм обучения в расчете на 10 тыс. населения области составлял 215 чел., в 2000 г. – уже 243, в 2001 г. – 273. При включении в этот показатель военных и негосударственных вузов эта цифра возрастает до 389 человек.[40]</span></p>
 
<p style="text-align: justify;"><span class="text">Общая статистика по сети образовательных учреждений также указывает на серьезный дисбаланс между уровнями образования.[41]Всего в Уральском федеральном округе насчитывается более 5 тыс. общеобразовательных учреждений, 360 учреждений начального профессионального и 232 среднего профессионального образования, 75 вузов (в том числе 19 негосударственных), в которых обучается более полутора миллионов школьников, 160,5 тыс.чел. учащихся начального профессионального, около 250 тысяч студентов среднего и 500 тысяч студентов высшего образования. Ежегодно в округе подготавливается более 100 тысяч квалифицированных рабочих и специалистов, которые распределяются для отраслей материального производства и непроизводственной сферы в соотношении 75,8 – 24,2 (табл. 3). В целом по России это соотношение в настоящее время составляет 70,0 против 30,0. В то время, как структура экономики уже представлена: 51,4 – сфера производства; 48,6 – непроизводственная сфера (таблица 3, рис. 6).[42]</span></p>
 
