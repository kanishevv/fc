<p align="center"><b><span class="text" style="text-align: justify;">Глава III. СУБЪЕКТЫ УЧЕБНОЙ И НАУЧНОЙ ДЕЯТЕЛЬНОСТИ</span></b></p>
 
<p align="center"><b><span class="text" style="text-align: justify;">В СИСТЕМЕ ВЫСШЕГО И ПОСЛЕВУЗОВСКОГО ПРОФЕССИОНАЛЬНОГО</span></b></p>
 
<p align="center"><b><span class="text" style="text-align: justify;">ОБРАЗОВАНИЯ, ИХ ПРАВА И ОБЯЗАННОСТИ</span></b></p>
 
