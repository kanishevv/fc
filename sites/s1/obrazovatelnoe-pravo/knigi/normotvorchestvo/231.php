?><b style="font-size: 13px;"><span class="text" style="text-align: center;"></span></b><p style="text-align: justify;"><b>Трансформации в сфере науки и образования и проблемы реализации&nbsp;</b><b>государственной образовательной политики в регионах</b></p><b><span class="text" style="text-align: center;"></span></b>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span><span class="text">Современное состояние системы образования свидетельствуют о сдержанном ее влиянии на развитие общества по ряду причин.</span></p>
 
<p style="text-align: justify;"><span class="text">Во-первых, отмечаются противоречия как внутри самой системы, так и за ее пределами. Во-вторых, система образования сохраняет в себе признаки общества прошлого (система управления, переподготовки педагогических и руководящих работников, стареющая материально-техническая база и др.). Одновременно она вбирает в себя признаки открытости для мирового сообщества, интегрированности с экономикой и глобализации инновационных процессов (обучающиеся поколенияnext, информационные технологии, новые материалы и средства для обучения и др.).</span></p>
 
<p style="text-align: justify;"><span class="text">Государство, с одной стороны, опирается на систему образования старой формации как адаптивную к его заказу, с другой стороны, возлагает на нее задачу реформирования общества. В этой ситуации требуется не только устранение обозначенных противоречий, но и консолидация усилий всех общественных и государственных органов управления образованием для решения новой задачи – стратегического развития общества средствами образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Принципы образовательной политики определяются во многом особенностями регионального уровня, содержанием социально-экономической концепции развития субъектов Федерации и кадровой спецификой региональных систем образования. Поэтому стратегические образовательные установки федерального масштаба, направленные, прежде всего, на интеграцию России в мировое сообщество, должны быть ориентированы на региональный, муниципальный, институциональный (организации, учреждения и их инфраструктура) уровни.</span></p>
 
<p style="text-align: justify;"><span class="text">Однако по мере расширения полномочий региональных органов власти федеративно-региональные процессы развития российской государственности вступают в противоречия. Российский регионализм демократизирует и диверсифицирует условия и сам процесс научно-технического развития, поскольку вносит в него элементы инициативы, конкуренции в борьбе за перспективы своего развития. Очевидно, что достичь научно-технического прогресса одновременно на всей территории страны невозможно. Поэтому для получения максимального экономического и социального эффекта необходимо следовать модели концентрированного развития перспективных регионов, способных обеспечить быструю модернизацию и возглавить процессы реформирования всей российской экономики.[33]</span></p>
 
<p style="text-align: justify;"><span class="text">Максимальный эффект можно получить только при осуществлении перехода от концепции регионального развития с позиции форсированного наращивания материального потенциала к концепции, ориентированной на развитие знания и инновации как на важнейшие движущие силы и инструмент реформирования.</span></p>
 
<p style="text-align: justify;"><span class="text">Наилучшие шансы, как полагает М.Н. Денисевич[34], имеет модель «регион – квазикорпорация», построенная по организационно-экономическому типу крупной корпорации. Концепция-модель «регион-квазикорпорация» подразумевает, что правительство и население, все элементы и подсистемы производства, социальной и управленческой деятельности должны работать в режиме единой стратегии достижения согласованных общих целей развития и руководствоваться единой политикой региона, нормами корпоративного управления, основанными на экономичных принципах затрат.</span></p>
 
<p style="text-align: justify;"><span class="text">Для «региона-квазикорпорации», избравшего для себя инновационную модель развития, становится целесообразным создание единой современной системы науки и образования, плотной сети их взаимодействия с поддержкой синергетического эффекта на важнейших направлениях стратегического развития.[35]</span></p>
 
<p style="text-align: justify;"><span class="text">Трансформации в сфере науки и образования затрагивают широкий спектр проблем, прежде всего, социальных и институциональных условий их развития. Эти изменения должны коснуться их общественного статуса, в частности, места в системе приоритетов социальной политики и внутренних механизмов развития учреждений науки и образования.</span></p>
 
<p style="text-align: justify;"><span class="text">На уровне региона требуют решения:</span></p>
 
<p style="text-align: justify;"><span class="text">-структурная перестройка систем образования;</span></p>
 
<p style="text-align: justify;"><span class="text">-разработка методик оценки социально-экономической эффективности программ развития и механизмов их мониторинга на основе системы показателей;</span></p>
 
<p style="text-align: justify;"><span class="text">-разработка методологии координации и управления крупными образовательно-социальными проектами и системами иерархических уровней;</span></p>
 
<p style="text-align: justify;"><span class="text">-разработка научно-методологического обеспечения интеграционных процессов в высшей школе с учетом тенденций развития сферы образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Далее, уровень интеллектуального потенциала страны, задаваемый требованиями образовательных стандартов и обеспечиваемый условиями отдельного образовательного учреждения, формируется в рамках государственной политики, прежде всего в регионах, и становится важнейшим фактором ее выживания.</span></p>
 
<p style="text-align: justify;"><span class="text">Сегодня становится ясным, что общепринятая, традиционная аттестация обучающихся и методы контроля за успеваемостью устарели. Эта проблематика существенно расширилась и поднялась до уровня перемен во внешнем мире. Такие проблемы, как качество образования и управления, контроль за издержками, профессионализация или регионализация требуют новых подходов к их решению.</span></p>
 
<p style="text-align: justify;"><span class="text">Но одного решения этих проблем недостаточно. Общественность, непосредственные работники системы образования имеют право и обязаны знать, в какой степени инвестиции будут способствовать достижению ожидаемых результатов. Поэтому оценка качества образования помогает действующим субъектам сделать обоснованные выводы о текущей ситуации и, что даже более важно, принять квалифицированные управленческие решения.</span></p>
 
<p style="text-align: justify;"><span class="text">Отсюда разработка системы показателей, характеризующих состояние и уровень достижения целей и задач развития образования, а также разработки обоснованных критериев оценки деятельности образовательных систем различных иерархических уровней, учитывающих тенденции развития территорий, и др., является одним из условий успешной реализации государственной образовательной политики и должно создаваться в большей степени на уровне регионов. Тем более, это возможно сделать на уровне федеральных округов, в рамках которых гораздо легче, чем в российском масштабе, обеспечить эффективное функционирование интегрированных образовательных структур, выполняющих функции как объективной оценки деятельности в целом систем образования и отдельных образовательных учреждений, так и разработки стратегии развития регионального образования в конкретных социально-экономических и нормативно-правовых условиях.</span></p>
 
<p style="text-align: justify;"><span class="text">Однако даже на уровне федеральных округов такая работа потребует значительных усилий как научной и педагогической общественности, так и органов исполнительной и законодательной власти. Это обусловлено тем, что при усиливающейсятенденции к регионализации образо­вания все же развитие образовательных систем субъектов Российской Федерации происходит неоднородно и неравномерно. Объективными причинами диспропорций в эволюции регио­нальных образовательных систем выступают социально-экономические и культурно-истори­ческие особенности развития конкретных тер­риторий, которые определяют характер и тем­пы реализации национально-региональных ин­новационных моделей в сфере образования. В ряду субъективных причин неравномернос­ти развития можно назвать некоторые отли­чительные черты региональной образователь­ной политики субъектов Федерации и действующих в них моделей управления образовани­ем. Они характеризуются уровнем развития ре­гионального образовательного сообщества, в том числе – управленческой позицией регио­нального лидера образования, и т. д.[36]</span></p>
 
<p style="text-align: justify;"><span class="text">Анализируемая тенденция, безусловно, име­ет положительные аспекты, которые опреде­ляются включением внутренних потенциалов саморазвития регионов в процесс модерниза­ции образовательных систем. В то же время реализация эффективной государственной об­разовательной политики, приоритетами кото­рой являются сохранение и укрепление едино­го образовательного пространства страны, формирование непрерывного образования на принципах территориальности и многоуровневости, социальная адресность и сбалансированность социальных интересов, обусловливает необхо­димость согласованного развития систем об­разования субъектов РФ и, прежде всего, согласованности управления образовательными системами различных иерархических уровней, основанного на принципах правового регулирования. Это позволит полу­чить дополнительный эффект, связанный с ко­ординацией процессов и организацией взаимо­действия и взаимообогащения региональных образовательных систем.</span></p>
 
<p style="text-align: justify;"><span class="text">Мировой опыт региональной антидепрессивной политики показал, что огромные суммы, которые выделяли национальные бюджеты на простое поддержание стагнирующей региональной экономики, лишены перспективы. Для того чтобы вывести регион из состояния нищеты и апатии, необходимо направить крупную часть социальных средств на ускоренное воспроизводство элементов инновационной микросреды, усиление социальной и профессиональной мобильности. Это позволит изнутри инициировать и создавать макроэкономические сдвиги в направлении стабилизации.</span></p>
 
<p style="text-align: justify;"><span class="text">Вот почему разработка механизмов мониторинга программ развития регионального образования на основе системы показателей, характеризующих состояние и уровень достижения целей и задач развития образования, является одним из условий успешной реализации государственной образовательной политики в регионах.</span></p>
 
<p style="text-align: justify;"><span class="text">Заказчиком таких разработок могут выступить органы исполнительной власти субъектов Федерации, профессиональные объединения и ассоциации педагогов, образовательные учреждения и представители организаций педагогической и родительской общественности, а оценка состояния региональных систем образования, формирование на ее основе содержания и процесса повышения квалификации управленческих кадров для сферы образования, затрагивающие интересы всех и каждого, в этом случае могут сыграть роль консолидирующего и системообразующего фактора в формировании стратегии развития регионов.</span></p>
 
<p align="center" style="text-align: justify;"><b><span class="text">
      <br>
    </span><span class="text" style="text-align: center;">Механизм институционального оформления и нормативного закрепления</span></b></p>
 
<p align="center" style="text-align: justify;"><b><span class="text" style="text-align: center;">региональной системы непрерывного образования – научная и практическая</span></b></p>
 
<p align="center" style="text-align: justify;"><b><span class="text" style="text-align: center;">проблема развития образования</span><span class="text">
      <br>
    </span></b></p>
 
<p style="text-align: justify;"><span class="text">Логическим продолжением реструктуризации социальной политики, становятся структурные изменения научных и образовательных приоритетов. Необходимость преодоления депрессивного состояния регионов требуют новой оценки сложившейся в России системы воспроизводства интеллектуального и социально-культурного потенциала нации.</span></p>
 
<p style="text-align: justify;"><span class="text">Одним из путей может служить развитие системы непрерывного образования. Непрерывное образование – это социально-педагогическая категория, выражающая идею поступательного развития образовательных потребностей человека как естественноисторического и конкретного социально-педагогического процесса. Глобальность идеи непрерывного образования нашла отражение в работе международной организации ЮНЕСКО, где концепция непрерывного образования с 1972 г. стала ведущим направлением деятельности в области общемировой образовательной политики. Термин «непрерывное образование» имеет три значения:</span></p>
 
<p style="text-align: justify;"><span class="text">1) как образование на протяжении всей жизни человека;</span></p>
 
<p style="text-align: justify;"><span class="text">2) как образование на протяжении трудовой жизни человека;</span></p>
 
<p style="text-align: justify;"><span class="text">3) как непрерывный глобальный процесс накопления, сохранения и приращения людьми обыденного и научного знания на протяжении всей истории человечества.</span></p>
 
<p style="text-align: justify;"><span class="text">Как социально-педагогическая категория непрерывное образование включает в себя следующие значения:</span></p>
 
<p style="text-align: justify;"><span class="text">- концептуальное, раскрывающее направленность развития образовательных потребностей личности как сущностной ее характеристики;</span></p>
 
<p style="text-align: justify;"><span class="text">- организационное, описывающее непрерывное образование как систему, связывающую его звенья по вертикали (от низших ступеней к высшим) и по горизонтали (от институционных форм образования к общественным, неформальным);</span></p>
 
<p style="text-align: justify;"><span class="text">- методологическое, когда непрерывное образование выступает в значении социально-педагогического принципа, который отражает новый методологический подход к образованию как социальному институту, реализующему современные общественные тенденции построения образования в виде целостной системы, направленной на развитие личности и выступающей в качестве условия социального прогресса, преемственности его институциональных и неформальных форм, образования и самообразования личности;</span></p>
 
<p style="text-align: justify;"><span class="text">- междисциплинарное понятие, выражающее направление интеграционных процессов в системе непрерывного профессионального образования, результатом которых является качественно новый уровень синтеза общественно необходимых требований к специалисту, преемственность между низшей, средней и высшей ступенью профессионального образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Система непрерывного образования представляет собой комплекс государственных и негосударственных учреждений и учебных заведений, обеспечивающих организационное и содержательное единство и преемственную взаимосвязь всех звеньев образования, совместно и скоординировано решающих задачи общеобразовательной, политехнической и профессиональной подготовки и воспитания каждого человека с учетом актуальных и перспективных общественных потребностей и удовлетворяющих его стремлению к самообразованию, всестороннему и гармоничному развитию на протяжении всей жизни. Существует сверхзадача непрерывного образования, имеющая четкую социально-педагогическую направленность: создание необходимых условий для всестороннего гармоничного развития каждого человека, независимо от возраста, профессии или специальности, места жительства. При этом необходимо учитывать его индивидуальные способности, мотивы, интересы, ценностные установки.[37]</span></p>
 
<p style="text-align: justify;"><span class="text">Целостность единой системы непрерывного образования придают ей новые интегративные свойства, сущность которых определяется ключевыми понятиями: преемственность, прогностичность, гибкость, динамичность.</span></p>
 
<p style="text-align: justify;"><span class="text">Преемственность предполагает постепенный переход от одной ступени образовательной лестницы к другим, последовательность смены требований к объему знаний, умений и навыков, глубина их усвоения, органическая взаимосвязь содержания, методов и форм учебно-воспитательного процесса.</span></p>
 
<p style="text-align: justify;"><span class="text">Прогностичность – это ориентированность системы на перспективные запросы и потребности, как в материальной, так и духовной сфере общественного развития.</span></p>
 
<p style="text-align: justify;"><span class="text">Подготовка специалистов немыслима без внесения своевременных коррективов в прогностичные модели выпускников, в квалификационные характеристики, учебные планы и программы на основе стыковки результатов экономической, социальной, научно-технической производственной и собственно педагогической прогнозированности.</span></p>
 
<p style="text-align: justify;"><span class="text">Нами предложена следующая структурная схема непрерывного образования (рис. 1), где можно выделить три плоскости:</span></p>
 
<p style="text-align: justify;"><span class="text">первая – это институционально оформленные уровни образования;</span></p>
 
<p style="text-align: justify;"><span class="text">вторая – законодательно закрепленные формы получения образования;</span></p>
 
<p style="text-align: justify;"><span class="text">третья – исторически сложившиеся виды накопителей и носителей знания.</span></p>
 
<p style="text-align: justify;"><span class="text">Рассмотрение непрерывного образования с таких позиций, на наш взгляд, позволит решить следующие задачи:</span></p>
 
<p style="text-align: justify;"><span class="text">-найти приемлемые формы интеграции между уровнями образования и придать им легитимность;</span></p>
 
<p style="text-align: justify;"><span class="text">-сформировать региональный заказ на подготовку специалистов и нормативно закрепить отношения между социальными партнерами в сфере образования и труда;</span></p>
 
<p style="text-align: justify;"><span class="text">-создать условия для широкого информационного обмена внутри системы образования и вне ее.</span></p>
 
<p style="text-align: justify;"><span class="text">При этом первая задача может быть решена путем создания институционально оформленной и нормативно закрепленной региональной системы непрерывного образования.</span></p>
 
<p align="center" style="text-align: justify;"><span class="text"><img width="592" alt="231-1.gif" src="/upload/medialibrary/54a/231-1.gif" height="486" title="231-1.gif" border="0" class="donotresize"></span></p>
 
<p style="text-align: justify;"><span class="text"> 
    <br>
   </span></p>
 
<p align="center" style="text-align: justify;"><span class="text">Рис. 1. Структурно-логическая схема реализации идеи непрерывного образования</span><span class="text">
    <br>
  </span></p>
 
<p style="text-align: justify;"><span class="text">Вторая задача успешно может быть решена путем урегулирования отношений между социальными партнерами в сфере образования и труда.</span></p>
 
<p style="text-align: justify;"><span class="text">Решению третьей задачи будет способствовать реализация широкомасштабного проекта создания Большого Евразийского университета в Екатеринбурге.</span></p>
 
<p style="text-align: justify;"><span class="text">Данный подход предполагает варианты интеграции интеллектуального потенциала как внутри образовательных систем, так и за их пределами, а также моделей реструктурирования сети образовательных учреждений в соответствии с потребностями экономики и направлениями модернизации.</span></p>
 
<p style="text-align: justify;"><span class="text">Развитие систем образования базируется на прогнозах потребностей экономики региона в профессиональных кадрах и соответствии сети образовательных учреждений данным прогнозам. При этом система образования не может быть только ориентирована на потребности экономики. Она должна ориентироваться на изменение демографической ситуации и учитывать законы развития общества.</span></p>
 
<p style="text-align: justify;"><span class="text">В связи с этим начальным звеном перестройки системы образования может являться структурный сдвиг в сторону сокращения сети учреждений начального и среднего профессионального образования и соответствующего расширения как предшествующего звена – общеобразовательной школы и высшего профессионального образования, представленного уровнем бакалавриата и магистратуры.</span></p>
 
<p style="text-align: justify;"><span class="text">Более оптимальным решением представляется преобразование большей части вузов в центры продолженной профессиональной (профильной) подготовки на базе обязательного общего среднего образования. Эта мера фактически вывела бы профессиональную подготовку молодежи на уровень программ западного колледжа и существенно подняла уровень профессионального образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Процессы интеграции учреждений образования должны идти по пути создания университетских комплексов в составе учреждений образования различных уровней (так называемая вертикальная интеграция): 1) университет, включающий несколько разнопрофильных институтов; 2) университет, в структуре которого представлены несколько колледжей и учреждений, реализующих однопрофильные образовательные программы; 3) университет, в структуре которого представлены разноуровневые образовательные программы (от предпрофильного, начального и среднего профессионального образования, до бакалавриата и магистратуры).</span></p>
 
<p style="text-align: justify;"><span class="text">Возможны варианты горизонтальной интеграции, когда интегрируются разнопрофильные образовательные учреждения одного уровня, например, несколько учреждений начального профессионального образования или колледжей, реализующих программы начального и среднего профессионального образования по разным профессиональным направлениям. Это может позволить расширить спектр предлагаемых образовательных услуг по формам обучения (повышение квалификации, переподготовка и др.), а также оптимизировать ресурсную составляющую образования (в том числе кадровую, финансовую, материально-техническую).</span></p>
 
<p style="text-align: justify;"><span class="text">Однако в реальной практике система непрерывного образования имеет весьма зыбкую почву и базируется в основном на неинституциональных формах взаимодействия, оптимизация которых представляет собой значимую научную и практическую проблему.</span></p>
 
<p align="center" style="text-align: justify;"><b><span class="text">
      <br>
    </span><span class="text" style="text-align: center;">Опыт интеграционных связей и перспективы формирования</span></b></p>
 
<p align="center" style="text-align: justify;"><b><span class="text" style="text-align: center;">региональных университетских комплексов</span><span class="text">
      <br>
    </span></b></p>
 
<p style="text-align: justify;"><span class="text">Законодательное закрепление процессов социальной и институциональной перестройки региональных систем образования потребует значительных средств и времени, поэтому уже сегодня необходимо предпринимать усилия для отработки реальных механизмов интеграции, оценки эффективности программ развития, создания систем управления качеством образования и др.</span></p>
 
<p style="text-align: justify;"><span class="text">В практике региональных систем образования есть опыт взаимодействия субъектов образования в образовательных комплексах в форме договоров, соглашений, контрактов и др. Примером уже наработанных интеграционных связей, являющихся, по сути, университетским комплексом, может служить УГТУ-УПИ.</span></p>
 
<p style="text-align: justify;"><span class="text">УГТУ-УПИ давно сотрудничает со многими учреждениями среднего профессионального образования Свердловской области (Богданович, Камышлов, Среднеуральск, Кировоград, Сысерть, Первоуральск и т.д.). Распоряжением правительства РФ в 2003 году в состав УГТУ-УПИ включен Нижнетагильский машиностроительный техникум, в котором обучается около 1200 студентов по 8 специальностям. Готовится решение о включении в состав вуза Среднеуральского колледжа метрологии и стандартизации. В рамках вновь созданного в составе университета Строительного института идут переговоры с представителями ссузов строительного профиля.</span></p>
 
<p style="text-align: justify;"><span class="text">В состав университета входят 16 факультетов очной формы обучения, факультет повышения квалификации преподавателей, Институт дополнительного образования и профессиональной переподготовки, Институт образовательных информационных технологий и 34 территориальных подразделения. В университете обучается более 40 тысяч студентов, из них около 25 тысяч – студенты очной формы обучения. Учебный процесс ведут 146 кафедр, из них 133 выпускающих.</span></p>
 
<p style="text-align: justify;"><span class="text">Научно-педагогический потенциал Уральского государственного технического университета составляют более 2500 научно-педагогических работников, среди которых 9 действительных членов Российской академии наук, 116 членов общественных академий России, более 250 докторов наук, профессоров и 1200 кандидатов наук, доцентов.</span></p>
 
<p style="text-align: justify;"><span class="text">В состав университета входят 34 территориальных подразделения в городах: Нижний Тагил, Верхняя Салда, Каменск-Уральский, Первоуральск, Ирбит, Сысерть, Верхотурье, Чусовое, Асбест, Нягань, Костанай и др.</span></p>
 
<p style="text-align: justify;"><span class="text">УГТУ-УПИ более 30 лет ведет целевую подготовку специалистов, являясь одним из лидеров в этой области. За эти годы были организованы филиалы кафедр на предприятиях и в организациях, создавались совместные институты для ведения учебной и научно-исследовательской работы. Сейчас целевая подготовка осуществляется в рамках корпоративных университетов, научно-образовательных комплексов. Разработана и внедряется программа стажировок.</span></p>
 
<p style="text-align: justify;"><span class="text">В университете создана система довузовского образования, являющаяся самой крупной и разветвленной в Уральском регионе, разработана схема непрерывного обучения по системе «техникум-вуз», которая пользуется популярностью у выпускников ссузов Свердловской области.</span></p>
 
<p style="text-align: justify;"><span class="text">Университет имеет значительный опыт реализации многоуровневой системы высшего образования. Первоначально в УГТУ-УПИ работала схема, в соответствии с которой инженерное и магистерское образование строились на базе бакалавриата.В вузе 11 направлений бакалавриата, 6 направлений магистра­туры (10 магистерских программ),существует многоуровневая система дополнительной профессиональной подготовки и повышения квалификации, которая проводится центром ДПП ИДОПП, факультетом повышения квалификации, факультетами и кафедрами. Развивается система электронного дистанционного образования.</span></p>
 
<p style="text-align: justify;"><span class="text">Сеть филиалов и представительств университета является основным источником профессионального и управленческого звена малых городов Урала, поддерживая на должном уровне развитие промышленного потенциала региона. В области научно-исследовательской деятельности университет особенно тесно и эффективно взаимодействует с Российской академией наук.</span></p>
 
<p style="text-align: justify;"><span class="text">Основным принципом создания такого университетского комплекса являются единство учебного, научного и инновационного процессов в университете и его связь с экономикой и социальной сферой. Единство и связь достигается объединением вокруг базового университета в инновационную инфраструктуру целого ряда организаций различного правового статуса, созданных при участии университета для реализации взаимодействия с промышленными предприятиями, научными организациями, образовательными учреждениями и бизнес-струкурами. Взаимодействие в инфраструктуре осуществляется на основе уставных документов, а такжеассоциативных или двухсторонних договоров (соглашений) осотрудничестве, долгосрочных программ или совместно реализуемых комплексныхпроектов.</span></p>
 
<p style="text-align: justify;"><span class="text"><b><i>Организационной формой университетского комплекса</i></b>может быть как<b><i>единое юридическое лицо</i></b>–<b><i>некоммерческая организация</i></b>(когда в состав автономного образовательного учреждения входят собственно вуз, научныеподразделения, образовательные учреждения других уровней, утрачивающиесвой юридический статус), так и<b><i>объединение юридических лиц в</i></b><b><i>форме ассоциаций, союзов</i></b>как некоммерческих организаций, имеющих илине имеющих права юридического лица.</span></p>
 
<p style="text-align: justify;"><span class="text">В настоящее время эксперимент по разработке моделей и механизмовсоздания и функционирования университетских комплексов продолжается. Входе эксперимента уточняются: модель университетского комплекса сединым юридическим лицом; модель университетского комплекса в видеассоциации юридических лиц; модель в виде учебно-научно-инновационного университетского комплекса (рис. 2, 3, 4, 5)</span></p>
 
