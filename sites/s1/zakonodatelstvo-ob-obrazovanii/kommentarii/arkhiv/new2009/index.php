<p><span class="text" style="text-align: justify;">Статья 20. Работники высших учебных заведений</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. В высших учебных заведениях предусматриваются должности научно-педагогического (профессорско-преподавательский состав, научные работники), инженерно-технического, административно-хозяйственного, производственного, учебно-вспомогательного и иного персонала.</span></p>
 
<p><span class="text" style="text-align: justify;">К профессорско-преподавательским относятся должности декана факультета, заведующего кафедрой, профессора, доцента, старшего преподавателя, преподавателя, ассистента.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Особенности заключения и прекращения трудовых договоров на замещение должностей научно-педагогических работников в высших учебных заведениях устанавливаются Трудовым кодексом Российской Федерации, а также распространяются на научно-педагогических работников образовательных учреждений дополнительного профессионального образования (повышения квалификации), имеющих факультеты и кафедры.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 2 в ред. Федерального закона от 24.04.2008 № 50-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3. В государственных и муниципальных высших учебных заведениях особенности замещения должностей ректоров, проректоров, руководителей их филиалов и институтов устанавливаются Трудовым кодексом Российской Федерации.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 3 в ред. Федерального закона от 24.04.2008 № 50-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3.1. В государственном или муниципальном высшем учебном заведении по решению его ученого совета, согласованному с органом исполнительной власти или исполнительно-распорядительным органом городского округа, муниципального района, в ведении которых находится такое высшее учебное заведение, может учреждаться должность президента высшего учебного заведения. При этом в устав высшего учебного заведения в установленном порядке вносятся соответствующие изменения.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Лицо, замещающее должность президента высшего учебного заведения, как правило, должно иметь опыт работы в должности ректора высшего учебного заведения. Совмещение должностей ректора и президента государственного или муниципального высшего учебного заведения не допускается.</span></p>
 
<p><span class="text" style="text-align: justify;">Кандидатура президента государственного или муниципального высшего учебного заведения представляется в ученый совет такого высшего учебного заведения органом исполнительной власти или исполнительно-распорядительным органом городского округа, муниципального района, в ведении которых находится такое высшее учебное заведение.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">Президент высшего учебного заведения избирается на заседании ученого совета тайным голосованием простым большинством голосов на срок до пяти лет. После избрания президента между ним и органом исполнительной власти или исполнительно-распорядительным органом городского округа, муниципального района, в ведении которых находится высшее учебное заведение, заключается трудовой договор на срок до пяти лет. Прекращение трудового договора с президентом высшего учебного заведения осуществляется по основаниям, установленным трудовым законодательством Российской Федерации, в том числе по основаниям прекращения трудового договора с руководителем организации.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 25.12.2008 № 281-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 3.1 введен Федеральным законом от 18.07.2006 № 113-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3.2. Президент высшего учебного заведения по согласованию с его ректором осуществляет следующие полномочия:</span></p>
 
<p><span class="text" style="text-align: justify;">1) участвует в деятельности попечительского совета и иных органов самоуправления высшего учебного заведения;</span></p>
 
<p><span class="text" style="text-align: justify;">2) участвует в разработке концепции развития высшего учебного заведения;</span></p>
 
<p><span class="text" style="text-align: justify;">3) представляет высшее учебное заведение в отношениях с государственными органами, органами местного самоуправления, общественными и иными организациями;</span></p>
 
<p><span class="text" style="text-align: justify;">4) участвует в решении вопросов совершенствования учебной, научной, воспитательной, организационной и управленческой деятельности высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">(п. 3.2 введен Федеральным законом от 18.07.2006 № 113-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Научно-педагогические работники высшего учебного заведения имеют право:</span></p>
 
<p><span class="text" style="text-align: justify;">1) в установленном порядке избирать и быть избранными в ученый совет высшего учебного заведения;</span></p>
 
<p><span class="text" style="text-align: justify;">2) участвовать в обсуждении и решении вопросов, относящихся к деятельности высшего учебного заведения;</span></p>
 
<p><span class="text" style="text-align: justify;">3) пользоваться бесплатно услугами библиотек, информационных фондов, учебных и научных подразделений, а также услугами социально-бытовых и других структурных подразделений высшего учебного заведения в соответствии с его уставом и (или) коллективным договором;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4) определять содержание учебных курсов, предметов, дисциплин (модулей) на основании федеральных государственных образовательных стандартов высшего профессионального образования или федеральных государственных требований, а также устанавливаемых в соответствии с пунктом 4 статьи 5 настоящего Федерального закона образовательных стандартов и требований;</span></p>
 
<p><span class="text" style="text-align: justify;">(пп. 4 в ред. Федерального закона от 01.12.2007 № 309-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5) выбирать методы и средства обучения, наиболее полно отвечающие их индивидуальным особенностям и обеспечивающие высокое качество учебного процесса;</span></p>
 
<p><span class="text" style="text-align: justify;">6) обжаловать приказы и распоряжения администрации высшего учебного заведения в установленном законодательством порядке;</span></p>
 
<p><span class="text" style="text-align: justify;">7) на организационное и материально-техническое обеспечение своей профессиональной деятельности.</span></p>
 
<p><span class="text" style="text-align: justify;">5. Научно-педагогические работники высшего учебного заведения обязаны:</span></p>
 
<p><span class="text" style="text-align: justify;">1) обеспечивать высокую эффективность педагогического и научного процессов;</span></p>
 
<p><span class="text" style="text-align: justify;">2) соблюдать устав высшего учебного заведения;</span></p>
 
<p><span class="text" style="text-align: justify;">3) формировать у обучающихся профессиональные качества по избранному направлению подготовки (специальности), гражданскую позицию, способность к труду и жизни в условиях современной цивилизации и демократии;</span></p>
 
<p><span class="text" style="text-align: justify;">4) развивать у обучающихся самостоятельность, инициативу, творческие способности;</span></p>
 
<p><span class="text" style="text-align: justify;">5) систематически заниматься повышением своей квалификации.</span></p>
 
<p><span class="text" style="text-align: justify;">6. Научные работники высшего учебного заведения имеют право выбирать методы и средства проведения научных исследований, отвечающие мерам безопасности, наиболее полно соответствующие их индивидуальным особенностям и обеспечивающие высокое качество научного процесса. Научные работники обязаны соблюдать устав высшего учебного заведения.</span></p>
 
<p><span class="text" style="text-align: justify;">7. Права и обязанности административно-хозяйственного, инженерно-технического, производственного, учебно-вспомогательного и иного персонала высшего учебного заведения определяются законодательством о труде Российской Федерации, уставом высшего учебного заведения, правилами внутреннего распорядка высшего учебного заведения и должностными инструкциями.</span></p>
 
