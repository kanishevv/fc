<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 131-1</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Обучение обязательно для детей обоего пола, французских и иностранных, в возрасте от шести до шестнадцати лет.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Настоящее положение не является препятствием к применению особых предписаний, требующих более длительного периода обучения.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L.131-2</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Обязательное обучение может быть дано либо в учреждениях или школах, публичных или частных, либо в семье, или любым другим лицом по выбору родителей.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L.131-3</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Выплата социальной помощи на ребенка, обязанного проходить школьное обучение, подчиняется условиям, зафиксированным в положениях Статей L. 552-4 и L. 552-5 Кодекса социального страхования, воспроизводимых ниже:</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">&laquo; Ст.L.552-4. &ndash; Выплата социальной помощи на ребенка, обязанного проходить школьное обучение, обусловлена предоставлением либо справки о записи в какое-либо образовательное учреждение, публичное или частное, либо справки компетентного государственного органа о том, что ребенок обучается в семье, либо медицинской справки о том, что он не может регулярно посещать какое-либо образовательное учреждение по состоянию здоровья.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Выплаты начинаются только с момента представления вышеуказанных документов. Они могут, однако, быть выплачены задним числом, если получатель докажет, что задержка с представлением документа произошла по не зависящим от него обстоятельствам.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Межминистерское постановление устанавливает правила применения данной статьи и, в частности, срок, в течение которого упомянутые выше документы должны быть составлены&raquo;.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">&laquo;Ст.L.552-5. &ndash; Право на социальные выплаты лицам, рассматриваемым как лица без определенного места жительства, в соответствии с законом № 69-3 от 3 января 1969г., касающимся исполнения передвижной деятельности и режима, применяемого к лицам, перемещающимся во Франции без постоянного места жительства, обусловлено подтверждением со стороны заинтересованных лиц посещаемости детьми, обязанными проходить школьное обучение, одного из образовательных учреждений в течение минимального срока в один месяц, фиксированного межминистерским постановлением. Это же постановление определяет условия, согласно которым должны быть представлены требуемые подтверждения&raquo;.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L.131-4</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Лицами, ответственными за применение настоящей главы, являются родители, опекуны или те, кто имеет ребенка на своем иждивении или занимается ребенком по просьбе его родителей, опекуна или компетентного органа, или обладающие постоянной фактической ответственностью за ребёнка.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 131-5</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Лица, ответственные за ребенка, обязанного проходить школьное обучение, определенному статьей L.131-1, должны записать его в образовательное учреждение, публичное или частное, или заявить мэру или инспектору академии, директору служб национального образования департамента, что они будут его обучать в семье. В этом случае требуется ежегодное заявление.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Те же формальности должны быть выполнены в течение восьми дней со дня изменения места проживания или вида обучения.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Настоящая обязанность применяется, начиная с начала учебного года по достижении ребенком шести лет.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Семьи, живущие вблизи двух или нескольких публичных школ, имеют право записать своих детей в любую из этих школ независимо от того, находятся они или нет на территории их коммуны, если только число учащихся в них не достигло уже регламентированного максимума.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Однако в коммунах, имеющих несколько публичных школ, круг деятельности каждой из этих школ определяется постановлением мэра, и семьи должны учитывать положения этого постановления.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Если в населенном пункте существует несколько детских садов и начальных школ, запись учащихся в публичные или частные школы производится по представлению справки о занесении в школьный список, предусмотренный в статье L. 131-6. Эта справка выдается мэром, который указывает в ней школу, которую ребенок должен посещать.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 131-6</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Ежегодно в начале учебного года мэр составляет список всех детей, проживающих в его коммуне, которые обязаны проходить школьное обучение.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Ответственные лица должны вписать туда детей, за которых они отвечают.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 131-7</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Инспектор академии приглашает лиц, ответственных за ребенка, подчиняться закону и сообщает им предусмотренные санкции наказания.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 131-8</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Если ребенок пропускает занятие, ответственные за него лица должны сразу же поставить в известность директора образовательного учреждения о причинах этого отсутствия.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Единственные причины, рассматриваемые как уважительные, суть следующие: болезнь ребенка, заразная или инфекционная болезнь члена семьи, торжественное собрание семьи, причины, вызванные неожиданными трудностями сообщения, временное отсутствие ответственных лиц, если ребенок следует за ними.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Другие причины оцениваются инспектором академии. Последний может консультироваться с выбранными им социальными ассистентами и поручить им провести расследование в том, что касается предполагаемых детей-нарушителей.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Инспектор академии посылает предупреждение лицам, ответственным за ребенка, и напоминают им о санкциях в следующих случаях:</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">1. Если, несмотря на приглашение директора или директрисы образовательного учреждения, они не доведут до сведения причины отсутствия ребенка или дадут неточные причины отсутствия;</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">2. Если ребенок пропустил занятия без уважительной причины или соответствующих разрешений, по крайней мере четыре раза по половине дневной учебной нагрузки в течение месяца.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L.131-9</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Инспектор академии осведомляет прокурора Республики о фактах, составляющих нарушение положений настоящей главы.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L.131-10</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Дети, обязанные проходить школьное обучение и получающие образование в своей семье, начиная с первого года и в дальнейшем каждые два года являются объектом компетентного обследования мэрией, имеющего целью установление причин домашнего обучения, изложенных ответственными за ребенка лицами, соответствия даваемого детям образования их состоянию здоровья и проверку условий жизни в семье.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Результат этого обследования сообщается инспектору академии, директору служб национального образования департамента.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Если обследование не было произведено, то его проведение ускоряется представителем Государства в департаменте.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Инспектор академии должен, по крайней мере, один раз в год, начиная с третьего месяца после заявления об обучении в семье, проверять, соответствует ли осуществляемое образование правам ребенка на образование, как оно определено в статье L. 122-1.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Этот контроль, предписываемый инспектором академии, имеет место непосредственно дома у родителей ребенка.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Этот контроль осуществляется без промедления в случае отсутствия заявления об обучении в семье, независимо от применения уголовных санкций.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Содержание знаний, требуемых от ученика, устанавливается декретом.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Результаты этого контроля сообщаются ответственным лицам за детей с указанием срока, в течение которого они должны представить свои объяснения или улучшить ситуацию, и санкций, которым они будут подвергнуты в противном случае.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Если по окончании нового срока, установленного инспектором академии, результаты контроля определяются как недостаточные, родителям предъявляется требование в течение последующих пятнадцати дней записать их ребенка в публичное или частное образовательное учреждение и поставить об этом в известность мэра, который информирует инспектора академии, школу или учреждение, которое они выбрали.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 0cm;"><b>Статья L. 131-11</b></p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Нарушение обязанностей, вытекающих из статей L.131-10 и L.442-2 настоящего Кодекса, санкционируются положениям статей 227-17-1 и 227-17-2 Уголовного кодекса, воспроизводимых ниже:</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">&laquo; Ст.227 &ndash; 17 &ndash; 1. Тот факт, что родители ребенка или любое лицо, имеющее для него родительскую ответственность или постоянную фактическую ответственность, не записали его в образовательное учреждение без уважительных причин, несмотря на требования инспектора академии, влечет за собой наказание в виде шести месяцев тюремного заключения и штрафом в 50 000 фр.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 35.4pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Тот факт, что директор частного учреждения, принимающий в классы вне контракта, не предпринял, несмотря на требования директора академии необходимых мер для того, чтобы проводимое там обучение соответствовало нормам обязательного образования, определяемым в статьях L.122 &ndash; 1 и L.131-10 Кодекса образования, и не произвел закрытия этих классов, влечет за собой наказание в виде шести месяцев тюремного заключения и штрафом в 50 000 фр. Кроме того, трибунал может приказать запретить ему руководство или обучение и закрыть учреждение&raquo;.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">&laquo;Ст.227-17-2. &ndash; Юридические лица могут быть объявлены уголовно ответственными при условиях, указанных в статье 121-2, за нарушения, определяемые вторым абзацем статьи 227-17-1.</p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Наказание, которым могут подвергнуться юридические лица, суть следующие:</p>
 
<p style="margin-left: 0cm; text-indent: 27pt;">1. Штраф в соответствии с правилами, указанными в статье 131-38.</p>
 
<p style="margin-left: 0cm; text-indent: 27pt;"> </p>
 
<p style="margin-left: 0cm; text-indent: 27pt;">2. Наказания, указанные в п.1, 2, 4, 8 и 9 статьи 131-39&raquo;.</p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"><b>Статья L.131-12</b></p>
 
<p style="margin-left: 0cm; text-indent: 0cm;"> </p>
 
<p style="margin-left: 0cm; text-align: justify; text-indent: 27pt;">Контроль за обязательностью обучения, посещением и прилежанием в школе так же, как санкциями относительно выплат семейных пособий и в области наказаний, определяются декретом Государственного Совета.</p>
