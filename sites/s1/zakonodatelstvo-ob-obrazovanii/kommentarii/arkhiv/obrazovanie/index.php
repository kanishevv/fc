<p><b>Статья 12. Образовательные учреждения</b></p>
 
<p>1. Образовательным является учреждение, осуществляющее образовательный процесс, то есть реализующее одну или несколько образовательных программ и (или) обеспечивающее содержание и воспитание обучающихся, воспитанников.</p>
 
<p>2. Образовательное учреждение является юридическим лицом.</p>
 
<p>3. Образовательные учреждения могут быть государственными (федеральными или находящимися в ведении субъекта Российской Федерации), муниципальными, негосударственными (частными, учреждениями общественных и религиозных организаций (объединений)).</p>
 
<p>Федеральное государственное образовательное учреждение - образовательное учреждение, находящееся в федеральной собственности и финансируемое за счет средств федерального бюджета.</p>
 
<p>Действие законодательства Российской Федерации в области образования распространяется на все образовательные учреждения на территории Российской Федерации независимо от их организационно-правовых форм и подчиненности.</p>
 
<p>(п. 3 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>4. К образовательным относятся учреждения следующих типов:</p>
 
<p>1) дошкольные;</p>
 
<p>2) общеобразовательные (начального общего, основного общего, среднего (полного) общего образования);</p>
 
<p>3) учреждения начального профессионального, среднего профессионального, высшего профессионального и послевузовского профессионального образования;</p>
 
<p>4) учреждения дополнительного образования взрослых;</p>
 
<p>5) специальные (коррекционные) для обучающихся, воспитанников с отклонениями в развитии;</p>
 
<p>6) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</p>
 
<p>7) учреждения для детей-сирот и детей, оставшихся без попечения родителей (законных представителей);</p>
 
<p>8) учреждения дополнительного образования детей;</p>
 
<p>9) другие учреждения, осуществляющие образовательный процесс.</p>
 
<p>5. Деятельность государственных и муниципальных образовательных учреждений регулируется типовыми положениями об образовательных учреждениях соответствующих типов и видов, утверждаемыми Правительством Российской Федерации, и разрабатываемыми на их основе уставами этих образовательных учреждений.</p>
 
<p>Для негосударственных образовательных учреждений типовые положения об образовательных учреждениях выполняют функции примерных.</p>
 
<p>6. Государственный статус образовательного учреждения (тип, вид и категория образовательного учреждения, определяемые в соответствии с уровнем и направленностью реализуемых им образовательных программ) устанавливается при его государственной аккредитации.</p>
 
<p>7. Филиалы, отделения, структурные подразделения образовательного учреждения могут по его доверенности осуществлять полностью или частично правомочия юридического лица.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
<p>8. Образовательные учреждения вправе образовывать образовательные объединения (ассоциации и союзы), в том числе с участием учреждений, предприятий и общественных организаций (объединений). Указанные образовательные объединения создаются в целях развития и совершенствования образования и действуют в соответствии со своими уставами. Порядок регистрации и деятельности указанных образовательных объединений регулируется законом.</p>
 
<p>9. Права и обязанности учреждений дополнительного образования, предусмотренные законодательством Российской Федерации, распространяются и на общественные организации (объединения), основной уставной целью которых является образовательная деятельность, только в части реализации ими дополнительных образовательных программ.</p>
 
