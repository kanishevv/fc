<p><span class="text" style="text-align: justify;">Статья 32. Учет, отчетность и контроль в высших учебных заведениях</span></p>
 
<p><span class="text" style="text-align: justify;">1. Высшие учебные заведения в установленном порядке осуществляют оперативный и бухгалтерский учет результатов своей работы, ведут статистическую и бухгалтерскую отчетность.</span></p>
 
<p><span class="text" style="text-align: justify;">2. Должностные лица несут установленную законодательством Российской Федерации дисциплинарную, административную и уголовную ответственность за искажение государственной отчетности.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Контроль за исполнением законодательства Российской Федерации в области бюджетной и финансовой дисциплины в высших учебных заведениях осуществляют органы законодательной и исполнительной власти в пределах своих полномочий.</span></p>
 
