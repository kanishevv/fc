<p style="text-align: justify;"><span class="text">Комментируемая статья посвящена уставу образовательного учреждения. Устав является учредительным документом большинства юридических лиц. По своему содержанию устав представляет собой основной локальный нормативный акт, закрепляющий основные положения о юридическом лице, правила его деятельности, структуру, устройство, виды деятельности, порядок взаимодействия с другими лицами, органами государственной власти и местного самоуправления, права и обязанности данного юридического лица. Согласно п. 4 комментируемой статьи иные локальные акты образовательного учреждения не могут противоречить его уставу.</span></p>

<p style="text-align: justify;"><span class="text">Устав образовательного учреждения принимается общим собранием (конференцией) данного учреждения и утверждается его учредителем (учредителями), после чего представляется в федеральный орган исполнительной власти, уполномоченный в области государственной регистрации юридических лиц и индивидуальных предпринимателей.</span></p>

<p style="text-align: justify;"><span class="text">Пункты 1 и 2 комментируемой статьи посвящены содержанию устава образовательного учреждения.</span></p>

<p style="text-align: justify;"><span class="text">Все положения устава образовательного учреждения делятся на две группы: существенные и факультативные. Существенными признаются те положения устава, которые должны быть обязательно указаны в нем. Перечень существенных положений устава образовательного учреждения приводиться в п. 1 комментируемой статьи. Факультативными называются положения устава, которые в соответствии с п. 2 комментируемой статьи включаются в него самим образовательным учреждением и утверждаются учредителем данной организации.</span></p>

<p style="text-align: justify;"><span class="text">Существенные положения устава образовательного учреждения, указанные в п. 1 комментируемой статьи, подразделяются на следующие группы:</span></p>

<ul>
  <li>
    <p style="text-align: justify;"><span class="text">средства индивидуализации образовательного учреждения (наименование, место нахождения);</span></p>
  </li>

  <li>
    <p style="text-align: justify;"><span class="text">положения, определяющие правовое положение образовательного учреждения (статус, учредитель, организационно-правовая форма, права и обязанности участников образовательного процесса);</span></p>
  </li>

  <li>
    <p style="text-align: justify;"><span class="text">положения, определяющие предмет деятельности образовательного учреждения (цели образовательного процесса, типы и виды реализуемых образовательных программ);</span></p>
  </li>

  <li>
    <p style="text-align: justify;"><span class="text">основные характеристики организации образовательного процесса (язык (языки), на котором ведутся обучение и воспитание, порядок приема обучающихся, воспитанников, продолжительность обучения на каждом этапе обучения, порядок и основания отчисления обучающихся, воспитанников, система оценок при промежуточной аттестации, формы и порядок ее проведения, режим занятий обучающихся, воспитанников, наличие платных образовательных услуг и порядок их предоставления (на договорной основе), порядок регламентации и оформления отношений образовательного учреждения и обучающихся, воспитанников и (или) их родителей (законных представителей));</span></p>
  </li>

  <li>
    <p style="text-align: justify;"><span class="text">структура финансовой и хозяйственной деятельности образовательного учреждения (порядок использования объектов собственности, закрепленных учредителем за образовательным учреждением, финансирования и материально-технического обеспечения деятельности образовательного учреждения, осуществления предпринимательской деятельности, источники и порядок формирования собственности образовательного учреждения);</span></p>
  </li>

  <li>
    <p style="text-align: justify;"><span class="text">положения, определяющие порядок управления образовательным учреждением (компетенция учредителя, структура, порядок формирования органов управления образовательного учреждения, их компетенция и порядок организации деятельности, порядок комплектования работников образовательного учреждения и условия оплаты их труда, порядок изменения устава образовательного учреждения, порядок реорганизации и ликвидации образовательного учреждения);</span></p>
  </li>

  <li>
    <p style="text-align: justify;"><span class="text">перечень видов локальных актов (приказов, распоряжений и других актов), регламентирующих деятельность образовательного учреждения.</span></p>
  </li>
</ul>

<p style="text-align: justify;"><span class="text">При разработке уставов государственных и муниципальных образовательных учреждений должны учитываться типовые положения об образовательных учреждениях. В настоящее время Правительством РФ разработаны и утверждены следующие типовые положения:</span></p>

