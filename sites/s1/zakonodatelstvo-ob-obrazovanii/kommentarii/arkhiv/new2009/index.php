<p><span class="text" style="text-align: justify;">Статья 2. Государственная политика и государственные гарантии прав граждан Российской Федерации в области высшего и послевузовского профессионального образования</span></p>
 
<p> </p>
 
<p><span class="text" style="text-align: justify;">1. Государственная политика в области высшего и послевузовского профессионального образования основывается на принципах, определенных Законом Российской Федерации &quot;Об образовании&quot;, а также на следующих принципах:</span></p>
 
<p><span class="text" style="text-align: justify;">1) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</span></p>
 
<p><span class="text" style="text-align: justify;">2) непрерывность и преемственность процесса образования;</span></p>
 
<p><span class="text" style="text-align: justify;">3) интеграция системы высшего и послевузовского профессионального образования Российской Федерации при сохранении и развитии достижений и традиций российской высшей школы в мировую систему высшего образования;</span></p>
 
<p><span class="text" style="text-align: justify;">4) конкурсность и гласность при определении приоритетных направлений развития науки, техники, технологий, а также подготовки специалистов, переподготовки и повышения квалификации работников;</span></p>
 
<p><span class="text" style="text-align: justify;">5) государственная поддержка подготовки специалистов, приоритетных направлений научных исследований в области высшего и послевузовского профессионального образования.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2. Утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ.</span></p>
 
<p><span class="text" style="text-align: justify;">3. Государство обеспечивает приоритетность развития высшего и послевузовского профессионального образования посредством:</span></p>
 
<p><span class="text" style="text-align: justify;">1) финансирования за счет средств федерального бюджета обучения в федеральных государственных образовательных учреждениях высшего профессионального образования не менее чем ста семидесяти студентов на каждые десять тысяч человек, проживающих в Российской Федерации;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">2) расширения доступа граждан Российской Федерации к высшему образованию;</span></p>
 
<p><span class="text" style="text-align: justify;">(пп. 2 в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">3) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</span></p>
 
<p><span class="text" style="text-align: justify;">4) предоставления обучающимся (студентам, аспирантам, докторантам и другим категориям обучающихся) в государственной системе высшего и послевузовского профессионального образования государственных стипендий, мест в общежитиях, иных мер социальной поддержки в соответствии с законодательством;</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 22.08.2004 № 122-ФЗ, от 31.12.2005 № 199-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5) создания условий для равной доступности высшего и послевузовского профессионального образования;</span></p>
 
<p><span class="text" style="text-align: justify;">6) утратил силу. - Федеральный закон от 22.08.2004 № 122-ФЗ;</span></p>
 
<p><span class="text" style="text-align: justify;">7) создания условий для интеграции высшего и послевузовского профессионального образования и науки.</span></p>
 
<p><span class="text" style="text-align: justify;">(пп. 7 введен Федеральным законом от 01.12.2007 № 308-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">4. Гражданам Российской Федерации гарантируется получение на конкурсной основе бесплатного высшего и послевузовского профессионального образования в государственных и муниципальных образовательных учреждениях высшего профессионального образования в пределах федеральных государственных образовательных стандартов, федеральных государственных требований и устанавливаемых в соответствии с пунктом 4 статьи 5 настоящего Федерального закона образовательных стандартов и требований, если образование данного уровня гражданин получает впервые.</span></p>
 
<p><span class="text" style="text-align: justify;">(в ред. Федеральных законов от 31.12.2005 № 199-ФЗ, от 01.12.2007 № 309-ФЗ)</span></p>
 
<p><span class="text" style="text-align: justify;">5. Гражданам Российской Федерации гарантируется свобода выбора формы получения высшего и послевузовского профессионального образования, образовательного учреждения и направления подготовки (специальности).</span></p>
 
<p><span class="text" style="text-align: justify;">Ограничения прав граждан на получение высшего и послевузовского профессионального образования могут быть установлены исключительно федеральным законом только в той мере, в какой это необходимо в целях защиты нравственности, здоровья, прав и законных интересов других лиц, обеспечения обороны страны и безопасности государства.</span></p>
 
