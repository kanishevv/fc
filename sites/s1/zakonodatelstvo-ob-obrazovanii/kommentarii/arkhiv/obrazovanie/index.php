<p><b>Статья 58. Внешнеэкономическая деятельность</b></p>
 
<p>1. Органы управления образованием, образовательные учреждения имеют право самостоятельно осуществлять внешнеэкономическую деятельность в порядке, установленном законодательством Российской Федерации.</p>
 
<p>(в ред. Федерального закона от 22.08.2004 № 122-ФЗ)</p>
 
