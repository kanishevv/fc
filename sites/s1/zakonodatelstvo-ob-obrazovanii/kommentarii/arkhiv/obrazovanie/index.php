<p><b>Статья 53. Занятие педагогической деятельностью</b></p>
 
<p>1. Порядок комплектования работников образовательных учреждений регламентируется их уставами.</p>
 
<p>К педагогической деятельности в образовательных учреждениях допускаются лица, имеющие образовательный ценз, который определяется типовыми положениями об образовательных учреждениях соответствующих типов и видов.</p>
 
<p>2. К педагогической деятельности в образовательных учреждениях не допускаются лица, которым она запрещена приговором суда или по медицинским показаниям, а также лица, имеющие неснятую или непогашенную судимость за умышленные тяжкие и особо тяжкие преступления, предусмотренные Уголовным кодексом Российской Федерации и Уголовным кодексом РСФСР.</p>
 
<p>Перечень соответствующих медицинских противопоказаний устанавливается Правительством Российской Федерации.</p>
 
<p>(п. 2 в ред. Федерального закона от 25.06.2002 № 71-ФЗ)</p>
 
