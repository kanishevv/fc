?><p>
	В общем плане понятие правового статуса применительно к образовательному учреждению уже была представлено и рассмотрено в предыдущей главе. Однако в отношении индивида это понятие требует несколько более детального анализа.
</p>
<p style="text-align: justify;">
	<span class="text">Под&nbsp;<b>правовым статусом</b>индивида (гражданина) в правовой науке обычно понимается совокупность юридически закрепленных прав, свобод и обязанностей индивида (человека), выражающая связь его с государством, а также юридических гарантий осуществления этих прав и обязанностей. Структура правового статуса достаточно сложна, поскольку он включает в себя:</span>
</p>
<ul>
	<li>
	<p style="text-align: justify;">
		<span class="text">Правовые нормы (нормы действующего законодательства).</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">Юридические права и обязанности (основной элемент, составляющий смысловое и содержательное ядро правового статуса).</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">Гражданство.</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">Правосубъектность данного субъекта правоотношений.</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">Юридические (общие и специальные) гарантии осуществления юридических прав и обязанностей.</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">Законные интересы данного субъекта (т.е. интересы, опосредованные нормами действующего законодательства и прежде всего Конституцией).</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">Юридическая ответственность (она может рассматриваться также в качестве одной из разновидностей (мерой) юридической гарантии).</span>
	</p>
 </li>
	<li>
	<p style="text-align: justify;">
		<span class="text">Правовые (общие, межотраслевые и отраслевые) принципы, на основе которых и в соответствии с которыми устанавливается и регулируется (изменяется) правовой статус.</span>
	</p>
 </li>
</ul>
<p style="text-align: justify;">
	<span class="text">Применительно к трактовке содержания и структуры правового статуса субъекта образовательных правоотношений, в том числе обучающегося, выделяются две составляющие этого статуса –<i>общая</i>и<i>факультативная.</i></span>
</p>
<p style="text-align: justify;">
	<span class="text"><b>Общую&nbsp;</b>часть образуют общие права, обязанности и ответственность обучающегося, обусловленные его пребыванием в образовательном учреждении, и зафиксированные в Уставе образовательного учреждения.<b>Факультативная&nbsp;</b>же часть формируется в зависимости от ряда текущих обстоятельств: образовательного уровня обучающегося, реализуемых образовательных программ, содержания договора на оказание образовательных услуг и т.д.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Важно подчеркнуть, что вхождение индивида в число обучающихся не влечет за собой никаких ограничений в его общеправовом статусе гражданина, так как он получает дополнительные социальные и административно-правовые гарантии от государства для реализации своего конституционного права на образование.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Правовой статус обучающихся определяется Законом РФ «Об образовании», Типовыми положениями о соответствующих образовательных учреждениях, локальными актами образовательного учреждения (прежде всего, его Уставом).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Понятие правового статуса обучающегося как управомоченного лица неразрывно связано с реализацией его<i><b>субъективного права</b></i><b>,</b>под которым понимается:</span>
</p>
<p style="text-align: justify;">
	<span class="text">а) возможность определенного дозволенного поведения (поведения направленного на удовлетворение своих потребностей и интересов путем добровольного и самодеятельного прохождения обучения<sup>26</sup>);</span>
</p>
<p style="text-align: justify;">
	<span class="text">б) возможность требовать от обязанного лица (образовательного учреждения, педагогического работника) определенного правового поведения, которое вытекает из его функциональных обязанностей (должностной инструкции);</span>
</p>
<p style="text-align: justify;">
	<span class="text">в) возможность обратиться к государственным органам (в т.ч. правоохранительным органам и органам правосудия) за правовой защитой своих нарушенных прав (правопритязание).</span>
</p>
<p style="text-align: justify;">
	<span class="text">В ст. 50 Закона РФ «Об образовании» представлен перечень основных прав обучающихся, к числу наиболее существенных из которых относятся:</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право совершеннолетних граждан РФ на выбор образовательного учреждения и формы получения образования;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на получение образования в соответствии с государственными образовательными стандартами, на обучение в пределах этих стандартов по индивидуальным учебным планам, на ускоренный курс обучения;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на бесплатное пользование библиотечно-информационными ресурсами библиотек;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на получение дополнительных (в том числе платных) образовательных услуг;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на участие в управлении образовательным учреждением;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на уважение своего человеческого достоинства;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на свободу совести, информации, на свободное выражение собственных мнений и убеждений;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на получение впервые бесплатного начального общего, основного общего, среднего (полного) общего, начального профессионального образования и на конкурсной основе среднего профессионального, высшего профессионального и послевузовского профессионального образования в государственных или муниципальных образовательных учреждениях в пределах государственных образовательных стандартов;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право обучающихся, воспитанников гражданских образовательных учреждений на свободное посещение мероприятий, не предусмотренных учебным планом;</span>
</p>
<p style="text-align: justify;">
	<span class="text">- право на перевод в другое образовательное учреждение, реализующее образовательную программу соответствующего уровня, при согласии этого образовательного учреждения и успешном прохождении ими аттестации и др.</span>
</p>
<p style="text-align: justify;">
	<span class="text">От понятия прав (субъективных прав) обучающихся следует отличать понятие<i><b>законного интереса,</b></i>под которым в юридической литературе понимается (в самом общем плане) обусловленное потребностями и социальными интересами личности ее стремление к получению и пользованию определенными социальными благами, которые опосредованы действующим правом (законодательством), т.е. обладание которыми дозволено государством в лице законодательства, и могут быть обеспечены и защищены в установленном законодательством порядке и формах компетентными органами и лицами (А.В. Малько, В.В. Субочев). Так, законными интересами личности обучающегося является его стремление к получении качественного образования, уважение, соблюдение и защита его прав и свобод со стороны образовательного учреждения в целом и педагогических работников, в частности и др.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Общим для субъективного права и конкретного законного интереса обучающегося в данном случае будет то, что оба они юридически гарантируются законодательством и соответствующими компетентными органами и лицами. В дальнейшем изложении нет необходимости в глубоком разграничении этих двух понятий, поэтому они будут рассматриваться как однопорядковые.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Неотъемлемой составляющей правового статуса обучающегося является<i><b>юридическая обязанность</b></i>как мера должного поведения, обеспеченная государством. Должное поведение включает в себя такие составляющие, как:</span>
</p>
<p style="text-align: justify;">
	<span class="text">а) обязанность воздерживаться от действий, противоречащих образовательному законодательству, наносящих вред образовательному процессу и образовательному учреждению, правам и интересам других обучающихся, педагогических работников;</span>
</p>
<p style="text-align: justify;">
	<span class="text">б) совершение определённых действий в интересах управомоченных лиц, органов и организаций (образовательного учреждения, прежде всего);</span>
</p>
<p style="text-align: justify;">
	<span class="text">в) обязанность претерпевания неблагоприятных последствий за совершённое правонарушение, предусмотренных санкциями норм образовательного и иного (административного, гражданского, уголовного) законодательства (выговор, строгий выговор, отчисление обучающегося из образовательного учреждения и др.).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Определенное своеобразие имеет правовая ответственность обучающихся, о которой предметно говорится в следующем параграфе настоящей главы. В данном случае кратко коснемся<i>видов</i>правового статуса обучающихся, которые помимо структуры, играют важную роль в характеристике правового статуса индивида и специфика которых обусловливаемых особенностями содержания статуса индивидов, занимающих различное социально-правовое положение.</span>
</p>
<p style="text-align: justify;">
	<span class="text">В числе этих видов обычно выделяют<i>общий, специальный (родовой)</i>и<i>индивидуальный</i>правовой статусы, наиболее отчетливо выражающие особенности положения (места и роли), в том числе и относительность этого положения, данного субъекта в системе социальных связей.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Понятием<i><b>общего правового статуса</b></i>гражданина (личности) охватывается те юридические права, обязанности и законные интересы, которые присущи всем гражданам и каждому гражданину в отдельности независимо от особенностей их социально-классового, половозрастного, семейного, служебного положения (И.П. Левченко, С.В. Облиенко). В этом отношении каждый обучающийся, как являющийся гражданином РФ, так и не имеющий российского гражданство, но обладающий правом обучения в образовательных учреждениях России (в соответствии с международными договорами и соглашениями) является носителем общего (прежде всего, конституционного) правового статуса Тем самым, на каждого такого субъекта распространяется действие ст. 43 Конституции Российской Федерации.</span>
</p>
<p style="text-align: justify;">
	<span class="text"><i><b> <br>
 </b></i></span><span class="text"><i><b>Специальные правовые статусы</b></i>включают в себя специфические права и обязанности отдельных категорий лиц, выделенных по выделенным выше признакам – социальному положению, половозрастным особенностям и т.д. В этом случае речь идет о специальных правах и обязанностях государственных служащих, рабочих, предпринимателей, военнослужащих, работников милиции и т.д., предусмотренных, соответственно, и специальным законодательством. В этом отношении правовой статус различных категорий (групп) обучающихся также является специальным статусом, поскольку их прав и обязанности предусмотрены, главным образом, образовательным законодательством (Законом РФ «Об образовании», Федеральным законом «О высшем и послевузовском профессиональном образовании»Ю подзаконными актами Минобрнауки РФ, Рособразования и Рособрнадзора, органами управления образования муниципальных образований, локальными актами образовательных учреждений. Применительно к общей категории обучающихся можно выделить такие подвиды специального статуса, различающиеся в зависимости от уровня образования и типа образовательного учреждения как правовые статусы обучающихся (воспитанников) в дошкольного и младшего школьного возраста, учащихся общеобразовательных школ (основное среднее и полное общее образование), учащиеся учреждений начального профессионального образования, студенты (слушатели) учреждений среднего и высшего профессионального образования, обучающиеся в системе послевузовского профессионального образования. Нужно также заметить, что одно и то же лицо может быть носителем разных специальных правовых статусов в зависимости от тех социальных позиций, которое оно занимает в обществе. Скажем, тот же студент высшего учебного заведения (специальный правовой статус) может быть студентом как государственного, так и негосударственного вуза; вуза, имеющего государственную аккредитацию, либо не имеющего. Правовые статусы студентов разных курсов также различаются, хотя нередко это может расцениваться как нарушение их прав и законных интересов в форме злоупотребления своим правом тех или иных организаций и учреждений. К примеру, читальным залом Государственной публичной библиотеки им В.Г. Белинского Свердловской области могут пользоваться лишь студенты 3 и выше курсов высших учебных заведений.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Своеобразием отличается понятие<i><b>индивидуального правового статуса</b></i>личности. В отличие от общего и специального правовых статусов, индивидуальный статус имеет статический и динамический аспект. Статический аспект отражает<i>возможность</i>реализации каждым индивидом своего общего и специального статуса в ситуации типичного (родового, видового) характера, предусмотренной этими статусами. Как правило, такой статус закрепляется, например, в договорах на обучение между конкретным образовательным учреждением и конкретным лицом или его законными представителями. Динамический же, или, иначе, конкретный, индивидуальный правовой статус характеризует<i>реальное</i>использование конкретной личностью прав и обязанностей в реальной сложившейся правовой ситуации. Так, если каждый студент имеет право на сдачу экзамена при соблюдении определенных требований условий, обеспечивающих допуск к экзамену, то студент N., который выполнил такие требования и условия, реально допускается к экзамену. Иными словами, индивидуальный правовой статус (конкретный статус) показывает, какие именно права и обязанности личностью фактически реализуются, используются на данный момент времени (Н.В. Витрук).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Кроме этих видов правового статуса обучающегося, могут быть и иные, выделяемые в зависимости от других критериев (факторов). Среди этих факторов такие, как:</span>
</p>
<p style="text-align: justify;">
	<span class="text">а) является ли образовательное учреждение, в котором обучается обучающийся, государственным или негосударственным, аккредитованным или не аккредитованным;</span>
</p>
<p style="text-align: justify;">
	<span class="text">б) к кому ведомству принадлежит образовательное учреждение гражданское, военное, учреждение системы МВД или МЧС и др.); </span>
</p>
<p style="text-align: justify;">
	<span class="text">в) особенности источников финансирования обучения (средства бюджетов различных уровней, физические или юридические лица);</span>
</p>
<p style="text-align: justify;">
	<span class="text">г) наличие договора на образование, его содержание;</span>
</p>
<p style="text-align: justify;">
	<span class="text">д) индивидуальные способности обучающегося и предшествующий уровень его образования;</span>
</p>
<p style="text-align: justify;">
	<span class="text">е) право обучающегося на льготы и т.д.</span>
</p>
