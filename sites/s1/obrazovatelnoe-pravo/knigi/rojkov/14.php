<p style="text-align: justify;">
	<span class="text"> <br>
 </span><span class="text">Налоговый кодекс РФ устанавливает, что в случаях, когда у налого­плательщиков организаций выплаты физическим лицам не отнесены к расходам, уменьшающим налоговую базу по налогу на прибыль орга­низаций, такие выплаты не облагаются ЕСН (п. 3 ст. 236 НК РФ). К вып­латам, не уменьшающим налоговую базу по налогу на прибыль, отно­сятся премии, выплачиваемые работникам учреждения, которые не предусмотрены трудовым договором (п. 21, 22 ст. 270 НК РФ).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Таким образом, не исчисляется ЕСН при выплате премий за счет доходов от коммерческой деятельности образовательного учрежде­ния (например, платных образовательных услуг), с которых уже произ­ведена уплата налога на прибыль организаций.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Кроме ЕСН, каждое образовательное учреждение платит&nbsp;<b>страховые&nbsp;</b><b>взносы на обязательное социальное страхование от несчастныхслучаев на производстве и профессиональных заболеваний</b>. Рас­сматриваемый вид обязательного страхования урегулирован Феде­ральным законом «Об обязательном социальном страховании от не­счастных случаев на производстве и профессиональных заболеваний» от 24 июля 1998 г. № 125-ФЗ (с последующими изменениями) и при­нятыми в соответствии с ним подзаконными актами.</span>
</p>
<p style="text-align: justify;">
	<span class="text">Страховые взносы начисляются на все выплаты работникам (застра­хованным), работающим в школе&nbsp;<b>по трудовому договору (в том чис­</b><b>ле внештатным, сезонным, временным, выполняющим работу по совместительству).&nbsp;</b>Образовательное учреждение не уплачивает страховые взносы по авторским и&nbsp;<b>гражданско-правовым догово­рам (оказания услуг, выполнения работ)</b>, если это специально не предусмотрено договором, а также по некоторым другим выплатам работникам, указанным в Перечне выплат, на которые не начисляются страховые взносы в Фонд социального страхования Российской Фе­дерации, утвержденном постановлением Правительства РФ от 7 июля1999 г. № 765 (например, выходное пособие при прекращении тру­дового договора, денежная компенсация за неиспользованный отпуск, сохраняемая в соответствии с законодательством средняя заработ­ная плата на период трудоустройства работникам, высвобождаемым в связи с осуществлением мероприятий по сокращению численности или штата или ликвидацией организации и др.).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Размер страхового тарифа определяется федеральным законом в зависимости от класса профессионального риска (т.е. уровня произ­водственного травматизма, профессиональной заболеваемости и расходов на обеспечение по страхованию, сложившегося по видам экономической деятельности страхователей). Для сферы образова­ния установлен первый (минимальный) класс профессионального рис­ка и, согласно Федеральному закону «О страховых тарифах на обяза­тельное социальное страхование от несчастных случаев на производ­стве и профессиональных заболеваний на 2006 год» от 22 декабря 2005 г. № 179-ФЗ, страховой тариф в размере 0,2% к начисленной оплате труда по всем основаниям (доходу) застрахованных лиц (т.е. работников).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Льготы по уплате данного страхового взноса предусмотрены только для негосударственных образовательных учреждений, единственны­ми собственниками имущества которых являются общественные орга­низации инвалидов. Такие образовательные учреждения платят стра­ховой взнос в размере 60% от установленного тарифа (т.е. не 0,2%, а0,12%).</span>
</p>
<p style="text-align: justify;">
	<span class="text">Рассмотрим налогообложение доходов от коммерческой деятель­ности на примере доходов от платных образовательных услуг.</span>
</p>
<p style="text-align: justify;">
	<i><span class="text">В первом квартале 2006 г. выручка муниципальной школы от плат­ных образовательных услуг (дополнительные занятия по английскому и немецкому языку) составила 20 000 руб.</span></i>
</p>
<p style="text-align: justify;">
	<i><span class="text">Бюджетное финансирование школы за данный период составило 180 000 рублей. Сметой доходов и расходов установлено финанси­рование услуг связи от двух источников. Расходы на услуги связи со­ставили 2000 рублей, из них 1300 рублей согласно смете были по­крыты за счет муниципального бюджета.</span></i>
</p>
<p style="text-align: justify;">
	<i><span class="text">За исключением услуг связи, расходы на оказание услуг составили:</span></i>
</p>
<p style="text-align: justify;">
	<i><span class="text">9000 руб. — оплата труда учителей на проведение данных занятий(с учетом ЕСН и страхового взноса)</span></i>
</p>
<p style="text-align: justify;">
	<i><span class="text">2800 руб. — приобретение учебных пособий (с НДС).</span></i>
</p>
<p style="text-align: justify;">
	<i><span class="text">Расчет налогов:</span></i>
</p>
<p style="text-align: justify;">
	<span class="text"><i>1.</i><i>НДС предоставление платных образовательных услуг не облага­ется (в приложении к лицензии такая образовательная деятельность указана) .</i></span>
</p>
<p style="text-align: justify;">
	<span class="text"><i>2.</i><i>Рассчитаем общую сумму расходов на оказание услуг:</i></span>
</p>
<p style="text-align: justify;">
	<i><span class="text">а)9000 руб. — оплата труда учителей на проведение данных заня­тий (с учетом ЕСН и страхового взноса);</span></i>
</p>
<p style="text-align: justify;">
	<i><span class="text">б) 2800 руб. — приобретение учебных пособий;</span></i>
